<h2 class="text-center">Génesis 3</h2>
<p class="text-justify">Footnotes:
[1] 3.1 En el Oriente antiguo, la serpiente era un símbolo de la sabiduría y de los poderes mágicos. Además, su astucia era proverbial (cf. Mt 10.16). Estas características la predisponían para hacer de ella la representación de una potencia maléfica y opuesta a los planes de Dios, que se vale de la seducción para incitar a desobedecer el mandato divino. Por eso, más tarde la serpiente fue identificada con el diablo. Cf. Jn 8.44; Ap 12.9; 20.2.
[2] 3.1 Astuta : Esta expresión tiene aquí un sentido negativo. Se trata de una astucia que no excluye el engaño y el fraude.
[3] 3.1-6 Cf. Ro 5.12.
[4] 3.7 Se dieron cuenta... desnudos : Véase Gn 2.25 n.
[5] 3.9 ¿Dónde estás?: Véase Gn 4.9 n.
[6] 3.13 Cf. 2 Co 11.3; 1 Ti 2.14.
[7] 3.15 Cf. Ro 16.20; Ap 12.17. Los cristianos han visto en esta mención de la descendencia de la mujer una velada referencia al Mesías en su lucha contra Satanás y en su victoria final sobre las fuerzas del mal.
[8] 3.17 El pecado de los seres humanos atrae la maldición sobre la tierra, que es la morada de estos, y de la que reciben el sustento. Véase Gn 12.2-3 n.
[9] 3.17 Con dolor comerás de ella : Véase Gn 2.15 n.
[10] 3.17-18 Cf. Ro 8.20; Heb 6.8.
[11] 3.20 En hebreo, el nombre Eva y la palabra que significa vida o viviente tienen un sonido semejante.
[12] 3.21 Nótese la compasión manifestada en este gesto. A pesar del pecado, el modo de actuar de Dios incluye también el perdón y la promesa de salvación. Véase un gesto similar en Gn 4.15 nota o.
[13] 3.22 Árbol de la vida : Cf. Ap 22.14.
[14] 3.24 Estos querubines eran considerados en el antiguo Oriente como guardianes de los templos y de los lugares sagrados. Véase Ex 25.18 n.
[15] 3.24 La imposibilidad de llegar al árbol de la vida hace efectivo el castigo anunciado en Gn 2.17. El destino original de la humanidad era la vida, pero al desobedecer el mandamiento divino se apartó de Dios, fuente de toda vida, y junto con el pecado entró la muerte en el mundo (Ro 5.12).</p>
