<br/>
<p class="text-justify">Rubén…no tendrás preeminencia. Como primogénito, Rubén debía haber asumido el papel de líder de la familia; pero la falta de dominio de sí mismo, evidente cuando se acostó con la concubina de su padre (35:22), dio como resultado la pérdida de la preeminencia y el honor.</p>
<br/>
<p class="text-justify">Simeón y Leví…violencia. El carácter de estos dos hermanos se vio en las atrocidades cometidas por ellos contra el pueblo de Siquem (34:25-31). Tenían un temperamento que reaccionaba violentamente a la menor provocación.</p>
<br/>
<p class="text-justify">los dispersaré. Simeón fue dispersado en el territorio de Judá (Jos 19:1-9), y a los levitas se les dio 48 ciudades con sus pastos en toda la tierra de Israel (Jos 21).</p>
<br/>
<p class="text-justify">Judá…tus hermanos…se inclinarán a ti. Jacob afirma la preeminencia de la tribu de Judá. A pesar de no ser el primogénito, le fue asignado el papel de líder de la familia.</p>
<br/>
