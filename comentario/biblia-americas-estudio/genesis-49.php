<br/>
<p class="text-justify">Zabulón…la orilla del mar…naves. Aunque la porción de Zabulón estaría separada de la costa por el territorio de Aser, quedaría lo bastante cerca del mar para que sus habitantes disfrutaran de la abundancia de los mares (Dt 33:18, 19).</p>
<br/>
<p class="text-justify">Isacar…un asno fuerte echado. Quizás esto sea para describir a Isacar como fuerte, pero falto de iniciativa.</p>
<br/>
<p class="text-justify">el lugar de reposo era bueno…la tierra era agradable. Tal vez sea una referencia al territorio de Isacar en el valle de Jezreel.</p>
<br/>
<p class="text-justify">Dan juzgará. En heb. la palabra Dan suena como la palabra heb. para juzgar.</p>
<br/>
<p class="text-justify">serpiente…que muerde los jarretes del caballo. Una posible referencia a la lucha que la tribu de Dan en el sur tuvo con sus poderosos vecinos, los filisteos. Muchas veces esta lucha tomó la forma de una guerra de guerrillas.</p>
<br/>
<p class="text-justify">A Gad salteadores lo asaltarán. La palabra heb. para asaltar suena como la palabra hebrea Gad. Por estar localizado al este del Jordán Gad era vulnerable a los ataques de los vecinos y al mismo tiempo estaba demasiado lejos de las otras tribus para obtener ayuda.</p>
<br/>
<p class="text-justify">Aser, su alimento será sustancioso. El territorio de la tribu de Aser estaba en la costa norte en una de las partes más fértiles de Israel.</p>
<br/>
<p class="text-justify">Neftalí es una cierva…palabras hermosas. El significado de esta descripción es incierto. Algunos piensan que los de Neftalí eran muy aptos para las artes, pero no hay nada en las Escrituras que dé evidencia de esto. Otros han sugerido que la descripción del animal en la primera línea continúa en la segunda. Proponen un pequeño cambio del texto y sugieren que el versículo enfatiza el carácter independiente de la tribu, realzado por su local en los montes.</p>
<br/>
<p class="text-justify">José, rama fecunda junto a un manantial. Otra figura para indicar prosperidad y abundancia (v. coment. en vers. 11).</p>
<br/>
<p class="text-justify">arqueros lo atacaron. Probablemente una descripción figurada de los ataques a José antes de su ascenso al poder en Egipto. Su habilidad para resistir esos ataques es atribuida al Poderoso de Jacob (vers. 24).</p>
<br/>
<p class="text-justify">Benjamín es lobo rapaz. Este vers. describe a Benjamín como un guerrero feroz, característica bien conocida de la tribu (Jue 20:15, 16, 19-25).</p>
<br/>
<p class="text-justify">sepultadme…en la tierra de Canaán. Véanse coments. en 47:29 y 48:21.</p>
