¡Aunque yo haya de morir contigo, nunca te negaré! Y todos los discípulos decían lo mismo. </p>
                                                    <h4><strong>Jesús ora en Getsemaní</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
 Entonces se fue con ellos a un lugar llamado Getsemaní. Una vez allí, dijo a los discípulos:
Quedaos aquí sentados, entre tanto que yo me aparto un poco de vosotros para orar. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 Sin embargo tomó consigo a Pedro, y a Jacobo y Juan, los hijos de Zebedeo, y comenzó a entristecerse y angustiarse sobremanera. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 En aquel trance les dijo:
Una tristeza mortal me ha llenado el alma. Quedaos aquí y velad conmigo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 Se separó un poco de ellos, se postró rostro en tierra y oró diciendo:
Padre mío, si es posible, haz que pase de mí esta copa de amargura. Pero no sea lo que yo quiero, sino lo que quieres tú. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
 Volvió adonde había dejado a los discípulos, y los halló dormidos. Le dijo a Pedro:
¿De modo que ni siquiera una hora podéis velar conmigo? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 Velad y orad, para no caer en tentación. Ya veo que vuestro espíritu está dispuesto, pero vuestro cuerpo es débil. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 Por segunda vez se alejó de ellos para seguir orando. Decía: 
Padre mío, si no es posible que esta copa pase de mí, hágase tu voluntad. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 Regresó junto a ellos y de nuevo los halló dormidos, porque tenían los ojos cargados de sueño. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 En seguida volvió a dejarlos, y por tercera vez se apartó para orar con las mismas palabras. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 Entonces fue adonde estaban los discípulos, y le s dijo: 
Dormid todavía y descansad... Pero no, pues ha llegado la hora y el Hijo del hombre va a ser entregado en manos de pecadores. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 ¡Levantaos, vamos! Ya está aquí el que me entrega.

Arresto de Jesús </p>
                                                    <h4><strong>Arresto de Jesús</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
 Aún no había acabado de pronunciar estas palabras, cuando Judas, uno de los doce, llegó al frente de una turba armada con espadas y con palos. Iban enviados por los sacerdotes y los dirigentes del pueblo, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 a los que Judas había dado esta contraseña: "Aquel a quien yo bese, ése es. ¡Apresadlo!" </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 Sin pérdida de tiempo se acercó a Jesús, y le dijo: 
¡Salve, Maestro! 
y lo besó. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 Jesús le respondió:
Amigo, ¿qué has venido a hacer? Entonces se acercaron a Jesús, le echaron mano y lo prendieron. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 En aquel momento, uno de los que estaban con Jesús desenvainó una espada y de un tajo le cortó una oreja a un siervo del sumo sacerdote. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 Jesús le dijo:
Envaina de nuevo esa espada, porque el que empuña espada, a espada morirá. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 ¿Acaso no piensas que yo ahora podría orar a mi Padre, y que él me enviaría al instante más de doce legiones de ángeles? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 Pero si hiciera eso, ¿cómo se iban a cumplir las Escrituras que dicen que estas cosas han de acontecer así? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 Luego dijo a la turba allí reunida: 
¿Soy, quizás, un bandido tan peligroso que habéis de venir con espadas y con palos a prenderme? Todos estos días me he sentado a enseñar en el templo, y no me habéis arrestado. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 Pero todo esto está sucediendo para que se cumpla lo escrito por los profetas. Entonces todos los discípulos huyeron y lo dejaron solo.

Jesús ante el Consejo </p>
                                                    <h4><strong>Jesús ante el concilio</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 Los que habían apresado a Jesús, lo condujeron a la casa de Caifás, el sumo sacerdote, donde se encontraban reunidos los escribas y los dirigentes judíos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
 Pedro, siguiéndole de lejos, llegó hasta el patio de la casa del sumo sacerdote. Una vez allí, se sentó entre los soldados para ver en que pararía finalmente todo lo que estaba ocurriendo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">59. </strong>
 Los principales sacerdotes y el concilio reunido en pleno, empezaron a buscar contra Jesús algún falso testimonio que les permitiera pedir para él la pena de muerte. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">60. </strong>
 Se ofrecieron muchos testigos falsos, pero sus testimonios no tenían valor. Finalmente se presentaron dos, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">61. </strong>
 que declararon: 
Este hombre ha dicho que él puede derribar el templo de Dios y reconstruirlo en tres días. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">62. </strong>
 Al oir esto, el sumo sacerdote se puso en pie y preguntó a Jesús: 
¿No tienes nada que contestar a lo que estos declaran en contra tuya? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">63. </strong>
 Pero Jesús guardó silencio. Insistió el sumo sacerdote:
En el nombre del Dios viviente te conjuro: Dinos si tú eres el Cristo, el Hijo de Dios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">64. </strong>
 
Tú lo has dicho 
le respondió Jesús
. Pero además de esto os digo que, a partir de ahora, veréis al Hijo del hombre sentado a la derecha del poder de Dios y viniendo sobre las nubes del cielo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">65. </strong>
 El sumo sacerdote se rasgó entonces la ropa y gritó:
¡Ha blasfemado! ¿Qué necesidad tenemos de más testigos? ¡Ya habéis oído la blasfemia! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">66. </strong>
 ¿Qué pensáis de esto?
¡Es reo de muerte! 
respondieron ellos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">67. </strong>
 Entonces lo maltrataron: unos le escupían en el rostro, otros le daban de puñetazos y otros le abofeteaban, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">68. </strong>
 mientras decían: 
A ver tú, Cristo, profetízanos quién es el que te golpea.

Pedro niega a Jesús </p>
                                                    <h4><strong>Pedro niega a Jesús</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">69. </strong>
 Pedro estaba sentado fuera, en el patio de la casa, cuando se acercó a él una criada y le dijo: 
Tú también andabas con Jesús el galileo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">70. </strong>
 Entonces Pedro lo negó ante todos los presentes, diciendo:
No sé de qué me estás hablando. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">71. </strong>
 Poco más tarde, saliendo él de aquel lugar, otra mujer lo vio y dijo a los que allí se encontraban: 
Este hombre es de los que estaban con Jesús el nazareno. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">72. </strong>
 Pedro volvió a negarlo, jurando que no le conocía:
¡Yo no conozco a ese hombre! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">73. </strong>
 Pero un rato después, algunas personas que estaban allí se acercaron a Pedro y le dijeron: 
Ciertamente tú eres uno de ellos, y no puedes negarlo porque hasta tienes acento galileo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">74. </strong>
 Pedro comenzó a maldecir y a jurar, diciendo:
¡Yo no conozco a ese hombre! Pero en aquel momento cantó el gallo, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">75. </strong>
 y él recordó las palabras de Jesús: "Antes que el gallo cante, me negarás tres veces". Entonces Pedro salió corriendo afuera y lloró con gran amargura. </p>
                                                        
