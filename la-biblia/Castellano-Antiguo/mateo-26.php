<li class="hide-for-large"><a href="whatsapp://send?text=Mateo 26 (CST)%0A%0A 1. Cuando Jesús concluyó este discurso, se dirigió a sus discípulos diciéndoles: 2. Como sabéis, dentro de dos días se celebra la Pascua, y el Hijo del hombre será entregado para que lo crucifiquen. 3. Entonces se reunieron los principales sacerdotes, los escribas y los ancianos del pueblo en la residencia del sumo sacerdote llamado Caifás. 4. Tomaron consejo entre sí sobre cómo tender una trampa a Jesús, con objeto de apresarlo y darle muerte; 5. pero decían: "No lo hagamos durante los días de la fiesta, para evitar que el pueblo se amotine contra nosotros".

Una mujer unge a Jesús en Betania 6. Jesús estaba entonces en Betania, en casa de uno a quien llamaban Simón el leproso. 7. En esto se le acercó una mujer que llevaba un frasco de alabastro lleno de un perfume de mirra muy caro, que derramó en la cabeza de Jesús mientras él estaba sentado a la mesa. 8. Los discípulos, al ver la acción de la mujer, la criticaron enojados. 
Eso es un despilfarro 
decían
. 9. Podíamos haber vendido este perfume por muy buen precio, y haber repartido el dinero a los pobres. 10. Pero Jesús, dándose cuenta de ello, les dijo:
¿Por qué molestáis a esta mujer? Lo que en mí ha hecho es una buena obra. 11. Porque pobres los vais a tener siempre entre vosotros, pero a mí no siempre me vais a tener. 12. El perfume que ha derramado en mi cuerpo es una preparación para mi sepultura. 13. Yo os aseguro que, dondequiera que en el mundo se prediquen estas buenas noticias, se contará también lo que ella ha hecho, para memoria suya.

Judas acuerda traicionar a Jesús 14. Uno de los doce apóstoles, el llamado Judas Iscariote, se presentó entonces a los principales sacerdotes 15. y les preguntó: 
¿Cuánto me daréis, si yo os entrego a Jesús? Ellos ajustaron el pago en treinta monedas de plata, 16. y a partir de ese momento empezó Judas a buscar una ocasión propicia para entregárselo.

La Cena del Señor 17. El primer día de la fiesta en que se comía el pan sin levadura, los discípulos fueron a preguntarle a Jesús:
¿Dónde quieres que te preparemos la cena de Pascua? 18. Id a la ciudad, a casa de quien ya sabéis, y decidle: "Esto dice el Maestro: Mi tiempo ya está cerca, y quisiera celebrar la Pascua en tu casa, junto con mis discípulos". 19. Entonces los discípulos, cumpliendo el encargo que Jesús les había dado, fueron allá y prepararon la cena. 20. Llegada la noche, se sentó a la mesa para cenar con los doce, 21. y en cierto momento, mientras comían, les dijo: 
Sabed que uno de vosotros va a entregarme. 22. Ellos se entristecieron profundamente, y comenzaron a preguntarle uno tras otro: 
¿Seré yo, Señor? 23. Él les respondió, diciendo: 
Uno que ha metido la mano en el plato conmigo será quien me entregue. 24. Es cierto que en el Hijo del hombre va a cumplirse lo que está profetizado, pero ¡ay de aquél por quien el Hijo del hombre es entregado! Mejor le habría sido no haber nacido. 25. Judas se acercó también a Jesús, y le preguntó: 
¿Acaso seré yo, Maestro? 
Tú lo has dicho 
le respondió. 26. Estando comiendo, Jesús tomó un pan, lo bendijo, lo partió y lo dio a sus discípulos, diciendo:
Tomad y comed: esto es mi cuerpo. 27. Luego tomó un vaso de vino, dio gracias por él, se lo pasó a ellos y dijo:
Bebed todos de él, 28. porque esto es mi sangre del nuevo pacto, la cual será derramada en favor de muchos, para perdón de los pecados. 29. Y tened presente que no volveré a beber de este vino, fruto de la vid, hasta aquel día en que lo beba nuevo, con vosotros, en el reino de mi Padre. 30. Después de esto y de haber cantado el himno, salieron de allí y se fueron al monte de los Olivos.

Jesús predice la negación de Pedro 31. Cuando llegaron, Jesús les dijo: 
Todos vosotros vais a sentiros escandalizados por mi causa esta noche, y se cumplirá lo que dicen las Escrituras: "Mataré al pastor, y las ovejas del rebaño serán dispersadas". 32. Pero tened esto presente: después que haya resucitado, iré a Galilea para encontrarme con vosotros. 33. A estas palabras, respondió Pedro diciéndole:
Aunque todos se sientan escandalizados por tu causa, yo jamás me escandalizaré. 34. Pedro 
le dijo Jesús
, te aseguro que esta noche, antes que el gallo cante, me negarás tres veces.

Jesús en Getsemaní 35. Pedro sostuvo con firmeza:
¡Aunque yo haya de morir contigo, nunca te negaré! Y todos los discípulos decían lo mismo. 36. Entonces se fue con ellos a un lugar llamado Getsemaní. Una vez allí, dijo a los discípulos:
Quedaos aquí sentados, entre tanto que yo me aparto un poco de vosotros para orar. 37. Sin embargo tomó consigo a Pedro, y a Jacobo y Juan, los hijos de Zebedeo, y comenzó a entristecerse y angustiarse sobremanera. 38. En aquel trance les dijo:
Una tristeza mortal me ha llenado el alma. Quedaos aquí y velad conmigo. 39. Se separó un poco de ellos, se postró rostro en tierra y oró diciendo:
Padre mío, si es posible, haz que pase de mí esta copa de amargura. Pero no sea lo que yo quiero, sino lo que quieres tú. 40. Volvió adonde había dejado a los discípulos, y los halló dormidos. Le dijo a Pedro:
¿De modo que ni siquiera una hora podéis velar conmigo? 41. Velad y orad, para no caer en tentación. Ya veo que vuestro espíritu está dispuesto, pero vuestro cuerpo es débil. 42. Por segunda vez se alejó de ellos para seguir orando. Decía: 
Padre mío, si no es posible que esta copa pase de mí, hágase tu voluntad. 43. Regresó junto a ellos y de nuevo los halló dormidos, porque tenían los ojos cargados de sueño. 44. En seguida volvió a dejarlos, y por tercera vez se apartó para orar con las mismas palabras. 45. Entonces fue adonde estaban los discípulos, y le s dijo: 
Dormid todavía y descansad... Pero no, pues ha llegado la hora y el Hijo del hombre va a ser entregado en manos de pecadores. 46. ¡Levantaos, vamos! Ya está aquí el que me entrega.

Arresto de Jesús 47. Aún no había acabado de pronunciar estas palabras, cuando Judas, uno de los doce, llegó al frente de una turba armada con espadas y con palos. Iban enviados por los sacerdotes y los dirigentes del pueblo, 48. a los que Judas había dado esta contraseña: "Aquel a quien yo bese, ése es. ¡Apresadlo!" 49. Sin pérdida de tiempo se acercó a Jesús, y le dijo: 
¡Salve, Maestro! 
y lo besó. 50. Jesús le respondió:
Amigo, ¿qué has venido a hacer? Entonces se acercaron a Jesús, le echaron mano y lo prendieron. 51. En aquel momento, uno de los que estaban con Jesús desenvainó una espada y de un tajo le cortó una oreja a un siervo del sumo sacerdote. 52. Jesús le dijo:
Envaina de nuevo esa espada, porque el que empuña espada, a espada morirá. 53. ¿Acaso no piensas que yo ahora podría orar a mi Padre, y que él me enviaría al instante más de doce legiones de ángeles? 54. Pero si hiciera eso, ¿cómo se iban a cumplir las Escrituras que dicen que estas cosas han de acontecer así? 55. Luego dijo a la turba allí reunida: 
¿Soy, quizás, un bandido tan peligroso que habéis de venir con espadas y con palos a prenderme? Todos estos días me he sentado a enseñar en el templo, y no me habéis arrestado. 56. Pero todo esto está sucediendo para que se cumpla lo escrito por los profetas. Entonces todos los discípulos huyeron y lo dejaron solo.

Jesús ante el Consejo 57. Los que habían apresado a Jesús, lo condujeron a la casa de Caifás, el sumo sacerdote, donde se encontraban reunidos los escribas y los dirigentes judíos. 58. Pedro, siguiéndole de lejos, llegó hasta el patio de la casa del sumo sacerdote. Una vez allí, se sentó entre los soldados para ver en que pararía finalmente todo lo que estaba ocurriendo. 59. Los principales sacerdotes y el concilio reunido en pleno, empezaron a buscar contra Jesús algún falso testimonio que les permitiera pedir para él la pena de muerte. 60. Se ofrecieron muchos testigos falsos, pero sus testimonios no tenían valor. Finalmente se presentaron dos, 61. que declararon: 
Este hombre ha dicho que él puede derribar el templo de Dios y reconstruirlo en tres días. 62. Al oir esto, el sumo sacerdote se puso en pie y preguntó a Jesús: 
¿No tienes nada que contestar a lo que estos declaran en contra tuya? 63. Pero Jesús guardó silencio. Insistió el sumo sacerdote:
En el nombre del Dios viviente te conjuro: Dinos si tú eres el Cristo, el Hijo de Dios. 64. Tú lo has dicho 
le respondió Jesús
. Pero además de esto os digo que, a partir de ahora, veréis al Hijo del hombre sentado a la derecha del poder de Dios y viniendo sobre las nubes del cielo. 65. El sumo sacerdote se rasgó entonces la ropa y gritó:
