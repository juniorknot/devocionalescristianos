El pago de impuestos al César 13. Sin embargo, más tarde enviaron a varios de los fariseos y de los herodianos, para que, hablando con él, trataran de hacerle decir alguna cosa por la que pudieran condenarle. 14. Se llegaron, pues, a Jesús, y le dijeron:
Maestro, sabemos que tú no mientes jamás, y que no te preocupa el qué dirán, sino que te dedicas a enseñar con toda verdad los caminos de Dios. Dinos, pues, ¿es de ley que nosotros, los judíos, le paguemos tributo al César? ¿Debemos hacerlo, o no? 15. Jesús, comprendiendo al punto la hipocresía con que le hacían la pregunta, les dijo: 
¿Por qué tratáis de ponerme a prueba? Traed acá una moneda y mostrádmela. 16. Cuando se la mostraron, preguntó: 
¿De quién dice la inscripción que es esta imagen?Respondieron:
De César. 17. Les dijo:
Pues bien, dadle a César lo que es de César, y a Dios lo que es de Dios. Ellos, admirados de la respuesta, se fueron.

El matrimonio en la resurrección 18. Pero más tarde se le acercaron los saduceos, que no creían en la resurrección. Estos le plantearon una nueva cuestión, diciendo: 19. Maestro, Moisés nos dejó escrito que, si un hombre muere dejando esposa pero sin haber tenido hijos, su hermano deberá casarse con la viuda para dar descendencia al hermano fallecido. 20. Pues bien, hubo una vez siete hermanos, el mayor de los cuales se casó, pero murió sin dejar descendencia. 21. Entonces, el segundo hermano se casó con la viuda, pero también murió sin haber tenido hijos. Lo mismo pasó con el tercero, 22. y así, los siete se casaron con ella, pero todos murieron sin descendencia; y la última en morir fue la mujer. 23. Nuestra pregunta ahora es la siguiente: En la resurrección, cuando ya todos hayan resucitado, ¿de cuál de los hermanos será ella esposa, habiendo estado casada con los siete? 24. Jesús les respondió: 
Lo que a vosotros os pasa es que os equivocáis, porque no conocéis debidamente las Escrituras ni el poder de Dios. 25. Pues cuando esos siete hermanos y la mujer resuciten de los muertos, ni ellos se casarán ni ella será dada en casamiento, sino que serán como los ángeles del cielo. 26. Y en cuanto a si hay o no resurrección de los muertos, ¿no habéis leído en el libro de Moisés lo que Dios le dijo desde la zarza en llamas: "Yo soy el Dios de Abraham, Dios de Isaac y Dios de Jacob"? 27. Pues bien, Dios no es Dios de muertos, sino de vivos. ¡Así que estáis totalmente equivocados!

El mandamiento más importante 28. Un escriba que los había oído disputar, y que sabía que Jesús había respondido bien, le preguntó: 
De todos los mandamientos, ¿cuál es el más importante? 29. Jesús le respondió:
El primero y más importante de todos los mandamientos es: "Oye, Israel, el Señor nuestro Dios es el único Dios. 30. Amarás, pues, al Señor tu Dios con todo tu corazón, con toda tu alma, con toda tu mente y con todas tus fuerzas". 31. Pero hay un segundo que le es semejante: "Amarás a tu prójimo como te amas a ti mismo". No hay ningún mandamiento superior a estos. 32. El escriba le dijo: 
Maestro, tienes razón. Es cierto lo que dices: Dios es único, y no hay otro fuera de él. 33. Y mucho más importante que ofrecer sacrificios y holocaustos sobre el altar, es amar a Dios con todo el corazón, con toda la inteligencia y con todas las fuerzas, y amar al prójimo como nos amamos a nosotros mismos. 34. Al oir aquellas palabras llenas de sabiduría, Jesús le dijo: 
No estás lejos del reino de Dios. Después de esto, nadie se atrevía a hacerle preguntas.

¿De quién es hijo el Cristo? 35. En otro momento, Jesús, que estaba enseñando en el templo, suscitó esta cuestión:
¿Cómo afirman los escribas que el Cristo ha de ser hijo del rey David? 36. Porque el propio David, inspirado por el Espíritu Santo, escribió:"Dijo el Señor a mi Señor: 
