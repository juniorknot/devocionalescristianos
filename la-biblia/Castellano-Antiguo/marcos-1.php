Jesús expulsa a un espíritu maligno 21. Llegaron a Cafarnaum, donde los sábados entraba Jesús en la sinagoga y se ponía a enseñar. 22. Los que le escuchaban se admiraban de su enseñanza, porque les hablaba como alguien que goza de toda autoridad, y no como los escribas. 23. En la sinagoga había un hombre poseído por un espíritu impuro, que empezó a gritar: 24. ¿Por qué nos molestas, Jesús de Nazaret? ¿Has venido a destruirnos? ¡Yo sé que tú eres el santo Hijo de Dios! 25. Jesús le ordenó:
¡Cállate y sal de ese hombre! 26. Al punto, el espíritu impuro salió de él, haciéndole gritar y sacudiéndolo con violentas convulsiones. 27. Tan asombrados quedaron todos los que lo vieron, que luego comentaban:
¿Qué es esto? ¿Qué nueva enseñanza es ésta, y qué autoridad tiene ese hombre, que hasta los espíritus impuros le obedecen? 28. Con la noticia de lo sucedido, la fama de Jesús se difundió rápidamente por toda la región de Galilea.

Jesús sana a muchos enfermos 29. Al salir de la sinagoga se dirigió, junto con Jacobo y Juan, a casa de Simón y Andrés. 30. La suegra de Simón estaba en cama, postrada por la fiebre; Jesús, al saberlo, 31. se acercó a la enferma, la tomó de la mano y la incorporó. En el mismo momento le desapareció la fiebre, y se puso a servirles la mesa. 32. Al atardecer, después de ponerse el sol, la casa se llenó de enfermos y endemoniados que la gente llevaba a Jesús, para que los sanara. 33. La gente de la ciudad, agolpándose ante la puerta, 34. pudo presenciar cómo curaba de diversos males a muchos que estaban enfermos, y cómo expulsaba a multitud de demonios. Pero a los demonios que expulsaba, Jesús no les permitía que hablaran de él y revelasen quién era.

Jesús ora en un lugar solitario 35. A la mañana siguiente, estando todavía muy oscuro, se levantó y se fue a un lugar desierto, a orar a solas. 36. Más tarde salieron en su busca Simón y los demás, 37. y cuando le encontraron le dijeron: 
La gente te anda buscando. 38. Él les respondió: 
Vámonos a otra parte, a las ciudades vecinas, para predicar también allí el mensaje, porque para esto he venido. 39. De ese modo recorrieron toda Galilea, predicando él en las sinagogas y expulsando de muchas personas los demonios que las poseían.

