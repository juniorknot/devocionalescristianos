                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 Y uno de ellos hirió a un siervo del sumo sacerdote, cortándole de un tajo la oreja derecha. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 
¡Dejadlo! ¡Ya basta! 
dijo Jesús, y tocando el sitio de la oreja de aquel hombre, se la restauró. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 Luego preguntó a los principales sacerdotes, a los jefes de la guardia del templo y a los ancianos que dirigían la turba:
¿Tan temible ladrón soy, que con espadas y con palos habéis venido a prenderme? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 Y puesto que todos los días estuve con vosotros en el templo, ¿por qué no me arrestasteis allí? Pero ya sé que esta hora es la vuestra, la hora del poder de las tinieblas.

Pedro niega a Jesús </p>
                                                    <h4><strong>Pedro niega a Jesús</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 Condujeron a Jesús a la casa del sumo sacerdote. Pedro los seguía de lejos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 Como la noche era fría, los soldados hicieron una hoguera en medio del patio, y se sentaron alrededor de ella. También Pedro se sentó con ellos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 En esto, una criada le vio allí, calentándose al fuego; "y de pronto, fijándose en él, "dijo: 
Este hombre andaba con Jesús. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 Pedro lo negó:
¡Mujer, yo ni siquiera le conozco! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
 Poco después vio otra persona a Pedro, y dijo también:
¡Tú eres uno de ellos! 
¡No, señor! ¡Yo no lo soy! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">59. </strong>
 Luego, como una hora más tarde, alguien afirmó:
No hay duda de que este es un discípulo de Jesús. Además es galileo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">60. </strong>
 Pedro lo negó nuevamente:
¡Hombre, no sé de qué estás hablando! 
pero mientras decía esto, cantó un gallo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">61. </strong>
 En ese mismo instante volvió el Señor la cabeza y miró a Pedro; y Pedro se acordó de las palabras del Señor: "Antes que el gallo cante, me habrás negado tres veces". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">62. </strong>
 Pedro salió entonces de allí, y lloró con gran amargura.

Los soldados se burlan de Jesús </p>
                                                    <h4><strong>Jesús escarnecido y azotado</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">63. </strong>
 Los hombres encargados de custodiar a Jesús se burlaban de él, y le daban de golpes. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">64. </strong>
 Le vendaron los ojos y comenzaron a pegarle en el rostro, mientras decían:
¡Profeta, a ver quién te ha pegado! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">65. </strong>
 Y no cesaban de insultarle.

Jesús ante Pilato y Herodes </p>
                                                    <h4><strong>Jesús ante el concilio</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">66. </strong>
 Al llegar el día se reunieron en concilio los dirigentes del pueblo con los principales sacerdotes y los escribas. Llevaron a Jesús ante ellos, y le preguntaron: </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">67. </strong>
 
Dinos si tú eres el Cristo.Les contestó:
Si yo os digo que sí, no me vais a creer; </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">68. </strong>
 y si os hago una pregunta, ni me vais a responder ni me vais a soltar. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">69. </strong>
 Pero ya viene la hora en que el Hijo del hombre se ha de sentar a la derecha del poder de Dios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">70. </strong>
 
¿Luego tú eres el Hijo de Dios? 
dijeron a una voz. 
Vosotros habéis dicho que lo soy. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">71. </strong>
 Entonces gritaron:
¡No tenemos necesidad de más testigos, puesto que nosotros lo hemos oído de su propia boca! </p>
                                                        
