El adulterio 27. Sabéis que también se dijo: "No cometerás adulterio". 28. Pero yo os digo: Cualquiera que mira con codicia a una mujer, ya adultera con ella en el fondo del corazón. 29. Por tanto, si tu ojo derecho es causa de que caigas en pecado, más vale que lo saques y lo eches de ti; porque es mejor que pierdas una parte de tu cuerpo, y no que todo él sea arrojado al infierno. 30. Y si tu mano derecha es causa de que caigas en pecado, más vale que la cortes y la eches de ti; porque es mejor que pierdas una parte de tu cuerpo, y no que todo él sea arrojado al infierno.

