Ramas injertadas 11. ¿Significa esto que los de Israel han tropezado y caído para siempre? ¿Que nunca más volverán a levantarse? ¡De ninguna manera! Pero sí es cierto que, valiéndose de su tropiezo, Dios ha puesto la salvación al alcance de los gentiles; y de este modo ha provocado los celos de los judíos, para que también ellos se afanen por obtenerla. 12. Ahora bien, si el tropiezo y la caída de los judíos han traído la gran riqueza de la salvación a los gentiles del mundo entero, ¿cuánto más beneficioso no será el hecho de que los judíos se conviertan a la fe de Cristo? 13. Me estoy dirigiendo a vosotros, los que no sois judíos, porque he sido constituido apóstol para trabajar entre vosotros y honrar con ese trabajo mi ministerio. 14. Además quisiera provocar a celos a los de mi propia sangre y raza, por ver si algunos de ellos logran alcanzar la salvación. 15. Porque tened esto presente: si el haber desechado Dios a Israel como su pueblo significó para los demás pueblos del mundo la oferta de reconciliación con Dios, ¡cuán gloriosa no habrá de ser su plena restauración el día que acepten a Cristo! Será como una resurrección, como el retorno de los muertos a la vida. 16. Mirad, pues, que si una porción de la masa preparada para hacer pan fue consagrada a Dios, la masa entera quedó consagrada. Igualmente, si las raíces de un árbol son santas, también las ramas lo son. 17. Pues bien, imaginemos que Israel es un olivo del que algunas de sus ramas fueron desgajadas. En lugar de ellas, y como si tú fueras una rama de olivo silvestre, se te injertó a ti, que no eres judío, para que pudieras participar del vigor de las raíces y de la misma savia que nutre al olivo auténtico. Pues ahora piensa que, por cuanto así se hizo contigo, 18. tú no debes jactarte de tu posición ni hacer menosprecio de las ramas desgajadas a las que viniste a sustituir, porque no eres tú quien sustenta a las raíces, sino que son las raíces las que te sustentan a ti. 19. Quizá te estés diciendo: "Esas ramas fueron desgajadas para injertarme a mí". 20. Si eso piensas, ten cuidado; y recuerda que tales ramas (los judíos) fueran desgajadas a causa de su incredulidad, y que tú ocupas su lugar porque tienes fe. No permitas, pues, que la soberbia crezca en ti, sino sé humilde y teme a Dios; 21. porque si Dios no perdonó, sino que cortó las primeras ramas del olivo, a ti tampoco te perdonará. 22. Mira que Dios es al mismo tiempo bondadoso y severo. Fue severo con los que cayeron y le desobedecieron; y es bondadoso contigo, si no te apartas de su benevolencia, porque si te apartas, también a ti te cortará. 23. Por otro lado, si los judíos se volvieran de su falta de fe, Dios es poderoso para injertarlos nuevamente en el olivo del que fueron desgajados. 24. Pues si a ti, que fuiste cortado de un olivo silvestre, Dios, actuando en contra de lo normal, te injertó en el olivo auténtico, con mucha más razón volverá a injertar a los judíos en el propio olivo auténtico del que fueron cortados.

