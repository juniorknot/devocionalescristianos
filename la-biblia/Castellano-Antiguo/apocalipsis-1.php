<h2 class="text-center"> Apocalipsis 1 - Castellano Antiguo (Nuevo Testamento)</h2>
<h4><strong>La revelación de Jesucristo</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">1. </strong>
Este es el libro de la revelación que Dios confió a Jesucristo para que mostrase a sus siervos las cosas que pronto han de acontecer. Jesucristo envió su ángel a su siervo Juan, para dársela a conocer, </p>
<p style="style=text-align: justify;"><strong id="numeros">2. </strong>
y Juan ha testificado de la palabra de Dios, del testimonio de Jesucristo y de todas las cosas que vio y oyó. </p>
<p style="style=text-align: justify;"><strong id="numeros">3. </strong>
Bendito el que lee esta profecía y benditos los que la escuchan y la guardan, porque la hora de su cumplimiento ya está cerca.
Saludos y doxología </p>
<h4><strong>Salutaciones a las siete iglesias</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">4. </strong>
Juan, a las siete iglesias de la provincia de Asia. Que sobre vosotros, hermanos míos, repose la gracia y la paz de Dios, el que es y era y ha de venir, de los siete espíritus que están delante de su trono, </p>
<p style="style=text-align: justify;"><strong id="numeros">5. </strong>
y de Jesucristo, el fiel testigo de la verdad. Él ha sido el primero en levantarse de los muertos para no volver a morir, y su soberanía se extiende sobre todos los reyes de la tierra. Movido por su amor infinito lavó con su sangre nuestros pecados, </p>
<p style="style=text-align: justify;"><strong id="numeros">6. </strong>
para hacer de nosotros un reino y sacerdotes para Dios, su Padre. ¡A él sea la gloria y el imperio por toda la eternidad! Amén. </p>
<p style="style=text-align: justify;"><strong id="numeros">7. </strong>
Mirad, él viene entre las nubes, ante los ojos de todos. Le verán incluso aquellos que le traspasaron. Todas las naciones se lamentarán por él. ¡Sí, amén! </p>
<p style="style=text-align: justify;"><strong id="numeros">8. </strong>
Yo soy el alfa y la omega, el principio y el fin de todas las cosas
dice Dios el Señor, el Todopoderoso, el que es y era y ha de venir.
Alguien semejante al Hijo del hombre </p>
<h4><strong>Una visión del Hijo del Hombre</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">9. </strong>
Yo, Juan, vuestro hermano y compañero en los sufrimientos, en el reino de Dios y en la paciente espera de Jesucristo, me encontraba en la isla de Patmos, desterrado por predicar la palabra de Dios y testificar de Jesucristo. </p>
<p style="style=text-align: justify;"><strong id="numeros">10. </strong>
Un domingo, estando yo en comunión con el Espíritu, oí detrás de mí una voz, vibrante como un toque de trompeta, </p>
<p style="style=text-align: justify;"><strong id="numeros">11. </strong>
que me decía:
Escribe en un libro las cosas que vas a ver, y envíalo a las siete iglesias de la provincia de Asia: Éfeso, Esmirna, Pérgamo, Tiatira, Sardis, Filadelfia y Laodicea. </p>
<p style="style=text-align: justify;"><strong id="numeros">12. </strong>
Me volví para ver de quién era aquella voz que me hablaba, y vi siete lámparas de oro. </p>
<p style="style=text-align: justify;"><strong id="numeros">13. </strong>
En medio de las lámparas, vestido con una túnica ceñida a la altura del pecho por un cinturón de oro, había uno semejante a un ser humano. </p>
<p style="style=text-align: justify;"><strong id="numeros">14. </strong>
Tenía la cabeza cubierta de pelo blanco, como la lana blanca, como la nieve; sus ojos eran penetrantes como llamas de fuego; </p>
<p style="style=text-align: justify;"><strong id="numeros">15. </strong>
los pies le refulgían como hechos de bronce bruñido, y su voz retumbaba como las olas cuando rompen contra el acantilado. </p>
<p style="style=text-align: justify;"><strong id="numeros">16. </strong>
Con la mano derecha sostenía siete estrellas, de la boca le salía una espada aguda de doble filo y el rostro le brillaba con el fulgor del sol cuando está en toda su fuerza. </p>
<p style="style=text-align: justify;"><strong id="numeros">17. </strong>
Al verle, caí como muerto a sus pies; pero él, poniendo su mano derecha sobre mí, me dijo:
¡No temas! Yo soy el primero y el último, </p>
<p style="style=text-align: justify;"><strong id="numeros">18. </strong>
y vivo, aunque estuve muerto. Vivo por toda la eternidad, y tengo en mi poder las llaves del infierno y de la muerte. </p>
<p style="style=text-align: justify;"><strong id="numeros">19. </strong>
Escribe las cosas que has visto, las que ahora están sucediendo y las que sucederán después, en el futuro. </p>
<p style="style=text-align: justify;"><strong id="numeros">20. </strong>
El significado de las siete estrellas que tengo en mi mano derecha, y de las siete lámparas de oro es este: las siete estrellas son los ángeles de las siete iglesias, y las siete lámparas son las siete iglesias. </p>
