¿Pero cómo 
replicaron
, si aquí sólo tenemos cinco panes y dos peces? ¿O esperas acaso que vayamos a comprar comida para esta muchedumbre? 14. Porque contando únicamente a los hombres, eran unos cinco mil. Sin embargo, Jesús ordenó:
Decid a todos que se sienten, repartidos en grupos de cincuenta personas. 15. Los discípulos obedecieron, y cuando la gente se hubo sentado, 16. Jesús tomó los cinco panes y los dos peces, y mirando al cielo los bendijo, los partió y se los dio a los discípulos para que ellos los distribuyeran entre la multitud; 17. y todos comieron hasta saciarse. Luego recogieron los trozos que habían sobrado, y con ellos llenaron doce cestas.

La confesión de Pedro 18. Otro día aconteció que Jesús estaba aparte, entregado a la oración; pero cerca de él se encontraban los discípulos. En cierto momento les preguntó:
¿Quién dice la gente que soy yo?Le respondieron: 19. Pues hay quienes dicen que eres Juan el Bautista; otros dicen que eres Elías o alguno de los antieguos profetas que ha resucitado. 20. Y vosotros, ¿quién decís que soy?
¡Tú eres el Cristo de Dios!"
respondió Pedro. 21. Pero Jesús les encargó severamente que no dijesen esto a nadie, 22. y añadió:
Primero es necesario que el Hijo del hombre padezca mucho; que los dirigentes judíos, los principales sacerdotes y los maestros de la ley le desprecien y lo lleven a la muerte, y que resucite al tercer día. 23. Después, dirigiéndose a todos, siguióq diciendo:
Todo aquel que quiera seguirme deberá renunciar a los mayores anhelos de su vida, y tendrá que cargar con la cruz de cada día. 24. Cualquiera que pretenda salvar su vida, la perderá; en cambio, el que por causa mía pierda su vida, la salvará. 25. Porque si alguien se está destruyendo a sí mismo, ¿de qué le servirá ganarse el mundo entero? 26. De aquellos que se avergüencen de mí y de mis palabras, se avergonzará también el Hijo del hombre cuando venga en su gloria y en la gloria del Padre y de los santos ángeles. 27. Por otra parte, os diré que algunos de los que están aquí no morirán sin haber visto el reino de Dios.

La transfiguración 28. Pasados unos ocho días de estas cosas, Jesús se hizo acompañar de Pedro, Juan y Jacobo, y subió con ellos a un monte a orar. 29. Mientras estaba orando, su rostro cambió de aspecto, y su ropa se hizo más blanca y resplandeciente. 30. Dos hombres, aparecieron a su lado, conversando con él; eran Moisés y Elías, 31. que estaban rodeados de un resplandor glorioso y hablaban de la muerte de Jesús, la cual había de acontecer en Jerusalén conforme al plan de Dios. 32. Pedro y los otros discípulos estaban rendidos de sueño, pero haciendo esfuerzos por permanecer despiertos vieron a Jesús envuelto en un brillo glorioso, y a los dos hombres que estaban a su lado. 33. En esto, cuando Moisés y Elías comenzaban a apartarse de Jesús, Pedro le dijo: 
¡Maestro, qué bueno es que estemos aquí! Si te parece, haremos tres enramadas, una para ti, otra para Moisés y otra para Elías.Pero lo cierto es que Pedro estaba muy turbado y no sabía lo que decía; 34. y no había terminado de hablar a Jesús, cuando los envolvió una nube resplandeciente, y el miedo se apoderó de ellos. 35. En aquel momento salió de la nube una voz, que decía:
Este es mi Hijo amado: escuchadle a él. 36. Al dejar de oírse la voz ya no vieron a nadie, sino tan sólo a Jesús. Acerca de lo ocurrido guardaron silencio por algún tiempo los tres discípulos, y no se lo contaron a nadie.

Jesús sana a un muchacho endemoniado 37. Al día siguiente, cuando bajaron de la montaña, una gran multitud vino a su encuentro. 38. Y de entre todos los que allí estaban salió la voz de un hombre, que clamaba:
¡Maestro, mira a mi hijo! Es el único que tengo, 39. pero hay un espíritu maligno que se apodera de él y le hace gritar, sacudirse con violencia, echar espuma por la boca y golpearse contra cualquier cosa. Apenas se aparta de él, 40. y aunque he rogado a tus discípulos que lo expulsen, no han podido hacer nada.Jesús respondió, diciendo: 41. Generación incrédula y perversa, ¿hasta cuándo tendré que estar con vosotros y soportaros? Tráeme acá al muchacho. 42. Mientras el muchacho se acercaba, el demonio lo derribó a tierra en medio de violentas convulsiones. Jesús increpó a aquel espíritu impuro, sanó al muchacho y se lo devolvió a su padre. 43. Una vez más, la gente quedó asombrada ante tal demostración de la grandeza de Dios, y estaban maravillados de las cosas que hacía Jesús, que entonces habló así a sus discípulos: 44. Escuchadme bien y no olvidéis esto que os digo: el Hijo del hombre será traicionado y entregado a los poderes humanos. 45. Pero ellos no le comprendieron, porque tenían velado el entendimiento. Además temían hacerle preguntas.

¿Quién va a ser el más importante? 46. Un día se pusieron a discutir unos con otros sobre cuál de ellos sería el más importante en el reino de Dios. 47. Jesús, que se dio cuenta de lo que había en lo íntimo de sus corazones, tomó a un niño, lo puso a su lado y les dijo: 48. Cualquiera que en nombre mío se preocupa de un niño como este, de mí mismo se preocupa; y el que de mí se preocupa, se preocupa del Padre, que me envió. ¿Queréis que os diga quién es el más importante entre todos vosotros? El más importante es el que a sí mismo se tiene por menos importante. 49. Juan, acercándose entonces a Jesús, le dijo:
Maestro, hemos visto a uno que expulsaba demonios en tu nombre; pero como no era de los nuestros, se lo hemos prohibido. 50. Jesús le dijo:
No se lo prohibáis, porque el que no está en contra nuestra, está a nuestro favor.

La oposición de los samaritanos 51. Cuando ya se cumplía el tiempo en que Jesús había de regresar al cielo, emprendió decididamente el camino a Jerusalén. 52. Envió delante de él unos mensajeros con el encargo de buscarle alojamiento, los cuales entraron en una aldea samaritana; pero allí no quisieron recibirle, porque no querían tener relación con nadie que se dirigiese a Jerusalén. 53. Al enterarse de esto, Jacobo y Juan se irritaron y dijeron a Jesús:
Señor, si quieres, mandaremos que baje fuego del cielo, como hizo Elías, para que los consuma. 55. Pero Jesús se volvió hacia ellos y los reprendió. 56. Luego se dirigieron a otra aldea.

Lo que cuesta seguir a Jesús 57. Mientras iban de camino, se acercó uno y le dijo:
Señor, yo deseo seguirte adondequiera que vayas. 58. Está bien 
respondió Jesús
, pero piensa que las zorras tienen cubiles y las aves nidos, mientras que el Hijo del hombre ni siquiera tiene un sitio donde reclinar la cabeza. 59. A otro hombre, Jesús lo invitó a seguirle, y él le respondió:
Sí. Señor, yo te seguiré; pero déjame que vaya primero a enterrar a mi padre. 60. Jesús le contestó:
Deja que los muertos entierren a sus muertos, y tú ven y anuncia conmigo el reino de Dios. 61. Otro le dijo:
