 Mientras el muchacho se acercaba, el demonio lo derribó a tierra en medio de violentas convulsiones. Jesús increpó a aquel espíritu impuro, sanó al muchacho y se lo devolvió a su padre. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 Una vez más, la gente quedó asombrada ante tal demostración de la grandeza de Dios, y estaban maravillados de las cosas que hacía Jesús, que entonces habló así a sus discípulos: </p>
                                                    <h4><strong>Jesús anuncia otra vez su muerte</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 
Escuchadme bien y no olvidéis esto que os digo: el Hijo del hombre será traicionado y entregado a los poderes humanos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 Pero ellos no le comprendieron, porque tenían velado el entendimiento. Además temían hacerle preguntas.

¿Quién va a ser el más importante? </p>
                                                    <h4><strong>¿Quién es el mayor?</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 Un día se pusieron a discutir unos con otros sobre cuál de ellos sería el más importante en el reino de Dios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
 Jesús, que se dio cuenta de lo que había en lo íntimo de sus corazones, tomó a un niño, lo puso a su lado y les dijo: </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 
Cualquiera que en nombre mío se preocupa de un niño como este, de mí mismo se preocupa; y el que de mí se preocupa, se preocupa del Padre, que me envió. ¿Queréis que os diga quién es el más importante entre todos vosotros? El más importante es el que a sí mismo se tiene por menos importante. </p>
                                                    <h4><strong>El que no es contra nosotros, por nosotros es</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 Juan, acercándose entonces a Jesús, le dijo:
Maestro, hemos visto a uno que expulsaba demonios en tu nombre; pero como no era de los nuestros, se lo hemos prohibido. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 Jesús le dijo:
No se lo prohibáis, porque el que no está en contra nuestra, está a nuestro favor.

La oposición de los samaritanos </p>
                                                    <h4><strong>Jesús reprende a Jacobo y a Juan</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 Cuando ya se cumplía el tiempo en que Jesús había de regresar al cielo, emprendió decididamente el camino a Jerusalén. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 Envió delante de él unos mensajeros con el encargo de buscarle alojamiento, los cuales entraron en una aldea samaritana; pero allí no quisieron recibirle, porque no querían tener relación con nadie que se dirigiese a Jerusalén. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 Al enterarse de esto, Jacobo y Juan se irritaron y dijeron a Jesús:
Señor, si quieres, mandaremos que baje fuego del cielo, como hizo Elías, para que los consuma. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 Pero Jesús se volvió hacia ellos y los reprendió. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 Luego se dirigieron a otra aldea.

Lo que cuesta seguir a Jesús </p>
                                                    <h4><strong>Los que querían seguir a Jesús</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 Mientras iban de camino, se acercó uno y le dijo:
Señor, yo deseo seguirte adondequiera que vayas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
 
Está bien 
respondió Jesús
, pero piensa que las zorras tienen cubiles y las aves nidos, mientras que el Hijo del hombre ni siquiera tiene un sitio donde reclinar la cabeza. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">59. </strong>
 A otro hombre, Jesús lo invitó a seguirle, y él le respondió:
Sí. Señor, yo te seguiré; pero déjame que vaya primero a enterrar a mi padre. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">60. </strong>
 Jesús le contestó:
Deja que los muertos entierren a sus muertos, y tú ven y anuncia conmigo el reino de Dios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">61. </strong>
 Otro le dijo:
Sí, Señor, te seguiré; pero permíteme ir primero a despedirme de mi familia. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">62. </strong>
 A éste le respondió Jesús:
El que pone la mano en el arado y vuelve atrás la mirada, no es apto para el reino de Dios. </p>
                                                        
