No llevéis para el camino ni bordón ni alforja, ni pan ni dinero. Tampoco llevéis otra ropa que la puesta. 4. Hospedaos en una sola casa en cada pueblo que visitéis. 5. Y si en algún lugar no quieren recibiros, salid de allí sacudiendo el polvo de vuestros pies, para que la gente tenga constancia de vuestra protesta.Fueron, pues, los discípulos de aldea en aldea, predicando el evangelio y sanando en todas partes a los enfermos. 7. Al llegar a conocimiento del tetrarca Herodes los milagros que hacía Jesús y los comentarios que suscitaban entre la gente, se sintió preocupado y perplejo. Porque unos decían: "Es Juan, que ha resucitado de entre los muertos"; 8. otros: "Es Elías, que se ha aparecido", y otros: "Seguro que ha resucitado alguno de los antiguos profetas". 9. Herodes, confuso al oir tales rumores, trataba de ver a Jesús, mientras se decía:
Yo ordené que decapitasen a Juan... ¿quién, pues, es éste, de quien se cuentan cosas tan extraordinarias?

Jesús alimenta a los cinco mil 10. Cuando los apóstoles regresaron, informaron a Jesús acerca del viaje que habían hecho. Luego se fue con ellos a un lugar apartado, próximo a la ciudad de Betsaida. 11. Pero la gente supo que estaba allí, y le siguió. Él los recibió a todos, y de nuevo les habló del reino de Dios y sanó a los enfermos. 12. Ya avanzada la tarde, se le acercaron los doce discípulos y le sugirieron:
Despide a la gente, para que puedan ir a los pueblos y campos de alrededor a buscar comida y alojamiento, porque aquí estamos en un lugar desierto. 13. Dadles vosotros de comer 
