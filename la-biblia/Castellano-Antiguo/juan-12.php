Jesús predice su muerte 20. Entre los forasteros que habían subido a Jerusalén para la fiesta de la Pascua, había también unos griegos, 21. que se dirigieron a Felipe, que era natural de Betsaida de Galilea, y le dijeron: 
Señor, quisiéramos conocer a Jesús. 22. Felipe se lo dijo a Andrés, y luego fueron los dos juntos a decírselo a Jesús. 23. Él les respondió que ya había llegado la hora en que el Hijo del hombre tenía que ser glorificado, 24. y que habría de morir, lo mismo que ha de morir el grano de trigo que cae en el surco. 
