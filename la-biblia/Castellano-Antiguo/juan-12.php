Esa voz no ha venido por causa mía, sino por vuestra causa. 31. Al mundo le ha llegado ya la hora del juicio, y a Satanás, el príncipe de este mundo, la hora de su derrota. 32. Cuando yo sea alzado de la tierra, atraeré a todos a mí. 33. Con estas palabras estaba dando a entender de qué forma había de morir. 34. La gente entonces le decía:
¿Pero es que vas a morir? Nosotros, gracias a la ley, tenemos entendido que el Cristo no morirá, sino que vivirá para siempre. ¿Cómo, pues, dices que es necesario que el Hijo del hombre sea alzado? ¿A quién te refieres al hablar del Hijo del hombre? 35. Jesús les respondió:
Todavía brillará la luz entre vosotros por algún tiempo. Mientras tengáis luz, caminad en ella para que la oscuridad no os sorprenda, porque el que camina envuelto en la oscuridad, no puede reconocer el camino. 36. Mientras la luz esté con vosotros, confiad en la luz, para que seáis hechos verdaderos portadores de la luz.

