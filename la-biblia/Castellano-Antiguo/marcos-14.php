<li class="hide-for-large"><a href="whatsapp://send?text=Marcos 14 (CST)%0A%0A 1. Dos días después comenzó la pascua, y con ella la fiesta en que se comían los panes sin levadura. Los principales sacerdotes y escribas buscaban la manera de tenderle al/guna trampa a Jesús, para arrestarlo y matarlo. 2. Pero decían:
No lo hagamos durante la pascua, porque podría provocarse una algarada popular. 3. Jesús se encontraba entonces en Betania, en casa de un tal Simón a quien llamaban "el leproso". Estando sentado a la mesa, se acercó a Jesús una mujer con un frasco de alabastro que contenía un perfume de nardo muy caro. Abrió el frasco y le derramó el perfume en la cabeza. 4. Algunos de los presentes se indignaron, y decían murmurando contra la mujer:
¡Vaya un derroche de perfume! 5. ¡Podíamos haberlo vendido por trescientos denarios y haber repartido el dinero entre los pobres! 6. Pero Jesús les dijo:
Dejadla, ¿por qué la mortificáis? Lo que ella ha hecho, bien hecho está, 7. porque a los pobres los tendréis siempre con vosotros y podréis ayudarlos cuando queráis; pero a mí no vais a tenerme por mucho tiempo. 8. Esta mujer ha hecho lo que ha podido, y se ha anticipado a ungir mi cuerpo para la sepultura. 9. Además os aseguro que, después de esto, dondequiera que se anuncien las buenas noticias del reino de Dios se recordará y ensalzará lo hecho por esta mujer. 10. Entonces Judas Iscariote, uno de los doce discípulos, se dirigió a los principales sacerdotes y "les propuso entregarles a Jesús. 11. Ellos, al escuchar a Judas, se llenaron de alegría y prometieron recompensarle con una cantidad de dinero; y Judas, desde aquel momento, comenzó a buscar la ocasión idónea para llevar a cabo su traición.

La Cena del Señor 12. El primer día de la fiesta de los panes sin levadura, es decir, el día en que se sacrificaban los corderos para la pascua, los discípulos preguntaron a Jesús dónde quería celebrar la cena de ritual. 13. Él, a fin de hacer los preparativos necesarios, envió dos discípulos a Jerusalén, y les dijo:
Cuando lleguéis a la ciudad, saldrá a vuestro encuentro un hombre que lleva un cántaro de agua. Seguidle, 14. y en la casa donde entre decidle al dueño: "El Maestro pregunta: ¿Cuál es el aposento donde he de comer la cena pascual en compañía de mis discípulos?" 15. El dueño os llevará a una gran sala en la parte alta de la casa. Preparad allí la cena para todos nosotros. 16. Fueron, pues, los dos discípulos y entraron en la ciudad. Todo sucedió como Jesús había dicho, y ellos prepararon la pascua. 17. Al llegar la noche se presentó Jesús con los restantes discípulos. 18. Se sentaron todos a la mesa, y ya habían empezado a comer cuando, de pronto, les anunció: 
Os digo que, ciertamente, uno de vosotros que estáis aquí, comiendo conmigo, me va a entregar. 19. Una profunda tristeza invadió a los discípulos, que uno tras otro comenzaron a preguntarle: 
Maestro, ¿seré yo? 20. Él, respondiendo, les dijo: 
Uno de vosotros doce, uno que moja conmigo su pan en el plato, será quien me entregue. 21. Y lo cierto es que el Hijo del hombre va a morir, como hace mucho tiempo declararon los profetas. Pero ¡ay de aquél que traiciona al Hijo del hombre! ¡Más le valiera no haber nacido! 22. Mientras comían, Jesús tomó un trozo de pan, lo bendijo, lo partió y se lo dio a ellos, diciendo: 
Comed, esto es mi cuerpo. 23. Luego tomó una copa de vino y, después de dar gracias, se la pasó a los discípulos. Todos bebieron de ella, y les dijo: 24. Esto es mi sangre, que va a ser derramada en favor de muchos, para sellar el nuevo pacto de Dios con el hombre. 25. Ya jamás volveré a beber de este vino, producto de la vid, hasta el día en que lo beba nuevo en el reino de Dios. 26. Después de haber cantado el himno que ponía fin a la cena, salieron de la casa y se fueron al monte de los Olivos. Allí les dijo Jesús:

Jesús predice la negación de Pedro 27. Esta noche os apartaréis todos de mí. Así lo anuncia la Escritura: "Mataré al pastor y se dispersarán las ovejas". 28. Pero después, cuando haya resucitado, me dirigiré a Galilea y allí os esperaré. Pedro le dijo: 29. ¡Aunque todos se aparten de ti, yo nunca te abandonaré!Le respondió Jesús: 30. Pedro, esta noche, antes que el gallo cante por segunda vez, me negarás tres veces. Protestó Pedro con gran vehemencia: 31. ¡Jamás! ¡Aunque haya de morir contigo, no te negaré! Y los demás aseguraban lo mismo.

Getsemaní 32. En su camino a Jerusalén llegaron a un lugar llamado Getsemaní, y allí dijo él a sus discípulos:
Sentaos aquí, mientras yo voy a orar. 33. Se llevó consigo a Pedro, Jacobo y Juan, y ya a solas con ellos comenzó a entristecerse y a angustiarse profundamente. 34. Les dijo:
Siento en el alma una tristeza mortal. Quedaos aquí y velad conmigo. 35. Se adelantó después unos pasos, y postrado en tierra oró que, a ser posible, no llegara a sucederle lo que él estaba temiendo. 36. Decía en su oración:
Padre, tú que todo lo puedes aparta de mí esta copa de amargura. Pero no sea lo que yo quiero, sino lo que quieres tú. 37. Regresó después adonde estaban los tres discípulos, y los halló dormidos. Hablando directamente a Pedro, dijo:
Simón, ¿sigues durmiendo? ¿Ni siquiera una hora has sido capaz de velar conmigo? 38. ¡Velad y orad, para que no os venza la tentación! Ciertamente tenéis dispuesto el espíritu, pero vuestro cuerpo es débil. 39. Se retiró de nuevo a orar, y repitió la misma súplica. 40. Al volver, los encontró de nuevo dormidos, porque sus ojos estaban cargados de sueño y se les cerraban. Y no sabían qué responderle. 41. Regresó por tercera vez, y entonces les dijo: 
Dormid y descansad... ¡Pero ya ha llegado la hora! Mirad, ahí vienen los que han de ponerme en manos de los pecadores. 42. ¡Levantaos, vamos! ¡Aquí está el que me entrega!

Arresto de Jesús 43. Aún no había acabado de hablar, cuando Judas, uno de los doce, se presentó en compañía de una turba armada de espadas y palos, enviada por los principales sacerdotes, escribas y dirigentes judíos. 44. Judas les había dado una contraseña para identificar a Jesús:
Aquel a quien yo bese, ése es. Apresadlo, atadle con seguridad y lleváoslo. 45. Así lo hizo, en efecto. Al llegar se acercó a Jesús, y después de decirle: "¡Maestro, Maestro!", le besó. 46. Al punto se lanzaron ellos sobre Jesús, y lo arrestaron. 47. Pero uno de los que estaban con él llevaba una espada, y en ese momento tiró de ella y le cortó una oreja a un siervo del sumo sacerdote. 48. Jesús les preguntó:
¿Soy yo quizás un ladrón tan peligroso que, para prenderme, habéis tenido que venir armados de ese modo? 49. ¿Por qué no me prendisteis en el templo, donde cada día estuve enseñando entre vosotros? Pero sin duda ha de ser así, para que se cumpla lo que dicen las Escrituras. 50. En aquella hora, los discípulos huyeron atemorizados. 51. Le seguía, en cambio, un muchacho que estaba allí, envuelto en una sábana. A este lo atraparon, 52. pero el muchacho, dejando la sábana en manos de los que le sujetaban, escapó desnudo.

Jesús ante el Consejo 53. Condujeron a Jesús a la casa del sumo sacerdote, donde luego se reunieron los principales sacerdotes, los dirigentes y los escribas. 54. Pedro, que de lejos había seguido a Jesús, entró tras él hasta el patio de la residencia del sumo sacerdote; y como la noche era fría, se sentó con los alguaciles ante un fuego que habían encendido. 55. Dentro, los principales sacerdotes y todo el concilio judío reunido trataban de encontrar una acusación que bastara para condenar a muerte a Jesús. Pero no la hallaban, 56. pues incluso habiéndose presentado muchos que testificaban falsamente, sus testimonios eran contradictorios. 57. Por fin hubo unos que afirmaron, mintiendo: 58. Nosotros le hemos oído decir: "Yo voy a destruir este templo hecho por manos de hombres, y en tres días edificaré otro sin intervención humana". 59. Pero ni aun así concordaban los testimonios. 60. Sin embargo, el sumo sacerdote se puso en pie y preguntó a Jesús: 
¿No respondes a esa acusación? ¿No tienes nada que alegar contra lo que estos testifican? 61. Como Jesús no respondía, el sumo sacerdote insistió: 
¿Eres tú el Cristo, el Hijo de Dios? 62. Contestó Jesús:
Yo soy. Pero además os digo:un día veréis al "Hijo del hombre 
sentado a la derecha "del poder de Dios 
y viniendo en las nubes "del cielo. 63. Al oir esto, el sumo sacerdote se rasgó las vestiduras y exclamó:
¿Qué necesidad tenemos de más testigos? 64. ¡Ya habéis oído la blasfemia! ¿Cuál es vuestro veredicto? Todos, a una voz, lo condenaron a muerte. 65. Y unos empezaron a escupirle, mientras otros le vendaban los ojos, le daban de bofetadas y le decían: 
A ver, profeta, ¿quién te ha pegado?Y los alguaciles le golpeaban.

