                                                                                <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
 Decía en su oración:
Padre, tú que todo lo puedes aparta de mí esta copa de amargura. Pero no sea lo que yo quiero, sino lo que quieres tú. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 Regresó después adonde estaban los tres discípulos, y los halló dormidos. Hablando directamente a Pedro, dijo:
Simón, ¿sigues durmiendo? ¿Ni siquiera una hora has sido capaz de velar conmigo? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 ¡Velad y orad, para que no os venza la tentación! Ciertamente tenéis dispuesto el espíritu, pero vuestro cuerpo es débil. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 Se retiró de nuevo a orar, y repitió la misma súplica. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
 Al volver, los encontró de nuevo dormidos, porque sus ojos estaban cargados de sueño y se les cerraban. Y no sabían qué responderle. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 Regresó por tercera vez, y entonces les dijo: 
Dormid y descansad... ¡Pero ya ha llegado la hora! Mirad, ahí vienen los que han de ponerme en manos de los pecadores. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 ¡Levantaos, vamos! ¡Aquí está el que me entrega!

Arresto de Jesús </p>
                                                    <h4><strong>Arresto de Jesús</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 Aún no había acabado de hablar, cuando Judas, uno de los doce, se presentó en compañía de una turba armada de espadas y palos, enviada por los principales sacerdotes, escribas y dirigentes judíos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 Judas les había dado una contraseña para identificar a Jesús:
Aquel a quien yo bese, ése es. Apresadlo, atadle con seguridad y lleváoslo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 Así lo hizo, en efecto. Al llegar se acercó a Jesús, y después de decirle: "¡Maestro, Maestro!", le besó. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 Al punto se lanzaron ellos sobre Jesús, y lo arrestaron. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
 Pero uno de los que estaban con él llevaba una espada, y en ese momento tiró de ella y le cortó una oreja a un siervo del sumo sacerdote. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 Jesús les preguntó:
¿Soy yo quizás un ladrón tan peligroso que, para prenderme, habéis tenido que venir armados de ese modo? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 ¿Por qué no me prendisteis en el templo, donde cada día estuve enseñando entre vosotros? Pero sin duda ha de ser así, para que se cumpla lo que dicen las Escrituras. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 En aquella hora, los discípulos huyeron atemorizados. </p>
                                                    <h4><strong>El joven que huyó</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 Le seguía, en cambio, un muchacho que estaba allí, envuelto en una sábana. A este lo atraparon, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 pero el muchacho, dejando la sábana en manos de los que le sujetaban, escapó desnudo.

Jesús ante el Consejo </p>
                                                    <h4><strong>Jesús ante el concilio</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 Condujeron a Jesús a la casa del sumo sacerdote, donde luego se reunieron los principales sacerdotes, los dirigentes y los escribas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 Pedro, que de lejos había seguido a Jesús, entró tras él hasta el patio de la residencia del sumo sacerdote; y como la noche era fría, se sentó con los alguaciles ante un fuego que habían encendido. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 Dentro, los principales sacerdotes y todo el concilio judío reunido trataban de encontrar una acusación que bastara para condenar a muerte a Jesús. Pero no la hallaban, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 pues incluso habiéndose presentado muchos que testificaban falsamente, sus testimonios eran contradictorios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 Por fin hubo unos que afirmaron, mintiendo: </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
 
Nosotros le hemos oído decir: "Yo voy a destruir este templo hecho por manos de hombres, y en tres días edificaré otro sin intervención humana". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">59. </strong>
 Pero ni aun así concordaban los testimonios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">60. </strong>
 Sin embargo, el sumo sacerdote se puso en pie y preguntó a Jesús: 
¿No respondes a esa acusación? ¿No tienes nada que alegar contra lo que estos testifican? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">61. </strong>
 Como Jesús no respondía, el sumo sacerdote insistió: 
¿Eres tú el Cristo, el Hijo de Dios? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">62. </strong>
 Contestó Jesús:
Yo soy. Pero además os digo:un día veréis al "Hijo del hombre 
sentado a la derecha "del poder de Dios 
y viniendo en las nubes "del cielo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">63. </strong>
 Al oir esto, el sumo sacerdote se rasgó las vestiduras y exclamó:
¿Qué necesidad tenemos de más testigos? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">64. </strong>
 ¡Ya habéis oído la blasfemia! ¿Cuál es vuestro veredicto? Todos, a una voz, lo condenaron a muerte. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">65. </strong>
 Y unos empezaron a escupirle, mientras otros le vendaban los ojos, le daban de bofetadas y le decían: 
A ver, profeta, ¿quién te ha pegado?Y los alguaciles le golpeaban.

Pedro niega a Jesús </p>
                                                    <h4><strong>Pedro niega a Jesús</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">66. </strong>
 Pedro, entre tanto, permanecía en el patio. En esto llegó una criada del sumo sacerdote </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">67. </strong>
 y vio a Pedro calentándose junto al fuego. Le miró atentamente y dijo en voz alta: 
Tú eres uno de los que andaban con Jesús el nazareno. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">68. </strong>
 Pedro lo negó rotundamente, y se fue a la entrada del patio diciendo:
¡No, no, yo no le conozco! ¡No sé de qué me hablas! En aquel instante cantó un gallo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">69. </strong>
 Más tarde volvió a verle la misma criada, que insistió delante de todos los que estaban allí: 
¡Ese es un discípulo de Jesús! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">70. </strong>
 Pedro lo negó de nuevo.Pero, poco después, los que estaban junto al fuego le dijeron: 
¡Seguro que eres uno de ellos! ¡Además eres galileo, y habláis todos con el mismo acento! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">71. </strong>
 Entonces Pedro prorrumpió en maldiciones y juramentos:
¡No es cierto! ¡Yo ni siquiera conozco a ese hombre del que estáis hablando! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">72. </strong>
 Pero después de esto cantó el gallo por segunda vez, y entonces se acordó Pedro de lo que Jesús le había dicho: "Antes que el gallo cante por segunda vez, me negarás tres veces". Al recordar estas palabras, rompió a llorar. </p>
                                                        
