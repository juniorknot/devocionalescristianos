¿No será éste el hijo de David? 24. Pero los fariseos, al oir tales comentarios, decían:
Este expulsa demonios por el poder de Beelzebú, el príncipe de los demonios. 25. Jesús, que sabía lo que ellos pensaban, les dijo:
