La señal de Jonás 38. Algunos de los escribas y fariseos dijeron entonces:
Maestro, quisiéramos ver alguna de las señales prodigiosas que tú haces. 39. Él les respondió:
Esta generación mala y adúltera quiere ver alguna señal, pero no tendrá más señal que la del profeta Jonás. 40. Porque de igual modo que Jonás estuvo tres días y tres noches en las entrañas del monstruo marino, también el Hijo del hombre estará tres días y tres noches en el corazón de la tierra. 41. En el día del juicio se levantarán los ninivitas junto con esta generación, y la condenarán, porque ellos se arrepintieron de sus pecados al escuchar la predicación de Jonás; y aquí, en este lugar, hay alguien más importante que Jonás. 42. También en el día del juicio se levantará la reina del sur junto con esta generación, y la condenará, porque ella vino desde el extremo de la tierra para escuchar la sabiduría de Salomón; y aquí, en este lugar, hay alguien más importante que Salomón. 43. Cuando un espíritu impuro sale de un hombre, se pone a buscar en la sequedad del desierto un lugar donde reposar, pero no lo halla. 44. Entonces se dice a sí mismo: "Me volveré a mi casa, de la que salí", y al regresar la encuentra vacía, barrida y adornada. 45. Al verla así, va y reúne otros siete espíritus peores que él, y todos juntos se meten a vivir en la casa; con lo cual, el estado final de aquel hombre es peor que al principio. Y eso es lo que le sucederá a esta perversa generación.

