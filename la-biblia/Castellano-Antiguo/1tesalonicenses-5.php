                        <h2 class="text-center"> 1 Tesalonicenses 5 - Castellano Antiguo (Nuevo Testamento)</h2>          
                                                                                <p style="style=text-align: justify;"><strong id="numeros">1. </strong>
 ¿Que cuándo sucederá todo eso?... No es preciso, hermanos, que os hable de ello, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">2. </strong>
 porque vosotros sabéis perfectamente que el día del Señor llegará de modo inesperado, como llega un ladrón por la noche. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">3. </strong>
 Será uno de los días en que la gente ande diciendo: "¡Disfrutamos de paz y seguridad!" Pero entonces, de pronto, caerá sobre ellos la destrucción. Será algo tan repentino como los dolores de la mujer que da a luz; y nadie logrará escapar. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">4. </strong>
 Pero vosotros, hermanos, no estáis a oscuras para que la llegada de ese día os sorprenda como un ladrón. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">5. </strong>
 Porque vosotros sois hijos de la luz, hijos del día. Ninguno de nosotros pertenece a la noche ni a las tinieblas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">6. </strong>
 Por lo tanto estemos en guardia y no durmamos como los demás; permanezcamos despiertos y seamos sobrios, a la espera del Señor. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">7. </strong>
 Porque mucha gente duerme y se emborracha por la noche; </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">8. </strong>
 pero nosotros, que pertenecemos al día, debemos ser sobrios y estar revestidos de la coraza de la fe y del amor, y cubiertos con la esperanza de la salvación como el soldado se cubre con su yelmo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">9. </strong>
 Porque Dios no nos ha escogido para descargar su ira sobre nosotros, sino para salvarnos por medio de nuestro Señor Jesucristo, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">10. </strong>
 que murió para que nosotros vivamos con él para siempre, ya sea que a su regreso estemos vivos o hayamos muerto. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">11. </strong>
 Así que animaos y edificaos mutuamente, tal como venís haciéndolo.

Instrucciones finales </p>
                                                    <h4><strong>Pablo exhorta a los hermanos</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">12. </strong>
 Por otra parte, hermanos, os rogamos que honréis a los siervos de Dios que trabajan entre vosotros, que os dirigen en nombre del Señor y os previenen contra el mal. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">13. </strong>
 Tenedlos en alta estima y amadlos de corazón, porque sin duda se esfuerzan por ayudaros. Y mantened la paz entre vosotros. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">14. </strong>
 Hermanos, también os pedimos esto: reprended a los perezosos, alentad a los desanimados, confortad a los débiles y sed pacientes con todos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">15. </strong>
 Mirad que ninguno entre vosotros pague mal con mal; muy al contrario, procurad siempre lo mejor entre vosotros y con todos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">16. </strong>
 Que la alegría presida vuestra vida. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">17. </strong>
 Nunca dejéis de orar. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">18. </strong>
 Dad gracias en todo momento y circunstancia, porque esto quiere Dios de quienes pertenecen a Cristo Jesús. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">19. </strong>
 No apaguéis el fuego del Espíritu Santo </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">20. </strong>
 ni tengáis en poco las profecías. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">21. </strong>
 Examinadlo todo, pero retened sólo lo bueno. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">22. </strong>
 Evitad toda clase de maldad. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">23. </strong>
 Que el mismo Dios de paz os santifique por completo, y que guarde irreprensible todo vuestro ser, espíritu, alma y cuerpo, para el día del regreso de nuestro Señor Jesucristo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">24. </strong>
 Dios, que os ha llamado a ser sus hijos, es fiel y actuará conforme a su promesa. </p>
                                                    <h4><strong>Salutaciones y bendición final</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">25. </strong>
 Hermanos, orad por nosotros. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">26. </strong>
 Saludad a todos los hermanos con un beso santo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">27. </strong>
 En nombre del Señor os ordeno que leáis esta carta a todos los hermanos en la fe. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">28. </strong>
 Que la gracia de nuestro Señor Jesucristo sea con vosotros. </p>
                                                        
