y elevar a los humildes. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 Llena de bienes a los 
hambrientos
y despide a los ricos con 
las manos vacías. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 Viene en auxilio de 
su siervo Israel, 
recordando la misericordia </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 de la que habló a 
nuestros antepasados, 
la cual prometió ejercer 
eternamente
sobre Abraham y 
sus descendientes. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 María se quedó con Elisabet durante unos tres meses, al término de los cuales regresó a su hogar.

Nacimiento de Juan el Bautista </p>
                                                    <h4><strong>Nacimiento de Juan el Bautista</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 Cumplido el tiempo de gestación, Elisabet dio a luz un hijo, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
 y la noticia de cómo el Señor había sido misericordioso con ella corrió entre vecinos y familiares, y todos acudieron a acompañarla en su inmensa alegría. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">59. </strong>
 Al octavo día del nacimiento fueron a circuncidar al niño, y la gente le llamaba con el nombre de su padre, Zacarías; </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">60. </strong>
 pero Elisabet dijo:
No, no es ese su nombre: el niño se llamará Juan. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">61. </strong>
 
¿Y por qué? 
decían
Nadie en tu familia se llama así. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">62. </strong>
 Entonces, por señas, preguntaron al padre cómo quería él que le llamasen. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">63. </strong>
 Zacarías pidió una tablilla para escribir, y con sorpresa de todos escribió: "Se llamará Juan". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">64. </strong>
 En aquel mismo instante recobró Zacarías el habla, y comenzó a alabar a Dios "e </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">65. </strong>
 ante el asombro de todos sus vecinos.Las noticias de lo sucedido se divulgaron pronto de uno a otro extremo de las montañas de Judea; </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">66. </strong>
 y todos los que las oían pensaban mucho en ello, y se preguntaban: "¿Quién llegará a ser ese niño? Porque no cabe duda de que la mano del Señor está con él".

El cántico de Zacarías </p>
                                                    <h4><strong>Profecía de Zacarías</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">67. </strong>
 Zacarías, su padre, lleno del Espíritu Santo, profetizó diciendo: </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">68. </strong>
 
¡Bendito sea el Señor Dios
de Israel, 
que ha venido a visitar y redimir a su pueblo! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">69. </strong>
 Porque nos envía un 
poderoso Salvador
que desciende de David, "su siervo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">70. </strong>
 Así lo había prometido en
tiempos pasados 
por medio de sus santos "profetas: </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">71. </strong>
 que nos salvaría de nuestros
enemigos
y de manos de todos los que nos odian, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">72. </strong>
 que tendría misericordia de
nuestros antepasados
y se acordaría de su santo pacto. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">73. </strong>
 Y esto es lo que juró a 
nuestro padre Abraham: 
que nos concedería </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">74. </strong>
 liberación del poder de 
nuestros enemigos, 
para que le sirvamos sin temor, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">75. </strong>
 con santidad y justicia, 
durante toda nuestra vida. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">76. </strong>
 En cuanto a ti, niño, 
serás llamado 
profeta del Altísimo, 
porque irás delante del Señor preparando su camino </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">77. </strong>
 y proclamando salvación
para su pueblo
mediante el perdón de sus pecados. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">78. </strong>
 Una salvación que alcanzará
merced a la entrañable
misericordia del Dios nuestro, 
que desde los cielos nos ha dado el amanecer
de un nuevo día, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">79. </strong>
 cuya luz alumbra a los que
habitan en tinieblas
y en sombra de muerte, 
y encamina nuestros pasos por senderos de paz. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">80. </strong>
 El niño crecía y se hacía espiritualmente más fuerte. Luego se fue a vivir a lugares desiertos, donde permaneció hasta el momento en que dio comienzo a su ministerio público en Israel. </p>
                                                        
