¡Verdaderamente este era Hijo de Dios! 55. No muy lejos de la cruz estaban mirando muchas mujeres que habían seguido a Jesús desde Galilea y le habían servido. 56. Entre ellas se encontraban María Magdalena, María la madre de Jacobo y de José, y la madre de los hijos de Zebedeo.

Sepultura de Jesús 57. Al caer la tarde llegó un hombre rico, de Arimatea, llamado José, que había sido discípulode Jesús. 58. Este hombre se dirigió a Pilato para pedirle el cuerpo de Jesús. Pilato ordenó que se le entregase, 59. y José tomó el cuerpo, lo envolvió en una sábana limpia 60. y lo puso en un sepulcro nuevo labrado en la peña. Luego hizo rodar una gran piedra con la que cerró la entrada, y se fue. 61. Allí se quedaron María Magdalena y la otra María, sentadas frente al sepulcro.

