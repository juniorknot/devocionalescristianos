                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 Pero Jesús, dando de nuevo un gran grito, entregó su espíritu. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 En aquel instante el velo del templo se rasgó en dos, de arriba abajo; la tierra tembló y las rocas se partieron. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 Se abrieron los sepulcros, y muchos cuerpos de santos que estaban muertos, resucitaron; </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 salieron de los sepulcros después que Jesús hubo resucitado, y fueron a la santa ciudad de Jerusalén, donde se aparecieron a muchos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 El centurión y los soldados que custodiaban a Jesús, llenos de pánico a causa del terremoto y de todas las cosas que estaban aconteciendo, exclamaron: 
¡Verdaderamente este era Hijo de Dios! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 No muy lejos de la cruz estaban mirando muchas mujeres que habían seguido a Jesús desde Galilea y le habían servido. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 Entre ellas se encontraban María Magdalena, María la madre de Jacobo y de José, y la madre de los hijos de Zebedeo.

Sepultura de Jesús </p>
                                                    <h4><strong>Jesús es sepultado</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 Al caer la tarde llegó un hombre rico, de Arimatea, llamado José, que había sido discípulode Jesús. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
 Este hombre se dirigió a Pilato para pedirle el cuerpo de Jesús. Pilato ordenó que se le entregase, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">59. </strong>
 y José tomó el cuerpo, lo envolvió en una sábana limpia </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">60. </strong>
 y lo puso en un sepulcro nuevo labrado en la peña. Luego hizo rodar una gran piedra con la que cerró la entrada, y se fue. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">61. </strong>
 Allí se quedaron María Magdalena y la otra María, sentadas frente al sepulcro.

La guardia ante el sepulcro </p>
                                                    <h4><strong>La guardia ante la tumba</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">62. </strong>
 Al siguiente día, el que sigue a la preparación de la Pascua, los principales sacerdotes y los fariseos fueron a ver a Pilato, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">63. </strong>
 y le dijeron: 
Señor, hemos recordado que aquel impostor dijo una vez: "Al cabo de tres días resucitaré". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">64. </strong>
 Queremos pedirte que mandes sellar el sepulcro hasta el tercer día, no vaya a suceder que vengan los discípulos de ese hombre, se lleven el cuerpo y luego digan al pueblo que ha resucitado de entre los muertos. Con lo cual las cosas se pondrían peor de lo que antes estaban. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">65. </strong>
 Pilato les dijo:
Bueno, ahí tenéis soldados de guardia. Id y aseguradlo como sabéis hacerlo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">66. </strong>
 Fueron ellos entonces, sellaron la piedra que cerraba el sepulcro y dejaron allí a la guardia. </p>
                                                        
