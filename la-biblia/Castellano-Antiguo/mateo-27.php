Jesús ante Pilato 11. Jesús estaba de pie delante del gobernador Pilato, el cual le preguntó: 
¿Eres tú el rey de los judíos? 
Tú lo dices 
le contestó Jesús. 12. Los principales sacerdotes y los dirigentes judíos le acusaban, pero él nada respondió. 13. Le preguntó Pilato:
¿No oyes cuántas cosas testifican estos contra ti? 14. Pero Jesús siguió callado, sin responder ni una sola palabra, de modo que el gobernador estaba asombrado. 15. Ahora bien, durante la celebración de la Pascua, el gobernador acostumbraba poner en libertad un preso, el que el propio pueblo elegía. 16. Aquel año había en la cárcel un preso famoso llamado Barrabás, 17. y cuando aquella mañana se congregó la gente, Pilato preguntó: 
¿A cuál de estos dos queréis que os ponga en libertad: a Barrabás o a Jesús, llamado el Cristo? 18. Al gobernador le constaba que Jesús había sido entregado por quienes envidiaban su popularidad. 19. Estaba Pilato sentado en el tribunal, cuando de parte de su esposa le llegó un mensaje que decía: "No te metas con ese hombre, que es inocente. Esta noche he sufrido mucho en sueños por causa suya". 20. Pero los principales sacerdotes y los dirigentes judíos persuadieron a la muchedumbre para que pidiese la libertad de Barrabás y mataran a Jesús. 21. Así pues, cuando el gobernador volvió a preguntar a cuál de los dos querían que soltara, gritaron: 
¡A Barrabás! 22. ¿Y que hago con Jesús, llamado el Cristo? Todos a una contestaron:
¡Crucifícalo! 23. Preguntó el gobernador:
Pero, ¿por qué? ¿Qué delito ha cometido? Pero la multitud siguió gritando sin cesar: 
¡Crucifícalo! ¡Crucifícalo! 24. Comprendiendo Pilato que nada conseguía, sino que cada vez era mayor el alboroto, ordenó que le llevasen agua, se lavó las manos en presencia de todo el pueblo y dijo:
Yo no me hago responsable de la sangre de este hombre, que es inocente. ¡Allá vosotros! 25. La turba en pleno respondió:
¡Que su sangre caiga sobre nosotros y sobre nuestros hijos! 26. Entonces Pilato mandó soltar a Barrabás; y a Jesús lo hizo azotar y lo entregó a los soldados, para que lo crucificaran.

Los soldados se burlan de Jesús 27. Los soldados del gobernador tomaron a Jesús y lo llevaron primero al pretorio, donde reunieron alrededor de él a toda la compañía, 28. lo desnudaron y le echaron sobre los hombros un manto escarlata. 29. Luego le pusieron sobre la cabeza una corona que habían tejido de espinos, y una caña en su mano derecha. Y hacían burla de Jesús arrodillándose ante él y diciendo: 
¡Salve, rey de los judíos! 30. También escupían sobre él, y quitándole la caña le golpeaban la cabeza.

La crucifixión 31. Por último, después de haberse burlado de Jesús, le despojaron del manto, le pusieron de nuevo su propia ropa y se lo llevaron para crucificarlo. 32. En el camino, al salir, hallaron a un hombre de Cirene llamado Simón, al cual obligaron a llevar la cruz de Jesús. 33. Cuando llegaron a un lugar que llamaban Gólgota (o sea, lugar de la Calavera), 34. los soldados le dieron a beber vinagre mezclado con hiel; pero Jesús, después de probarlo, no quiso beberlo. 35. Una vez que lo hubieron crucificado, los soldados echaron sus ropas a suertes y se las repartieron entre ellos. 36. Luego se sentaron allí para vigilarlo. 37. En la cruz, por encima de su cabeza, pusieron un letrero con la causa de la condena. Decía: "Este es Jesús, el rey de los judíos". 38. Crucificaron también entonces a dos maleantes, uno a la derecha y otro a la izquierda de Jesús. 39. La gente, al pasar, le injuriaba, y meneando la cabeza 40. decía:
¿No eres tú el que puede derribar el templo y volver a construirlo en tres días? ¡Pues sálvate a ti mismo! Si eres hijo de Dios, bájate de la cruz. 41. De la misma manera, también los principales sacerdotes, los escribas, los fariseos y los dirigentes judíos, burlándose de Jesús, decían: 42. A otros ha salvado, pero a sí mismo no puede salvarse. Si es el rey de Israel, que se baje ahora de la cruz y creeremos en él. 43. Y puesto que confió en Dios, que Dios lo salve, si es que le quiere. ¿No dice él que es hijo de Dios? 44. De igual forma le injuriaban los ladrones que habían sido crucificados junto a él.

Muerte de Jesús 45. Pero aquel día, desde la hora sexta (mediodía) hasta la hora novena (tres de la tarde), la tierra quedó sumida en profundas tinieblas. 46. Cerca ya de la hora novena, Jesús dio un gran grito:
Elí, Elí, ¿lemá sabactani? (que significa: "Dios mío, Dios mío, ¿por qué me has desamparado?") 47. Algunos de los que estaban allí creyeron que estaba llamando a Elías; 48. y en aquel mismo momento, uno fue corriendo a empapar una esponja en vinagre, la puso en una caña y la alzó para que Jesús bebiese. g 49. Pero los demás decían: s
