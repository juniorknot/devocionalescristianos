<p style="style=text-align: justify;"><strong id="numeros">4. </strong>
y el ejercitaros en la paciencia os hará perfectos y cabales, aptos para enfrentaros a cualquier circunstancia adversa que se os presente. </p>
<p style="style=text-align: justify;"><strong id="numeros">5. </strong>
Si a alguno de vosotros le falta sabiduría, pídasela a Dios, porque él la da con liberalidad a cuantos se la piden, y no hace ninguna clase de reproches. </p>
<p style="style=text-align: justify;"><strong id="numeros">6. </strong>
Pero, eso sí, habéis de pedirla con fe, porque el que duda es semejante a las olas del mar, que se agitan de acá para allá según el punto de donde sopla el viento. </p>
<p style="style=text-align: justify;"><strong id="numeros">8. </strong>
En efecto, las personas que dudan y nunca llegan a tomar una decisión son inestables en todo lo que emprenden; y como andan vacilantes y no piden con fe, tampoco pueden esperar respuesta del Señor. </p>
<p style="style=text-align: justify;"><strong id="numeros">9. </strong>
El hermano en Cristo que es de condición social humilde, alégrese, porque el Señor lo elevará a la más alta riqueza. </p>
<p style="style=text-align: justify;"><strong id="numeros">10. </strong>
Y el rico, alégrese humillándose a sí mismo y no olvidando que él pasará como se pasa la flor de la hierba; </p>
<p style="style=text-align: justify;"><strong id="numeros">11. </strong>
porque el sol, cuando sale, abrasa y seca la hierba, y su flor se cae habiendo perdido toda su belleza. Así se marchitará también el rico junto con todos los negocios que haya emprendido. </p>
<h4><strong>Soportando las pruebas</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">12. </strong>
Dichoso el hombre que no cede a la tentación, porque un día, habiendo soportado la prueba, recibirá la corona de vida que Dios ha prometido a los que le aman. </p>
<p style="style=text-align: justify;"><strong id="numeros">13. </strong>
Pero si alguien se siente tentado a hacer algo malo, no diga que es Dios quien le tienta, porque Dios no tienta a nadie, ni por nadie puede ser tentado. </p>
<p style="style=text-align: justify;"><strong id="numeros">14. </strong>
La tentación es la seductora atracción que ejercen sobre el hombre sus propios malos deseos. </p>
<p style="style=text-align: justify;"><strong id="numeros">15. </strong>
Estos conciben y dan a luz el pecado, y luego, del pecado cometido, nace la muerte. </p>
<p style="style=text-align: justify;"><strong id="numeros">16. </strong>
Así pues, amados hermanos, estad atentos a no equivocaros. </p>
<p style="style=text-align: justify;"><strong id="numeros">17. </strong>
Todo lo que recibimos de bueno y perfecto procede de Dios, creador de todas las luces del universo y en quien no hay cambio alguno ni sombra de variación. </p>
<p style="style=text-align: justify;"><strong id="numeros">18. </strong>
Por su propia voluntad nos ha engendrado mediante la verdad de su mensaje, para que vivamos una vida nueva y seamos primicias de una nueva creación.
Hay que poner en práctica la palabra </p>
<h4><strong>Hacedores de la palabra</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">19. </strong>
Amados hermanos, recordad que debéis estar dispuestos a escuchar siempre, hablar poco y airaros lo menos posible. </p>
<p style="style=text-align: justify;"><strong id="numeros">20. </strong>
Precisamente esto, nuestra ira, nos impide hacer lo que es justo delante de Dios. </p>
<p style="style=text-align: justify;"><strong id="numeros">21. </strong>
Así pues, desechad cuanto en vuestra vida pueda haber de corrupción y abundancia de maldad, y recibid humildemente el mensaje plantado en vosotros, que es poderoso para traer la salvación a vuestras almas. </p>
<p style="style=text-align: justify;"><strong id="numeros">22. </strong>
Sin embargo, no nos engañemos: no es este un mensaje al que solo hayamos de prestar oídos. Sobre todo es menester ponerlo por obra. </p>
<p style="style=text-align: justify;"><strong id="numeros">23. </strong>
Cualquiera que se limite a escucharlo y no lo lleve a la práctica, se parecerá a la persona que contempla su rostro en un espejo, </p>
<p style="style=text-align: justify;"><strong id="numeros">24. </strong>
y luego, cuando se va, se olvida de su propio aspecto. </p>
<p style="style=text-align: justify;"><strong id="numeros">25. </strong>
En cambio, el que pone su atención y persevera en la perfecta ley que Dios nos ha dado para que podamos conducirnos con libertad, no solo la recordará, sino que actuará conforme a ella, y Dios le bendecirá en todo lo que haga. </p>
<p style="style=text-align: justify;"><strong id="numeros">26. </strong>
Si alguno entre vosotros se tiene por religioso, pero no refrena su lengua, se engaña a sí mismo y su religiosidad es una absoluta vaciedad. </p>
<p style="style=text-align: justify;"><strong id="numeros">27. </strong>
La auténtica religión no contaminada consiste en cuidar de los más débiles: ocuparse de los huérfanos y las viudas, y no dejarse manchar por el mundo. </p>
