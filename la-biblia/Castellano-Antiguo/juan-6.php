<li class="hide-for-large"><a href="whatsapp://send?text=Juan 6 (CST)%0A%0A 1. Después de esto, Jesús se fue a la otra parte del mar de Galilea (llamado también lago de Tiberias). Tras él iba una multitud, que le seguía movida por el deseo de ver las señales milagrosas que hacía curando a los enfermos. 3. Jesús subió a un monte, y se sentó allí en compañía de sus discípulos. 4. Eran los días anteriores a la celebración de la Pascua, la gran fiesta judía. 5. Cuando Jesús miró hacia la ladera del monte y vio aquella muchedumbre que le había seguido, le dijo a Felipe, uno de sus discípulos:
¿Dónde podríamos comprar pan para dar de comer a toda esa gente? 6. (Jesús hizo esta pregunta con intención de probar la fe de Felipe, pero en realidad él ya tenía pensado lo que se había de hacer). 7. Respondió Felipe:
¡Ni siquiera doscientos denarios bastarían para que cada uno pudiese comer un poco! 8. Otro de los discípulos, Andrés, el hermano de Simón Pedro, informó a su vez: 
Ahí hay un muchacho que tiene cinco panes de cebada y dos pececillos, pero ¿qué puede ser eso para tanta gente? 10. Decid a todos que se sienten 
ordenó Jesús. La multitud (unas cinco mil personas contando sólo a los hombres) se sentó en la tierra, que estaba cubierta de una espesa capa de hierba. 11. Jesús tomó entonces los panes, dio gracias a Dios por ellos y los repartió entre los que estaban sentados. Luego hizo lo mismo con los peces. 12. Cuando ya todos habían comido hasta quedar satisfechos, ordenó a los discípulos:
Recoged los trozos sobrantes para que nada se pierda. 13. Así lo hicieron, y llenaron doce cestos con los trozos que habían quedado de los cinco panes de cebada. 14. Al ver la gente la gran señal milagrosa que Jesús había hecho, comenzaron a exclamar:
¡Sin duda es este el profeta que esperábamos que había de venir al mundo! 15. Jesús se dio cuenta en aquella ocasión de que muchos pretendían llevárselo, incluso a la fuerza, para coronarlo rey, y por eso se retiró de nuevo al monte, él solo.

Jesús camina sobre el agua 16. Al anochecer bajaron sus discípulos hasta la orilla del agua. 17. Como ya había oscurecido y Jesús seguía sin regresar, decidieron subir a una barca y cruzar el lago en dirección a Cafarnaum; 18. pero no pudieron avanzar mucho, porque comenzó a soplar un fuerte viento que los azotaba con violencia y levantaba grandes olas. 19. Cuando a fuerza de remos habían logrado recorrer unos veinticinco o treinta estadios (unos cinco ó seis kilómetros), divisaron de pronto a Jesús, que caminaba sobre las aguas acercándose a la barca; mas no lo reconocieron, y el terror se apoderó de ellos. 20. Jesús les gritó:
¡Soy yo, no tengáis miedo! 21. Entonces ellos se tranquilizaron y lo recibieron con alegría en la barca. Poco después arribaron al punto adonde se dirigían. 22. A la mañana siguiente, la gente que permanecía al otro lado del mar advirtió que los discípulos se habían marchado, ellos solos, en la única barca que había allí. Jesús no iba con ellos. 23. Entre tanto, varias barcas procedentes de Tiberias fueron llegando a aquel lugar, donde el Señor, después de dar gracias, había repartido el pan para que comieran todos los que le habían seguido. 24. Ahora, al ver que Jesús no estaba allí ni tampoco sus discípulos, la gente subió a las barcas, y todos pusieron rumbo a Cafarnaum para encontrar a Jesús.

Jesús, el pan de vida 25. Le vieron por fin en la otra orilla del mar, y le preguntaron:
Rabí, ¿cuándo llegaste aquí? 26. Jesús les respondió:
Estoy seguro de que vosotros no me venís buscando porque hayáis visto las señales milagrosas que he hecho, sino porque os di de comer hasta quedar saciados. 27. Pues bien, poned todo vuestro empeño en trabajar, no por una comida perecedera, sino por la comida permanente de la vida eterna, que es la comida que yo, el Hijo del hombre, os ofrezco, porque para eso me ha enviado Dios el Padre a este mundo. 28. Algunos le preguntaron:
¿Qué debemos hacer para llevar a cabo las obras que son voluntad de Dios? 29. Jesús respondió:
La obra que es voluntad de Dios consiste en que creáis en el que él ha enviado. 30. Pero dinos, ¿cuál es tu obra? ¿Con qué señal, con qué milagro nos demuestras que tú eres el Mesías en quien debemos creer? 31. Nuestros antepasados creyeron en Dios, que los alimentó con maná cuando estaban en el desierto, como está escrito: "Les dio a comer pan del cielo". 32. Estad, pues, seguros que fue Moisés quien os dio el pan del cielo 
les dijo Jesús
, sino mi Padre. Y es también mi Padre quien ahora os está ofreciendo el verdadero pan del cielo. 33. Porque el pan de Dios es aquel que ha descendido del cielo para dar vida al mundo. 34. Señor 
dijeron ellos
, ¡danos de ese pan todos los días de nuestra vida! 35. Respondió Jesús:
Yo soy el pan de vida. Los que a mí vienen, nunca más tendrán hambre; los que en mí creen, no volverán a tener sed. 36. Pero ya os lo he dicho: vosotros seguís sin creer en mí, a pesar de haberme visto. 37. Sin embargo, sabed que yo recibiré a todos los que el Padre me dé y vengan a mí, y no rechazaré a ninguno que él me haya enviado; 38. porque yo no he venido del cielo para hacer mi voluntad, sino la voluntad del que me envió. 39. Y la voluntad de mi Padre, que me envió, es que yo no pierda a ninguno de los que él me ha confiado, sino que en el día último los resucite a todos para vida eterna. 40. Así pues, la voluntad de mi Padre es que todos los que ven al Hijo y creen en él tengan vida eterna, y que yo los resucite en el día último. 41. Los judíos murmuraron entonces contra Jesús, porque había dicho: "Yo soy el pan que ha bajado del cielo". 42. Decían:
¿Pero no es éste Jesús, el hijo de José? ¿Cómo se atreve a decir que ha bajado del cielo, cuando nosotros conocemos a su padre y a su madre? 43. Jesús les dijo:
No andéis murmurando entre vosotros por lo que os he dicho. 44. Nadie puede venir a mí, si el Padre, que me envió, no lo trae; y a quien él traiga, yo lo resucitaré en el día último. 45. Dice la Escritura: "Dios los instruirá a todos"; por tanto, todos aquellos que escuchen lo que el Padre dice, aprenderán de él la verdad y vendrán a mí. 46. Pero esto no significa que alguien haya visto al Padre, aparte del que ha venido de Dios. Unicamente él lo ha visto. 47. Os aseguro que todo el que cree en mí tiene ya la vida eterna, 48. porque yo soy el pan de vida. 49. Recordad que vuestros antepasados comieron el maná en el desierto, y murieron porque en él no había verdadera vida. 50. Mas yo soy el pan que ha descendido del cielo, para que quien coma de él, no muera. 51. Yo soy el pan de vida que ha descendido del cielo: el que coma de este pan, vivirá para siempre. Este pan es mi propia carne, que yo daré para que el mundo reciba la vida verdadera. 52. Los judíos se pusieron entonces a discutir unos con otros sobre el significado de aquellas palabras. Decían:
¿Acaso estará éste pensando en darnos a comer su carne? 53. Os aseguro 
les dijo Jesús
, que quien no coma la carne del Hijo del hombre ni beba su sangre, no podrá tener vida eterna en sí mismo. 54. El que come mi carne y bebe mi sangre, sí tiene vida eterna, y yo lo resucitaré el día último. 55. Porque mi carne es verdadera comida, y mi sangre verdadera bebida. 56. El que come mi carne y bebe mi sangre, permanece en mí y yo en él. 57. Del mismo modo que yo vivo por el poder del Padre viviente, que me envió, los que me comen vivirán por mí. 58. Yo soy el pan que ha descendido del cielo: cualquiera que coma de este pan vivirá para siempre; no morirá, como murieron vuestros antepasados a pesar de haber comido el maná en el desierto. 59. Todas estas cosas dijo Jesús cuando estaba enseñando en la sinagoga de Cafarnaum.

Muchos discípulos abandonan a Jesús 60. Al acabar de hablar, muchos de sus discípulos comentaban entre sí:
Esto es muy difícil de entender. ¡Quién sabe lo que ha querido decirnos! 61. Jesús, conociendo lo que murmuraban los discípulos, les preguntó: 
¿Acaso lo que he dicho os ofende? 62. ¿Pues qué pensaríais si vieseis al Hijo del hombre regresar al cielo, al lugar donde primero estaba? 63. La vida que permanece procede del espíritu; en cambio, lo que procede de la carne no aprovecha para nada. Las palabras que os he hablado, son espíritu y vida que permanece para siempre. 64. Sin embargo, ya sé que entre vosotros hay algunos que no creen en mí (esto lo dijo Jesús porque sabía desde el principio quiénes eran los que no creían, y quién el que había de traicionarlo). 65. Añadió Jesús:
A eso me refería al deciros que nadie puede venir a mí a menos que el Padre lo traiga. 66. A partir de aquel momento se volvieron atrás muchos de los que le seguían, y no quisieron andar más con Jesús. 67. Entonces, volviéndose él a los doce, les preguntó:
¿También vosotros queréis iros y dejarme? 68. Señor 
le contestó Simón Pedro
