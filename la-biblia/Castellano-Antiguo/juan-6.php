                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 Dice la Escritura: "Dios los instruirá a todos"; por tanto, todos aquellos que escuchen lo que el Padre dice, aprenderán de él la verdad y vendrán a mí. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 Pero esto no significa que alguien haya visto al Padre, aparte del que ha venido de Dios. Unicamente él lo ha visto. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
 Os aseguro que todo el que cree en mí tiene ya la vida eterna, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 porque yo soy el pan de vida. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 Recordad que vuestros antepasados comieron el maná en el desierto, y murieron porque en él no había verdadera vida. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 Mas yo soy el pan que ha descendido del cielo, para que quien coma de él, no muera. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 Yo soy el pan de vida que ha descendido del cielo: el que coma de este pan, vivirá para siempre. Este pan es mi propia carne, que yo daré para que el mundo reciba la vida verdadera. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 Los judíos se pusieron entonces a discutir unos con otros sobre el significado de aquellas palabras. Decían:
¿Acaso estará éste pensando en darnos a comer su carne? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 
Os aseguro 
les dijo Jesús
, que quien no coma la carne del Hijo del hombre ni beba su sangre, no podrá tener vida eterna en sí mismo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 El que come mi carne y bebe mi sangre, sí tiene vida eterna, y yo lo resucitaré el día último. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 Porque mi carne es verdadera comida, y mi sangre verdadera bebida. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 El que come mi carne y bebe mi sangre, permanece en mí y yo en él. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 Del mismo modo que yo vivo por el poder del Padre viviente, que me envió, los que me comen vivirán por mí. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
 Yo soy el pan que ha descendido del cielo: cualquiera que coma de este pan vivirá para siempre; no morirá, como murieron vuestros antepasados a pesar de haber comido el maná en el desierto. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">59. </strong>
 Todas estas cosas dijo Jesús cuando estaba enseñando en la sinagoga de Cafarnaum.

Muchos discípulos abandonan a Jesús </p>
                                                    <h4><strong>Palabras de vida eterna</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">60. </strong>
 Al acabar de hablar, muchos de sus discípulos comentaban entre sí:
Esto es muy difícil de entender. ¡Quién sabe lo que ha querido decirnos! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">61. </strong>
 Jesús, conociendo lo que murmuraban los discípulos, les preguntó: 
¿Acaso lo que he dicho os ofende? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">62. </strong>
 ¿Pues qué pensaríais si vieseis al Hijo del hombre regresar al cielo, al lugar donde primero estaba? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">63. </strong>
 La vida que permanece procede del espíritu; en cambio, lo que procede de la carne no aprovecha para nada. Las palabras que os he hablado, son espíritu y vida que permanece para siempre. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">64. </strong>
 Sin embargo, ya sé que entre vosotros hay algunos que no creen en mí (esto lo dijo Jesús porque sabía desde el principio quiénes eran los que no creían, y quién el que había de traicionarlo). </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">65. </strong>
 Añadió Jesús:
A eso me refería al deciros que nadie puede venir a mí a menos que el Padre lo traiga. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">66. </strong>
 A partir de aquel momento se volvieron atrás muchos de los que le seguían, y no quisieron andar más con Jesús. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">67. </strong>
 Entonces, volviéndose él a los doce, les preguntó:
¿También vosotros queréis iros y dejarme? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">68. </strong>
 
Señor 
le contestó Simón Pedro
, ¿y a quién podríamos ir? Tú eres el único que tiene palabras de vida eterna, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">69. </strong>
 y nosotros hemos creído en ti, y sabemos que tú eres el Cristo, el Hijo del Dios viviente. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">70. </strong>
 
Pues bien 
les dijo
, recordad que yo os he escogido a vosotros, a los doce, y que uno de vosotros es un diablo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">71. </strong>
 Se refería a Judas, hijo de Simón Iscariote, que iba a traicionar a Jesús a pesar de ser uno de los doce discípulos. </p>
                                                        
