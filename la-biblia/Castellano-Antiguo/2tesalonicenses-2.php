                                                    <h4><strong>Manifestación del hombre de pecado</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">1. </strong>
 En cuanto al regreso de nuestro Señor Jesucristo y nuestro encuentro con él, os rogamos, hermanos, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">2. </strong>
 que no os inquietéis ni os alarméis por rumores que afirmen como un hecho la llegada del día del Señor. No hagáis caso a nadie que pretenda estar informado al respecto por el Espíritu Santo, o que afirme haber recibido mensajes de Dios; ni tampoco a nadie que aluda a cartas escritas supuestamente por mí. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">3. </strong>
 No os dejéis engañar, porque ese día no vendrá sin que primero llegue un tiempo de rebeldía extrema contra Dios, y sin que se manifieste el "hombre de pecado", el hijo de perdición, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">4. </strong>
 el adversario de todo lo que recibe el nombre de Dios o es objeto de adoración. Es ese un personaje que incluso se atreverá a sentarse en el templo de Dios para hacerse pasar por Dios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">5. </strong>
 ¿Acaso no recordáis que ya os hablé de todo esto cuando me encontraba ahí, entre vosotros? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">6. </strong>
 Como también os hablé de un poder que le impide manifestarse, y que no se lo permitirá mientras no llegue el momento oportuno. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">7. </strong>
 La fuerza misteriosa de la maldad está en acción, pero todavía en el día de hoy la detiene alguien que ha de ser quitado. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">8. </strong>
 Entonces es cuando aparecerá aquel ser inicuo, al que el Señor Jesús matará con el aliento de su boca y destruirá con el resplandor de su venida. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">9. </strong>
 Ese "hombre de pecado" vendrá por obra de Satanás, lleno de poder maligno, haciendo señales extrañas y falsos milagros. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">10. </strong>
 Engañará inicuamente a los que se van a perder por haberse negado a aceptar el amor de la verdad que los habría hecho salvos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">11. </strong>
 Por eso, Dios les envía un poder que los persuadirá a creer aquellas mentiras, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">12. </strong>
 y luego los condenará por no haber creído a la verdad y haberse deleitado en el pecado.

Exhortación a la perseverancia </p>
                                                    <h4><strong>Escogidos para salvación</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">13. </strong>
 Pero nosotros hemos de dar siempre gracias a Dios a causa vuestra, hermanos amados del Señor, porque Dios os escogió desde el principio para que tengáis salvación mediante la acción santificadora del Espíritu Santo y por la fe que habéis puesto en la verdad. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">14. </strong>
 Con ese fin, y por nuestro anuncio del evangelio, Dios os llamó a participar de la gloria de nuestro Señor Jesucristo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">15. </strong>
 Por eso, hermanos, permaneced firmes y retened la enseñanza de la verdad, la cual os hemos enseñado, tanto durante el tiempo que estuvimos entre vosotros como después por nuestras cartas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">16. </strong>
 Que el mismo Señor Jesucristo, y Dios nuestro Padre, que nos amó y que por pura gracia nos dio eterno consuelo y una esperanza que no tiene parangón, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">17. </strong>
 os consuele y os confirme en todo lo bueno que digáis y hagáis. </p>
                                                        
