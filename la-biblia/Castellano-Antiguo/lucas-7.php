Jesús y Juan el Bautista 18. Los discípulos de Juan el Bautista tuvieron noticias de las obras que hacía Jesús, y fueron a contárselo a Juan. 19. Este encargó a dos de ellos que le preguntaran si él era realmente el Cristo que esperaban, o si debían esperar a otro. 20. Fueron, pues, y le dijeron: 
Juan el Bautista nos ha encargado que te preguntemos si tú eres el que había de venir, o si tenemos que seguir esperando a otro. 21. Aquellos dos discípulos de Juan vieron entonces cómo Jesús sanaba a muchos que estaban enfermos, inválidos o poseídos por espíritus malignos, y cómo daba la vista a los ciegos; 22. y les dijo:
Volved a Juan y hacedle saber lo que vosotros mismos habéis visto y oído: los ciegos ven, los cojos andan, los leprosos quedan limpios de su enfermedad, los sordos oyen, los muertos resucitan y a los pobres se les anuncia la buena noticia de la salvación. 23. ¡Y dichoso aquél que no se escandalice por mis palabras ni mis obras! 24. Los mensajeros de Juan se fueron, y entonces Jesús comenzó a hablar de Juan a la gente. Decía:
