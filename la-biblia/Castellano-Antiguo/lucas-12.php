                                                    <h4><strong>Jesús, causa de división</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 Yo he venido a traer fuego a la tierra, ¡y ojalá ya estuviera encendido! ¡Ojalá ya hubiera terminado yo mi tarea! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 Pero aún me espera un bautismo con el que he de ser bautizado, y hasta que salga de él no dejaré de sentirme angustiado. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 ¿Pensáis que sólo he venido a traer paz a la tierra? ¡Pues no!, porque también he venido a traer divisiones. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 De aquí en adelante, las familias se dividirán: si son cinco, se enfrentarán tres de un lado y dos del otro; </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 el padre se pondrá en contra del hijo, y el hijo en contra del padre; la madre en contra de la hija, y la hija en contra de la madre; la suegra en contra de su nuera, y la nuera en contra de su suegra.

Señales de los tiempos </p>
                                                    <h4><strong>¿Cómo no reconocéis este tiempo?</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 En otra ocasión habló Jesús de este modo al gentío que se había reunido:
Cuando vosotros veis las nubes que empiezan a formarse por poniente, decís: "Va a llover", y así sucede. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 Y cuando sopla el viento del sur, decís: "Va a hacer calor" y, en efecto, lo hace. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 ¡Hipócritas!, vosotros, que sabéis interpretar el aspecto de la tierra y del cielo, ¿cómo no sois capaces de interpretar leas señales del tiempo actual? </p>
                                                    <h4><strong>Arréglate con tu adversario</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 ¿Por qué no juzgáis por vosotros mismos lo que es justo? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
 Si mientras vas de camino a los tribunales te encuentras con tu adversario, trata de arreglarte con él antes de que te lleve a la fuerza ante el juez, no sea que te metan en la cárcel, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">59. </strong>
 de la que no saldrás hasta que hayas saldado toda tu deuda. </p>
                                                        
