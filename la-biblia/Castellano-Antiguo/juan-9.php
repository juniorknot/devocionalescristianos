¿Pero qué te hizo? ¿Cómo te curó? 27. Ya os lo he explicado, pero parece que no me entendéis. ¿O es, quizá, que no me oísteis bien?... Pero si me oísteis, ¿para qué me pedís que os lo vuelva a contar? ¿O acaso es que queréis haceros discípulos suyos? 28. Entonces se pusieron a insultarle, y le decían:
