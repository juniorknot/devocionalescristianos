 Entonces, con voz muy fuerte, dijo: 
 –¡Dios te ha bendecido más que a todas las mujeres, y ha bendecido a tu hijo! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 ¿Quién soy yo, para que venga a visitarme la madre de mi Señor? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 Pues tan pronto como oí tu saludo, mi hijo se estremeció de alegría en mi vientre. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 ¡Dichosa tú por haber creído que han de cumplirse las cosas que el Señor te ha dicho! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 María dijo: 
"Mi alma alaba la grandeza del Señor;[12] 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
 mi espíritu se alegra 
en Dios mi Salvador. [13] 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 Porque Dios ha puesto sus ojos en mí, 
su humilde esclava, 
y desde ahora siempre me llamarán 
dichosa; 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 porque el Todopoderoso 
ha hecho en mí grandes cosas. 
¡Santo es su nombre! 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 Dios tiene siempre misericordia 
de quienes lo reverencian. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 Actuó con todo su poder: 
deshizo los planes de los orgullosos, 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 derribó a los reyes de sus tronos 
y puso en alto a los humildes. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 Llenó de bienes a los hambrientos 
y despidió a los ricos 
con las manos vacías. [14] 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 Ayudó al pueblo de Israel, su siervo, 
y no se olvidó de tratarlo 
con misericordia. [15] 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 Así lo había prometido 
a nuestros antepasados, 
a Abraham[16] y a sus futuros descendientes." </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 María se quedó con Isabel unos tres meses, y después regresó a su casa. </p>
                                                    <h4><strong>Nacimiento de Juan el Bautista</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 Al cumplirse el tiempo en que Isabel debía dar a luz, tuvo un hijo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
 Sus vecinos y parientes fueron a felicitarla cuando supieron que el Señor había sido tan bueno con ella. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">59. </strong>
 A los ocho días, llevaron a circuncidar al niño, y querían ponerle el nombre de su padre, Zacarías. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">60. </strong>
 Pero su madre dijo: 
 –No. Tiene que llamarse Juan. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">61. </strong>
 Le contestaron: 
 –No hay nadie en tu familia con ese nombre. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">62. </strong>
 Entonces preguntaron por señas al padre del niño, para saber qué nombre quería ponerle. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">63. </strong>
 El padre pidió una tabla para escribir, y escribió: 'Su nombre es Juan. ' y todos se quedaron admirados. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">64. </strong>
 En aquel mismo momento Zacarías volvió a hablar, y comenzó a alabar a Dios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">65. </strong>
 Todos los vecinos estaban asombrados, y en toda la región montañosa de Judea se contaba lo sucedido. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">66. </strong>
 Todos los que lo oían se preguntaban a sí mismos: "¿Qué llegará a ser este niño?" Porque ciertamente el Señor mostraba su poder en favor de él. </p>
                                                    <h4><strong>Profecía de Zacarías</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">67. </strong>
 Zacarías, el padre del niño, lleno del Espíritu Santo y hablando proféticamente, dijo: 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">68. </strong>
 "¡Bendito sea el Señor, Dios de Israel, 
porque ha venido a rescatar a su pueblo! 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">69. </strong>
 Nos ha enviado un poderoso salvador, 
un descendiente de David, su siervo. [17] 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">70. </strong>
 Esto es lo que había prometido 
en el pasado 
por medio de sus santos profetas: 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">71. </strong>
 que nos salvaría de nuestros enemigos 
y de todos los que nos odian, 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">72. </strong>
 que tendría compasión 
de nuestros antepasados 
y que no se olvidaría de su santa alianza. [18] 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">73. </strong>
 y este es el juramento que había hecho 
a nuestro padre Abraham: [19] 
que nos permitiría 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">74. </strong>
 vivir sin temor alguno, 
libres de nuestros enemigos, 
para servirle 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">75. </strong>
 con santidad y justicia, 
y estar en su presencia toda nuestra vida. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">76. </strong>
 En cuanto a ti, hijito mío, 
serás llamado profeta del Dios altísimo, 
porque irás delante del Señor 
preparando sus caminos, [20] 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">77. </strong>
 para hacer saber a su pueblo 
que Dios les perdona sus pecados 
y les da la salvación. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">78. </strong>
 Porque nuestro Dios, 
en su gran misericordia, 
nos trae de lo alto el sol de un nuevo día, 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">79. </strong>
 para dar luz a los que viven 
en la más profunda oscuridad, [21] 
y dirigir nuestros pasos 
por el camino de la paz." </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">80. </strong>
 El niño crecía y se hacía fuerte espiritualmente, y vivió en los desiertos hasta el día en que se dio a conocer a los israelitas. </p>
                                                        
