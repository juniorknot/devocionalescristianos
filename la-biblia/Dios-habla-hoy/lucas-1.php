<li class="hide-for-large"><a href="whatsapp://send?text=Lucas 1 (DHH)%0A%0A 1. Muchos han emprendido la tarea de escribir la historia de los hechos que Dios ha llevado a cabo entre nosotros, 2. según nos los transmitieron quienes desde el comienzo fueron testigos presenciales y después recibieron el encargo de anunciar el mensaje. 3. yo también, excelentísimo Teófilo, lo he investigado todo con cuidado desde el principio, y me ha parecido conveniente escribirte estas cosas ordenadamente, 4. para que conozcas bien la verdad de lo que te han enseñado. 5. En el tiempo en que Herodes[1] era rey del país de los judíos, vivía un sacerdote llamado Zacarías, perteneciente al turno de Abías. [2] Su esposa, llamada Isabel, descendía de Aarón. 6. Los dos eran justos delante de Dios y obedecían los mandatos y leyes del Señor de manera intachable. 7. Pero no tenían hijos, porque Isabel era estéril; además, los dos eran y a muy ancianos. 8. Un día en que al grupo sacerdotal de Zacarías le tocó el turno de oficiar delante de Dios, 9. según era costumbre entre los sacerdotes, le tocó en suerte a Zacarías entrar en el santuario del templo del Señor para quemar incienso. [3] 10. Mientras se quemaba el incienso, todo el pueblo estaba orando afuera. 11. En esto se le apareció a Zacarías un ángel del Señor, de pie al lado derecho del altar del incienso. 12. Al ver al ángel, Zacarías se quedó sorprendido y lleno de miedo. 13. Pero el ángel le dijo: 
 –Zacarías, no tengas miedo, porque Dios ha oído tu oración, y tu esposa Isabel te va a dar un hijo, al que pondrás por nombre Juan. 14. Tú te llenarás de gozo, y muchos se alegrarán de su nacimiento, 15. porque tu hijo va a ser grande delante del Señor. No tomará vino ni licor, [4] y estará lleno del Espíritu Santo desde antes de nacer. 16. Hará que muchos de la nación de Israel se vuelvan al Señor su Dios. 17. Este Juan irá delante del Señor, con el espíritu y el poder del profeta Elías, [5] para reconciliar a los padres con los hijos y para que los rebeldes aprendan a obedecer. De este modo preparará al pueblo para recibir al Señor. 18. Zacarías preguntó al ángel: 
 –¿Cómo puedo estar seguro de esto? Porque yo soy muy anciano y mi esposa también. 19. El ángel le contestó: 
 –Yo soy Gabriel, [6] y estoy al servicio de Dios; él me mandó a hablar contigo y darte estas buenas noticias. 20. Pero ahora, como no has creído lo que te he dicho, vas aquedarte mudo; no podrás hablar hasta que, a su debido tiempo, suceda todo esto. 21. Mientras tanto, la gente estaba afuera esperando a Zacarías y preguntándose por qué tardaba tanto en salir del santuario. 22. Cuando al fin salió, no les podía hablar; entonces se dieron cuenta de que había tenido una visión en el santuario, pues les hablaba por señas; y siguió así, sin poder hablar. 23. Cumplido su servicio, Zacarías se fue a su casa. 24. Después de esto, su esposa Isabel quedó encinta, y durante cinco meses no salió de su casa, pensando: 25. "El Señor me ha hecho esto ahora, para que la gente y a no me desprecie."[7] 26. A los seis meses, Dios mandó al ángel Gabriel a un pueblo de Galilea llamado Nazaret, 27. donde vivía una joven llamada María; era virgen, pero estaba comprometida para casarse con un hombre llamado José, descendiente del rey David. 28. El ángel entró en el lugar donde ella estaba, y le dijo: 
 –¡Salve, llena de gracia! El Señor está contigo. 29. María se sorprendió de estas palabras, y se preguntaba qué significaría aquel saludo. 30. El ángel le dijo: 
 –María, no tengas miedo, pues tú gozas del favor de Dios. 31. Ahora vas a quedar encinta: tendrás un hijo, y le pondrás por nombre Jesús. [8] 32. Será un gran hombre, al que llamarán Hijo del Dios altísimo, y Dios el Señor lo hará Rey, como a su antepasado David, 33. para que reine por siempre sobre el pueblo de Jacob. Su reinado no tendrá fin. [9] 34. María preguntó al ángel: 
 –¿Cómo podrá suceder esto, si no vivo con ningún hombre? 35. El ángel le contestó: 
 –El Espíritu Santo vendrá sobre ti, [10] y el poder del Dios altísimo se posará sobre ti. Por eso, el niño que va a nacer será llamado Santo e Hijo de Dios. [11] 36. También tu parienta Isabel va a tener un hijo, a pesar de que es anciana; la que decían que no podía tener hijos, está encinta desde hace seis meses. 37. Para Dios no hay nada imposible. 38. Entonces María dijo: 
 –Yo soy esclava del Señor; que Dios haga conmigo como me has dicho. 
 Con esto, el ángel se fue. 39. Por aquellos días, María se fue de prisa a un pueblo de la región montañosa de Judea, 40. y entró en la casa de Zacarías y saludó a Isabel. 41. Cuando Isabel oyó el saludo de María, la criatura se le estremeció en el vientre, y ella quedó llena del Espíritu Santo. 42. Entonces, con voz muy fuerte, dijo: 
 –¡Dios te ha bendecido más que a todas las mujeres, y ha bendecido a tu hijo! 43. ¿Quién soy yo, para que venga a visitarme la madre de mi Señor? 44. Pues tan pronto como oí tu saludo, mi hijo se estremeció de alegría en mi vientre. 45. ¡Dichosa tú por haber creído que han de cumplirse las cosas que el Señor te ha dicho! 46. María dijo: 
"Mi alma alaba la grandeza del Señor;[12] 47. mi espíritu se alegra 
en Dios mi Salvador. [13] 48. Porque Dios ha puesto sus ojos en mí, 
su humilde esclava, 
y desde ahora siempre me llamarán 
dichosa; 49. porque el Todopoderoso 
ha hecho en mí grandes cosas. 
¡Santo es su nombre! 50. Dios tiene siempre misericordia 
de quienes lo reverencian. 51. Actuó con todo su poder: 
deshizo los planes de los orgullosos, 52. derribó a los reyes de sus tronos 
y puso en alto a los humildes. 53. Llenó de bienes a los hambrientos 
y despidió a los ricos 
con las manos vacías. [14] 54. Ayudó al pueblo de Israel, su siervo, 
y no se olvidó de tratarlo 
con misericordia. [15] 55. Así lo había prometido 
a nuestros antepasados, 
