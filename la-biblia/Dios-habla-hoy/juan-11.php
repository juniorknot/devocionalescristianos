<li class="hide-for-large"><a href="whatsapp://send?text=Juan 11 (DHH)%0A%0A 1. Había un hombre enfermo que se llamaba Lázaro, natural de Betania, el pueblo de María y de su hermana Marta. [1] 2. Esta María, que era hermana de Lázaro, fue la que derramó perfume sobre los pies del Señor y los secó con sus cabellos. [2] 3. Así pues, las dos hermanas mandaron a decir a Jesús: 
 –Señor, tu amigo querido está enfermo. 4. Jesús, al oírlo, dijo: 
 –Esta enfermedad no va a terminar en muerte, sino que ha deservir para mostrar la gloria de Dios, y también la gloria del Hijo de Dios. 5. Aunque Jesús quería mucho a Marta, a su hermana y a Lázaro, 6. cuando le dijeron que Lázaro estaba enfermo se quedó dos días más en el lugar donde se encontraba. 7. Después dijo a sus discípulos: 
 –Vamos otra vez a Judea. 8. Los discípulos le dijeron: 
 –Maestro, hace poco los judíos de esa región trataron de matarte a pedradas, [3] ¿y otra vez quieres ir allá? 9. Jesús les dijo: 
 –¿No es cierto que el día tiene doce horas? Pues si uno anda de día, no tropieza, porque ve la luz que hay en este mundo; 10. pero si uno anda de noche, tropieza, porque le falta la luz. 11. Después añadió: 
 –Nuestro amigo Lázaro se ha dormido, pero voy a despertarlo. 12. Los discípulos le dijeron: 
 –Señor, si se ha dormido, es señal de que va a sanar. 13. Pero lo que Jesús les decía es que Lázaro había muerto, mientras que los discípulos pensaban que se había referido al sueño natural. 14. Entonces Jesús les dijo claramente: 
 –Lázaro ha muerto. 15. y me alegro de no haber estado allí, porque así es mejor para ustedes, para que crean. Pero vamos a verlo. 16. Entonces Tomás, al que llamaban el Gemelo, dijo a los otros discípulos: 
 –Vamos también nosotros, para morir con él. 17. Al llegar, Jesús se encontró con que y a hacía cuatro días que Lázaro había sido sepultado. 18. Betania se hallaba cerca de Jerusalén, a unos tres kilómetros; 19. y muchos de los judíos habían ido a visitar a Marta y a María, para consolarlas por la muerte de su hermano. 20. Cuando Marta supo que Jesús estaba llegando, salió a recibirlo; pero María se quedó en la casa. 21. Marta le dijo a Jesús: 
 –Señor, si hubieras estado aquí, mi hermano no habría muerto. 22. Pero yo sé que aun ahora Dios te dará todo lo que le pidas. 23. Jesús le contestó: 
 –Tu hermano volverá a vivir. 24. Marta le dijo: 
 –Sí, y a sé que volverá a vivir cuando los muertos resuciten, en el día último. [4] 25. Jesús le dijo entonces: 
 –Yo soy la resurrección y la vida. El que cree en mí, aunque muera, vivirá; 26. y todo el que todavía está vivo y cree en mí, no morirá jamás. ¿Crees esto? 27. Ella le dijo: 
 –Sí, Señor, yo creo que tú eres el Mesías, el Hijo de Dios, el que tenía que venir al mundo. 28. Después de decir esto, Marta fue a llamar a su hermana María, y le dijo en secreto: 
 –El Maestro está aquí y te llama. 29. Tan pronto como lo oyó, María se levantó y fue a ver a Jesús. 30. Jesús no había entrado todavía en el pueblo; estaba en el lugar donde Marta se había encontrado con él. 31. Al ver que María se levantaba y salía rápidamente, los judíos que estaban con ella en la casa, consolándola, la siguieron pensando que iba al sepulcro a llorar. 32. Cuando María llegó a donde estaba Jesús, se puso de rodillas a sus pies, diciendo: 
 –Señor, si hubieras estado aquí, mi hermano no habría muerto. 33. Jesús, al ver llorar a María y a los judíos que habían llegado con ella, se conmovió profundamente y se estremeció, 34. y les preguntó: 
 –¿Dónde lo sepultaron? Le dijeron: 
 –Ven a verlo, Señor. 35. y Jesús lloró. 36. Los judíos dijeron entonces: 
 –¡Miren cuánto lo quería! 37. Pero algunos de ellos decían: 
 –Este, que dio la vista al ciego, ¿no podría haber hecho algo para que Lázaro no muriera? 38. Jesús, otra vez muy conmovido, se acercó a la tumba. Era una cueva, cuya entrada estaba tapada con una piedra. 39. Jesús dijo: 
 –Quiten la piedra. 
 Marta, la hermana del muerto, le dijo: 
 –Señor, y a huele mal, porque hace cuatro días que murió. 40. Jesús le contestó: 
 –¿No te dije que, si crees, verás la gloria de Dios? 41. Quitaron la piedra, y Jesús, mirando al cielo, dijo: 
 –Padre, te doy gracias porque me has escuchado. 42. yo sé que siempre me escuchas, pero lo digo por el bien de esta gente que está aquí, para que crean que tú me has enviado. 43. Después de decir esto, gritó: 
 –¡Lázaro, sal de ahí! 44. y el que había estado muerto salió, con las manos y los pies atados con vendas y la cara envuelta en un lienzo. Jesús les dijo: 
 –Desátenlo y déjenlo ir. 45. Por esto creyeron en Jesús muchos de los judíos que habían ido a acompañar a María y que vieron lo que él había hecho. 46. Pero algunos fueron a ver a los fariseos, y les contaron lo que había hecho Jesús. 47. Entonces los fariseos y los jefes de los sacerdotes reunieron a la Junta Suprema, y dijeron: 
