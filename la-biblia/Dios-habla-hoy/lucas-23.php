 –Hemos encontrado a este hombre alborotando a nuestra nación. Dice que no debemos pagar impuestos al emperador, [1] y además afirma que él es el Mesías, el Rey. 3. Pilato le preguntó: 
 –¿Eres tú el Rey de los judíos?
 –Tú lo has dicho –contestó Jesús. 4. Entonces Pilato dijo a los jefes de los sacerdotes y a la gente: 
 –No encuentro en este hombre razón para condenarlo. 5. Pero ellos insistieron con más fuerza: 
 –Con sus enseñanzas está alborotando a todo el pueblo. Comenzó en Galilea, y ahora sigue haciéndolo aquí, en Judea. 6. Al oír esto, Pilato preguntó si el hombre era de Galilea. 7. y al saber que Jesús era de la jurisdicción de Herodes, se lo envió, pues él también se encontraba aquellos días en Jerusalén. 8. Al ver a Jesús, Herodes se puso muy contento, porque durante mucho tiempo había querido verlo, pues había oído hablar de él y esperaba verlo hacer algún milagro. [2] 9. Le hizo muchas preguntas, pero Jesús no le contestó nada. [3] 10. También estaban allí los jefes de los sacerdotes y los maestros de la ley, que lo acusaban con gran insistencia. 11. Entonces Herodes y sus soldados lo trataron con desprecio, y para burlarse de él lo vistieron con ropas lujosas, como de rey. Luego Herodes lo envió nuevamente a Pilato. 12. Aquel día se hicieron amigos Pilato y Herodes, que antes eran enemigos. 13. Pilato reunió a los jefes de los sacerdotes, a las autoridades y al pueblo, 14. y les dijo: 
