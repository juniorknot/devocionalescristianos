 –¡Fuera con ese! ¡Déjanos libre a Barrabás! 19. A este Barrabás lo habían metido en la cárcel por una rebelión ocurrida en la ciudad, y por un asesinato. 20. Pilato, que quería dejar libre a Jesús, les habló otra vez; 21. pero ellos gritaron más alto: 
 –¡Crucifícalo! ¡Crucifícalo! 22. Por tercera vez Pilato les dijo: 
 –Pues ¿qué mal ha hecho? yo no encuentro en él nada que merezca la pena de muerte. Lo voy a castigar y después lo dejaré libre. 23. Pero ellos insistían a gritos, pidiendo que lo crucificara; y tanto gritaron que consiguieron lo que querían. 24. Pilato decidió hacer lo que le estaban pidiendo; 25. así que dejó libre al hombre que habían escogido, el que estaba en la cárcel por rebelión y asesinato, y entregó a Jesús a la voluntad de ellos. 26. Cuando llevaron a Jesús a crucificarlo, echaron mano de un hombre de Cirene llamado Simón, que venía del campo, y lo hicieron cargar con la cruz y llevarla detrás de Jesús. 27. Mucha gente y muchas mujeres que lloraban y gritaban de tristeza por él, lo seguían. 28. Pero Jesús las miró y les dijo: 
 –Mujeres de Jerusalén, no lloren por mí, sino por ustedes mismas y por sus hijos. 29. Porque vendrán días en que se dirá: 'Dichosas las que no pueden tener hijos, las mujeres que no dieron a luz ni tuvieron hijos que criar. '[5] 30. Entonces comenzará la gente a decir a los montes: '¡Caigan sobre nosotros!', y a las colinas: '¡Escóndannos!' 31. Porque si con el árbol verde hacen todo esto, ¿qué no harán con el seco? 32. También llevaban a dos criminales, para crucificarlos junto con Jesús. 33. Cuando llegaron al sitio llamado La Calavera, [6] crucificaron a Jesús y a los dos criminales, uno a su derecha y otro a su izquierda. v [ 34. Jesús dijo: "Padre, perdónalos, porque no saben lo que hacen."] 35. La gente estaba allí mirando; y hasta las autoridades se burlaban de él, diciendo: 
 –Salvó a otros; que se salve a sí mismo ahora, si de veras es el Mesías de Dios y su escogido. 36. Los soldados también se burlaban de Jesús. Se acercaban y le daban a beber vino agrio, [9] 37. diciéndole: 
 –¡Si tú eres el Rey de los judíos, sálvate a ti mismo! 38. y había un letrero sobre su cabeza, que decía: "Este es el Rey de los judíos." 39. Uno de los criminales que estaban colgados, lo insultaba: 
 –¡Si tú eres el Mesías, sálvate a ti mismo y sálvanos también a nosotros! 40. Pero el otro reprendió a su compañero, diciéndole: 
 –¿No tienes temor de Dios, tú que estás bajo el mismo castigo? 41. Nosotros estamos sufriendo con toda razón, porque estamos pagando el justo castigo de lo que hemos hecho; pero este hombre no hizo nada malo. 42. Luego añadió: 
 –Jesús, acuérdate de mí cuando comiences a reinar. 43. Jesús le contestó: 
 –Te aseguro que hoy estarás conmigo en el paraíso. 44. Desde el mediodía y hasta las tres de la tarde, toda la tierra quedó en oscuridad. 45. El sol dejó de brillar, y el velo[10] del templo se rasgó por la mitad. 46. Jesús gritó con fuerza y dijo: 
