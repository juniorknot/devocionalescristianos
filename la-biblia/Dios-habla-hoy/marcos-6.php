<li class="hide-for-large"><a href="whatsapp://send?text=Marcos 6 (DHH)%0A%0A 1. Jesús se fue de allí a su propia tierra, y sus discípulos fueron con él. 2. Cuando llegó el sábado, comenzó a enseñar en la sinagoga. Y muchos oyeron a Jesús, y se preguntaron admirados: 
 –¿Dónde aprendió este tantas cosas? ¿De dónde ha sacado esasabiduría y los milagros que hace? 3. ¿No es este el carpintero, el hijo de María y hermano de Santiago, José, Judas y Simón? ¿Y no viven sus hermanas también aquí, entre nosotros? y no tenían fe en él. 4. Pero Jesús les dijo: 
 –En todas partes se honra a un profeta, menos en su propiatierra, entre sus parientes y en su propia casa. 5. No pudo hacer allí ningún milagro, aparte de poner las manos sobre unos pocos enfermos y sanarlos. 6. y estaba asombrado porque aquella gente no creía en él. Jesús recorría las aldeas cercanas, enseñando. 7. Llamó a los doce discípulos, y comenzó a enviarlos de dos en dos, dándoles autoridad sobre los espíritus impuros. [1] 8. Les ordenó que no llevaran nada para el camino, sino solamente un bastón. No debían llevar pan ni provisiones ni dinero. 9. Podían ponerse sandalias, pero no llevar ropa de repuesto. 10. Les dijo: 
 –Cuando entren ustedes en una casa, quédense allí hasta que se vayan del lugar. 11. y si en algún lugar no los reciben ni los quieren oír, salgan de allí y sacúdanse el polvo de los pies, [2] para que les sirva a ellos de advertencia. [3] 12. Entonces salieron los discípulos a decirle a la gente que se volviera a Dios. 13. También expulsaron muchos demonios, y curaron a muchos enfermos ungiéndolos con aceite. [4] 14. El rey Herodes oyó hablar de Jesús, cuya fama había corrido por todas partes. Pues unos decían: "Juan el Bautista ha resucitado, y por eso tiene este poder milagroso." 15. Otros decían: "Es el profeta Elías." y otros: "Es un profeta, como los antiguos profetas."[5] 16. Al oír estas cosas, Herodes decía: 
 –Ese es Juan. Yo mandé cortarle la cabeza y ahora haresucitado. 17. Es que, por causa de Herodías, Herodes había mandado arrestar a Juan, y lo había hecho encadenar en la cárcel. Herodías era esposa de Filipo, hermano de Herodes, pero Herodes se había casado con ella. [6] 18. y Juan había dicho a Herodes: "No debes tener como tuya a la mujer de tu hermano."[7] 19. Herodías odiaba por eso a Juan, y quería matarlo; pero no podía, 20. porque Herodes le tenía miedo, sabiendo que era un hombre justo y santo, y lo protegía. Y aunque al oírlo se quedaba sin saber qué hacer, Herodes escuchaba a Juan de buena gana. 21. Pero Herodías vio llegar su oportunidad cuando Herodes, en su cumpleaños, dio un banquete a sus jefes y comandantes y a las personas importantes de Galilea. 22. La hija de Herodías entró en el lugar del banquete y bailó, y el baile gustó tanto a Herodes y a los que estaban cenando con él, que el rey dijo a la muchacha: 
 –Pídeme lo que quieras, y te lo daré. 23. y le juró una y otra vez que le daría cualquier cosa que pidiera, aunque fuera la mitad del país que él gobernaba. 24. Ella salió, y le preguntó a su madre: 
 –¿Qué pediré? Le contestó: 
 –Pídele la cabeza de Juan el Bautista. 25. La muchacha entró de prisa donde estaba el rey, y le dijo: 
 –Quiero que ahora mismo me des en un plato la cabeza de Juan elBautista. 26. El rey se puso muy triste; pero como había hecho un juramento en presencia de sus invitados, no quiso negarle lo que le pedía. 27. Así que mandó en seguida a un soldado con la orden de llevarle la cabeza de Juan. Fue el soldado a la cárcel, le cortó la cabeza a Juan 28. y se la llevó en un plato. Se la dio a la muchacha, y ella se la entregó a su madre. 29. Cuando los seguidores de Juan lo supieron, recogieron el cuerpo y se lo llevaron a enterrar. [8] 30. Después de esto, los apóstoles se reunieron con Jesús y le contaron todo lo que habían hecho y enseñado. 31. Jesús les dijo: 
 –Vengan, vamos nosotros solos a descansar un poco en un lugartranquilo. Porque iba y venía tanta gente, que ellos ni siquiera teníantiempo para comer. 32. Así que Jesús y sus apóstoles se fueron en una barca a un lugar apartado. 33. Pero muchos los vieron ir, y los reconocieron; entonces de todos los pueblos corrieron allá, y llegaron antes que ellos. 34. Al bajar Jesús de la barca, vio la multitud, y sintió compasión de ellos, porque estaban como ovejas que no tienen pastor; y comenzó a enseñarles muchas cosas. [9] 35. Por la tarde, sus discípulos se le acercaron y le dijeron: 
 –Ya es tarde, y este es un lugar solitario. 36. Despide a la gente, para que vayan por los campos y las aldeas de alrededor y se compren algo de comer. 37. Pero Jesús les contestó: 
 –Denles ustedes de comer. 
 Ellos respondieron: 
 –¿Quieres que vayamos a comprar pan por el equivalente alsalario de doscientos días, para darles de comer? 38. Jesús les dijo: 
 –¿Cuántos panes tienen ustedes? Vayan a verlo. 
