                                                                                <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
 Al llegar la noche, la barca y a estaba en medio del lago. Jesús, que se había quedado solo en tierra, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 vio que remaban con dificultad, porque tenían el viento en contra. A la madrugada, fue Jesús hacia ellos caminando sobre el agua, y a iba a pasar junto a ellos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 Cuando lo vieron andar sobre el agua, pensaron que era un fantasma, y gritaron; </p>
