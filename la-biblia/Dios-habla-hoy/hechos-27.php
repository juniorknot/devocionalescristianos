<li class="hide-for-large"><a href="whatsapp://send?text=Hechos 27 (DHH)%0A%0A 1. Cuando decidieron mandarnos a Italia, Pablo y los otros presos fueron entregados a un capitán que se llamaba Julio, del batallón llamado del Emperador. 2. Nos embarcamos, pues, en un barco del puerto de Adramitio que estaba a punto de salir para los puertos de Asia. Estaba también con nosotros Aristarco, [1] que era de Tesalónica, ciudad de Macedonia. 3. Al día siguiente llegamos al puerto de Sidón, donde Julio trató a Pablo con mucha consideración, pues lo dejó visitar a sus amigos y ser atendido por ellos. 4. Saliendo de Sidón, navegamos protegidos del viento por la isla de Chipre, porque teníamos el viento en contra. 5. Bordeamos la costa de Cilicia y Panfilia, y llegamos a Mira, una ciudad de Licia. 6. El capitán de los soldados encontró allí un barco de Alejandría que iba a Italia, y nos hizo embarcar para seguir el viaje. 7. Durante varios días viajamos despacio, y con mucho trabajo llegamos frente a Cnido. Como todavía teníamos el viento en contra, pasamos frente a Salmona dando la vuelta a la isla de Creta; 8. y navegando con dificultad a lo largo de la costa, llegamos aun lugar llamado Buenos Puertos, cerca del pueblo de Lasea. 9. Se había perdido mucho tiempo, y a era peligroso viajar por mar porque se acercaba el invierno. Por eso, Pablo les aconsejó: 10. –Señores, veo que este viaje va a ser muy peligroso, y que vamos a perder tanto el barco como su carga, y que hasta podemos perder la vida. 11. Pero el capitán de los soldados hizo más caso al dueño del barco y al capitán del mismo que a Pablo. 12. y como aquel puerto no era bueno para pasar el invierno, casi todos pensaron que sería mejor salir de allí e intentar llegar a Fenice, un puerto de Creta que mira al noroeste y al sudoeste, y pasar allí el invierno. 13. Pensando que podrían seguir el viaje porque comenzaba a soplar un viento suave del sur, salieron y navegaron junto a la costa de Creta. 14. Pero poco después un viento huracanado del nordeste azotó el barco, 15. y comenzó a arrastrarlo. Como no podíamos mantener el barco de cara al viento, tuvimos que dejarnos llevar por él. 16. Pasamos por detrás de una pequeña isla llamada Cauda, donde el viento no soplaba tan fuerte, y con mucho trabajo pudimos recoger el bote salvavidas. 17. Después de subirlo a bordo, usaron sogas para reforzar el barco. Luego, como tenían miedo de encallar en los bancos de arena llamados la Sirte, echaron el ancla flotante y se dejaron llevar por el viento. 18. Al día siguiente, la tempestad era todavía fuerte, así que comenzaron a arrojar al mar la carga del barco; 19. y al tercer día, con sus propias manos, arrojaron también los aparejos del barco. 20. Por muchos días no se dejaron ver ni el sol ni las estrellas, y con la gran tempestad que nos azotaba habíamos perdido y a toda esperanza de salvarnos. 21. Como habíamos pasado mucho tiempo sin comer, Pablo se levantó en medio de todos y dijo: 
