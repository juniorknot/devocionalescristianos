                                                                                <p style="style=text-align: justify;"><strong id="numeros">2. </strong>
 Haré que sus enemigos traten a Babilonia 
como a trigo que se lanza al aire; 
haré que dejen sin habitantes 
su territorio. 
Cuando llegue el día del desastre, 
la atacarán por todas partes. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">3. </strong>
 ¡Que preparen sus arcos los arqueros! 
¡Que se pongan las corazas! 
¡No tengan compasión 
de los jóvenes de Babilonia; 
destruyan su ejército por completo! 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">4. </strong>
 Por todo el país de los caldeos 
la gente quedará tendida por las calles, 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">5. </strong>
 pues aunque Israel y Judá 
han pecado contra mí 
y han llenado de pecado su país, 
yo, el Señor todopoderoso, 
el Dios Santo de Israel, 
aún no los he abandonado. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">6. </strong>
 "¡Huyan de Babilonia! 
¡Sálvese quien pueda! 
¡No mueran por causa del pecado de ella! 
Esta es la hora de mi venganza, 
y le pagaré como merece. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">7. </strong>
 Babilonia era en mi mano 
una copa de oro 
que embriagaba a todo el mundo. 
Las naciones bebían de ese vino 
y perdían el sentido. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">8. </strong>
 Pero de pronto Babilonia 
cayó hecha pedazos. 
¡Pónganse a llorar por ella! 
Traigan remedios para sus heridas, 
a ver si se cura." 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">9. </strong>
 y a le pusimos remedios a Babilonia, 
pero no se curó. 
¡Déjenla! Vámonos de aquí, 
cada uno a su patria, 
pues su crimen llega hasta el cielo, 
se levanta hasta las nubes. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">10. </strong>
 El Señor hizo triunfar nuestro derecho. 
Vengan, vamos a contar en Sión 
lo que ha hecho el Señor nuestro Dios. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">11. </strong>
 El Señor quiere destruir Babilonia, 
y ha despertado en los reyes de Media[1] 
ese mismo sentimiento. 
Esa es la venganza del Señor 
por lo que hicieron con su templo. 
¡Preparen las flechas! 
¡Sujeten los escudos! 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">12. </strong>
 ¡Den la señal de atacar los muros 
de Babilonia! 
¡Refuercen la guardia! 
¡Pongan centinelas! 
¡Tiendan emboscadas! 
Porque el Señor preparó y llevó a cabo 
los planes que había anunciado 
contra los habitantes de Babilonia. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">13. </strong>
 Tú, Babilonia, 
que estás junto a grandes ríos 
y tienes grandes riquezas, 
¡ya te ha llegado tu fin, 
el término de tu existencia! 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">14. </strong>
 El Señor todopoderoso ha jurado 
por su vida: 
"¡Te llenaré de enemigos, 
como de langostas, 
y ellos cantarán victoria sobre ti!" 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">15. </strong>
 El Señor, con su poder, hizo la tierra; 
con su sabiduría afirmó el mundo; 
con su inteligencia extendió el cielo. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">16. </strong>
 Con voz de trueno hace rugir 
el agua en el cielo, 
hace subir las nubes 
desde el extremo de la tierra, 
hace brillar los relámpagos 
en medio de la lluvia 
y saca el viento 
de donde lo tiene guardado. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">17. </strong>
 Necio e ignorante es todo hombre. 
Los ídolos defraudan al que los fabrica: 
son imágenes engañosas y sin vida; 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">18. </strong>
 son objetos sin valor, ridículos, 
que el Señor, en el juicio, destruirá. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">19. </strong>
 ¡Qué diferente es el Dios de Jacob, 
creador de todo lo que existe! 
Él escogió a Israel como su propiedad. 
El Señor todopoderoso: 
ese es su nombre. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">20. </strong>
 "¡Babilonia, tú eres mi mazo, 
mi arma de guerra! 
Contigo destrozo naciones 
y destruyo reinos. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">21. </strong>
 Contigo destrozo caballos y jinetes, 
carros y cocheros. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">22. </strong>
 Contigo destrozo hombres y mujeres, 
ancianos y jóvenes, 
muchachos y muchachas. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">23. </strong>
 Contigo destrozo pastores y rebaños, 
labradores y bueyes, 
gobernadores y funcionarios. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">24. </strong>
 "Pero ante los propios ojos de ustedes 
pagaré a Babilonia y a todos los caldeos 
como merecen, 
por el mal que hicieron a Sión. 
yo, el Señor lo afirmo. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">25. </strong>
 yo estoy en contra tuya, 
montaña destructora, 
que destruyes toda la tierra. 
yo, el Señor, lo afirmo. 
Levantaré la mano para castigarte, 
te haré rodar desde los peñascos 
y te convertiré en un cerro quemado. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">26. </strong>
 Ninguna de las piedras de tus ruinas 
servirá para construir edificios. 
Serás un desierto para siempre. 
yo, el Señor, lo afirmo. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">27. </strong>
 "¡Den en el país la señal de ataque! 
¡Toquen la trompeta 
y preparen a las naciones 
para atacar a Babilonia! 
¡Levanten contra ella a los reinos 
de Ararat, 
de Miní y de Asquenaz! 
¡Nombren un general que dirija la batalla! 
¡Hagan avanzar la caballería 
como langostas furiosas! 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">28. </strong>
 Preparen a las naciones para atacarla; 
preparen a los reyes de Media, 
y a sus gobernadores y funcionarios, 
y toda la tierra que dominan." 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">29. </strong>
 La tierra tiembla y se estremece, 
porque el Señor está cumpliendo 
sus planes 
de convertir Babilonia 
en un horrible desierto. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">30. </strong>
 Los soldados babilonios 
dejaron de luchar, 
se quedaron en sus fortalezas; 
sus fuerzas se agotaron, 
y hasta parecían mujeres. 
Sus ciudades fueron invadidas 
y sus casas incendiadas. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">31. </strong>
 Uno tras otro corren los mensajeros 
a anunciar al rey de Babilonia 
que su ciudad fue conquistada 
por completo. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">32. </strong>
 El enemigo ocupó los pasos de los ríos, 
incendió los puestos de defensa 
y los soldados se llenaron de terror. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">33. </strong>
 Porque el Señor todopoderoso, 
el Dios de Israel, dice: 
"¡Babilonia es como una era 
pisoteada en el tiempo de la trilla, 
y muy pronto va a llegarle 
el tiempo de la cosecha!" 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">34. </strong>
 Dice Jerusalén, la ciudad de Sión: 
"Nabucodonosor, el rey de Babilonia, 
me hizo pedazos y me devoró; 
me dejó como un plato vacío. 
Como un monstruo del mar, me tragó; 
se llenó con lo que más le gustó de mí, 
y el resto lo tiró. 
¡Que pague Babilonia por la violencia 
que me hizo! 
¡Que paguen los caldeos 
por la gente que me mataron!" 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">35. </strong>
 (TEXT OMITTED) </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
 El Señor dice: 
"Yo te voy a hacer justicia, 
me voy a vengar de tus enemigos. 
Voy a dejar completamente secos 
el río y los manantiales de Babilonia, 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 que quedará convertida en un montón de ruinas, 
en guarida de chacales, 
en un lugar inhabitable 
que a todos causará espanto y horror. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 Todos ellos rugirán como leones 
y gruñirán como cachorros. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 Cuando ardan de calor, yo les daré a beber 
bebidas que los embriaguen 
y les hagan perder el sentido. 
Así caerán en un sueño eterno 
del que no despertarán. 
yo, el Señor, lo afirmo. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
 Luego los llevaré al matadero, 
como se lleva a los corderos, 
chivos y carneros." 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 ¡Babilonia, [2] la ciudad famosa en todo el mundo, 
ha caído, ha sido conquistada! 
¡Cómo se espantan las naciones al verla! 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 El mar inundó Babilonia, 
la cubrió con sus grandes olas. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 Sus ciudades se convirtieron 
en horrible desierto, 
en tierra seca y desolada, 
donde nadie vive, 
por donde nadie pasa. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 "Castigaré al dios Bel en Babilonia, 
le haré vomitar lo que se había tragado, 
y no volverán las naciones a acudir a él. 
¡Ya cayeron las murallas de Babilonia! 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 Pueblo mío, sal de ahí, 
¡y que cada cual salve su vida 
de mi ardiente ira! 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 No se asusten, no tengan miedo 
por los rumores que corren en el país. 
Cada año correrán rumores diferentes, 
rumores de violencia en el país 
y de luchas entre gobernantes. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
 Así pues, vendrá el día 
en que castigaré a los ídolos de Babilonia. 
Todo el país quedará humillado 
y cubierto de cadáveres. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 El cielo y la tierra y todo lo que existe 
se alegrarán cuando caiga Babilonia, 
cuando vengan del norte sus destructores. 
yo, el Señor, lo afirmo. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 Sí, Babilonia ha de caer 
por los muertos que le causó a Israel 
y por los muertos que causó 
en todo el mundo." 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 Ustedes, los que escaparon de la matanza, 
huyan sin parar, 
y aun cuando estén lejos, 
piensen en el Señor 
y acuérdense de Jerusalén. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 Ustedes dicen: "Sentimos vergüenza 
al oír cómo nos insultan. 
La deshonra cubre nuestros rostros, 
porque gente extranjera ha entrado 
hasta los sitios más santos del templo." 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 Pero el Señor afirma: 
"Viene el día 
en que castigaré a los ídolos de Babilonia, 
y en todo su territorio 
gemirán los heridos. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 y aunque Babilonia suba hasta el cielo 
para poner bien alta su fortaleza, 
aun allí le enviaré enemigos 
que la destruyan. 
yo, el Señor, lo afirmo." 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 Desde Babilonia llegan gritos de dolor, 
desde el país de los caldeos 
llega un gran ruido. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 Es el Señor, que destruye a Babilonia 
y pone fin a su bullicio. 
Los enemigos, como olas rugientes, 
caerán sobre ella con gran estruendo. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 Vinieron a destruir Babilonia; 
y a capturaron a sus guerreros 
y les hicieron pedazos sus arcos. 
Porque el Señor es un Dios 
que a cada cual le da lo que merece. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 El Rey, el Señor todopoderoso, afirma: 
"Emborracharé a los jefes de Babilonia, 
a sus sabios y gobernadores, 
y a sus funcionarios y soldados, 
y caerán en un sueño eterno 
del que no despertarán. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
 yo, el Señor todopoderoso, digo: 
Las anchas murallas de Babilonia 
serán derribadas por completo, 
y sus enormes puertas 
serán incendiadas. 
Inútilmente habrán trabajado 
las naciones, 
pues sus fatigas terminarán 
en el fuego." </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">59. </strong>
 En el año cuarto del gobierno de Sedequías, rey de Judá, el profeta Jeremías dio un encargo a Seraías, hijo de Nerías y nieto de Maaseías. Seraías acompañaba al rey Sedequías en su viaje a Babilonia, como encargado de arreglar el hospedaje del rey. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">60. </strong>
 Jeremías había escrito en un libro su mensaje acerca de todas las calamidades que habrían de venir sobre Babilonia, es decir, todo lo escrito acerca de ella, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">61. </strong>
 y le dijo a Seraías: "Cuando llegues a Babilonia, ten cuidado de leer todo este mensaje. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">62. </strong>
 Luego dirás: 'Señor, tú has dicho que destruirás este lugar, de manera que nadie vuelva a vivir aquí, ni hombres ni animales, porque lo vas a convertir en un desierto para siempre. ' </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">63. </strong>
 y luego que termines de leer este libro, átale una piedra y échalo al río Éufrates, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">64. </strong>
 y di: 'Así se hundirá Babilonia, y no se volverá a levantar del desastre que el Señor le va a enviar. ' " Aquí terminan las palabras de Jeremías. </p>
                                                        
