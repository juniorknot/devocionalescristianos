 "Israel es como una oveja perdida, perseguida por leones. Primero se lo comió el rey de Asiria; después, Nabucodonosor, rey de Babilonia, le royó los huesos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">18. </strong>
 Por eso yo, el Señor todopoderoso, el Dios de Israel, digo: Voy a castigar al rey de Babilonia y a su país, como lo hice con el rey de Asiria. [2] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">19. </strong>
 Haré que Israel vuelva a su tierra, y como una oveja tendrá pastos abundantes en el Carmelo, en Basán, en la región montañosa de Efraín y en Galaad. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">20. </strong>
 yo, el Señor, afirmo: En aquel tiempo, Israel y Judá estarán libres de culpas y pecados, porque yo perdonaré a los que deje con vida. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">21. </strong>
 "¡Ataquen la región de Merataim 
y a los habitantes de Pecod![3] 
¡Persíganlos, destrúyanlos por completo! 
¡Cumplan mis órdenes en todo! 
yo, el Señor, lo ordeno." 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">22. </strong>
 En el país se oye estruendo de batalla, 
de gran destrucción. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">23. </strong>
 ¡Babilonia, el martillo del mundo entero, 
ha quedado roto, hecho pedazos! 
¡Qué horror sienten las naciones 
al ver lo que a Babilonia le ha pasado! 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">24. </strong>
 Tú misma, Babilonia, 
te tendiste una trampa, 
y sin darte cuenta caíste en ella. 
Quedaste presa, atrapada, 
porque te opusiste al Señor. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">25. </strong>
 El Señor abrió el depósito de sus armas 
y sacó las armas de su ira, 
porque el Señor todopoderoso 
tiene una tarea que llevar a cabo 
en la nación de los caldeos. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">26. </strong>
 ¡Atáquenla por todas partes! 
¡Abran sus graneros! 
¡Amontonen a la gente y destrúyanla! 
¡Que no quede nadie con vida! 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">27. </strong>
 ¡Maten a todos sus soldados, 
envíen los al matadero! 
¡Ay de ellos! ¡Les llegó su día, 
la hora de su castigo! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">28. </strong>
 (Gente escapada de Babilonia llega a Sión contando cómo el Señor nuestro Dios ha dado su merecido a Babilonia por lo que ella hizo con el templo. ) 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">29. </strong>
 "¡Llamen a todos los arqueros, 
para que ataquen a Babilonia! 
¡Acampen alrededor de la ciudad 
para que nadie escape! 
¡Páguenle como merece! 
¡Hagan con ella 
lo mismo que ella hizo! 
Porque ha sido insolente conmigo, 
con el Señor, el Dios Santo de Israel. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">30. </strong>
 Por eso sus jóvenes 
caerán muertos por las calles 
y todos sus guerreros morirán en ese día. 
yo, el Señor, lo afirmo." 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">31. </strong>
 El Señor todopoderoso dice: 
"Nación insolente, 
yo estoy en contra tuya; 
ha llegado tu día, la hora de castigarte. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">32. </strong>
 Nación insolente, tropezarás y caerás, 
y nadie te levantará. 
Prenderé fuego a todas tus ciudades, 
y ese fuego destruirá 
todos tus contornos." </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">33. </strong>
 El Señor todopoderoso dice: "El pueblo de Israel y el pueblo de Judá están sufriendo la opresión. Sus enemigos los tienen presos y no quieren soltarlos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">34. </strong>
 Pero su redentor[4] es fuerte y se llama el Señor todopoderoso. Él se encargará de hacerles justicia; traerá paz al país y terror a los habitantes de Babilonia." 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">35. </strong>
 El Señor dice: 
"¡Guerra a Babilonia y a sus habitantes! 
¡A sus jefes y a sus sabios! 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
 ¡Guerra a sus adivinos: 
que se vuelvan locos! 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 ¡Guerra a sus soldados: 
que tiemblen de miedo! 
¡Guerra a sus caballos y sus carros! 
¡Guerra a sus soldados mercenarios: 
que se vuelvan como mujeres! 
¡Guerra a sus tesoros: que se los roben! 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 ¡Guerra a sus ríos: que se sequen! 
Porque Babilonia es un país 
de ídolos horribles, 
y por ellos ha perdido el sentido. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 "Por eso, en Babilonia harán sus guaridas los gatos monteses y los chacales, y allí vivirán los avestruces. Jamás en todos los siglos venideros volverá a ser habitada. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
 Quedará como Sodoma y Gomorra y las ciudades vecinas, cuando fueron destruidas. Nadie volverá a vivir allí, ni siquiera de paso. Yo, el Señor, lo afirmo. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 "Desde lejanas tierras del norte, 
una nación poderosa y numerosos reyes 
se preparan a venir. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 Están armados de arcos y lanzas; 
son crueles, no tienen compasión, 
sus gritos son 
como el estruendo del mar, 
y van montados a caballo. 
Están listos para la batalla 
contra Babilonia. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 El rey de Babilonia, al saber esta noticia, 
se ha quedado paralizado de miedo, 
y siente angustia y dolor 
como mujer de parto. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 "Vendré repentinamente como un león que sale de los matorrales del Jordán y se lanza a los lugares donde siempre hay pasto fresco, y haré huir de ahí a los de Babilonia, y haré que gobierne la persona que yo escoja. Pues ¿quién puede compararse a mí? ¿Quién puede desafiarme? ¿Qué pastor me puede hacer frente? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 Escuchen el plan que yo, el Señor, he preparado contra Babilonia; el proyecto que he formulado contra el país de los caldeos: Aun a los corderos más pequeños se los llevarán, y las mismas praderas serán también destrozadas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 La caída de Babilonia hará tanto ruido, que la tierra temblará, y sus gritos se oirán en las demás naciones." </p>
                                                        
