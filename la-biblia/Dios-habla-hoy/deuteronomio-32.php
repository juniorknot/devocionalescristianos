mi discurso será como el rocío, 
como llovizna sobre la hierba, 
como gotas de agua sobre el pasto. 3. "Proclamaré el nombre del Señor: 
¡reconozcan la grandeza  del Dios nuestro! 4. Él es nuestro protector; 
sus obras son perfectas, 
sus acciones son justas. 
Es el Dios de la verdad, 
en él no hay injusticia; 
¡él es justo y verdadero! 5. "Gente malvada y perversa, 
que ha ofendido a Dios, 
que son indignos de ser sus hijos: 6. ¿Así es como le pagan al Señor? 
Pueblo necio y sin sabiduría, 
¿no es él tu padre, tu creador? 
¡Él te creó y te dio el ser! 7. "Vuelve atrás la mirada, 
piensa en los tiempos pasados; 
pide a tu padre que te lo diga, 
y a los ancianos que te lo cuenten: 8. Hubo una vez en que el Altísimo 
hizo reparto de hombres y naciones, 
y fijó las fronteras de los pueblos. 
Pero tomó en cuenta a los israelitas, 9. pues la herencia del Señor, la gente suya, 
es el pueblo de Jacob. 10. Los encontró por el desierto, 
por tierras secas y azotadas por el viento; 
los envolvió en sus brazos, los instruyó 
y los cuidó como a la niña de sus ojos. 11. Como águila que revolotea sobre el nido 
y anima a sus polluelos a volar, 
así el Señor extendió sus alas 
y , tomándolos, los llevó a cuestas. 12. "El Señor los guió, y nadie más; 
¡ningún dios extraño tuvo que ayudarlo! 13. Los llevó en marcha triunfal 
por las regiones altas del país, 
los alimentó con los frutos del campo, 
de la roca les dio a beber miel 
y del duro pedernal les dio aceite; 14. de sus ganados tuvieron leche y cuajada, 
y comieron lo mejor de los corderos, 
carneros de Basán y machos cabríos; 
comieron el mejor grano de trigo 
y bebieron el vino, la sangre de las uvas. 15. "Pero engordó Jesurún, y dio coses 
(tanto engordó que brillaba de gordo), 
y abandonó a Dios, su creador; 
despreció a su protector y salvador. 16. Provocaron los celos y la ira de Dios 
al adorar ídolos repugnantes; 17. ofrecieron sacrificios a demonios, 
a dioses falsos  que nunca antes conocieron; 
dioses nuevos, recién llegados, 
a los que jamás sus padres dieron culto. 18. "Olvidaste, Israel, a tu padre y protector; 
olvidaste al Dios que te dio la vida. 19. y Dios se enojó al ver esto, 
y rechazó a sus hijos y a sus hijas; 20. y dijo: 'Voy a volverles la espalda, 
