"Mientras yo esa mirando los cuernos, vi que de entre ellos salía otro cuerno más pequeño, y entonces le arrancaron tres cuernos para dejar lugar al último que le había salido, el cual tenía ojos como los de un ser humano y una boca que hablaba con mucha arrogancia. 9. "Seguí mirando, 
hasta que fueron puestos unos tronos 
y un Anciano[4] se sentó. 
Su vestido era blanco como la nieve, 
y su cabello como lana limpia. 
El trono y sus ruedas 
eran llamas de fuego, 10. y un río de fuego salía de delante de él. 
Miles y miles le servían, 
y millones y millones estaban de pie en su presencia. 
El tribunal dio principio a la sesión, 
y los libros fueron abiertos. [5] 11. "Yo estaba mirando, atraído por las cosas tan arrogantes que el cuerno pequeño decía; y mientras estaba mirando, mataron al monstruo y lo despedazaron, y luego lo echaron al fuego para que se quemara. 12. También a los otros monstruos se les quitó el poder, pero los dejaron seguir viviendo hasta que les llegara su hora. 13. "Yo seguía viendo estas visiones en la noche. De pronto: 
"Vi que venía entre las nubes 
alguien parecido a un hijo de hombre, [6] 
el cual fue a donde estaba el Anciano; 
y le hicieron acercarse a él. 14. y le fue dado el poder, 
la gloria y el reino, 
y gente de todas las naciones 
y lenguas le servían. 
Su poder será siempre el mismo, 
y su reino jamás será destruido. 15. "Yo, Daniel, sentí que el terror se apoderaba de mí; y muy preocupado por todo lo que había visto, 16. me acerqué a uno de los que estaban allí de pie, y le pedí que me explicara todo aquello. Él aceptó explicármelo, y me dijo: 17. 'Estos cuatro monstruos son cuatro reyes que dominarán el mundo. [7] 18. Pero después el reino será entregado al pueblo del Dios altísimo, [8] y será suyo por toda la eternidad. ' 19. "Yo quería saber más acerca del cuarto monstruo, que era tan diferente de los otros, pues su aspecto era horrible: tenía dientes de hierro y garras de bronce; todo lo devoraba y destrozaba, y pisoteaba las sobras. 20. También quería yo saber más acerca de sus diez cuernos, y del cuerno pequeño que tenía ojos y una boca que hablaba con mucha arrogancia, pues hasta parecía más grande que los otros cuernos, y tres cuernos habían caído para dejarle lugar. 21. Entonces vi que este cuerno luchaba contra el pueblo de Dios, y lo vencía, 22. hasta que llegó el Anciano e hizo justicia al pueblo del Dios altísimo, pues se había cumplido el tiempo para que el pueblo de Dios tomara posesión del reino. 23. y dijo: 
'El cuarto monstruo será un cuarto reino 
que habrá sobre la tierra, 
diferente de todos los demás. 
Devorará toda la tierra, 
la pisoteará y la destrozará. 24. Los diez cuernos son diez reyes 
que reinarán en ese reino. 
Después de ellos subirá otro al poder, 
que será muy diferente de los primeros 
y que derribará a tres de estos reyes. 25. Insultará al Dios altísimo 
e irá acabando con su pueblo; 
tratará de cambiar la ley de Dios 
y las fiestas religiosas, 
