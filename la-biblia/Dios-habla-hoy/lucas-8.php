<li class="hide-for-large"><a href="whatsapp://send?text=Lucas 8 (DHH)%0A%0A 1. Después de esto, Jesús anduvo por muchos pueblos y aldeas, anunciando la buena noticia del reino de Dios. Los doce apóstoles lo acompañaban, 2. como también algunas mujeres que él había curado de espíritus malignos y enfermedades. [1] Entre ellas iba María, la llamada Magdalena, de la que habían salido siete demonios; 3. también Juana, esposa de Cuza, el que era administrador de Herodes; y Susana; y muchas otras que los ayudaban con lo que tenían. 4. Muchos salieron de los pueblos para ver a Jesús, de manera que se reunió mucha gente. Entonces les contó esta parábola: 5. "Un sembrador salió a sembrar su semilla. Y al sembrar, una parte de la semilla cayó en el camino, y fue pisoteada, y las aves se la comieron. 6. Otra parte cayó entre las piedras; y cuando esa semilla brotó, se secó por falta de humedad. 7. Otra parte de la semilla cayó entre espinos; y al nacer juntamente, los espinos la ahogaron. 8. Pero otra parte cayó en buena tierra; y creció, y dio una buena cosecha, hasta de cien granos por semilla." 
 Esto dijo Jesús, y añadió con voz muy fuerte: "¡Los que tienen oídos, oigan!" 9. Los discípulos le preguntaron a Jesús qué quería decir aquella parábola. 10. Les dijo: "A ustedes Dios les da a conocer los secretos de su reino; pero a los otros les hablo por medio de parábolas, para que por más que miren no vean, y por más que oigan no entiendan. [2] 11. "Esto es lo que quiere decir la parábola: La semilla representa el mensaje de Dios; 12. y la parte que cayó por el camino representa a los que oyen el mensaje, pero viene el diablo y se lo quita del corazón, para que no crean y se salven. 13. La semilla que cayó entre las piedras representa a los que oyen el mensaje y lo reciben con gusto, pero no tienen suficiente raíz; creen por algún tiempo, pero a la hora de la prueba fallan. 14. La semilla que cayó entre espinos representa a los que escuchan, pero poco a poco se dejan ahogar por las preocupaciones, las riquezas y los placeres de la vida, de modo que no llegan a dar fruto. 15. Pero la semilla que cayó en buena tierra, son las personas que con corazón bueno y dispuesto escuchan y hacen caso del mensaje y, permaneciendo firmes, dan una buena cosecha. 16. "Nadie enciende una lámpara para después taparla con algo oponerla debajo de la cama, sino que la pone en alto, para que tengan luz los que entran. [3] 17. De la misma manera, no hay nada escondido que no llegue a descubrirse, ni nada secreto que no llegue a conocerse y ponerse en claro. 18. "Así pues, oigan bien, pues al que tiene se le dará más; pero al que no tiene, hasta lo que cree tener se le quitará."[4] 19. La madre y los hermanos de Jesús se presentaron donde él estaba, pero no pudieron acercarse a él porque había mucha gente. 20. Alguien avisó a Jesús: 
 –Tu madre y tus hermanos están ahí fuera, y quieren verte. 21. Él contestó: 
 –Los que oyen el mensaje de Dios y lo ponen en práctica, esos son mi madre y mis hermanos. 22. Un día, Jesús entró en una barca con sus discípulos, y les dijo: 
 –Vamos al otro lado del lago. 
 Partieron, pues, 23. y mientras cruzaban el lago, Jesús se durmió. En esto se desató una fuerte tormenta sobre el lago, y la barca empezó a llenarse de agua y corrían peligro de hundirse. 24. Entonces fueron a despertar a Jesús, diciéndole: 
 –¡Maestro! ¡Maestro! ¡Nos estamos hundiendo! 
 Jesús se levantó y dio una orden al viento y a las olas, y todo se calmó y quedó tranquilo. 25. Después dijo a los discípulos: 
 –¿Qué pasó con su fe? 
 Pero ellos, asustados y admirados, se preguntaban unos a otros: 
 –¿Quién será este, que da órdenes al viento y al agua, y lo obedecen? 26. Por fin llegaron a la tierra de Gerasa, que está al otro lado del lago, frente a Galilea. 27. Al bajar Jesús a tierra, salió del pueblo un hombre que estaba endemoniado, y se le acercó. Hacía mucho tiempo que no se ponía ropa ni vivía en una casa, sino entre las tumbas. 28. Cuando vio a Jesús, cayó de rodillas delante de él, gritando: 
 –¡No te metas conmigo, Jesús, Hijo del Dios altísimo! ¡Te ruego que no me atormentes! 29. Dijo esto porque Jesús había ordenado al espíritu impuro que saliera de él. Muchas veces el demonio se había apoderado de él; y aunque la gente le sujetaba las manos y los pies con cadenas para tenerlo seguro, él las rompía y el demonio lo hacía huir a lugares desiertos. 30. Jesús le preguntó: 
 –¿Cómo te llamas? 
