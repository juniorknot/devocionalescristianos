 Como había muchos cerdos comiendo en el cerro, los espíritus le rogaron que los dejara entrar en ellos; y Jesús les dio permiso. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">33. </strong>
 Los demonios salieron entonces del hombre y entraron en los cerdos, y estos echaron a correr pendiente abajo hasta el lago, y allí se ahogaron. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">34. </strong>
 Los que cuidaban de los cerdos, cuando vieron lo sucedido, salieron huyendo y fueron a contarlo en el pueblo y por el campo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">35. </strong>
 La gente salió a ver lo que había pasado. Y cuando llegaron a donde estaba Jesús, encontraron sentado a sus pies al hombre de quien habían salido los demonios, vestido y en su cabal juicio; y tuvieron miedo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
 y los que habían visto lo sucedido, les contaron cómo había sido sanado aquel endemoniado. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 Toda la gente de la región de Gerasa comenzó entonces a rogar a Jesús que se fuera de allí, porque tenían mucho miedo. Así que Jesús entró en la barca y se fue. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 El hombre de quien habían salido los demonios le rogó que le permitiera ir con él, pero Jesús le ordenó que se quedara, y le dijo: </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 –Vuelve a tu casa y cuenta todo lo que Dios ha hecho por ti. 
 El hombre se fue y contó por todo el pueblo lo que Jesús había hecho por él. </p>
                                                    <h4><strong>La hija de Jairo, y la mujer que tocó el manto de Jesús</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
 Cuando Jesús regresó al otro lado del lago, la gente lo recibió con alegría, porque todos lo estaban esperando. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 En esto llegó uno llamado Jairo, que era jefe de la sinagoga. Este hombre se postró a los pies de Jesús y le rogó que fuera a su casa, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 porque tenía una sola hija, de unos doce años, que estaba apunto de morir. 
 Mientras Jesús iba, se sentía apretujado por la multitud que lo seguía. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 Entre la gente había una mujer que desde hacía doce años estaba enferma, con derrames de sangre, y que había gastado en médicos todo lo que tenía, sin que ninguno la hubiera podido sanar. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 Esta mujer se acercó a Jesús por detrás y tocó el borde de su capa, y en el mismo momento el derrame de sangre se de tuvo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 Entonces Jesús preguntó: 
 –¿Quién me ha tocado? 
 Como todos negaban haberlo tocado, Pedro dijo: 
 –Maestro, la gente te oprime y empuja por todos lados. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 Pero Jesús insistió: 
 –Alguien me ha tocado, porque me he dado cuenta de que de mí ha salido poder. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
 La mujer, al ver que no podía esconderse, fue temblando a arrodillarse a los pies de Jesús. Le confesó delante de todos porqué razón lo había tocado, y cómo había sido sanada en el acto. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 Jesús le dijo: 
 –Hija, por tu fe has sido sanada. Vete tranquila. [5] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 Todavía estaba hablando Jesús, cuando llegó un mensajero y le dijo al jefe de la sinagoga: 
 –Tu hija ha muerto; no molestes más al Maestro. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 Pero Jesús lo oyó y le dijo: 
 –No tengas miedo; solamente cree, y tu hija se salvará. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 Al llegar a la casa, no dejó entrar con él a nadie más que a Pedro, a Santiago y a Juan, junto con el padre y la madre de la niña. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 Todos estaban llorando y lamentándose por ella, pero Jesús les dijo: 
 –No lloren; la niña no está muerta, sino dormida. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 Todos se rieron de él, porque sabían que estaba muerta. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 Entonces Jesús la tomó de la mano y dijo con voz fuerte: 
 –¡Niña, levántate! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 y ella volvió a la vida; al momento se levantó, y Jesús mandó que le dieran de comer. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 Sus padres estaban muy admirados; pero Jesús les ordenó que no contaran a nadie lo que había pasado. </p>
                                                        
