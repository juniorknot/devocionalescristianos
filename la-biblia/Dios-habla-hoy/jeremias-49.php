                                                    <h4><strong>Profecía sobre Damasco</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">23. </strong>
 Mensaje acerca de Damasco: 
"Las ciudades de Hamat y Arpad[5] están avergonzadas, 
porque les han llegado malas noticias. 
Se llenan de terror, 
no pueden estar tranquilas. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">24. </strong>
 Damasco, y a sin fuerzas, 
se dispone a huir. 
La dominan el pánico, 
la angustia y el dolor, 
como a una mujer de parto. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">25. </strong>
 La ciudad famosa, la ciudad feliz, 
ha quedado abandonada. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">26. </strong>
 Ese día quedarán sus jóvenes 
tendidos en las calles, 
y todos sus guerreros morirán. 
yo, el Señor todopoderoso, lo afirmo. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">27. </strong>
 Voy a prender fuego 
a las murallas de Damasco, 
y ese fuego destruirá 
los palacios de Ben-hadad." </p>
                                                    <h4><strong>Profecía sobre Cedar y Hazor</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">28. </strong>
 Mensaje del Señor acerca de la tribu de Quedar y de los jefes de Hasor, a los cuales derrotó Nabucodonosor, rey de Babilonia: 
"¡Adelante, ataquen a Quedar! 
¡Destruyan a esa tribu de oriente! 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">29. </strong>
 Apodérense de sus tiendas 
y de sus rebaños, 
de sus lonas y de todos sus utensilios. 
Quítenles sus camellos y grítenles: 
'¡Hay terror por todas partes!' 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">30. </strong>
 ¡Salgan corriendo, habitantes de Hasor! 
¡Vayan a esconderse! 
yo, el Señor, lo digo. 
Pues Nabucodonosor, el rey de Babilonia, 
ha hecho planes contra ustedes. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">31. </strong>
 yo, el Señor, les ordeno: 
¡Adelante, ataquen a ese pueblo 
que vive confiado y tranquilo, 
sin puertas ni cerrojos 
y completamente solo! 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">32. </strong>
 ¡Róbenle sus camellos y todo su ganado! 
Voy a dispersar en todas direcciones 
a esa gente que se afeita las sienes;[6] 
de todas partes les traeré el desastre. 
yo, el Señor, lo afirmo. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">33. </strong>
 Hasor será para siempre 
un desierto y guarida de chacales. 
Nadie volverá a vivir allí 
ni siquiera de paso." </p>
                                                    <h4><strong>Profecía sobre Elam</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">34. </strong>
 Cuando Sedequías comenzó a reinar en Judá, el Señor dirigió este mensaje a Jeremías acerca de Elam: 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">35. </strong>
 "Yo, el Señor todopoderoso, afirmo: 
Voy a romper los arcos de Elam, 
que son su arma principal. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
 Voy a traer vientos contra este pueblo 
desde las cuatro direcciones, 
para que lo dispersen por todas partes, 
hasta que no quede país 
adonde no lleguen sus refugiados. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 Haré temblar a Elam 
ante sus enemigos mortales, 
le enviaré calamidades, 
desataré mi ira contra él, 
y le enviaré la guerra 
hasta destruirlo por completo. 
yo, el Señor, lo afirmo. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 Pondré mi trono en Elam, 
y haré morir a sus reyes y a sus jefes. 
yo, el Señor, lo afirmo. 
 </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 Pero en el futuro 
cambiaré la suerte de Elam. 
yo, el Señor, lo afirmo." </p>
                                                        
