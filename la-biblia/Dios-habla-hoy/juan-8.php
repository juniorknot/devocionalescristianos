<li class="hide-for-large"><a href="whatsapp://send?text=Juan 8 (DHH)%0A%0A 1. Pero Jesús se dirigió al Monte de los Olivos, 2. y al día siguiente, al amanecer, volvió al templo. La gente se le acercó, y él se sentó y comenzó a enseñarles. 3. Los maestros de la ley y los fariseos llevaron entonces a una mujer, a la que habían sorprendido cometiendo adulterio. La pusieron en medio de todos los presentes, 4. y dijeron a Jesús: 
 –Maestro, esta mujer ha sido sorprendida en el acto mismo de cometer adulterio. 5. En la ley, Moisés nos ordenó que se matara a pedradas a esta clase de mujeres. [1] ¿Tú qué dices? 6. Ellos preguntaron esto para ponerlo a prueba, y tener así de qué acusarlo. Pero Jesús se inclinó y comenzó a escribir en la tierra con el dedo. 7. Luego, como seguían preguntándole, se enderezó y les dijo: 
 –Aquel de ustedes que no tenga pecado, que le tire la primera piedra. [2] 8. y volvió a inclinarse y siguió escribiendo en la tierra. 9. Al oír esto, uno tras otro comenzaron a irse, y los primeros en hacerlo fueron los más viejos. Cuando Jesús se encontró solo con la mujer, que se había quedado allí, 10. se enderezó y le preguntó: 
 –Mujer, ¿dónde están? ¿Ninguno te ha condenado? 11. Ella le contestó: 
 –Ninguno, Señor. 
 Jesús le dijo: 
 –Tampoco yo te condeno; ahora, vete y no vuelvas a pecar. [3] 12. Jesús se dirigió otra vez a la gente, diciendo: 
 –Yo soy la luz del mundo;[4] el que me sigue, tendrá la luz que le da vida, y nunca andará en la oscuridad. 13. Los fariseos le dijeron: 
 –Tú estás dando testimonio a favor tuyo: ese testimonio no tiene valor. 14. Jesús les contestó: 
 –Mi testimonio sí tiene valor, aunque lo dé yo mismo a mi favor. Pues yo sé de dónde vine y a dónde voy; en cambio, ustedes no lo saben. 15. Ustedes juzgan según los criterios humanos. Yo no juzgo a nadie; 16. pero si juzgo, mi juicio está de acuerdo con la verdad, porque no juzgo yo solo, sino que el Padre que me envió juzga conmigo. 17. En la ley de ustedes está escrito que cuando dos testigos dicen lo mismo, su testimonio tiene valor. [5] 18. Pues bien, yo mismo soy un testigo a mi favor, y el Padre queme envió es el otro testigo. 19. Le preguntaron: 
 –¿Dónde está tu padre? 
 Jesús les contestó: 
 –Ustedes no me conocen a mí, ni tampoco a mi Padre; si me conocieran a mí, también conocerían a mi Padre. 20. "A donde yo voy, ustedes no pueden ir" Jesús dijo estas cosas mientras enseñaba en el templo, en el lugar donde estaban los cofres de las ofrendas. Pero nadie lo arrestó, porque todavía no había llegado su hora. 21. Jesús les volvió a decir: 
 –Yo me voy, y ustedes me van a buscar, pero morirán en su pecado. A donde yo voy, ustedes no pueden ir. 22. Los judíos dijeron: 
 –¿Acaso estará pensando en matarse, y por eso dice que no podemos ir a donde él va? 23. Jesús les dijo: 
 –Ustedes son de aquí abajo, pero yo soy de arriba; ustedes sonde este mundo, pero yo no soy de este mundo. 24. Por eso les dije que morirán en sus pecados; porque si no creen que yo Soy, [6] morirán en sus pecados. 25. Entonces le preguntaron: 
 –¿Quién eres tú? Jesús les respondió: 
 –En primer lugar, ¿por qué he de hablar con ustedes? 26. Tengo mucho que decir y que juzgar de ustedes, pero el que me ha enviado dice la verdad, y lo que yo le digo al mundo es lo mismo que le he oído decir a él. 27. Pero ellos no entendieron que les hablaba del Padre. 28. Por eso les dijo: 
 –Cuando ustedes levanten en alto al Hijo del hombre, reconocerán que yo Soy, y que no hago nada por mi propia cuenta; solamente digo lo que el Padre me ha enseñado. 29. Porque el que me ha enviado está conmigo; mi Padre no me ha dejado solo, porque yo siempre hago lo que a él le agrada. 30. Cuando Jesús dijo esto, muchos creyeron en él. 31. Jesús les dijo a los judíos que habían creído en él: 
 –Si ustedes se mantienen fieles a mi palabra, serán de veras mis discípulos; 32. conocerán la verdad, y la verdad los hará libres. 33. Ellos le contestaron: 
 –Nosotros somos descendientes de Abraham, y nunca hemos sido esclavos de nadie; ¿cómo dices tú que seremos libres? 34. Jesús les dijo: 
 –Les aseguro que todos los que pecan son esclavos del pecado. 35. Un esclavo no pertenece para siempre a la familia; pero un hijo sí pertenece para siempre a la familia. 36. Así que, si el Hijo los hace libres, ustedes serán verdaderamente libres. [7] 37. y a sé que ustedes son descendientes de Abraham; pero quieren matarme porque no aceptan mi palabra. 38. yo hablo de lo que el Padre me ha mostrado; así también ustedes, hagan lo que del Padre han escuchado. 39. Ellos le dijeron: 
 –¡Nuestro padre es Abraham! 
 Pero Jesús les contestó: 
 –Si ustedes fueran de veras hijos de Abraham, harían lo que él hizo. 40. Sin embargo, aunque les he dicho la verdad que Dios me ha enseñado, ustedes quieren matarme. ¡Abraham nunca hizo nada así! 41. Ustedes hacen lo mismo que hace su padre. Ellos le dijeron: 
 –¡Nosotros no somos hijos bastardos; tenemos un solo Padre, que es Dios![8] 42. Jesús les contestó: 
 –Si de veras Dios fuera su padre, ustedes me amarían, porque y o vengo de Dios y aquí estoy. No he venido por mi propia cuenta, sino que Dios me ha enviado. 43. ¿Por qué no pueden entender ustedes mi mensaje? Pues simplemente porque no pueden escuchar mi palabra. 44. El padre de ustedes es el diablo; ustedes le pertenecen, y tratan de hacer lo que él quiere. El diablo ha sido un asesino desde el principio. No se mantiene en la verdad, y nunca dice la verdad. Cuando dice mentiras, habla como lo que es; porque es mentiroso y es el padre de la mentira. 45. Pero como yo digo la verdad, ustedes no me creen. 46. ¿Quién de ustedes puede demostrar que yo tengo algún pecado? y si digo la verdad, ¿por qué no me creen?[9] 47. El que es de Dios, escucha las palabras de Dios; pero como ustedes no son de Dios, no quieren escuchar. 48. Los judíos le dijeron entonces: 
