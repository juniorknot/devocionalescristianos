                                                    <h4><strong>La preexistencia de Cristo</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 Los judíos le dijeron entonces: 
 –Tenemos razón cuando decimos que eres un samaritano y que tienes un demonio. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 Jesús les contestó: 
 –No tengo ningún demonio. Lo que hago es honrar a mi Padre; en cambio, ustedes me deshonran. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 yo no busco mi gloria; hay alguien que la busca, y él es el que juzga. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 Les aseguro que quien hace caso de mi palabra, no morirá. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 Los judíos le contestaron: 
 –Ahora estamos seguros de que tienes un demonio. Abraham y todos los profetas murieron, y tú dices: 'El que hace caso de mi palabra, no morirá. ' </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 ¿Acaso eres tú más que nuestro padre Abraham? Él murió, y los profetas también murieron. ¿Quién te has creído que eres? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 Jesús les contestó: 
 –Si yo me glorifico a mí mismo, mi gloria no vale nada. Pero el que me glorifica es mi Padre, el mismo que ustedes dicen que es su Dios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 Pero ustedes no lo conocen. Yo sí lo conozco; y si dijera que no lo conozco, sería yo tan mentiroso como ustedes. Pero ciertamente lo conozco, y hago caso de su palabra. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 Abraham, el antepasado de ustedes, se alegró porque iba a ver mi día; y lo vio, y se llenó de gozo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 Los judíos dijeron a Jesús: 
 –Todavía no tienes cincuenta años, ¿y dices que has visto a Abraham? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
 Jesús les contestó: 
 –Les aseguro que yo existo desde antes que existiera Abraham. [10] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">59. </strong>
 Entonces ellos tomaron piedras para arrojárselas; pero Jesús se escondió y salió del templo. </p>
                                                        
