<li class="hide-for-large"><a href="whatsapp://send?text=Lucas 7 (DHH)%0A%0A 1. Cuando Jesús terminó de hablar a la gente, se fue a Cafarnaúm. 2. Vivía allí un capitán romano que tenía un criado al que estimaba mucho, el cual estaba enfermo y a punto de morir. 3. Cuando el capitán oyó hablar de Jesús, mandó a unos ancianos de los judíos a rogarle que fuera a sanar a su criado. 4. Ellos se presentaron a Jesús y le rogaron mucho, diciendo: 
 –Este capitán merece que lo ayudes, 5. porque ama a nuestra nación y él mismo hizo construir nuestra sinagoga. 6. Jesús fue con ellos, pero cuando y a estaban cerca de la casa, el capitán mandó unos amigos a decirle: "Señor, no te molestes, porque yo no merezco que entres en mi casa; 7. por eso, ni siquiera me atreví a ir en persona a buscarte. Solamente da la orden, para que sane mi criado. 8. Porque yo mismo estoy bajo órdenes superiores, y a la vez tengo soldados bajo mi mando. Cuando le digo a uno de ellos que vaya, va; cuando le digo a otro que venga, viene; y cuando mando a mi criado que haga algo, lo hace." 9. Jesús se quedó admirado al oír esto, y mirando a la gente que lo seguía dijo: 
 –Les aseguro que ni siquiera en Israel he encontrado tanta fe como en este hombre. 10. Al regresar a la casa, los enviados encontraron que el criado ya estaba sano. 11. Después de esto, Jesús se dirigió a un pueblo llamado Naín. Iba acompañado de sus discípulos y de mucha gente. 12. Al llegar cerca del pueblo, vio que llevaban a enterrar a un muerto, hijo único de su madre, que era viuda. Mucha gente del pueblo la acompañaba. 13. Al verla, el Señor tuvo compasión de ella y le dijo: 
 –No llores. 14. En seguida se acercó y tocó la camilla, y los que la llevaban se de tuvieron. Jesús le dijo al muerto: 
 –Joven, a ti te digo: ¡Levántate! 15. Entonces el que había estado muerto se sentó y comenzó a hablar, y Jesús se lo entregó a la madre. 16. Al ver esto, todos tuvieron miedo y comenzaron a alabar a Dios, diciendo: 
 –Un gran profeta ha aparecido entre nosotros. 
 También decían: 
 –Dios ha venido a ayudar a su pueblo. 17. y por toda Judea y sus alrededores se supo lo que había hecho Jesús. 18. Juan tuvo noticias de todas estas cosas, pues sus seguidores se las contaron. Llamó a dos de ellos 19. y los envió al Señor, a preguntarle si él era de veras el que había de venir o si debían esperar a otro. 20. Los enviados de Juan se acercaron, pues, a Jesús y le dijeron: 
 –Juan el Bautista nos ha mandado a preguntarte si tú eres el que ha de venir, o si debemos esperar a otro. 21. En aquel mismo momento Jesús curó a muchas personas de sus enfermedades y sufrimientos, y de los espíritus malignos, y dio la vista a muchos ciegos. 22. Luego les contestó: 
 –Vayan y díganle a Juan lo que han visto y oído. Cuéntenle que los ciegos ven, los cojos andan, los leprosos quedan limpios de su enfermedad, los sordos oyen, [1] los muertos vuelven a la vida y a los pobres se les anuncia la buena noticia. [2] 23. ¡Y dichoso aquel que no pierda su fe en mí! 24. Cuando los enviados de Juan se fueron, Jesús comenzó a hablar a la gente acerca de Juan, diciendo: "¿Qué salieron ustedes a ver al desierto? ¿Una caña sacudida por el viento? 25. y si no, ¿qué salieron a ver? ¿Un hombre vestido con ropas lujosas? Ustedes saben que los que se visten lujosamente y viven en placeres, están en las casas de los reyes. 26. En fin, ¿qué salieron a ver? ¿Un profeta? Sí, de veras, y uno que es mucho más que profeta. 27. Juan es aquel de quien dice la Escritura: 
'Yo envío mi mensajero delante de ti, 
para que te prepare el camino. '[3] 28. Les digo que, entre todos los hombres, ninguno ha sido más grande que Juan; y , sin embargo, el más pequeño en el reino de Dios es más grande que él." 29. Todos los que oyeron a Juan, incluso los que cobraban impuestos para Roma, se hicieron bautizar por él, cumpliendo así las justas exigencias de Dios; 30. pero los fariseos y los maestros de la ley no se hicieron bautizar por Juan, despreciando de este modo lo que Dios había querido hacer en favor de ellos. [4] 31. "¿A qué compararé la gente de este tiempo? ¿A qué se parece? 32. Se parece a los niños que se sientan a jugar en la plaza y gritan a sus compañeros: 'Tocamos la flauta, pero ustedes no bailaron; cantamos canciones tristes, pero ustedes no lloraron. ' 33. Porque vino Juan el Bautista, que ni come pan ni bebe vino, y ustedes dicen que tiene un demonio. 34. Luego ha venido el Hijo del hombre, que come y bebe, y ustedes dicen que es glotón y bebedor, amigo de gente de mala fama y de los que cobran impuestos para Roma. [5] 35. Pero la sabiduría de Dios se demuestra por todos sus resultados."[6] 36. Un fariseo invitó a Jesús a comer, y Jesús fue a su casa. Estaba sentado a la mesa, 37. cuando una mujer de mala vida, que vivía en el mismo pueblo y que supo que Jesús había ido a comer a casa del fariseo, llegó con un frasco de alabastro lleno de perfume. 38. Llorando, se puso junto a los pies de Jesús y comenzó abañarlos con lágrimas. Luego los secó con sus cabellos, los besó y derramó sobre ellos el perfume. 39. El fariseo que había invitado a Jesús, al ver esto, pensó: "Si este hombre fuera de veras un profeta, se daría cuenta de qué clase de persona es esta que lo está tocando: una mujer de mala vida." 40. Entonces Jesús le dijo al fariseo: 
 –Simón, tengo algo que decirte. El fariseo contestó: 
 –Dímelo, Maestro. 41. Jesús siguió: 
 –Dos hombres le debían dinero a un prestamista. Uno le debía quinientos denarios, y el otro cincuenta; 42. y como no le podían pagar, el prestamista les perdonó la deuda a los dos. Ahora dime, ¿cuál de ellos le amará más?[7] 43. Simón le contestó: 
 –Me parece que el hombre a quien más le perdonó. 
 Jesús le dijo: 
 –Tienes razón. 44. Entonces, mirando a la mujer, Jesús dijo a Simón: 
