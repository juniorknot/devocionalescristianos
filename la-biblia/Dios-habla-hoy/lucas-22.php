 –Oren, para que no caigan en tentación. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 Se alejó de ellos como a la distancia de un tiro de piedra, y se puso de rodillas para orar. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 Dijo: "Padre, si quieres, líbrame de este trago amargo; pero que no se haga mi voluntad, sino la tuya." v [ </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 En esto se le apareció un ángel del cielo, para darle fuerzas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 En medio de su gran sufrimiento, Jesús oraba aún más intensamente, y el sudor le caía a tierra como grandes gotas desangre. ]</p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 Cuando se levantó de la oración, fue a donde estaban los discípulos, y los encontró dormidos, vencidos por la tristeza. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 Les dijo: 
 –¿Por qué están durmiendo? Levántense y oren, para que no caigan en tentación. </p>
                                                    <h4><strong>Arresto de Jesús</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
 Todavía estaba hablando Jesús, cuando llegó mucha gente. el que se llamaba Judas, que era uno de los doce discípulos, iba a la cabeza. Este se acercó a besar a Jesús, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 pero Jesús le dijo: 
 –Judas, ¿con un beso traicionas al Hijo del hombre? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 Los que estaban con Jesús, al ver lo que pasaba, le preguntaron: 
 –Señor, ¿atacamos con espada? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 y uno de ellos hirió al criado del sumo sacerdote, cortándole la oreja derecha. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 Jesús dijo: 
 –Déjenlos; y a basta. 
 y le tocó la oreja al criado, y lo sanó. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 Luego dijo a los jefes de los sacerdotes, a los oficiales del templo y a los ancianos, que habían venido a llevárselo: 
 –¿Por qué han venido ustedes con espadas y con palos, como si yo fuera un bandido? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 Todos los días he estado con ustedes en el templo, [13] y no trataron de arrestarme. Pero esta es la hora de ustedes, la hora del poder de las tinieblas. [14] </p>
                                                    <h4><strong>Pedro niega a Jesús</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 Arrestaron entonces a Jesús y lo llevaron a la casa del sumo sacerdote. Pedro lo seguía de lejos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 Allí, en medio del patio, habían hecho fuego, y se sentaron alrededor; y Pedro se sentó también entre ellos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 En esto, una sirvienta, al verlo sentado junto al fuego, se quedó mirándolo y dijo: 
 –También este estaba con él. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 Pero Pedro lo negó, diciendo: 
 –Mujer, yo no lo conozco. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
 Poco después, otro lo vio y dijo: 
 –Tú también eres de ellos. 
 Pedro contestó: 
 –No, hombre, no lo soy. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">59. </strong>
 Como una hora después, otro insistió: 
 –Seguro que este estaba con él. Además es de Galilea. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">60. </strong>
 Pedro dijo: 
 –Hombre, no sé de qué hablas. En ese mismo momento, mientras Pedro aún estaba hablando, cantó un gallo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">61. </strong>
 Entonces el Señor se volvió y miró a Pedro, y Pedro se acordó de que el Señor le había dicho: "Hoy, antes que el gallo cante, me negarás tres veces."[15] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">62. </strong>
 y salió Pedro de allí y lloró amargamente. </p>
                                                    <h4><strong>Jesús escarnecido y azotado</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">63. </strong>
 Los hombres que estaban vigilando a Jesús se burlaban de él y lo golpeaban. [16] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">64. </strong>
 Le taparon los ojos, y le preguntaban: 
 –¡Adivina quién te pegó! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">65. </strong>
 y lo insultaban diciéndole otras muchas cosas. </p>
                                                    <h4><strong>Jesús ante el concilio</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">66. </strong>
 Cuando se hizo de día, se reunieron los ancianos de los judíos, los jefes de los sacerdotes y los maestros de la ley, y llevaron a Jesús ante la Junta Suprema. Allí le preguntaron: </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">67. </strong>
 –Dinos, ¿eres tú el Mesías? 
 Él les contestó: 
 –Si les digo que sí, no me van a creer. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">68. </strong>
 y si les hago preguntas, no me van a contestar. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">69. </strong>
 Pero desde ahora el Hijo del hombre estará sentado a la derecha del Dios todopoderoso. [17] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">70. </strong>
 Luego todos le preguntaron: 
 –¿Así que tú eres el Hijo de Dios? 
 Jesús les contestó: 
 –Ustedes mismos han dicho que lo soy. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">71. </strong>
 Entonces ellos dijeron: 
 –¿Qué necesidad tenemos de más testigos? Nosotros mismos lo hemos oído de sus propios labios. </p>
                                                        
