 –Vayan a prepararnos la cena de Pascua. 9. Ellos le preguntaron: 
 –¿Dónde quieres que la preparemos? 10. Jesús les contestó: 
 –Cuando entren ustedes en la ciudad, encontrarán a un hombre que lleva un cántaro de agua. Síganlo hasta la casa donde entre, 11. y digan al dueño de la casa: 'El Maestro pregunta: ¿Cuál es el cuarto donde voy a comer con mis discípulos la cena de Pascua?' 12. Él les mostrará en el piso alto un cuarto grande y arreglado para la cena. Preparen allí lo necesario. 13. Ellos fueron y lo encontraron todo como Jesús se lo había dicho, y prepararon la cena de Pascua. [3] 14. Cuando llegó la hora, Jesús y los apóstoles se sentaron ala mesa. 15. Jesús les dijo: 
 –¡Cuánto he querido celebrar con ustedes esta cena de Pascua antes de mi muerte! 16. Porque les digo que no la celebraré de nuevo hasta que se cumpla en el reino de Dios. 17. Entonces tomó en sus manos una copa y , habiendo dado gracias a Dios, dijo: 
 –Tomen esto y repártanlo entre ustedes; 18. porque les digo que no volveré a beber del producto de la vid, hasta que venga el reino de Dios. 19. Después tomó el pan en sus manos y , habiendo dado gracias a Dios, lo partió y se lo dio a ellos, diciendo: 
 –Esto es mi cuerpo, [4] entregado a muerte en favor de ustedes. Hagan esto en memoria de mí. 20. Lo mismo hizo con la copa después de la cena, diciendo: 
 –Esta copa es la nueva alianza confirmada con mi sangre, [5] la cual es derramada en favor de ustedes. 21. Pero ahora la mano del que me va a traicionar está aquí, con la mía, sobre la mesa. 22. Pues el Hijo del hombre ha de recorrer el camino que se le ha señalado, pero ¡ay de aquel que lo traiciona! 23. Entonces comenzaron a preguntarse unos a otros quién sería el traidor. 24. Los discípulos tuvieron una discusión sobre cuál de ellos debía ser considerado el más importante. [6] 25. Jesús les dijo: "Entre los paganos, los reyes gobiernan con tiranía a sus súbditos, y a los jefes se les da el título de benefactores. 26. Pero ustedes no deben ser así. Al contrario, el más importante entre ustedes tiene que hacerse como el más joven, y el que manda tiene que hacerse como el que sirve. [7] 27. Pues ¿quién es más importante, el que se sienta a la mesa a comer o el que sirve? ¿Acaso no lo es el que se sienta a la mesa? En cambio yo estoy entre ustedes como el que sirve. [8] 28. "Ustedes han estado siempre conmigo en mis pruebas. 29. Por eso, yo les doy un reino, como mi Padre me lo dio a mí, 30. y ustedes comerán y beberán a mi mesa en mi reino, y se sentarán en tronos para juzgar a las doce tribus de Israel."[9] 31. Dijo también el Señor: 
 –Simón, Simón, mira que Satanás los ha pedido a ustedes para sacudirlos como si fueran trigo; 32. pero yo he rogado por ti, para que no te falte la fe. Y tú, cuando te hayas vuelto a mí, ayuda a tus hermanos a permanecer firmes. 33. Simón le dijo: 
 –Señor, estoy dispuesto a ir contigo a la cárcel, y hasta amor ir contigo. 34. Jesús le contestó: 
 –Pedro, te digo que hoy mismo, antes que cante el gallo, tres veces negarás que me conoces. 35. Luego Jesús les preguntó: 
 –Cuando los mandé sin dinero ni provisiones ni sandalias, [10] ¿acaso les faltó algo? 
 Ellos contestaron: 
 –Nada. 36. Entonces les dijo: 
 –Ahora, en cambio, el que tenga dinero, que lo traiga, y también provisiones; y el que no tenga espada, que venda su abrigo y se compre una. 37. Porque les digo que tiene que cumplirse en mí esto que dicen las Escrituras: 'Y fue contado entre los malvados. '[11] Pues todo lo que está escrito de mí, tiene que cumplirse. 38. Ellos dijeron: 
 –Señor, aquí hay dos espadas. 
 y él contestó: 
 –Basta y a de hablar. 39. Luego Jesús salió y , según su costumbre, se fue al Monte de los Olivos; y los discípulos lo siguieron. 40. Al llegar al lugar, les dijo: 
 –Oren, para que no caigan en tentación. 41. Se alejó de ellos como a la distancia de un tiro de piedra, y se puso de rodillas para orar. 42. Dijo: "Padre, si quieres, líbrame de este trago amargo; pero que no se haga mi voluntad, sino la tuya." v [ 43. En esto se le apareció un ángel del cielo, para darle fuerzas. 44. En medio de su gran sufrimiento, Jesús oraba aún más intensamente, y el sudor le caía a tierra como grandes gotas desangre. ] 45. Cuando se levantó de la oración, fue a donde estaban los discípulos, y los encontró dormidos, vencidos por la tristeza. 46. Les dijo: 
 –¿Por qué están durmiendo? Levántense y oren, para que no caigan en tentación. 47. Todavía estaba hablando Jesús, cuando llegó mucha gente. el que se llamaba Judas, que era uno de los doce discípulos, iba a la cabeza. Este se acercó a besar a Jesús, 48. pero Jesús le dijo: 
 –Judas, ¿con un beso traicionas al Hijo del hombre? 49. Los que estaban con Jesús, al ver lo que pasaba, le preguntaron: 
 –Señor, ¿atacamos con espada? 50. y uno de ellos hirió al criado del sumo sacerdote, cortándole la oreja derecha. 51. Jesús dijo: 
