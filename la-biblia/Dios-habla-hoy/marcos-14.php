                                                                                <p style="style=text-align: justify;"><strong id="numeros">27. </strong>
 Jesús les dijo: 
 –Todos ustedes van a perder su fe en mí. Así lo dicen las Escrituras: 'Mataré al pastor, y las ovejas se dispersarán. '[8] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">28. </strong>
 Pero cuando yo resucite, los volveré a reunir en Galilea. [9] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">29. </strong>
 Pedro le dijo: 
 –Aunque todos pierdan su fe, yo no. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">30. </strong>
 Jesús le contestó: 
 –Te aseguro que esta misma noche, antes que cante el gallo por segunda vez, me negarás tres veces. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">31. </strong>
 Pero él insistía: 
 –Aunque tenga que morir contigo, no te negaré. 
 y todos decían lo mismo. </p>
                                                    <h4><strong>Jesús ora en Getsemaní</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">32. </strong>
 Luego fueron a un lugar llamado Getsemaní. Jesús dijo a sus discípulos: 
 –Siéntense aquí, mientras yo voy a orar. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">33. </strong>
 y se llevó a Pedro, a Santiago y a Juan, y comenzó asentirse muy afligido y angustiado. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">34. </strong>
 Les dijo: 
 –Siento en mi alma una tristeza de muerte. [10] Quédense ustedes aquí, y permanezcan despiertos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">35. </strong>
 En seguida Jesús se fue un poco más adelante, se inclinó hasta tocar el suelo con la frente, y pidió a Dios que, de ser posible, no le llegara ese momento. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
 En su oración decía: "Abbá, [11] Padre, para ti todo es posible: líbrame de este trago amargo; pero que no se haga lo que yo quiero, sino lo que quieres tú." </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 Luego volvió a donde ellos estaban, y los encontró dormidos. Le dijo a Pedro: 
 –Simón, ¿estás durmiendo? ¿Ni siquiera una hora pudiste mantenerte despierto? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 Manténganse despiertos y oren, para que no caigan en tentación. Ustedes tienen buena voluntad, pero son débiles. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 Se fue otra vez, y oró repitiendo las mismas palabras. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
 Cuando volvió, encontró otra vez dormidos a los discípulos, porque sus ojos se les cerraban de sueño. Y no sabían qué contestarle. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 Volvió por tercera vez, y les dijo: 
 –¿Siguen ustedes durmiendo y descansando? y a basta, ha llegado la hora en que el Hijo del hombre va a ser entregado en manos de los pecadores. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 Levántense, vámonos; y a se acerca el que me traiciona. </p>
                                                    <h4><strong>Arresto de Jesús</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 Todavía estaba hablando Jesús cuando Judas, uno de los doce discípulos, llegó acompañado de mucha gente armada con espadas y con palos. Iban de parte de los jefes de los sacerdotes, de los maestros de la ley y de los ancianos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 Judas, el traidor, les había dado una contraseña, diciéndoles: "Al que yo bese, ese es; arréstenlo y llévenselo bien sujeto." </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 Así que se acercó a Jesús y le dijo: 
 –¡Maestro! y lo besó. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 Entonces le echaron mano a Jesús y lo arrestaron. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
 Pero uno de los que estaban allí sacó su espada y le cortó una oreja al criado del sumo sacerdote. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 y Jesús preguntó a la gente: 
 –¿Por qué han venido ustedes con espadas y con palos a arrestarme, como si yo fuera un bandido? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 Todos los días he estado entre ustedes enseñando en el templo, y nunca me arrestaron. Pero esto sucede para que se cumplan las Escrituras. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 Todos los discípulos dejaron solo a Jesús, y huyeron. </p>
                                                    <h4><strong>El joven que huyó</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 Pero un joven lo seguía, cubierto solo con una sábana. A este lo agarraron, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 pero él soltó la sábana y escapó desnudo. </p>
                                                    <h4><strong>Jesús ante el concilio</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 Llevaron entonces a Jesús ante el sumo sacerdote, y se juntaron todos los jefes de los sacerdotes, los ancianos y los maestros de la ley. [12] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 Pedro lo siguió de lejos hasta dentro del patio de la casa del sumo sacerdote, y se quedó sentado con los guardianes del templo, calentándose junto al fuego. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 Los jefes de los sacerdotes y toda la Junta Suprema buscaban alguna prueba para condenar a muerte a Jesús; pero no la encontraban. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 Porque aunque muchos presentaban falsos testimonios contra él, se contradecían unos a otros. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 Algunos se levantaron y lo acusaron falsamente, diciendo: </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
 –Nosotros lo hemos oído decir: 'Yo voy a destruir este templo que hicieron los hombres, y en tres días levantaré otro no hecho por los hombres. ' </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">59. </strong>
 Pero ni aun así estaban de acuerdo en lo que decían. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">60. </strong>
 Entonces el sumo sacerdote se levantó en medio de todos, y preguntó a Jesús: 
 –¿No contestas nada?[13] ¿Qué es esto que están diciendo contra ti? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">61. </strong>
 Pero Jesús se quedó callado, sin contestar nada. El sumo sacerdote volvió a preguntarle: 
 –¿Eres tú el Mesías, el Hijo del Dios bendito? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">62. </strong>
 Jesús le dijo: 
 –Sí, yo soy. Y ustedes verán al Hijo del hombre sentado a la derecha del Todopoderoso, y viniendo en las nubes del cielo. [14] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">63. </strong>
 Entonces el sumo sacerdote se rasgó las ropas en señal de indignación, y dijo: 
 –¿Qué necesidad tenemos de más testigos? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">64. </strong>
 Ustedes lo han oído decir palabras ofensivas contra Dios. ¿Qué les parece? Todos estuvieron de acuerdo en que era culpable y debía morir. [15] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">65. </strong>
 Algunos comenzaron a escupirlo, y a taparle los ojos y golpearlo, diciéndole: 
 –¡Adivina quién te pegó! 
 y los guardianes del templo le pegaron en la cara. [16] </p>
                                                    <h4><strong>Pedro niega a Jesús</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">66. </strong>
 Pedro estaba abajo, en el patio. En esto llegó una de las sirvientas del sumo sacerdote; </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">67. </strong>
 y al ver a Pedro, que se estaba calentando junto al fuego, se quedó mirándolo y le dijo: 
 –Tú también andabas con Jesús, el de Nazaret. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">68. </strong>
 Pedro lo negó, diciendo: 
 –No lo conozco, ni sé de qué estás hablando. Y salió fuera, a la entrada. Entonces cantó un gallo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">69. </strong>
 La sirvienta vio otra vez a Pedro y comenzó a decir a los demás: 
 –Este es uno de ellos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">70. </strong>
 Pero él volvió a negarlo. Poco después, los que estaban allí dijeron de nuevo a Pedro: 
 –Seguro que tú eres uno de ellos, pues también eres de Galilea. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">71. </strong>
 Entonces Pedro comenzó a jurar y perjurar, diciendo: 
 –¡No conozco a ese hombre de quien ustedes están hablando! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">72. </strong>
 En aquel mismo momento cantó el gallo por segunda vez, y Pedro se acordó de que Jesús le había dicho: 'Antes que cante el gallo por segunda vez, me negarás tres veces. ' y se echó a llorar. </p>
                                                        
