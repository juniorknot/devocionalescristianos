<li class="hide-for-large"><a href="whatsapp://send?text=Marcos 14 (DHH)%0A%0A 1. Faltaban dos días para la fiesta de la Pascua, cuando se come el pan sin levadura. [1] Los jefes de los sacerdotes y los maestros de la ley buscaban la manera de arrestar a Jesús por medio de algún engaño, y matarlo. 2. Pues algunos decían: 
 –No durante la fiesta, para que la gente no se alborote. 3. Jesús había ido a Betania, a casa de Simón, al que llamaban el leproso. Mientras estaba sentado a la mesa, llegó una mujer que llevaba un frasco de alabastro lleno de perfume de nardo puro, de mucho valor. Rompió el frasco y derramó el perfume sobre la cabeza de Jesús. 4. Algunos de los presentes se enojaron, y se dijeron unos a otros: 
 –¿Por qué se ha desperdiciado este perfume? 5. Podía haberse vendido por el equivalente al salario de trescientos días, para ayudar a los pobres. Y criticaban a aquella mujer. 6. Pero Jesús dijo: 
 –Déjenla; ¿por qué la molestan? Ha hecho una obra buena conmigo. 7. Pues a los pobres siempre los tendrán entre ustedes, y pueden hacerles bien cuando quieran; pero a mí no siempre me van a tener. 8. Esta mujer ha hecho lo que ha podido: ha perfumado mi cuerpo de antemano para mi entierro. 9. Les aseguro que en cualquier lugar del mundo donde se anuncie la buena noticia, se hablará también de lo que hizo esta mujer, y así será recordada. 10. Judas Iscariote, uno de los doce discípulos, fue a ver a los jefes de los sacerdotes para entregarles a Jesús. 11. Al oírlo, se alegraron y prometieron darle dinero a Judas, que comenzó a buscar el momento más oportuno de entregar a Jesús. 12. El primer día de la fiesta en que se comía el pan sin levadura, cuando se sacrificaba el cordero de Pascua, los discípulos de Jesús le preguntaron: 
 –¿Dónde quieres que vayamos a prepararte la cena de Pascua? 13. Entonces envió a dos de sus discípulos, diciéndoles: 
 –Vayan a la ciudad. Allí encontrarán a un hombre que lleva un cántaro de agua; síganlo, 14. y donde entre, digan al dueño de la casa: 'El Maestro pregunta: ¿Cuál es el cuarto donde voy a comer con mis discípulos la cena de Pascua?' 15. Él les mostrará en el piso alto un cuarto grande, arreglado y ya listo para la cena. Prepárennos allí lo necesario. 16. Los discípulos salieron y fueron a la ciudad. Lo encontraron todo como Jesús les había dicho, y prepararon la cena de Pascua. [2] 17. Al anochecer llegó Jesús con los doce discípulos. 18. Mientras estaban a la mesa, comiendo, Jesús les dijo: 
 –Les aseguro que uno de ustedes, que está comiendo conmigo, me va a traicionar. [3] 19. Ellos se pusieron tristes, y comenzaron a preguntarle uno por uno: 
 –¿Acaso seré y o? 20. Jesús les contestó: 
 –Es uno de los doce, que está mojando el pan en el mismo plato que y o. [4] 21. El Hijo del hombre ha de recorrer el camino que dicen las Escrituras; pero ¡ay de aquel que lo traiciona! Hubiera sido mejor para él no haber nacido. 22. Mientras comían, Jesús tomó en sus manos el pan y , habiendo pronunciado la bendición, lo partió y se lo dio a ellos, diciendo: 
 –Tomen, esto es mi cuerpo. [5] 23. Luego tomó en sus manos una copa y , habiendo dado gracias a Dios, se la pasó a ellos, y todos bebieron. 24. Les dijo: 
 –Esto es mi sangre, con la que se confirma la alianza, sangre que es derramada en favor de muchos. [6] 25. Les aseguro que no volveré a beber del producto de la vid, hasta el día en que beba el vino nuevo en el reino de Dios. 26. Después de cantar los salmos, [7] se fueron al Monte de los Olivos. 27. Jesús les dijo: 
 –Todos ustedes van a perder su fe en mí. Así lo dicen las Escrituras: 'Mataré al pastor, y las ovejas se dispersarán. '[8] 28. Pero cuando yo resucite, los volveré a reunir en Galilea. [9] 29. Pedro le dijo: 
 –Aunque todos pierdan su fe, yo no. 30. Jesús le contestó: 
 –Te aseguro que esta misma noche, antes que cante el gallo por segunda vez, me negarás tres veces. 31. Pero él insistía: 
 –Aunque tenga que morir contigo, no te negaré. 
 y todos decían lo mismo. 32. Luego fueron a un lugar llamado Getsemaní. Jesús dijo a sus discípulos: 
 –Siéntense aquí, mientras yo voy a orar. 33. y se llevó a Pedro, a Santiago y a Juan, y comenzó asentirse muy afligido y angustiado. 34. Les dijo: 
 –Siento en mi alma una tristeza de muerte. [10] Quédense ustedes aquí, y permanezcan despiertos. 35. En seguida Jesús se fue un poco más adelante, se inclinó hasta tocar el suelo con la frente, y pidió a Dios que, de ser posible, no le llegara ese momento. 36. En su oración decía: "Abbá, [11] Padre, para ti todo es posible: líbrame de este trago amargo; pero que no se haga lo que yo quiero, sino lo que quieres tú." 37. Luego volvió a donde ellos estaban, y los encontró dormidos. Le dijo a Pedro: 
 –Simón, ¿estás durmiendo? ¿Ni siquiera una hora pudiste mantenerte despierto? 38. Manténganse despiertos y oren, para que no caigan en tentación. Ustedes tienen buena voluntad, pero son débiles. 39. Se fue otra vez, y oró repitiendo las mismas palabras. 40. Cuando volvió, encontró otra vez dormidos a los discípulos, porque sus ojos se les cerraban de sueño. Y no sabían qué contestarle. 41. Volvió por tercera vez, y les dijo: 
