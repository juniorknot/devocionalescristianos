<p style="style=text-align: justify;"><strong id="numeros">76. </strong>
Muéstrame tu misericordia, y ven a consolarme, pues ésa fue tu promesa a este siervo tuyo.</p>
<p style="style=text-align: justify;"><strong id="numeros">77. </strong>
Compadécete de mí, y viviré, pues en tu ley encuentro mi deleite.</p>
<p style="style=text-align: justify;"><strong id="numeros">78. </strong>
Avergüenza a esos arrogantes que me calumnian; por mi parte, yo meditaré en tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">79. </strong>
Que se unan a mí aquellos que te honran, aquellos que conocen tus testimonios.</p>
<p style="style=text-align: justify;"><strong id="numeros">80. </strong>
Perfecciona mi corazón con tus estatutos, para que no tenga de que avergonzarme.</p>
<h4><strong>Caf</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">81. </strong>
Caf
Siento que me muero esperando tu salvación, pero sigo confiando en tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">82. </strong>
Los ojos se me apagan esperando tu promesa, y me pregunto: «¿Cuándo vendrás a consolarme?»</p>
<p style="style=text-align: justify;"><strong id="numeros">83. </strong>
Me siento tan inútil como un odre viejo, pero tengo presentes tus estatutos.</p>
<p style="style=text-align: justify;"><strong id="numeros">84. </strong>
¿Cuántos años más me quedan de vida? ¿Cuándo dictarás sentencia contra mis enemigos?</p>
<p style="style=text-align: justify;"><strong id="numeros">85. </strong>
Gente altanera me han tendido trampas, pues no actúan de acuerdo con tu ley.</p>
<p style="style=text-align: justify;"><strong id="numeros">86. </strong>
Todos tus mandamientos son verdaderos; ¡ayúdame, porque sin razón soy perseguido!</p>
<p style="style=text-align: justify;"><strong id="numeros">87. </strong>
Poco ha faltado para que me derriben, pero ni así me he apartado de tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">88. </strong>
¡Dame vida, conforme a tu misericordia, para que cumpla los testimonios que has emitido!</p>
<h4><strong>Lámed</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">89. </strong>
Lámed
Señor, tu palabra es eterna, y permanece firme como los cielos.</p>
<p style="style=text-align: justify;"><strong id="numeros">90. </strong>
Tu fidelidad es la misma por todas las edades; tú afirmaste la tierra, y ésta permanece firme.</p>
<p style="style=text-align: justify;"><strong id="numeros">91. </strong>
Por tus decretos, todo subsiste hoy, y todo está a tu servicio.</p>
<p style="style=text-align: justify;"><strong id="numeros">92. </strong>
Si en tu ley no hallara mi regocijo, la aflicción ya habría acabado conmigo.</p>
<p style="style=text-align: justify;"><strong id="numeros">93. </strong>
Jamás me olvidaré de tus mandamientos, porque con ellos me has devuelto la vida.</p>
<p style="style=text-align: justify;"><strong id="numeros">94. </strong>
Mi vida te pertenece; ¡sálvame, Señor, pues yo estudio tus mandamientos!</p>
<p style="style=text-align: justify;"><strong id="numeros">95. </strong>
Los malvados buscan cómo destruirme, pero yo trato de entender tus testimonios.</p>
<p style="style=text-align: justify;"><strong id="numeros">96. </strong>
He visto que aun lo perfecto se acaba, pero tus mandamientos son eternos.</p>
<h4><strong>Mem</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">97. </strong>
Mem
¡Cuánto amo yo tus enseñanzas! ¡Todo el día medito en ellas!</p>
<p style="style=text-align: justify;"><strong id="numeros">98. </strong>
Me has hecho más sabio que a mis perseguidores, porque tus enseñanzas están siempre conmigo.</p>
<p style="style=text-align: justify;"><strong id="numeros">99. </strong>
Entiendo más que mis maestros, porque tus testimonios son mi meditación.</p>
<p style="style=text-align: justify;"><strong id="numeros">100. </strong>
Comprendo mejor que los ancianos, porque obedezco tus mandamientos;</p>
<p style="style=text-align: justify;"><strong id="numeros">101. </strong>
Me he apartado de todo mal camino, para obedecer tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">102. </strong>
No me he apartado de tus juicios porque eres tú quien me dirige.</p>
<p style="style=text-align: justify;"><strong id="numeros">103. </strong>
¡Cuán dulces son tus palabras en mi boca! ¡Son más dulces que la miel en mis labios!</p>
<p style="style=text-align: justify;"><strong id="numeros">104. </strong>
Tus mandamientos me han dado inteligencia; por eso odio toda senda de mentira.</p>
<h4><strong>Nun</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">105. </strong>
Nun
Tu palabra es una lámpara a mis pies; ¡es la luz que ilumina mi camino!</p>
<p style="style=text-align: justify;"><strong id="numeros">106. </strong>
Me comprometí, y no me arrepiento: voy a obedecer tus justas sentencias.</p>
<p style="style=text-align: justify;"><strong id="numeros">107. </strong>
Señor, me encuentro muy afligido; dame vida, conforme a tu promesa.</p>
<p style="style=text-align: justify;"><strong id="numeros">108. </strong>
Señor, espero que te agraden mis votos; enséñame a entender tus juicios.</p>
<p style="style=text-align: justify;"><strong id="numeros">109. </strong>
Mi vida está siempre en peligro, pero yo no me olvido de tu ley.</p>
<p style="style=text-align: justify;"><strong id="numeros">110. </strong>
Gente malvada me ha tendido trampas, pero yo no me he apartado de tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">111. </strong>
Tengo tu palabra como herencia eterna, porque ellos me alegran el corazón.</p>
<p style="style=text-align: justify;"><strong id="numeros">112. </strong>
De corazón me dispongo a cumplir tus estatutos siempre, hasta el fin de mis días.</p>
<h4><strong>Sámec</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">113. </strong>
Sámej
No soporto a la gente hipócrita, pero amo tus enseñanzas.</p>
<p style="style=text-align: justify;"><strong id="numeros">114. </strong>
Tú eres mi escondite; eres mi escudo; en tu palabra he puesto mi esperanza.</p>
<p style="style=text-align: justify;"><strong id="numeros">115. </strong>
Ustedes los malhechores: ¡apártense de mí, que quiero obedecer los mandamientos de mi Dios!</p>
<p style="style=text-align: justify;"><strong id="numeros">116. </strong>
Aliméntame con tu palabra, y viviré; ¡no permitas que se frustre mi esperanza!</p>
<p style="style=text-align: justify;"><strong id="numeros">117. </strong>
Sosténme, y estaré a salvo, y siempre me alegraré en tus estatutos.</p>
<p style="style=text-align: justify;"><strong id="numeros">118. </strong>
Tú humillas a los que se apartan de tus estatutos, porque su astucia es pura falsedad.</p>
<p style="style=text-align: justify;"><strong id="numeros">119. </strong>
Consumes como a basura a todos los malvados, por eso yo amo tus testimonios.</p>
<p style="style=text-align: justify;"><strong id="numeros">120. </strong>
Todo mi ser se estremece de temor; ¡tiemblo a causa de tus sentencias!</p>
<h4><strong>Ayin</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">121. </strong>
Ayin
Yo practico el derecho y la justicia; ¡no me dejes caer en poder de mis enemigos!</p>
<p style="style=text-align: justify;"><strong id="numeros">122. </strong>
Trata con bondad a este siervo tuyo; que no se aprovechen de mí los soberbios.</p>
<p style="style=text-align: justify;"><strong id="numeros">123. </strong>
Mis ojos desfallecen esperando que me salves, y que con tu palabra me hagas justicia.</p>
<p style="style=text-align: justify;"><strong id="numeros">124. </strong>
Trátame con misericordia, y enséñame tus estatutos.</p>
<p style="style=text-align: justify;"><strong id="numeros">125. </strong>
Yo soy tu siervo, y quiero entender y llegar a conocer tus testimonios.</p>
<p style="style=text-align: justify;"><strong id="numeros">126. </strong>
Señor, ¡llegó el momento de que actúes, pues los malvados han anulado tu ley!</p>
<p style="style=text-align: justify;"><strong id="numeros">127. </strong>
Por eso yo amo tus mandamientos, porque son mejores que el oro más puro.</p>
<p style="style=text-align: justify;"><strong id="numeros">128. </strong>
Yo estimo la rectitud y pureza de tus mandamientos; por eso me he alejado de la senda de mentira.</p>
<h4><strong>Pe</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">129. </strong>
Pe
Tus testimonios son admirables; por eso son el tesoro de mi alma.</p>
<p style="style=text-align: justify;"><strong id="numeros">130. </strong>
La enseñanza de tus palabras ilumina; y hasta la gente sencilla las entienden.</p>
<p style="style=text-align: justify;"><strong id="numeros">131. </strong>
Grandes son mi sed y mis ansias por recibir y entender tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">132. </strong>
Mírame, y ten misericordia de mí, como la tienes con quienes te aman.</p>
<p style="style=text-align: justify;"><strong id="numeros">133. </strong>
Ordena mis pasos con tu palabra, Para que el pecado no me domine.</p>
<p style="style=text-align: justify;"><strong id="numeros">134. </strong>
Protégeme de los hombres violentos, porque deseo obedecer tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">135. </strong>
Alumbra con tu presencia a este siervo tuyo, y enséñame tus estatutos.</p>
<p style="style=text-align: justify;"><strong id="numeros">136. </strong>
De mis brota el llanto a mares, porque hay gente que no obedece tu ley.</p>
<h4><strong>Tsade</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">137. </strong>
Tsade
Tú, Señor, eres justo, y tus sentencias son rectas.</p>
<p style="style=text-align: justify;"><strong id="numeros">138. </strong>
Los mandamientos que nos has dado son también justos y verdaderos.</p>
<p style="style=text-align: justify;"><strong id="numeros">139. </strong>
Pero el enojo me consume, porque mis enemigos olvidaron tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">140. </strong>
Tu palabra es todo pureza; por eso yo, tu siervo, la amo.</p>
<p style="style=text-align: justify;"><strong id="numeros">141. </strong>
Yo soy insignificante, y nada valgo, pero no me olvido de tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">142. </strong>
Tu justicia es siempre justa, y tu ley es la verdad.</p>
<p style="style=text-align: justify;"><strong id="numeros">143. </strong>
La aflicción y la angustia me dominan, pero el gozo de tus mandamientos me levanta.</p>
<p style="style=text-align: justify;"><strong id="numeros">144. </strong>
Tus testimonios son siempre justos; dame entendimiento y viviré.</p>
<h4><strong>Cof</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">145. </strong>
Cof
Señor, yo te llamo con todo el corazón; ¡respóndeme, y cumpliré tus estatutos!</p>
<p style="style=text-align: justify;"><strong id="numeros">146. </strong>
¡Sálvame, pues a ti elevo mi clamor! ¡Quiero obedecer tus testimonios!</p>
<p style="style=text-align: justify;"><strong id="numeros">147. </strong>
Clamo a ti antes de que amanezca, y me quedo esperando tu respuesta.</p>
<p style="style=text-align: justify;"><strong id="numeros">148. </strong>
Me mantengo despierto toda la noche para meditar en tus mandatos.</p>
<p style="style=text-align: justify;"><strong id="numeros">149. </strong>
Señor, escúchame, por tu misericordia; ¡dame vida, conforme a tu justicia!</p>
<p style="style=text-align: justify;"><strong id="numeros">150. </strong>
Mis malvados perseguidores ya están cerca, pero de tu ley están muy lejos.</p>
<p style="style=text-align: justify;"><strong id="numeros">151. </strong>
Pero tú, Señor, sí estás muy cerca, y todos tus mandamientos son verdad.</p>
<p style="style=text-align: justify;"><strong id="numeros">152. </strong>
Hace tiempo llegué a conocer tus mandatos, los cuales estableciste para la eternidad.</p>
<h4><strong>Resh</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">153. </strong>
Resh
Mira mi aflicción, y ven a salvarme, pues no me he olvidado de tu ley.</p>
<p style="style=text-align: justify;"><strong id="numeros">154. </strong>
¡Defiéndeme, y ponme a salvo! ¡Dame vida con tu palabra!</p>
<p style="style=text-align: justify;"><strong id="numeros">155. </strong>
Los malvados están lejos de tu salvación, porque no buscan tus estatutos.</p>
<p style="style=text-align: justify;"><strong id="numeros">156. </strong>
Señor, tú eres muy bondadoso; ¡dame vida de acuerdo a tu justicia!</p>
<p style="style=text-align: justify;"><strong id="numeros">157. </strong>
Son muchos los enemigos que me persiguen, pero yo no me aparto de tus testimonios.</p>
<p style="style=text-align: justify;"><strong id="numeros">158. </strong>
No soporto ver a esos traidores, porque no obedecen tus palabras.</p>
<p style="style=text-align: justify;"><strong id="numeros">159. </strong>
Señor, ¡mira cuánto amo tus mandamientos! ¡dame vida, conforme a tu misericordia!</p>
<p style="style=text-align: justify;"><strong id="numeros">160. </strong>
La verdad es la esencia de tu palabra, y tus juicios son siempre justos.</p>
<h4><strong>Sin</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">161. </strong>
Shin
Los poderosos me persiguen sin motivo, pero yo respeto y honro tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">162. </strong>
Es tal la alegría que me causa tu palabra que es como hallar un gran tesoro.</p>
<p style="style=text-align: justify;"><strong id="numeros">163. </strong>
No soporto la mentira. ¡La aborrezco! Pero amo tus enseñanzas.</p>
<p style="style=text-align: justify;"><strong id="numeros">164. </strong>
Siete veces al día te alabo porque tus juicios son siempre justos.</p>
<p style="style=text-align: justify;"><strong id="numeros">165. </strong>
Los que aman tu ley viven en completa paz, porque saben que no tropezarán.</p>
<p style="style=text-align: justify;"><strong id="numeros">166. </strong>
Señor, espero que me salves; mientras viva, cumpliré tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">167. </strong>
Toda mi vida he obedecido tus estatutos, pues son el gran amor de mi vida.</p>
<p style="style=text-align: justify;"><strong id="numeros">168. </strong>
He cumplido tus mandamientos y tus testimonios pues tú sabes todo lo que hago.</p>
<h4><strong>Tau</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">169. </strong>
Tav
Señor, ¡escucha mi clamor! ¡Dame entendimiento, conforme a tu palabra!</p>
<p style="style=text-align: justify;"><strong id="numeros">170. </strong>
¡Que llegue mi oración a tu presencia! ¡Líbrame, como lo has prometido!</p>
<p style="style=text-align: justify;"><strong id="numeros">171. </strong>
De mis labios se desbordará la alabanza cuando me hayas enseñado tus estatutos.</p>
<p style="style=text-align: justify;"><strong id="numeros">172. </strong>
Con mis labios proclamaré tu palabra, porque todos tus mandamientos son justos.</p>
<p style="style=text-align: justify;"><strong id="numeros">173. </strong>
Extiende tu mano para socorrerme, porque he elegido obedecer tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">174. </strong>
Señor, yo espero que me salves, porque me deleito en tu ley.</p>
<p style="style=text-align: justify;"><strong id="numeros">175. </strong>
Concédeme vida para alabarte, y sosténme con tus juicios.</p>
<p style="style=text-align: justify;"><strong id="numeros">176. </strong>
Ando sin rumbo, como oveja perdida; ¡ven en busca de este siervo tuyo que no ha olvidado tus mandamientos!</p>
