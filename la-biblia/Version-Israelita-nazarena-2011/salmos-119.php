Nunca descuidaré tus preceptos, pues has preservado mi vida con ellos.</p>
<p style="style=text-align: justify;"><strong id="numeros">94. </strong>
¡Soy tuyo, sálvame! pues me he vuelto a tus preceptos.</p>
<p style="style=text-align: justify;"><strong id="numeros">95. </strong>
Los malvados esperan destruirme, pero yo pondero tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">96. </strong>
He visto que todo tiene su límite, pero tu mandamiento es amplio sin medida.</p>
<h4><strong>Mem</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">97. </strong>
[Mem]
¡Cuánto amo tu Torah! Ella es mi estudio todo el día.</p>
<p style="style=text-align: justify;"><strong id="numeros">98. </strong>
Tus mandamientos me hacen más sabio que mis enemigos; siempre están junto a mí.</p>
<p style="style=text-align: justify;"><strong id="numeros">99. </strong>
He adquirido más discernimiento que todos mis maestros, porque tus decretos son mi estudio.</p>
<p style="style=text-align: justify;"><strong id="numeros">100. </strong>
He adquirido más entendimiento que mis mayores, porque observo tus preceptos.</p>
<p style="style=text-align: justify;"><strong id="numeros">101. </strong>
Me he apartado de todo mal camino para poder guardar tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">102. </strong>
No me he apartado de tus reglas, pues tú me has instruido.</p>
<p style="style=text-align: justify;"><strong id="numeros">103. </strong>
Cuán agradable es tu palabra a mi paladar, más dulce que la miel.</p>
<p style="style=text-align: justify;"><strong id="numeros">104. </strong>
Yo pondero tus preceptos; por eso detesto todo camino falso.</p>
<h4><strong>Nun</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">105. </strong>
[Nun]
Lámpara es a mis pies tu palabra, una luz a mi camino.</p>
<p style="style=text-align: justify;"><strong id="numeros">106. </strong>
He jurado firmemente cumplir tus justas reglas.</p>
<p style="style=text-align: justify;"><strong id="numeros">107. </strong>
Estoy muy afligido; Yahweh, presérvame conforme a tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">108. </strong>
Acepta, Yahweh, mis ofrendas voluntarias; enséñame tus reglas.</p>
<p style="style=text-align: justify;"><strong id="numeros">109. </strong>
Aunque mi vida está siempre en peligro, no descuido tu Torah.</p>
<p style="style=text-align: justify;"><strong id="numeros">110. </strong>
Aunque los malvados me han puesto una trampa, no me he apartado de tus preceptos.</p>
<p style="style=text-align: justify;"><strong id="numeros">111. </strong>
Tus decretos son mi herencia eterna; son el deleite de mi corazón.</p>
<p style="style=text-align: justify;"><strong id="numeros">112. </strong>
Estoy resuelto a seguir tus leyes hasta lo último, para siempre.</p>
<h4><strong>Sámec</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">113. </strong>
[Sámekh]
Detesto a los vacilantes, pero amo tu Torah.</p>
<p style="style=text-align: justify;"><strong id="numeros">114. </strong>
Tú eres mi protección y mi escudo; espero por tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">115. </strong>
Apártense de mí, malhechores, para que pueda observar los mandamientos de mi Elohim.</p>
<p style="style=text-align: justify;"><strong id="numeros">116. </strong>
Susténtame como prometiste, para que viva; no frustres mi esperanza.</p>
<p style="style=text-align: justify;"><strong id="numeros">117. </strong>
Sosténme para que me salve, y siempre me inspiraré en tus leyes.</p>
<p style="style=text-align: justify;"><strong id="numeros">118. </strong>
Tú rechazas a todos los que se desvían de tus leyes, porque son falsos y engañosos.</p>
<p style="style=text-align: justify;"><strong id="numeros">119. </strong>
Eliminas a los malvados como si fueran escoria; con razón amo tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">120. </strong>
Mi carne se crispa por temor de ti; me lleno de pavor ante tus reglas.</p>
<h4><strong>Ayin</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">121. </strong>
[Áyin]
He hecho lo que es justo y correcto; no me abandones a los que quieren perjudicarme.</p>
<p style="style=text-align: justify;"><strong id="numeros">122. </strong>
Garantiza el bienestar de tu siervo; no permitas que me perjudiquen los arrogantes.</p>
<p style="style=text-align: justify;"><strong id="numeros">123. </strong>
Mis ojos desfallecen por tu liberación, por tu promesa de victoria.</p>
<p style="style=text-align: justify;"><strong id="numeros">124. </strong>
Actúa con tu servidor como conviene a tu amor; enséñame tus leyes.</p>
<p style="style=text-align: justify;"><strong id="numeros">125. </strong>
Yo soy tu servidor; dame entendimiento, para que conozca tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">126. </strong>
Es tiempo de actuar, Yahweh, pues han violado tu Torah.</p>
<p style="style=text-align: justify;"><strong id="numeros">127. </strong>
Con razón amo tus mandamientos más que el oro, que el oro fino.</p>
<p style="style=text-align: justify;"><strong id="numeros">128. </strong>
En verdad por todos [tus] preceptos camino rectamente; detesto todo camino falso.</p>
<h4><strong>Pe</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">129. </strong>
[Pe]
Maravillosos son tus decretos; por eso los observo.</p>
<p style="style=text-align: justify;"><strong id="numeros">130. </strong>
Las palabras que escribiste alumbran, y dan entendimiento a los simples.</p>
<p style="style=text-align: justify;"><strong id="numeros">131. </strong>
Abro mi boca y suspiro, anhelando tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">132. </strong>
Vuélvete hacia mí y ten compasión de mí, según tu regla con los que aman tu nombre.</p>
<p style="style=text-align: justify;"><strong id="numeros">133. </strong>
Afirma mis pies conforme a tu promesa; no dejes que me domine la maldad.</p>
<p style="style=text-align: justify;"><strong id="numeros">134. </strong>
Líbrame de la opresión del hombre, para que pueda guardar tus preceptos.</p>
<p style="style=text-align: justify;"><strong id="numeros">135. </strong>
Muéstrate favorable a tu servidor, y enséñame tus leyes.</p>
<p style="style=text-align: justify;"><strong id="numeros">136. </strong>
Mis ojos derraman torrentes de agua porque nadie Obedece tu Torah.</p>
<h4><strong>Tsade</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">137. </strong>
[Tsade]
Tú eres justo, oh Yahweh; tus reglas son rectas.</p>
<p style="style=text-align: justify;"><strong id="numeros">138. </strong>
Has ordenado decretos justos; son firmemente duraderos.</p>
<p style="style=text-align: justify;"><strong id="numeros">139. </strong>
Me consume el celo porque mis adversarios descuidan tus palabras.</p>
<p style="style=text-align: justify;"><strong id="numeros">140. </strong>
Tu palabra es sumamente pura, y tu servidor la ama.</p>
<p style="style=text-align: justify;"><strong id="numeros">141. </strong>
Aunque soy pequeño y despreciado, no he descuidado tus preceptos.</p>
<p style="style=text-align: justify;"><strong id="numeros">142. </strong>
Tu justicia es eterna; tu Torah es verdadera.</p>
<p style="style=text-align: justify;"><strong id="numeros">143. </strong>
Aunque vengan sobre mí la angustia y la aflicción, tus mandamientos son mi deleite.</p>
<p style="style=text-align: justify;"><strong id="numeros">144. </strong>
Tus justos decretos son eternos; dame entendimiento, para que viva.</p>
<h4><strong>Cof</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">145. </strong>
[Qof]
Con todo mi corazón estoy clamando; respóndeme, Yahweh, para que observe tus leyes.</p>
<p style="style=text-align: justify;"><strong id="numeros">146. </strong>
Yo clamo a ti, sálvame, para que guarde tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">147. </strong>
Me levanto antes del alba y pido ayuda; espero por tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">148. </strong>
Mi ojos saludan cada vigilia de la noche, mientras medito en tu promesa.</p>
<p style="style=text-align: justify;"><strong id="numeros">149. </strong>
Oye mi voz como conviene a tu amor; oh Yahweh, presérvame, según es tu regla.</p>
<p style="style=text-align: justify;"><strong id="numeros">150. </strong>
Los que persiguen la intriga se acercan; están lejos de tu Torah.</p>
<p style="style=text-align: justify;"><strong id="numeros">151. </strong>
Tú, Yahweh, estás cerca, y todos tus mandamientos son verdaderos.</p>
<p style="style=text-align: justify;"><strong id="numeros">152. </strong>
Yo sé por tus decretos de antaño que los estableciste para siempre.</p>
<h4><strong>Resh</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">153. </strong>
[Resh]
Mira mi aflicción y líbrame, porque no he descuidado tu Torah.</p>
<p style="style=text-align: justify;"><strong id="numeros">154. </strong>
Defiende mi causa y rescátame; presérvame conforme a tu promesa.</p>
<p style="style=text-align: justify;"><strong id="numeros">155. </strong>
La liberación está lejos del malvado, porque no se han vuelto a tus leyes.</p>
<p style="style=text-align: justify;"><strong id="numeros">156. </strong>
Grandes son tus compasiones, oh Yahweh; según es tu regla, presérvame.</p>
<p style="style=text-align: justify;"><strong id="numeros">157. </strong>
Muchos son mis perseguidores y adversarios; no me he apartado de tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">158. </strong>
He visto traidores y los he aborrecido, porque no tienen en mente tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">159. </strong>
Mira que he amado tus preceptos; oh Yahweh, presérvame, como conviene a tu amor.</p>
<p style="style=text-align: justify;"><strong id="numeros">160. </strong>
La esencia de tu palabra es la verdad; tus reglas justas son eternas.</p>
<h4><strong>Sin</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">161. </strong>
[Shin]
Príncipes me han perseguido sin razón; mi corazón se entusiasma con tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">162. </strong>
Me gozo por tu promesa como quien obtiene grandes despojos.</p>
<p style="style=text-align: justify;"><strong id="numeros">163. </strong>
Detesto y aborrezco la falsedad; amo tu ley.</p>
<p style="style=text-align: justify;"><strong id="numeros">164. </strong>
Siete veces al día te alabo por tus reglas justas.</p>
<p style="style=text-align: justify;"><strong id="numeros">165. </strong>
Los que aman tu Torah disfrutan de bienestar; no se hallan en adversidad.</p>
<p style="style=text-align: justify;"><strong id="numeros">166. </strong>
Yo espero tu liberación, oh Yahweh; observo tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">167. </strong>
Obedezco tus decretos y los amo grandemente.</p>
<p style="style=text-align: justify;"><strong id="numeros">168. </strong>
Obedezco tus preceptos y decretos; todos mis caminos están ante ti.</p>
<h4><strong>Tau</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">169. </strong>
[Tau]
Llegue a ti mi clamor, oh Yahweh; dame entendimiento conforme a tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">170. </strong>
Que mi petición llegue ante ti; sálvame conforme a tu promesa.</p>
<p style="style=text-align: justify;"><strong id="numeros">171. </strong>
Mis labios rebosarán en alabanza, porque tú me enseñas tus leyes.</p>
<p style="style=text-align: justify;"><strong id="numeros">172. </strong>
Mi lengua declarará tu promesa, porque todos tus mandamientos son justos.</p>
<p style="style=text-align: justify;"><strong id="numeros">173. </strong>
Prepara tu mano para socorrerme, porque he escogido tus preceptos.</p>
<p style="style=text-align: justify;"><strong id="numeros">174. </strong>
He suspirado por tu liberación, oh Yahweh; tu Torah es mi deleite.</p>
<p style="style=text-align: justify;"><strong id="numeros">175. </strong>
Permíteme vivir, para que te alabe; que tus reglas sean mi ayuda;</p>
<p style="style=text-align: justify;"><strong id="numeros">176. </strong>
He andado errante como oveja perdida; busca a tu servidor, porque no he descuidado tus mandamientos.</p>
