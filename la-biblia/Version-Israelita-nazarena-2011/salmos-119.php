Felices los de conducta intachable, los que siguen la Torah de Yahweh. 2. Felices los que observan sus decretos, los que lo buscan de todo corazón. 3. No han hecho lo malo, sino que han seguido Sus caminos. 4. Tú has mandado que se guarden diligentemente tus preceptos. 5. Quisiera que fueran firmes mis caminos en guardar tus leyes; 6. entonces no quedaría yo avergonzado cuando me fije en todos tus mandamientos. 7. Te alabaré con corazón sincero a medida que aprendo tus reglas. 8. Guardaré tus leyes; no me abandones por completo. 9. [Bet]

¿Cómo podrá un joven mantener puro su camino? -aferrándose a tu palabra. 10. Te he buscado con todo mi corazón; no dejes que me aparte de tus mandamientos 11. En mi corazón atesoro tu palabra; para no pecar contra ti. 12. Bendito seas, Yahweh; adiéstrame en tus leyes. 13. Con mis labios ensayo todas las reglas que proclamaste. 14. En el camino de tus decretos me gozo como en muchas riquezas. 15. Estudio tus preceptos; me fijo en tus caminos; 16. me deleito en tus leyes; no descuidaré tu palabra. 17. [Guímel]

Trata con bondad a tu servidor, para que viva y guarde tu palabra. 18. Abre mis ojos para que perciba las maravillas de tu Torah. 19. Soy sólo un peregrino en esta tierra; no me ocultes tus mandamientos. 20. Mi vida se consume anhelando tus reglas todo el tiempo. 21. Tú reprendes a los insolentes malditos que se apartan de tus mandamientos. 22. Aparta de mí el insulto y el abuso, que yo observo tus decretos. 23. Aunque se reúnan príncipes y hablen contra mí, tu servidor estudia tus leyes. 24. Porque tus decretos son mi deleite, mis compañeros íntimos. 25. [Dálet]

Mi vida se pega al polvo; reavívame de acuerdo a tu palabra. 26. Yo he declarado mi camino, y tú me has respondido; adiéstrame en tus leyes. 27. Hazme entender el camino de tus preceptos, para que estudie tus maravillosos actos. 28. Estoy deshecho de tristeza; sosténme conforme a tu palabra. 29. Aleja de mí todo camino falso; favoréceme con tu Torah. 30. He escogido el camino de la fidelidad; he puesto tus reglas delante de mí. 31. Me aferro a tus decretos; oh Yahweh, no me avergüences. 32. Con fervor me apego a tus mandamientos, porque tú amplías mi entendimiento. 33. [He]

Enséñame, oh Yahweh, el camino de tus leyes; yo las guardaré hasta lo último. 34. Dame entendimiento, para que observe tu Torah y la guarde de todo corazón. 35. Guíame en la senda de tus mandamientos, porque esa es mi preocupación. 36. Inclina mi corazón a tus decretos y no al amor a las ganancias. 37. Aparta mis ojos de mirar la falsedad; presérvame por tus caminos. 38. Cúmplele tu promesa a tu servidor, que es para los que te adoran. 39. Aleja el oprobio que temo, porque tus reglas son buenas. 40. Mira que yo he ansiado tus preceptos; presérvame por tu justicia. 41. [Waw]

Que me alcance tu amor, oh Yahweh, tu liberación, como has prometido. 42. Tendré una respuesta para los que me insultan, porque he puesto mi confianza en tu palabra. 43. No quites por completo de mi boca la verdad, pues he puesto mi confianza en tus reglas. 44. Siempre Obedeceré tu Torah, para siempre jamás. 45. Andaré con libertad, porque he buscado tus preceptos. 46. Hablaré de tus decretos, y no me avergonzaré en presencia de reyes. 47. Me deleitaré en tus mandamientos, que amo. 48. Alzaré mis manos hacia tus mandamientos, que amo; yo estudio tus leyes. 49. Acuérdate de la palabra que diste a tu servidor, por la cual me diste esperanza. 50. Este es mi consuelo en mi aflicción, que tu promesa me ha preservado. 51. Aunque el arrogante se ha burlado cruelmente de mí, yo no me he apartado de tu Torah. 52. Me acuerdo de tus reglas de antaño, oh Yahweh, y hallo consuelo en ellas. 53. La ira se ha apoderado de mí por los malvados que olvidan tu Torah. 54. Tus leyes me han sido una fuente de fortaleza dondequiera que vivo. 55. De noche me acuerdo de tu nombre, oh Yahweh, y obedezco tu Torah. 56. Esto me ha tocado, porque he observado tus preceptos. 57. [Jet]

Yahweh es mi porción; he resuelto guardar tus palabras. 58. Te he implorado de todo corazón, que tengas compasión de mí, conforme a tu promesa. 59. He considerado mis caminos, y me he vuelto a tus decretos. 60. Me he apresurado sin dilación a observar tus mandamientos. 61. Aunque los lazos de los malvados me han rodeado, no he abandonado tu Torah. 62. A media noche me levanto para alabarte por tus justas reglas. 63. Soy compañero de todos los que te honran, de los que guardan tus preceptos. 64. Tu amor, oh Yahweh, llena la tierra; enséñame tus leyes. 65. [Tet]

Tú has tratado bien a tu servidor, conforme a tu palabra, oh Yahweh. 66. Enséñame el criterio sano y el conocimiento, porque he puesto mi confianza en tus mandamientos. 67. Antes de humillarme yo me extraviaba, pero ahora guardo tu palabra. 68. Tú eres bueno y benévolo; enséñame tus leyes. 69. Aunque los arrogantes me han acusado falsamente, yo observo tus preceptos de todo corazón. 70. Las mentes de ellos están espesas como grasa; en cuanto a mí, tu Torah es mi deleite. 71. Es bueno para mí el haber sido humillado, para que aprendiera tus leyes. 72. Prefiero la Torah que proclamas a millares de piezas de oro y plata. 73. [Yod]

Tus manos me hicieron y me formaron; dame entendimiento para aprender tus mandamientos. 74. Los que te honran me verán y se alegrarán, porque he puesto mi esperanza en tu palabra. 75. Yo sé, oh Yahweh, que tus reglas son justas; con razón me has humillado. 76. Que tu amor me consuele conforme a tu promesa para tu servidor. 77. Que me alcance tu justicia, para que viva, pues tu Torah es mi deleite. 78. Que se avergüencen los insolentes, pues me han perjudicado sin motivo; yo estudiaré tus preceptos. 79. Que se vuelvan a mí los que te honran, los que conocen tus decretos. 80. Que siga yo de todo corazón tus leyes para que no quede avergonzado. 81. [Kaf]

Yo suspiro por tu liberación; espero por tu palabra. 82. Mis ojos desfallecen por tu promesa; digo: “¿Cuándo me consolarás?” 83. Aunque he venido a ser como cuero secado al humo, no he abandonado tus leyes. 84. ¿Cuánto me queda de vida? ¿Cuándo les harás juicio a mis perseguidores? 85. Los insolentes me han cavado hoyos, burlando tus leyes. 86. Todos tus mandamientos son duraderos; me persiguen sin motivo, ¡ayúdame! 87. Aunque casi me eliminaron de la tierra, no abandoné tus preceptos. 88. Como conviene a tu amor, presérvame, para que guarde los decretos que proclamaste. 89. [Lámed]

Yahweh existe para siempre; tu palabra permanece firme en el cielo. 90. Tu fidelidad es para todas las generaciones; tú has establecido la tierra, y ella permanece. 91. Ellas permanecen hasta hoy para [cumplir] tus reglas, porque todas son tus servidoras. 92. Si tu Torah no hubiera sido mi deleite yo habría perecido en mi aflicción. 93. Nunca descuidaré tus preceptos, pues has preservado mi vida con ellos. 94. ¡Soy tuyo, sálvame! pues me he vuelto a tus preceptos. 95. Los malvados esperan destruirme, pero yo pondero tus decretos. 96. He visto que todo tiene su límite, pero tu mandamiento es amplio sin medida. 97. [Mem]

¡Cuánto amo tu Torah! Ella es mi estudio todo el día. 98. Tus mandamientos me hacen más sabio que mis enemigos; siempre están junto a mí. 99. He adquirido más discernimiento que todos mis maestros, porque tus decretos son mi estudio. 100. He adquirido más entendimiento que mis mayores, porque observo tus preceptos. 101. Me he apartado de todo mal camino para poder guardar tu palabra. 102. No me he apartado de tus reglas, pues tú me has instruido. 103. Cuán agradable es tu palabra a mi paladar, más dulce que la miel. 104. Yo pondero tus preceptos; por eso detesto todo camino falso. 105. [Nun]

Lámpara es a mis pies tu palabra, una luz a mi camino. 106. He jurado firmemente cumplir tus justas reglas. 107. Estoy muy afligido; Yahweh, presérvame conforme a tu palabra. 108. Acepta, Yahweh, mis ofrendas voluntarias; enséñame tus reglas. 109. Aunque mi vida está siempre en peligro, no descuido tu Torah. 110. Aunque los malvados me han puesto una trampa, no me he apartado de tus preceptos. 111. Tus decretos son mi herencia eterna; son el deleite de mi corazón. 112. Estoy resuelto a seguir tus leyes hasta lo último, para siempre. 113. [Sámekh] 

Detesto a los vacilantes, pero amo tu Torah. 114. Tú eres mi protección y mi escudo; espero por tu palabra. 115. Apártense de mí, malhechores, para que pueda observar los mandamientos de mi Elohim. 116. Susténtame como prometiste, para que viva; no frustres mi esperanza. 117. Sosténme para que me salve, y siempre me inspiraré en tus leyes. 118. Tú rechazas a todos los que se desvían de tus leyes, porque son falsos y engañosos. 119. Eliminas a los malvados como si fueran escoria; con razón amo tus decretos. 120. Mi carne se crispa por temor de ti; me lleno de pavor ante tus reglas. 121. [Áyin]

He hecho lo que es justo y correcto; no me abandones a los que quieren perjudicarme. 122. Garantiza el bienestar de tu siervo; no permitas que me perjudiquen los arrogantes. 123. Mis ojos desfallecen por tu liberación, por tu promesa de victoria. 124. Actúa con tu servidor como conviene a tu amor; enséñame tus leyes. 125. Yo soy tu servidor; dame entendimiento, para que conozca tus decretos. 126. Es tiempo de actuar, Yahweh, pues han violado tu Torah. 127. Con razón amo tus mandamientos más que el oro, que el oro fino. 128. En verdad por todos [tus] preceptos camino rectamente; detesto todo camino falso. 129. [Pe]

Maravillosos son tus decretos; por eso los observo. 130. Las palabras que escribiste alumbran, y dan entendimiento a los simples. 131. Abro mi boca y suspiro, anhelando tus mandamientos. 132. Vuélvete hacia mí y ten compasión de mí, según tu regla con los que aman tu nombre. 133. Afirma mis pies conforme a tu promesa; no dejes que me domine la maldad. 134. Líbrame de la opresión del hombre, para que pueda guardar tus preceptos. 135. Muéstrate favorable a tu servidor, y enséñame tus leyes. 136. Mis ojos derraman torrentes de agua porque nadie Obedece tu Torah. 137. [Tsade]

Tú eres justo, oh Yahweh; tus reglas son rectas. 138. Has ordenado decretos justos; son firmemente duraderos. 139. Me consume el celo porque mis adversarios descuidan tus palabras. 140. Tu palabra es sumamente pura, y tu servidor la ama. 141. Aunque soy pequeño y despreciado, no he descuidado tus preceptos. 142. Tu justicia es eterna; tu Torah es verdadera. 143. Aunque vengan sobre mí la angustia y la aflicción, tus mandamientos son mi deleite. 144. Tus justos decretos son eternos; dame entendimiento, para que viva. 145. [Qof]

Con todo mi corazón estoy clamando; respóndeme, Yahweh, para que observe tus leyes. 146. Yo clamo a ti, sálvame, para que guarde tus decretos. 147. Me levanto antes del alba y pido ayuda; espero por tu palabra. 148. Mi ojos saludan cada vigilia de la noche, mientras medito en tu promesa. 149. Oye mi voz como conviene a tu amor; oh Yahweh, presérvame, según es tu regla. 150. Los que persiguen la intriga se acercan; están lejos de tu Torah. 151. Tú, Yahweh, estás cerca, y todos tus mandamientos son verdaderos. 152. Yo sé por tus decretos de antaño que los estableciste para siempre. 153. [Resh]

Mira mi aflicción y líbrame, porque no he descuidado tu Torah. 154. Defiende mi causa y rescátame; presérvame conforme a tu promesa. 155. La liberación está lejos del malvado, porque no se han vuelto a tus leyes. 156. Grandes son tus compasiones, oh Yahweh; según es tu regla, presérvame. 157. Muchos son mis perseguidores y adversarios; no me he apartado de tus decretos. 158. He visto traidores y los he aborrecido, porque no tienen en mente tu palabra. 159. Mira que he amado tus preceptos; oh Yahweh, presérvame, como conviene a tu amor. 160. La esencia de tu palabra es la verdad; tus reglas justas son eternas. 161. [Shin]

Príncipes me han perseguido sin razón; mi corazón se entusiasma con tu palabra. 162. Me gozo por tu promesa como quien obtiene grandes despojos. 163. Detesto y aborrezco la falsedad; amo tu ley. 164. Siete veces al día te alabo por tus reglas justas. 165. Los que aman tu Torah disfrutan de bienestar; no se hallan en adversidad. 166. Yo espero tu liberación, oh Yahweh; observo tus mandamientos. 167. Obedezco tus decretos y los amo grandemente. 168. Obedezco tus preceptos y decretos; todos mis caminos están ante ti. 169. [Tau]

Llegue a ti mi clamor, oh Yahweh; dame entendimiento conforme a tu palabra. 170. Que mi petición llegue ante ti; sálvame conforme a tu promesa. 171. Mis labios rebosarán en alabanza, porque tú me enseñas tus leyes. 172. Mi lengua declarará tu promesa, porque todos tus mandamientos son justos. 173. Prepara tu mano para socorrerme, porque he escogido tus preceptos. 174. He suspirado por tu liberación, oh Yahweh; tu Torah es mi deleite. 175. Permíteme vivir, para que te alabe; que tus reglas sean mi ayuda; 176. He andado errante como oveja perdida; busca a tu servidor, porque no he descuidado tus mandamientos.... %0A%0Ahttp://bt3.me/gwme"><span class="icon-whatsapp"></span> WHATSAPP</a></li>
