                                                                                <p style="style=text-align: justify;"><strong id="numeros">2. </strong>
 Y diciendo: "Haced penitencia, porque se ha acercado el reino de los cielos". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">3. </strong>
 Pues éste es(c), de quien habló el profeta Isaías, diciendo:
Voz del que clama en el desierto:
Aparejad el camino del Señor;
haced derechas sus veredas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">4. </strong>
 Y el mismo Juan tenía un vestido de pelos de camellos(d), y un ceñidor de cuero alrededor de sus lomos, y su comida eran langostas y miel silvestre(e). </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">5. </strong>
 Entonces salía a él Jerusalén, y toda la Judea, y toda la tierra de la comarca del Jordán; </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">6. </strong>
 Y eran bautizados por él(f) en el Jordán, confesando sus pecados. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">7. </strong>
 Mas viendo, que muchos de los fariseos(g) y de los saduceos venían a su bautismo, les dijo: "Raza de víboras, ¿quién os ha enseñado a huir de la ira venidera? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">8. </strong>
 Haced, pues, fruto digno de penitencia, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">9. </strong>
 Y no queráis decir dentro de vosotros(h): 'A Abraham tenemos por padre'; porque os digo, que poderoso es Dios para levantar hijos a Abraham de estas piedras(i). </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">10. </strong>
 Porque ya está puesta la segur a la raíz de los árboles; pues todo árbol que no hace buen fruto, cortado será, y echado en el fuego(j). </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">11. </strong>
 Yo, en verdad, os bautizo en agua para penitencia; mas el que ha de venir(k) en pos de mí, más fuerte es que yo, cuyo calzado no soy digno de llevar(l). Él os bautizará en el Espíritu Santo(m) y en fuego. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">12. </strong>
 Su bieldo(n) en su mano está; y limpiará bien su era, y recogerá su trigo en el granero, mas quemará las pajas en fuego que no se podrá apagar jamás". </p>
                                                    <h4><strong>El bautismo de Jesús</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">13. </strong>
 Entonces(o) vino Jesús de la Galilea al Jordán, a Juan, para ser bautizado por él. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">14. </strong>
 Mas Juan se lo estorbaba, diciendo: "Yo debo ser bautizado por ti, ¿y tú vienes a mí(p)?" </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">15. </strong>
 Y respondiendo Jesús, le dijo: "Deja ahora, porque así nos conviene cumplir toda justicia". Entonces le dejó(q). </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">16. </strong>
 Y después que Jesús fue bautizado, subió luego del agua; y he aquí se le abrieron los cielos; y vio al Espíritu de Dios(r) que descendía como paloma y que venía sobre él. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">17. </strong>
 Y he aquí una voz de los cielos que decía: "Éste es mi Hijo el amado, en quien me he complacido(s)". </p>
                                                        
