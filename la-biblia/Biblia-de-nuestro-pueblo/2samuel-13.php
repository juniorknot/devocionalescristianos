<li class="hide-for-large"><a href="whatsapp://send?text=2 Samuel 13 (BNP)%0A%0A 1. ABSALÓN
Tamar, violada por su hermano
 Pasó cierto tiempo. Absalón, hijo de David, tenía una hermana muy guapa, llamada Tamar, y Amnón, hijo de David, se enamoró de ella tan apasionadamente, 2. que se puso enfermo por ella, porque su hermana Tamar era soltera, y a Amnón le parecía imposible intentar nada con ella. 3. Amnón tenía un amigo llamado Jonadab, hijo de Samá, hermano de David. Jonadab era muy hábil, 4. y le dijo:
-¿Qué te pasa, príncipe, que cada día tienes peor cara? ¿Por qué no me lo cuentas?
Amnón respondió:
-Es por Tamar, la hermana de mi hermano Absalón; estoy enamorado de ella. 5. Entonces Jonadab le propuso:
-Acuéstate como si estuvieras enfermo, y cuando tu padre venga a verte, le pides que vaya tu hermana Tamar a darte de comer: que te prepare algo allí delante, para que tú lo veas, y te lo sirva ella misma. 6. Amnón se acostó y se fingió enfermo. El rey fue a verlo y Amnón le dijo:
-Por favor, que venga mi hermana Tamar y me fría aquí delante dos buñuelos y que me los sirva ella misma. 7. David envió un recado a casa de Tamar:
-Vete a casa de tu hermano Amnón y prepárale algo de comer. 8. Tamar fue a casa de su hermano Amnón, que estaba acostado, tomó harina, la amasó, la preparó y frió los buñuelos delante de Amnón. 9. Luego los sacó de la sartén delante de él, pero Amnón no quiso comer, y ordenó:
-¡Salgan todos!
Cuando salieron todos, 10. Amnón dijo a Tamar:
-Trae la comida a la alcoba y dame tú misma de comer.
Tamar tomó los buñuelos y se los llevó a su hermano a la alcoba; 11. pero al acercarse a él para darle de comer, Amnón la sujetó y le dijo:
-Ven, hermana mía, acuéstate conmigo. 12. Ella replicó:
-No, hermano mío; no me fuerces, que eso no se hace en Israel, no cometas tal infamia. 13. ¿Dónde iré yo con mi deshonra? Tú quedarás como un infame en Israel. Por favor, díselo al rey, que no se opondrá a que yo sea tuya. 14. Pero Amnón no quiso hacerle caso, la forzó violentamente y se acostó con ella. 15. Después sintió un terrible aborrecimiento hacia ella, un aborrecimiento mayor que el amor que le había tenido, y le dijo:
-¡Levántate, vete! 16. Pero ella le suplicó:
-¡No, hermano; despacharme ahora sería una maldad más grave que la que acabas de hacer conmigo!
Pero él no le hizo caso; 17. llamó a un sirviente y ordenó:
-¡Échenme a ésta a la calle! ¡Y ciérrenle la puerta! 18. Ella llevaba una túnica con mangas, porque así vestían tradicionalmente las hijas solteras del rey. El sirviente la sacó a la calle y le cerró la puerta. 19. Tamar se echó polvo a la cabeza, se rasgó la túnica y se fue gritando por el camino, con las manos en la cabeza. 20. Su hermano Absalón le preguntó:
-¿Ha estado contigo tu hermano Amnón? Bueno, hermana, tú calla; es tu hermano, no te atormentes por eso.
Tamar se quedó, desolada, en casa de su hermano Absalón. 21. El rey David oyó lo que había pasado y se indignó, -pero no quiso dar un disgusto a su hijo Amnón, a quien amaba por ser su primogénito-. 22. Absalón no dirigió una palabra ni buena ni mala a Amnón, pero le guardó rencor por haber violado a su hermana Tamar. 23. Asesinato de Amnón
 Dos años después, la gente de Absalón estaba esquilando sus ovejas en Baal-Jasor, junto a Efrón, y Absalón convidó a todos los hijos del rey. 24. Se presentó al rey y le dijo:
-Tu servidor está ahora esquilando las ovejas. Dígnese venir conmigo el rey y su corte. 25. El rey respondió:
-No, hijo; no vamos a ir todos a serte una carga.
Él insistió, pero David no quiso ir, y lo despidió con su bendición. 26. Absalón le dijo:
-Que venga con nosotros por lo menos mi hermano Amnón.
El rey preguntó:
-¿Para qué va a ir contigo? 27. Pero Absalón insistió, y entonces David mandó con él a Amnón y a todos los hijos del rey. Absalón preparó un banquete digno de un rey 28. y ordenó a sus criados:
-Fíjense bien. Cuando Amnón esté ya bebido y yo les dé la orden de herirlo, lo matarán, sin miedo ninguno; yo se lo mando. Tengan ánimo y sean valientes. 29. Los criados de Absalón cumplieron sus órdenes. Entonces todos los hijos del rey emprendieron la huida cada uno en su mulo. 30. Iban todavía de camino, y ya le llegó a David la noticia:
-¡Absalón ha matado a todos los hijos del rey y no queda ninguno! 31. El rey se levantó, se rasgó las vestiduras y se echó por tierra. Todos los ministros se rasgaron las vestiduras. 32. Pero Jonadab, hijo de Samá, hermano de David, dijo:
-No piense su majestad que han matado a todos los hijos del rey. Sólo ha muerto Amnón. Absalón lo decidió el día que Amnón violó a su hermana Tamar. 33. Así que no se preocupe su majestad pensando que han muerto todos los hijos del rey, porque sólo ha muerto Amnón, 34. y Absalón ha huido.
