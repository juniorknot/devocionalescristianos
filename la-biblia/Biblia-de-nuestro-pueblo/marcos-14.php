<p style="style=text-align: justify;"><strong id="numeros">31. </strong>
Oración en el huerto
Mt 26,36-46; Lc 22,39-46; cfr. Jn 18,1
Llegados al lugar llamado Getsemaní, dijo a sus discípulos:
-Siéntense aquí mientras yo voy a orar. </p>
<h4><strong>Jesús ora en Getsemaní</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">32. </strong>
Llevó con él a Pedro, Santiago y Juan y empezó a sentir tristeza y angustia. </p>
<p style="style=text-align: justify;"><strong id="numeros">33. </strong>
Entonces les dijo:
-Siento una tristeza de muerte; quédense aquí y permanezcan despiertos. </p>
<p style="style=text-align: justify;"><strong id="numeros">34. </strong>
Se adelantó un poco, se postró en tierra y oraba que, si era posible, se alejase de él aquella hora. </p>
<p style="style=text-align: justify;"><strong id="numeros">35. </strong>
Decía:
-Padre-, tú lo puedes todo, aparta de mí esta copa. Pero no se haga mi voluntad, sino la tuya. </p>
<p style="style=text-align: justify;"><strong id="numeros">36. </strong>
Volvió, y los encontró dormidos. Dice a Pedro:
-Simón, ¿duermes? ¿No has sido capaz de estar despierto una hora? </p>
<p style="style=text-align: justify;"><strong id="numeros">37. </strong>
Permanezcan despiertos y oren para no caer en la tentación. El espíritu está dispuesto, pero la carne es débil. </p>
<p style="style=text-align: justify;"><strong id="numeros">38. </strong>
Volvió otra vez y oró repitiendo las mismas palabras. </p>
<p style="style=text-align: justify;"><strong id="numeros">39. </strong>
Al volver, los encontró otra vez dormidos, porque los ojos se les cerraban de sueño; y no supieron qué contestar. </p>
<p style="style=text-align: justify;"><strong id="numeros">40. </strong>
Volvió por tercera vez y les dice:
-¡Todavía dormidos y descansando! Basta, ha llegado la hora en que el Hijo del Hombre será entregado en poder de los pecadores. </p>
<p style="style=text-align: justify;"><strong id="numeros">41. </strong>
Vamos, levántense, se acerca el traidor. </p>
<p style="style=text-align: justify;"><strong id="numeros">42. </strong>
Arresto de Jesús
Mt 26,47-56; Lc 22,47-53; cfr. Jn 18,2-12
Todavía estaba hablando cuando se presentó Judas, uno de los Doce, y con él gente armada de espadas y palos, enviada por los sumos sacerdotes, los letrados y los ancianos. </p>
<h4><strong>Arresto de Jesús</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
El traidor les había dado una contraseña: Al que yo bese, ése es; arréstenlo y llévenlo con cuidado. </p>
<p style="style=text-align: justify;"><strong id="numeros">44. </strong>
Enseguida, acercándose a Jesús, le dijo: ¡Maestro!, y le dio un beso. </p>
<p style="style=text-align: justify;"><strong id="numeros">45. </strong>
Los otros se le tiraron encima y lo arrestaron. </p>
<p style="style=text-align: justify;"><strong id="numeros">46. </strong>
Uno de los presentes desenvainó la espada y de un tajo cortó una oreja al sirviente del sumo sacerdote. </p>
<p style="style=text-align: justify;"><strong id="numeros">47. </strong>
Jesús se dirigió a ellos:
-Como si se tratara de un asaltante, han salido armados de espadas y palos para capturarme. </p>
<p style="style=text-align: justify;"><strong id="numeros">48. </strong>
Diariamente estaba con ustedes enseñando en el templo y no me arrestaron. Pero se ha de cumplir la Escritura. </p>
<p style="style=text-align: justify;"><strong id="numeros">49. </strong>
Y todos lo abandonaron y huyeron. </p>
<p style="style=text-align: justify;"><strong id="numeros">50. </strong>
Le seguía, también, un muchacho cubierto sólo por una sábana. Lo agarraron; </p>
<h4><strong>El joven que huyó</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
pero él, soltando la sábana, se les escapó desnudo. </p>
<p style="style=text-align: justify;"><strong id="numeros">52. </strong>
Jesús ante el Consejo
Mt 26,57-68; Lc 22,54s.63-71; cfr. Jn 18,13s.19-24
Condujeron a Jesús a casa del sumo sacerdote, y se reunieron todos los sumos sacerdotes con los ancianos y los letrados. </p>
<h4><strong>Jesús ante el concilio</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
Pedro le fue siguiendo a distancia hasta entrar en el palacio del sumo sacerdote. Se quedó sentado con los empleados, calentándose junto al fuego. </p>
<p style="style=text-align: justify;"><strong id="numeros">54. </strong>
El sumo sacerdote y el Consejo en pleno buscaban un testimonio contra Jesús que permitiera condenarlo a muerte, y no lo encontraban, </p>
<p style="style=text-align: justify;"><strong id="numeros">55. </strong>
ya que aunque muchos testimoniaban en falso contra él, sus testimonios no concordaban. </p>
<p style="style=text-align: justify;"><strong id="numeros">56. </strong>
Algunos se levantaron y declararon en falso contra él: </p>
<p style="style=text-align: justify;"><strong id="numeros">57. </strong>
-Le hemos oído decir: Yo he de destruir este santuario, construido por manos humanas, y en tres días construiré otro, no edificado con manos humanas. </p>
<p style="style=text-align: justify;"><strong id="numeros">58. </strong>
Pero tampoco en este punto concordaba su testimonio. </p>
<p style="style=text-align: justify;"><strong id="numeros">59. </strong>
Entonces el sumo sacerdote se puso de pie en medio y preguntó a Jesús:
-¿No respondes nada a lo que éstos declaran contra ti? </p>
<p style="style=text-align: justify;"><strong id="numeros">60. </strong>
Él seguía callado sin responder nada. De nuevo le preguntó el sumo sacerdote:
-¿Eres tú el Mesías, el Hijo del Bendito? </p>
<p style="style=text-align: justify;"><strong id="numeros">61. </strong>
Jesús respondió:
-Yo soy. Verán al Hijo del Hombre sentado a la derecha del Todopoderoso y llegando entre las nubes del cielo. </p>
<p style="style=text-align: justify;"><strong id="numeros">62. </strong>
El sumo sacerdote, rasgándose sus vestiduras, dijo:
-¿Qué falta nos hacen los testigos? </p>
<p style="style=text-align: justify;"><strong id="numeros">63. </strong>
Ustedes mismos han oído la blasfemia. ¿Qué les parece?
Todos sentenciaron que era reo de muerte. </p>
<p style="style=text-align: justify;"><strong id="numeros">64. </strong>
Algunos se pusieron a escupirle, a taparle los ojos y darle bofetadas diciendo:
-¡Adivina quién fue!
También los empleados le daban bofetadas. </p>
<p style="style=text-align: justify;"><strong id="numeros">65. </strong>
Negaciones de Pedro
Mt 26,69-75; Lc 22,56-62; cfr. Jn 18,15-18.25-27
Estaba Pedro abajo en el patio, cuando una sirvienta del sumo sacerdote, </p>
<h4><strong>Pedro niega a Jesús</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">66. </strong>
viendo a Pedro que se calentaba, se le quedó mirando y le dijo:
-También tú estabas con el Nazareno, con Jesús. </p>
<p style="style=text-align: justify;"><strong id="numeros">67. </strong>
Él lo negó:
-Ni sé ni entiendo lo que dices.
Salió al vestíbulo [y un gallo cantó]. </p>
<p style="style=text-align: justify;"><strong id="numeros">68. </strong>
La sirvienta lo vio y empezó a decir otra vez a los presentes:
-Éste es uno de ellos. </p>
<p style="style=text-align: justify;"><strong id="numeros">69. </strong>
De nuevo lo negó. Al poco tiempo también los presentes decían a Pedro:
-Realmente eres de ellos, porque eres galileo. </p>
<p style="style=text-align: justify;"><strong id="numeros">70. </strong>
Entonces empezó a echar maldiciones y a jurar que no conocía al hombre del que hablaban. </p>
<p style="style=text-align: justify;"><strong id="numeros">71. </strong>
Al instante cantó por segunda vez el gallo. Pedro recordó lo que le había dicho Jesús: Antes que el gallo cante dos veces me habrás negado tres. Y se puso a llorar. </p>
<p style="style=text-align: justify;"><strong id="numeros">72. </strong>
Jesús ante Pilato
Mt 27,1s.11-26; Lc 23,1-5.13-25; cfr. Jn 18,28-19,1.4-16
Ni bien amaneció, el Consejo en pleno, sumos sacerdotes, ancianos y letrados se pusieron a deliberar. Ataron a Jesús, lo condujeron y se lo entregaron a Pilato. </p>
