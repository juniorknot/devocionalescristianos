<li class="hide-for-large"><a href="whatsapp://send?text=Marcos 14 (BNP)%0A%0A 1. Pero decían que no debía ser durante las fiestas, para que no se amotinase el pueblo. 2. Unción en Betania
Mt 26,6-13; cfr. Lc 7,36-50; Jn 12,1-8
 Estando él en Betania, invitado en casa de Simón el Leproso, llegó una mujer con un frasco muy costoso de perfume de nardo puro. Quebró el frasco y se lo derramó en la cabeza. 3. Algunos comentaban indignados:
-¿A qué viene este derroche de perfume? 4. Se podía haber vendido el perfume por trescientos denarios para dárselos a los pobres. Y la reprendían. 5. Pero Jesús dijo:
-Déjenla, ¿por qué la molestan? Ha hecho una obra buena conmigo. 6. A los pobres los tendrán siempre entre ustedes y podrán socorrerlos cuando quieran; pero a mí no siempre me tendrán. 7. Ha hecho lo que podía: se ha adelantado a preparar mi cuerpo para la sepultura. 8. Les aseguro que en cualquier parte del mundo donde se proclame la Buena Noticia, se mencionará también lo que ella ha hecho. 9. Traición de Judas
Mt 26,14-16; Lc 22,3-6
 Judas Iscariote, uno de los Doce, se dirigió a los sumos sacerdotes para entregárselo. 10. Al oírlo se alegraron y prometieron darle dinero. Y él se puso a buscar una oportunidad para entregarlo. 11. Pascua y Eucaristía
Mt 26,17-19; Lc 22,7-13
 El primer día de los Ázimos, cuando se inmolaba la víctima pascual, le dijeron los discípulos:
-¿Dónde quieres que vayamos a prepararte la cena de Pascua? 12. Él envió a dos discípulos encargándoles:
-Vayan a la ciudad y les saldrá al encuentro un hombre llevando un cántaro de agua. Síganlo 13. y donde entre, digan al dueño de casa: Dice el Maestro, que dónde está la sala en la que va a comer la cena de Pascua con sus discípulos. 14. Él les mostrará un salón en el piso superior, preparado con divanes. Preparen allí la cena. 15. Salieron los discípulos, se dirigieron a la ciudad, encontraron lo que les había dicho y prepararon la cena de Pascua. 16. Mt 26,20-25; Lc 22,21-23; cfr. Jn 13,21-30
 Al atardecer llegó con los Doce. 17. Se pusieron a la mesa y, mientras comían, dijo Jesús:
-Les aseguro que uno de ustedes me va a entregar, uno que come conmigo. 18. Entristecidos, empezaron a preguntarle uno por uno:
-¿Soy yo? 19. Respondió:
-Uno de los Doce, que moja el pan conmigo en la fuente. 20. El Hijo del Hombre se va, como está escrito de él; pero, ¡ay de aquél por quien el Hijo del Hombre será entregado! Más le valdría a ese hombre no haber nacido. 21. Mt 26,26-29; Lc 22,14-20;
cfr. Jn 6,51-59; 1 Cor 11,23-25
 Mientras cenaban, tomó pan, pronunció la bendición, lo partió y se lo dio diciendo:
-Tomen, esto es mi cuerpo. 22. Y tomando la copa, pronunció la acción de gracias, se la dio y bebieron todos de ella. 23. Les dijo:
-Ésta es mi sangre, sangre de la alianza, que se derrama por todos. 24. Les aseguro que no volveré a beber el fruto de la vid hasta el día en que beba el vino nuevo en el reino de Dios. 25. Anuncio del abandono
Mt 26,30-35; Lc 22,31-34; cfr. Jn 13,36-38
 Cantaron los salmos y salieron hacia el monte de los Olivos. 26. Jesús les dijo:
-Todos van a fallar, como está escrito:
Heriré al pastor
y se dispersarán las ovejas. 27. Pero, cuando resucite, iré delante de ustedes a Galilea. 28. Pedro le contestó:
-Aunque todos fallen, yo no. 29. Le dijo Jesús:
-Te aseguro que tú hoy mismo, esta noche, antes que el gallo cante dos veces, me habrás negado tres. 30. Él insistía:
-Aunque tenga que morir contigo, no te negaré.
Lo mismo decían los demás. 31. Oración en el huerto
Mt 26,36-46; Lc 22,39-46; cfr. Jn 18,1
 Llegados al lugar llamado Getsemaní, dijo a sus discípulos:
-Siéntense aquí mientras yo voy a orar. 32. Llevó con él a Pedro, Santiago y Juan y empezó a sentir tristeza y angustia. 33. Entonces les dijo:
-Siento una tristeza de muerte; quédense aquí y permanezcan despiertos. 34. Se adelantó un poco, se postró en tierra y oraba que, si era posible, se alejase de él aquella hora. 35. Decía:
-Padre-, tú lo puedes todo, aparta de mí esta copa. Pero no se haga mi voluntad, sino la tuya. 36. Volvió, y los encontró dormidos. Dice a Pedro:
-Simón, ¿duermes? ¿No has sido capaz de estar despierto una hora? 37. Permanezcan despiertos y oren para no caer en la tentación. El espíritu está dispuesto, pero la carne es débil. 38. Volvió otra vez y oró repitiendo las mismas palabras. 39. Al volver, los encontró otra vez dormidos, porque los ojos se les cerraban de sueño; y no supieron qué contestar. 40. Volvió por tercera vez y les dice:
-¡Todavía dormidos y descansando! Basta, ha llegado la hora en que el Hijo del Hombre será entregado en poder de los pecadores. 41. Vamos, levántense, se acerca el traidor. 42. Arresto de Jesús
