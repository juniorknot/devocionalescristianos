gobernadores y alcaldes </p>
<p style="style=text-align: justify;"><strong id="numeros">24. </strong>
y pagaré a Babilonia
y a todos los caldeos,
en presencia de ustedes,
todo el mal que hicieron a Sión
-oráculo del Señor-. </p>
<p style="style=text-align: justify;"><strong id="numeros">25. </strong>
Aquí estoy contra ti,
Monte Exterminio,
que exterminó la tierra entera
-oráculo del Señor-;
extenderé contra ti mi brazo,
te haré rodar peñas abajo,
te convertiré en Monte Quemado; </p>
<p style="style=text-align: justify;"><strong id="numeros">26. </strong>
ya no sacarán de ti piedras
angulares o de cimiento,
porque serás desolación eterna
-oráculo del Señor-. </p>
<p style="style=text-align: justify;"><strong id="numeros">27. </strong>
Levanten la bandera en la tierra,
toquen la trompeta
entre las naciones,
convocando a la guerra santa;
recluten contra ella los reinos
de Ararat, Miní y Asquenaz,
nombren contra ella un general,
avancen los caballos
como langostas erizadas; </p>
<p style="style=text-align: justify;"><strong id="numeros">28. </strong>
llamen a guerra santa a las naciones,
a los reyes medos,
con sus gobernadores y alcaldes
y toda la tierra de sus dominios. </p>
<p style="style=text-align: justify;"><strong id="numeros">29. </strong>
Temblará y se retorcerá
la tierra cuando se cumpla
el plan del Señor contra Babilonia,
cuando deje el territorio babilonio
como un desierto despoblado. </p>
<p style="style=text-align: justify;"><strong id="numeros">30. </strong>
Los soldados de Babilonia
dejan de luchar,
se agachan en los fortines,
se acaba su valentía,
se han vuelto mujeres;
han quemado sus edificios
y roto sus cerrojos. </p>
<p style="style=text-align: justify;"><strong id="numeros">31. </strong>
Un correo releva a otro,
un mensajero releva a otro,
para anunciar al rey de Babilonia
que su ciudad está
enteramente conquistada, </p>
<p style="style=text-align: justify;"><strong id="numeros">32. </strong>
los pasos de los ríos tomados,
las compuertas incendiadas
y los soldados presa del pánico. </p>
<p style="style=text-align: justify;"><strong id="numeros">33. </strong>
Así dice el Señor Todopoderoso,
Dios de Israel:
La capital de Babilonia
era un campo en tiempo de trilla:
muy pronto llegará
el tiempo de la cosecha. </p>
<p style="style=text-align: justify;"><strong id="numeros">34. </strong>
Nabucodonosor, rey de Babilonia,
me ha comido, me ha devorado,
me ha dejado como un plato vacío,
me ha engullido como un dragón,
se ha llenado la panza
con mis manjares
y me ha vomitado; </p>
<p style="style=text-align: justify;"><strong id="numeros">35. </strong>
recaiga sobre Babilonia
mi carne violentada
-dice de la población de Sión-,
recaiga mi sangre
sobre los caldeos
-dice Jerusalén-. </p>
<p style="style=text-align: justify;"><strong id="numeros">36. </strong>
Y así responde el Señor:
Aquí estoy yo para defender tu causa
y ejecutar tu venganza:
secaré su mar,
agotaré sus manantiales, </p>
<p style="style=text-align: justify;"><strong id="numeros">37. </strong>
Babilonia se convertirá en escombros,
en guarida de chacales,
objeto de burla y espanto,
vacía de habitantes. </p>
<p style="style=text-align: justify;"><strong id="numeros">38. </strong>
Rugen a coro como leones,
gruñen como cachorros de león: </p>
<p style="style=text-align: justify;"><strong id="numeros">39. </strong>
haré que sus festines
acaben en fiebre,
los emborracharé
para que celebren una orgía
y duerman un sueño eterno,
sin despertar
-oráculo del Señor-. </p>
<p style="style=text-align: justify;"><strong id="numeros">40. </strong>
Los haré bajar al matadero
como corderos o carneros
o chivos. </p>
<p style="style=text-align: justify;"><strong id="numeros">41. </strong>
¡Ay, Babilonia conquistada,
capturado el orgullo del mundo!
¡Ay, Babilonia convertida
en el espanto de las naciones! </p>
<p style="style=text-align: justify;"><strong id="numeros">42. </strong>
El mar subió hasta Babilonia
y la inundó
con el tumulto de su oleaje; </p>
<p style="style=text-align: justify;"><strong id="numeros">43. </strong>
sus ciudades quedaron desoladas
como tierra seca y árida,
tierra que nadie habita,
que no atraviesa el mortal. </p>
<p style="style=text-align: justify;"><strong id="numeros">44. </strong>
Tomaré cuentas a Bel en Babilonia
y le sacaré el bocado de la boca.
Ya no confluirán a él los pueblos,
y hasta las murallas de Babilonia
se desplomarán. </p>
<p style="style=text-align: justify;"><strong id="numeros">45. </strong>
¡Pueblo mío, salgan!
Ponte a salvo
de la ira ardiente del Señor. </p>
<p style="style=text-align: justify;"><strong id="numeros">46. </strong>
No se acobarden ni teman
por las noticias que circulan,
cada año una nueva noticia:
Violencia en el país,
señores contra señores. </p>
<p style="style=text-align: justify;"><strong id="numeros">47. </strong>
Porque llega un tiempo
en que castigaré
a los ídolos de Babilonia:
el país quedará confuso
y los caídos yacerán en medio de él. </p>
<p style="style=text-align: justify;"><strong id="numeros">48. </strong>
Clamarán contra Babilonia
cielo y tierra y lo que hay en ellos
cuando venga sobre ella
desde el norte el destructor
-oráculo del Señor-. </p>
<p style="style=text-align: justify;"><strong id="numeros">49. </strong>
También Babilonia ha de caer
por las víctimas de Israel,
como por Babilonia cayeron
víctimas de todo el mundo. </p>
<p style="style=text-align: justify;"><strong id="numeros">50. </strong>
Los que evitaron su espada,
caminen sin detenerse,
invocando desde lejos al Señor,
recordando a Jerusalén. </p>
<p style="style=text-align: justify;"><strong id="numeros">51. </strong>
Nos avergonzamos
al oír la infamia,
nos cubre la cara la vergüenza,
entraron extranjeros
en el santuario del Señor. </p>
<p style="style=text-align: justify;"><strong id="numeros">52. </strong>
Por eso, llegarán días
-oráculo del Señor-
en que castigaré a sus ídolos
y por todo el país
se quejarán los heridos. </p>
<p style="style=text-align: justify;"><strong id="numeros">53. </strong>
Aunque Babel se eleve
hasta el cielo
y fortifique en la altura su fortaleza,
yo le enviaré destructores
-oráculo del Señor-. </p>
<p style="style=text-align: justify;"><strong id="numeros">54. </strong>
Se oyen los gritos de Babilonia,
grave quebranto de los caldeos, </p>
<p style="style=text-align: justify;"><strong id="numeros">55. </strong>
porque el Señor devasta Babilonia,
pone fin a su enorme griterío,
por mucho que rujan
sus olas como un océano
y resuene el estruendo de sus voces. </p>
<p style="style=text-align: justify;"><strong id="numeros">56. </strong>
Porque llega a Babilonia
el destructor:
caerán prisioneros sus soldados,
se romperán sus arcos.
Porque el Señor es un Dios
que recompensa
y les dará la paga. </p>
<p style="style=text-align: justify;"><strong id="numeros">57. </strong>
Emborracharé a sus nobles
y a sus maestros,
a sus gobernadores y alcaldes
y a sus soldados,
y dormirán un sueño eterno
sin despertarse
-oráculo del Rey que se llama
Señor Todopoderoso-. </p>
<p style="style=text-align: justify;"><strong id="numeros">58. </strong>
Así dice el Señor Todopoderoso:
La gruesa muralla de Babilonia
será desmantelada,
sus altas puertas serán incendiadas,
para nada trabajaron los pueblos,
para el fuego se fatigaron las naciones. </p>
<p style="style=text-align: justify;"><strong id="numeros">59. </strong>
Encargo del profeta Jeremías a Serayas, hijo de Nerías, hijo de Majsías, cuando fue a Babilonia con Sedecías, rey de Judá, el año cuarto de su reinado -Serayas era jefe de la caravana-. </p>
<p style="style=text-align: justify;"><strong id="numeros">60. </strong>
Jeremías había escrito en un rollo todas las desgracias que iban a suceder a Babilonia, todas las palabras citadas acerca de Babilonia. </p>
<p style="style=text-align: justify;"><strong id="numeros">61. </strong>
Y Jeremías dijo a Serayas:
-Cuando llegues a Babilonia, busca un sitio y proclama todas estas palabras. </p>
<p style="style=text-align: justify;"><strong id="numeros">62. </strong>
Dirás: Señor, tú has amenazado destruir este lugar hasta dejarlo deshabitado, sin hombres ni animales, convertido en perpetua desolación. </p>
<p style="style=text-align: justify;"><strong id="numeros">63. </strong>
Y cuando termines de leer el rollo, le atarás una piedra y lo arrojarás al Éufrates, </p>
<p style="style=text-align: justify;"><strong id="numeros">64. </strong>
y dirás: Así se hundirá Babilonia y no se levantará, por las desgracias que yo envío contra ella.
Aquí terminan las palabras de Jeremías. </p>
