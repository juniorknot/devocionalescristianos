Yo movilizo
contra Babilonia y los caldeos
un viento mortífero, 2. despacho contra Babilonia
gente que la lance al viento
que la limpiarán
y vaciarán su territorio; 3. el día de la desgracia la sitiarán;
que no se vaya el arquero
ni se retire el que viste coraza;
no perdonen a sus soldados,
aniquilen su ejército, 4. caigan heridos en tierra caldea,
caigan atravesados en sus calles. 5. Porque Israel y Judá
no son viudas de su Dios,
el Señor Todopoderoso,
mientras que el país caldeo
es deudor del Santo de Israel. 6. Huyan de Babilonia,
sálvese el que pueda,
no perezca por culpa de ella;
porque es la hora
de la venganza del Señor,
cuando le pagará su merecido. 7. Babilonia era en la mano del Señor
una copa de oro
que emborrachaba a toda la tierra,
de su vino bebían las naciones
y se perturbaban. 8. Cayó de repente Babilonia
y se rompió: laméntense por ella.
Traigan bálsamo para sus heridas,
a ver si se sana; 9. hemos tratado a Babilonia
y no se sana, déjenla,
vamos cada uno a nuestra tierra;
su condena llega al cielo,
alcanza a las nubes; 10. el Señor nos ha rehabilitado,
vamos a Sión a contar las hazañas
del Señor, nuestro Dios. 11. Afilen las flechas,
sujeten el escudo,
el Señor incita a los jefes medos,
porque quiere destruir a Babilonia:
es la venganza del Señor,
la venganza de su templo. 12. Alcen la bandera
contra las murallas de Babilonia,
refuercen la guardia,
pongan centinelas,
preparen emboscadas;
porque el Señor ejecuta
lo que pensó y anunció
contra los habitantes de Babilonia. 13. Ciudad opulenta,
que vive entre canales:
te llega el fin, te cortan la trama. 14. El Señor Todopoderoso
lo jura por su vida:
Aunque tu muchedumbre
sea más que la langosta,
sobre ti cantarán victoria. 15. Él hizo la tierra con su poder,
fundó el universo con maestría,
desplegó el cielo con habilidad. 16. Cuando él truena,
retumban las aguas del cielo,
hace subir las nubes
desde el horizonte,
con los rayos desata la lluvia
y saca los vientos de sus silos. 17. El hombre, con su saber,
se embrutece;
el orfebre, con su ídolo, fracasa: 18. son imágenes falsas, sin aliento,
son vanidad y no sirven para nada:
el día de la cuenta perecerán. 19. No es así la porción de Jacob,
sino que lo hizo todo:
Israel es la tribu de su propiedad,
y su Nombre es Señor Todopoderoso. 20. Tú eres mi maza, mi arma de guerra:
machacaré contigo las naciones,
destruiré a los reyes, 21. machacaré contigo caballos y jinetes,
machacaré contigo
carros y conductores, 22. machacaré contigo
hombres y mujeres,
machacaré contigo
ancianos y jóvenes,
machacaré contigo
jóvenes y doncellas, 23. machacaré contigo pastores y rebaños,
machacaré contigo
labradores y yuntas,
machacaré contigo
gobernadores y alcaldes 24. y pagaré a Babilonia
y a todos los caldeos,
en presencia de ustedes,
todo el mal que hicieron a Sión
-oráculo del Señor-. 25. Aquí estoy contra ti,
Monte Exterminio,
que exterminó la tierra entera
-oráculo del Señor-;
extenderé contra ti mi brazo,
te haré rodar peñas abajo,
te convertiré en Monte Quemado; 26. ya no sacarán de ti piedras
angulares o de cimiento,
porque serás desolación eterna
-oráculo del Señor-. 27. Levanten la bandera en la tierra,
toquen la trompeta
entre las naciones,
convocando a la guerra santa;
recluten contra ella los reinos
de Ararat, Miní y Asquenaz,
nombren contra ella un general,
avancen los caballos
como langostas erizadas; 28. llamen a guerra santa a las naciones,
a los reyes medos,
con sus gobernadores y alcaldes
y toda la tierra de sus dominios. 29. Temblará y se retorcerá
la tierra cuando se cumpla
el plan del Señor contra Babilonia,
cuando deje el territorio babilonio
como un desierto despoblado. 30. Los soldados de Babilonia
dejan de luchar,
se agachan en los fortines,
se acaba su valentía,
se han vuelto mujeres;
han quemado sus edificios
