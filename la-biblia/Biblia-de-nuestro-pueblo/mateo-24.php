destrucción del Templo
Mc 13,1-13; Lc 21,5-19
 Jesús salió del templo y, mientras caminaba, se le acercaron los discípulos y le señalaron las construcciones del templo. 2. Él les contestó:
-¿Ven todo eso? Les aseguro que se derrumbará sin que quede piedra sobre piedra. 3. Estando sentado en el monte de los Olivos, se le acercaron los discípulos aparte y le preguntaron:
-Dinos cuándo sucederá eso y cuál es la señal de tu llegada y del fin del mundo. 4. Jesús les respondió:
-¡Tengan cuidado, y que nadie los engañe! 5. Porque muchos se presentarán en mi nombre, diciendo que son el Mesías, y engañarán a muchos. 6. Oirán hablar de guerras y noticias de guerras. ¡Tengan cuidado y no se alarmen! Todo eso ha de suceder, pero todavía no es el final. 7. Se alzará pueblo contra pueblo, reino contra reino. Habrá carestías y terremotos en diversos lugares. 8. Todo eso es el comienzo de los dolores de parto. 9. Los entregarán para torturarlos y matarlos; todos los pueblos los odiarán a causa de mi nombre. 10. Entonces muchos fallarán, se traicionarán y se odiarán mutuamente. 11. Surgirán muchos falsos profetas que engañarán a muchos. 12. Y, al crecer la maldad, se enfriará el amor de muchos. 13. Pero el que aguante hasta el final se salvará. 14. La Buena Noticia del reino se proclamará a todas las naciones, y entonces llegará el final. 15. La gran tribulación
Mc 13,14-23; Lc 21,20-24
 Cuando vean instalado en el lugar sagrado el ídolo abominable anunciado por el profeta Daniel -el lector que lo entienda-, 16. entonces los que viven en Judea que escapen a los montes; 17. el que esté en la azotea que no baje a recoger sus cosas; 18. el que se encuentre en el campo que no vuelva a buscar el manto. 19. ¡Ay de las embarazadas y de las que tengan niños pequeños en aquellos días! 20. Recen para que la huida no suceda en invierno o en sábado. 21. Habrá una tribulación tan grande como no la hubo desde el comienzo del mundo hasta ahora, ni la habrá en el futuro. 22. Si no se acortara aquel tiempo, no se salvaría ni uno. Pero, en atención a los elegidos, se acortará aquel tiempo. 23. Entonces, si alguien les dice que el Mesías está aquí o allí, no le crean. 24. Surgirán falsos mesías y falsos profetas, que harán milagros y prodigios, hasta el punto de engañar, si fuera posible, incluso a los elegidos. 25. Miren que los he prevenido. 26. Si les dicen: Miren, está en el desierto, no salgan; o: Miren, está en un lugar secreto, no hagan caso. 27. Porque como el relámpago que aparece en el oriente y brilla hasta el occidente, así será la llegada del Hijo del Hombre. 28. Donde esté el cadáver allí se reunirán los buitres. 29. La parusía
Mc 13,24-27; Lc 21,25-28
 Inmediatamente después de esa tribulación, el sol se oscurecerá, la luna no irradiará su resplandor; las estrellas caerán del cielo y los ejércitos celestes temblarán. 30. Entonces aparecerá en el cielo la señal del Hijo del Hombre. Todas las razas del mundo harán duelo y verán al Hijo del Hombre llegar en las nubes del cielo, con gloria y poder grande. 31. Enviará a sus ángeles a reunir, con un gran toque de trompeta, a los elegidos de los cuatro vientos, de un extremo a otro del cielo. 32. El día y la hora
Mc 13,28-32; Lc 21,29-33
 Aprendan el ejemplo de la higuera: cuando las ramas se ponen tiernas y brotan las hojas, saben que está cerca la primavera. 33. Lo mismo ustedes, cuando vean que sucede todo eso, sepan que el fin está cerca, a las puertas. 34. Les aseguro que no pasará esta generación antes de que suceda todo eso. 35. Cielo y tierra pasarán, mas mis palabras no pasarán. 36. En cuanto al día y a la hora, no los conoce nadie, ni los ángeles del cielo ni el Hijo; sólo los conoce el Padre. (Lc 17,26-36) 37. La llegada del Hijo del Hombre será como en tiempos de Noé: 38. en [aquellos] días anteriores al diluvio la gente comía y bebía y se casaban, hasta que Noé se metió en el arca. 39. Y ellos no se enteraron hasta que vino el diluvio y se los llevó a todos. Así será la llegada del Hijo del Hombre. 40. Estarán dos hombres en un campo: a uno se lo llevarán, al otro lo dejarán; 41. dos mujeres estarán moliendo: a una se la llevarán, a la otra la dejarán. 42. Por tanto estén prevenidos porque no saben el día que llegará su Señor. 43. Ustedes ya saben que si el dueño de casa supiera a qué hora de la noche va a llegar el ladrón, estaría vigilando y no permitiría que asalten su casa. 44. Por tanto, estén preparados, porque el Hijo del Hombre llegará cuando menos lo esperen. 45. Vigilancia
