 Escucha, cielo, y hablaré;
oye, tierra, los dichos de mi boca; 2. descienda como lluvia mi doctrina,
caiga como rocío mi palabra;
como llovizna sobre la hierba,
como aguacero sobre el césped; 3. voy a proclamar el Nombre del Señor:
reconozcan la grandeza de nuestro Dios. 4. Él es la Roca, sus obras son perfectas,
sus caminos son justos;
es un Dios fiel, sin maldad,
es justo y recto. 5. Hijos degenerados, se portaron mal con él,
generación malvada y pervertida. 6. ¿Así le pagas al Señor,
pueblo necio e insensato?
¿No es él tu padre y tu creador,
el que te hizo y te constituyó? 7. Acuérdate de los días remotos,
considera las épocas pasadas,
pregunta a tu padre y te lo contará,
a tus ancianos y te lo dirán: 8. Cuando el Altísimo daba a cada pueblo su herencia,
y distribuía a los hijos de Adán,
trazando las fronteras de las naciones,
según el número de los hijos de Dios, 9. la parte del Señor fue su pueblo,
Jacob fue el lote de su herencia. 10. Lo encontró en una tierra desierta,
en una soledad poblada de aullidos;
lo rodeó cuidando de él,
lo guardó como a las niñas de sus ojos. 11. Como el águila incita a su nidada
revoloteando sobre los pichones,
así extendió sus alas, los tomó
y los llevó sobre sus plumas. 12. El Señor sólo los condujo,
no hubo dioses extraños con él. 13. Los puso a caballo de sus montañas,
y los alimentó con las cosechas de sus campos;
los crió con miel silvestre,
con aceite de rocas de pedernal; 14. con cuajada de vaca y leche de ovejas,
con grasa de corderos y carneros,
ganado de Basán y cabritos,
con la mejor harina de trigo,
y por bebida, con la sangre fermentada de la uva. 15. Comió Jacob hasta saciarse,
engordó mi cariño, y tiró coces
-estabas gordo y cebado y corpulento-
y rechazó a Dios, su creador;
deshonró a su Roca salvadora. 16. Le dieron celos con dioses extraños,
lo irritaron con sus abominaciones, 17. ofrecieron víctimas a demonios que no son dios,
a dioses desconocidos,
nuevos, importados de cerca,
a los que no veneraban sus padres. 18. ¡Despreciaste a la Roca que te engendró,
y olvidaste al Dios que te dio a luz! 19. Lo vio el Señor, e irritado
rechazó a sus hijos e hijas, 20. pensando: Les esconderé mi rostro,
y veré en qué acaban,
porque son una generación depravada,
unos hijos desleales; 21. ellos me han dado celos con un dios ilusorio,
me han irritado con ídolos vacíos;
yo les daré celos con un pueblo ilusorio,
los irritaré con una nación insensata. 22. Está ardiendo el fuego de mi ira
y abrasará hasta el fondo del abismo,
consumirá la tierra y sus cosechas
y quemará los cimientos de los montes. 23. Amontonaré desastres contra ellos,
agotaré en ellos mis flechas; 24. andarán debilitados por el hambre,
consumidos de fiebres y epidemias malignas;
les enviaré los dientes de las fieras
y el veneno de las serpientes que se arrastran; 25. en las calles, los diezmará la espada;
en las casas, el espanto,
tanto a los jóvenes como las doncellas,
a los niños de pecho como a los ancianos. 26. Yo pensaba: Voy a dispersarlos
y a borrar su memoria entre los hombres. 27. Pero no quise soportar las burlas del enemigo,
y la mala interpretación del adversario,
que dirían: Nuestra mano ha vencido,
no es el Señor quien lo ha hecho. 28. Porque son una nación que ha perdido el juicio
y carece de inteligencia. 29. Si fueran sensatos, lo entenderían,
comprenderían su destino. 30. ¿Cómo es que uno persigue a mil
y dos ponen en fuga a diez mil?
¿No es porque su Roca los ha vendido,
porque el Señor los ha entregado? 31. Porque su roca no es como nuestra Roca;
nuestros mismos enemigos pueden juzgarlo. 32. Su viña es un retoño de las viñas de Sodoma,
de los campos de Gomorra;
sus uvas son uvas venenosas
y sus racimos son amargos; 33. su vino es ponzoña de monstruos
y veneno mortal de víboras. 34. ¿No tengo todo esto recogido
y sellado en mis archivos? 35. Mía será la venganza y el desquite
en la hora en que tropiecen sus pies,
porque el día de su perdición se acerca
y su suerte se apresura 36. -porque el Señor defenderá a su pueblo
y tendrá compasión de sus siervos-.
Cuando vea que sus manos flaquean,
que se consumen amos y criados, 37. dirá: ¿Dónde están sus dioses
o la roca donde se refugiaban? 38. ¿No comían la grasa de sus sacrificios
y bebían el vino de sus libaciones?
Que se levanten para socorrerlos,
que sean su refugio. 39. Pero ahora miren: yo soy yo,
y no hay otro fuera de mí;
yo doy la muerte y la vida,
yo desgarro y yo sano,
y no hay quien se libre de mi mano. 40. Levanto la mano al cielo y juro:
Tan verdad como que vivo eternamente, 41. cuando afile el relámpago de mi espada
