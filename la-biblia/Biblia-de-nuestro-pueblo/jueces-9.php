<p style="style=text-align: justify;"><strong id="numeros">40. </strong>
Abimelec lo persiguió. Gaal emprendió la huida y muchos cayeron muertos cuando huían hacia las puertas de la ciudad. </p>
<p style="style=text-align: justify;"><strong id="numeros">41. </strong>
Abimelec se volvió a Arumá, y Zebul desterró de Siquén a Gaal y sus parientes. </p>
<p style="style=text-align: justify;"><strong id="numeros">42. </strong>
Al día siguiente, los de Siquén se pusieron en campaña, y Abimelec se enteró; </p>
<p style="style=text-align: justify;"><strong id="numeros">43. </strong>
tomó a su gente, la dividió en tres cuerpos y se emboscó en el campo. Cuando los vio salir de la ciudad, se lanzó al ataque y los destrozó. </p>
<p style="style=text-align: justify;"><strong id="numeros">44. </strong>
Abimelec y los de su grupo se abalanzaron contra la ciudad y tomaron posiciones en las puertas, mientras los otros dos grupos atacaban y derrotaban a los del campo. </p>
<p style="style=text-align: justify;"><strong id="numeros">45. </strong>
Todo aquel día estuvo Abimelec atacando la ciudad; al fin la conquistó, pasó a cuchillo a todos sus habitantes, la arrasó y la sembró de sal. </p>
<p style="style=text-align: justify;"><strong id="numeros">46. </strong>
Al saberlo los de Torre Siquén, se refugiaron en la cripta del templo del dios del Pacto. </p>
<p style="style=text-align: justify;"><strong id="numeros">47. </strong>
Abimelec se enteró de que estaban reunidos los de Torre Siquén; </p>
<p style="style=text-align: justify;"><strong id="numeros">48. </strong>
subió al Monte Salmón con toda su gente, empuñó un hacha, cortó una rama de un árbol y se la echó al hombro, mientras decía a los suyos:
-¡Apúrense, hagan lo mismo que hago yo! </p>
<p style="style=text-align: justify;"><strong id="numeros">49. </strong>
Cada uno cortó una rama y siguieron a Abimelec. Apoyaron las ramas sobre la cripta y prendieron fuego al techo. Murieron todos los de Torre Siquén, unos mil entre hombres y mujeres. </p>
<p style="style=text-align: justify;"><strong id="numeros">50. </strong>
Después Abimelec fue a Tebes, la sitió y la conquistó. </p>
<p style="style=text-align: justify;"><strong id="numeros">51. </strong>
En medio de la villa había una torre fortificada, y allí se refugiaron todos los hombres y mujeres de la población, aseguraron por dentro los cerrojos y se subieron a la azotea. </p>
<p style="style=text-align: justify;"><strong id="numeros">52. </strong>
Abimelec llegó junto a la torre, intentando asaltarla, se aproximó a la puerta para prenderle fuego, </p>
<p style="style=text-align: justify;"><strong id="numeros">53. </strong>
pero una mujer le dejó caer sobre la cabeza una piedra de moler y le partió el cráneo. </p>
<p style="style=text-align: justify;"><strong id="numeros">54. </strong>
Abimelec llamó en seguida a su escudero y le dijo:
-Saca la espada y remátame, para que no se diga lo mató una mujer.
Su escudero lo atravesó con su espada, y murió. </p>
<p style="style=text-align: justify;"><strong id="numeros">55. </strong>
Al ver los israelitas que Abimelec había muerto, cada cual regresó a su casa. </p>
<p style="style=text-align: justify;"><strong id="numeros">56. </strong>
Así pagó Dios a Abimelec lo mal que se portó con su padre, asesinando a sus setenta hermanos. </p>
<p style="style=text-align: justify;"><strong id="numeros">57. </strong>
Y todo el mal que hicieron los de Siquén, Dios lo hizo recaer sobre ellos. Sobre ellos cayó la maldición de Yotán, hijo de Yerubaal. </p>
