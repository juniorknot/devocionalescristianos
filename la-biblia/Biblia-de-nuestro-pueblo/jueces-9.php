<li class="hide-for-large"><a href="whatsapp://send?text=Jueces 9 (BNP)%0A%0A 1. Abimelec
 Abimelec, hijo de Yerubaal, fue a Siquén, a casa de sus tíos maternos, y les propuso a ellos y a todos los parientes de su abuelo materno lo siguiente: 2. -Digan a todos los señores de Siquén: ¿Qué es mejor para ustedes, que los gobiernen setenta hombres, es decir, todos los hijos de Yerubaal, o que los gobierne uno solo? Y no olviden que yo soy de su misma sangre. 3. Sus tíos maternos lo comunicaron a los siquemitas, y éstos se pusieron de parte de Abimelec, pensando:
-¡Es pariente nuestro! 4. Le dieron setecientos gramos de plata del templo de Baal-Berit, y con ese dinero Abimelec pagó a unos cuantos desocupados y aventureros que se pusieron a sus órdenes. 5. Luego fue a casa de su padre, a Ofrá, y asesinó a sus hermanos, los hijos de Yerubaal, a setenta hombres en la misma piedra. Sólo quedó Yotán, el hijo menor de Yerubaal, que se había escondido. 6. Los de Siquén y todos los de Bet-Miló se reunieron para proclamar rey a Abimelec, junto a la encina de Siquén. 7. En cuanto se enteró Yotán, subió hasta la cumbre del monte Garizín, y desde allí gritó con voz potente:
-¡Escúchenme, vecinos de Siquén, y que Dios los escuche a ustedes! 8. Una vez fueron los árboles a elegirse rey, y dijeron al olivo: Sé nuestro rey. 9. Pero el olivo dijo: ¿Y voy a dejar mi aceite, con el que se honra a dioses y hombres, para ir a mecerme sobre los árboles? 10. Entonces dijeron a la higuera: Ven a ser nuestro rey. 11. Pero la higuera dijo: ¿Y voy a dejar mi dulce fruto sabroso para ir a mecerme sobre los árboles? 12. Entonces dijeron a la vid: Ven a ser nuestro rey. 13. Pero la vid dijo: ¿Y voy a dejar mi mosto, que alegra a dioses y hombres, para ir a mecerme sobre los árboles? 14. Entonces dijeron todos a la zarza: Ven a ser nuestro rey. 15. Y les dijo la zarza: Si de veras quieren ungirme como su rey, vengan a cobijarse bajo mi sombra, y si no, salga fuego de la zarza y devore a los cedros del Líbano. 16. Y ahora díganme, ¿han obrado con sinceridad y lealtad proclamando rey a Abimelec? ¿Se han portado bien con Yerubaal y su familia? ¿Se han portado con él como merecían los favores que les hizo? 17. -Mi padre luchó por ustedes exponiéndose a la muerte y los libró del poder de Madián-. 18. Al contrario, ustedes se han levantado contra la familia de mi padre, asesinando a sus hijos, setenta hombres, en la misma piedra, y han nombrado rey de los siquemitas a Abimelec, hijo de una criada de mi padre, con el pretexto de que es pariente de ustedes. 19. Si hoy se han portado sincera y lealmente con Yerubaal y su familia, celébrenlo con Abimelec y que él lo celebre con ustedes; 20. pero si no es así, ¡salga fuego de Abimelec que devore a los de Siquén y a los de Bet-Miló, salga fuego de los de Siquén y de los de Bet-Miló que devore a Abimelec! 21. Luego Yotán emprendió la huida y marchó a Beer; allí se quedó por miedo a su hermano Abimelec. 22. Abimelec gobernó a Israel tres años. 23. Dios envió un espíritu de discordia entre Abimelec y los siquemitas, que lo traicionaron. 24. Así, el asesinato de los setenta hijos de Yerubaal, la sangre de sus hermanos, recayó sobre Abimelec, que los había asesinado, y sobre los de Siquén, cómplices del asesinato. 25. Los de Siquén preparaban emboscadas contra él en las cimas de los montes y despojaban a los caminantes que pasaban por allí. Abimelec se enteró. 26. Gaal, hijo de Obed, vino a Siquén con sus hermanos y se ganó la confianza de los siquemitas. 27. Salieron al campo, a la vendimia, pisaron la uva y celebraron la fiesta; fueron al templo de su dios y comieron y bebieron entre maldiciones a Abimelec. 28. Gaal, hijo de Obed, les dijo:
-¿Qué autoridad tiene Abimelec sobre Siquén para que seamos sus esclavos? ¡Es un hijo de Yerubaal, y Zebul, es su lugarteniente, ellos sirvieron en casa de Jamor, padre de Siquén! ¿Por qué vamos a ser sus esclavos? 29. ¡Ah, si yo tuviera poder sobre este pueblo! Quitaría de en medio a Abimelec. Lo desafiaría diciéndole: Refuerza tu ejército y sal a combatir. 30. Zebul, gobernador de la ciudad, oyó el discurso de Gaal, hijo de Obed, y se enfureció, 31. entonces mandó emisarios a Abimelec, avisándole:
-Mira, Gaal, hijo de Obed, ha venido con sus parientes a Siquén y están sublevando la ciudad contra ti. 32. Ven de noche con tu gente y escóndete en el campo; 33. por la mañana, al salir el sol, ataca a la ciudad. Gaal y los suyos saldrán a presentarte batalla; entonces actúa, que es tu ocasión. 34. Abimelec se puso en marcha de noche con su gente y se emboscaron frente a Siquén, divididos en cuatro cuerpos. 35. Gaal, hijo de Obed, salió y se detuvo a las puertas de la ciudad, y Abimelec con su gente surgió de la emboscada. 36. Cuando Gaal los vio, dijo a Zebul:
-Mira, baja gente de las cumbres de los montes.
Zebul contestó:
