<li class="hide-for-large"><a href="whatsapp://send?text=2 Crónicas 20 (BNP)%0A%0A 1. Algún tiempo después los moabitas, los amonitas y algunos meunitas vinieron a combatir contra Josafat. 2. Informaron a éste:
-Una gran multitud procedente de Edom, al otro lado del Mar Muerto, se dirige contra ti; ya se encuentran en Jasasón Tamar -la actual Engadí-. 3. Josafat, asustado, decidió recurrir al Señor, proclamando un ayuno en todo Judá. 4. Judíos de todas las ciudades se reunieron para pedir consejo al Señor. 5. Josafat se colocó en medio de la asamblea de Judá y Jerusalén, en el templo, delante del atrio nuevo, 6. y exclamó:
-Señor, Dios de nuestros padres. ¿No eres tú el Dios del cielo, el que gobierna los reinos de la tierra, lleno de fuerza y de poder, al que nadie puede resistir? 7. ¿No fuiste tú, Dios nuestro, quien expulsaste a los moradores de esta tierra delante de tu pueblo, Israel, y la entregaste para siempre a los descendientes de tu amigo Abrahán? 8. La habitaron y construyeron en ella un santuario en tu honor, pensando: 9. Cuando nos ocurra una calamidad -espada, inundación, peste o hambre-nos presentaremos ante ti en este templo -porque en él estás presente-, te invocaremos en nuestro peligro y tú nos escucharás y salvarás. 10. Cuando Israel venía de Egipto no le permitiste atravesar el territorio de los amonitas, el de los moabitas y la montaña de Seír; en vez de destruirlos se alejó de ellos. 11. Y ahora nos lo pagan disponiéndose a expulsarnos de la propiedad que tú nos concediste. 12. Tú los juzgarás, Dios nuestro, porque nosotros nada podemos contra ese gran ejército que se nos viene encima. No sabemos qué hacer si no es poner los ojos en ti. 13. Todos los judíos con sus mujeres e hijos, incluso los chiquillos, permanecían de pie ante el Señor. 14. En medio de la asamblea, un descendiente de Asaf, el levita Yajziel, hijo de Zacarías, hijo de Benayas, hijo de Yeguiel, hijo de Matanías, tuvo una inspiración del Señor 15. y dijo:
-Judíos, habitantes de Jerusalén, y tú, rey Josafat, presten atención. Así dice el Señor: No se asusten ni se acobarden ante esa inmensa multitud, porque la batalla no es cosa de ustedes, sino de Dios. 16. Mañana bajarán contra ellos cuando vayan subiendo la Cuesta de Hassís; les saldrán al encuentro al final del barranco que hay frente al desierto de Yeruel. 17. No tendrán necesidad de combatir; permanezcan quietos y firmes contemplando cómo el Señor los salva. Judá y Jerusalén, no se asusten ni acobarden. Salgan mañana a su encuentro, que el Señor estará con ustedes. 18. Josafat se postró rostro en tierra y todos los judíos y los habitantes de Jerusalén cayeron ante el Señor para adorarlo. 19. Los levitas corajitas descendientes de Quehat se levantaron para alabar a grandes voces al Señor, Dios de Israel. 20. De madrugada se pusieron en marcha hacia el desierto de Tecua. Cuando salían, Josafat se detuvo y dijo:
