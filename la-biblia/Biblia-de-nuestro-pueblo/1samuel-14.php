<li class="hide-for-large"><a href="whatsapp://send?text=1 Samuel 14 (BNP)%0A%0A 1. Hazaña de Jonatán
 Un día Jonatán, hijo de Saúl, dijo a su escudero:
-Acerquémonos hasta el destacamento filisteo, al otro lado de la cañada.
Pero no se lo dijo a su padre. 2. Saúl se encontraba entonces en las afueras de Guibeá, bajo el granado que estaba cerca de donde trillaban el trigo. Su tropa eran unos seiscientos hombres. 3. Ajías, hijo de Ajitub, hermano de Icabod, hijo de Fineés, hijo de Elí, sacerdote del Señor en Siló, llevaba un efod.
La tropa no se dio cuenta de que Jonatán se alejaba. 4. A ambos lados de la cañada que Jonatán intentaba pasar para llegar al destacamento filisteo había dos salientes rocosos: uno se llamaba Bosés y el otro Sene. 5. Uno se erguía hacia el norte, frente a Micmás, y el otro hacia el sur, frente a Guibeá. 6. Jonatán dijo a su escudero:
-Vamos a pasar hacia el destacamento de esos incircuncisos; a lo mejor el Señor nos da la victoria; no le cuesta salvar con muchos o con pocos. 7. El escudero respondió:
-Haz lo que quieras; estoy a tu disposición. 8. Jonatán dijo:
-Mira, vamos a pasar hasta donde estén esos hombres y dejaremos que nos descubran. 9. Si nos dicen: ¡Alto! ¡No se muevan hasta que lleguemos a ustedes!, nos quedaremos quietos donde estamos, sin subir hacia ellos. 10. Pero si nos dicen: ¡Suban acá!, subiremos, porque el Señor nos los entrega; ésta será la contraseña. 11. El destacamento filisteo los descubrió, y comentaron:
-Miren, unos hebreos que salen de las cuevas donde se habían escondido. 12. Luego dijeron a Jonatán y a su escudero:
-Suban aquí, que les contaremos una cosa.
Jonatán ordenó entonces a su escudero:
-Sube detrás de mí, porque el Señor los ha entregado a Israel. 13. Jonatán subió gateando, seguido de su escudero; los filisteos iban cayendo ante los golpes de Jonatán, y su escudero, detrás, los iba rematando. 14. Ésta fue la primera victoria de Jonatán y su escudero: mataron unos veinte hombres, como quién abre un surco en media parcela de campo. 15. Todos los que estaban en el campamento y toda la tropa se llenaron de miedo. Temieron también los de la guarnición y la fuerza de choque. Al mismo tiempo hubo un temblor de tierra y se produjo un pánico sobrehumano. 16. Desde Guibeá de Benjamín vieron los centinelas de Saúl que el ejército enemigo huía a la desbandada. 17. Entonces Saúl ordenó a los suyos:
-Pasen revista, a ver quién se ha separado de los nuestros.
Pasaron revista, y faltaban Jonatán y su escudero. 18. Saúl ordenó a Ajías:
-Trae aquí el efod -Porque Ajías era el que llevaba entonces el efod en Israel-. 19. Mientras Saúl hablaba al sacerdote, el tumulto del campamento filisteo iba en aumento. Saúl dijo al sacerdote:
-Retira la mano. 20. Todo el ejército de Saúl se reunió y se lanzó al combate; los filisteos se acuchillaban unos a otros, en medio de una enorme confusión. 21. Y los hebreos movilizados hacía tiempo por los filisteos, y que habían subido con ellos al campamento, se pasaron a los israelitas de Saúl y Jonatán. 22. Todos los israelitas que se habían escondido en la serranía de Efraín oyeron que los filisteos iban huyendo, y también se juntaron en su persecución. 23. El Señor salvó aquel día a Israel. La lucha llegó hasta Bet-Avén. Los que seguían a Saúl eran unos dos mil hombres. La lucha se extendió por toda la serranía de Efraín. 24. Saúl cometió aquel día un grave error, conjurando a la tropa:
-Maldito el que pruebe un bocado antes de la tarde, mientras me vengo de mis enemigos.
Nadie probó bocado. 25. Por el suelo había unos panales, 26. y el ejército se acercó a los panales, que destilaban miel, pero nadie se la llevó a la boca, por miedo al juramento. 27. Jonatán que no había oído el juramento impuesto al pueblo por su padre, alargó la punta del palo que llevaba en la mano, lo hundió en el panal de miel, se lo llevó a la boca y se le iluminó la mirada. 28. Uno de la tropa dijo:
-Tu padre nos ha impuesto un juramento maldiciendo al que probase hoy un bocado, y eso que la tropa está agotada. 29. Jonatán exclamó:
-¡Mi padre ha traído la desgracia al país! Miren cómo se me han iluminado los ojos, con sólo probar un poco de esta miel. 30. Si la tropa hubiera comido hoy de los despojos ganados al enemigo, ¡cuánto mayor habría sido la derrota de los filisteos! 31. Aquel día destrozaron a los filisteos desde Micmás hasta Ayalón, y el ejército acabó agotado. 32. Entonces la tropa se lanzó sobre el botín y agarró ovejas, vacas y terneros, los degollaron en el suelo y los comieron con la sangre. 33. Avisaron a Saúl:
-Mira que la tropa está pecando contra el Señor, porque come carne con sangre.
Saúl respondió:
