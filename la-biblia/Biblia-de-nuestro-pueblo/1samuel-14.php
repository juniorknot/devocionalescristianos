<p style="style=text-align: justify;"><strong id="numeros">38. </strong>
Aquel día no obtuvo respuesta. Entonces ordenó:
-Acérquense todos los jefes del pueblo, para ver quién ha cometido hoy este pecado. </p>
<p style="style=text-align: justify;"><strong id="numeros">39. </strong>
Porque, ¡por la vida del Señor, salvador de Israel!, aunque sea mi hijo Jonatán, morirá sin remedio.
Nadie le respondió. </p>
<p style="style=text-align: justify;"><strong id="numeros">40. </strong>
Entonces se dirigió a todo Israel:
-Ustedes se quedarán de un lado y yo con mi hijo Jonatán nos pondremos al otro.
Le respondieron:
-Haz lo que te parezca bien. </p>
<p style="style=text-align: justify;"><strong id="numeros">41. </strong>
Entonces Saúl consultó al Señor, Dios de Israel:
-¿Por qué no respondes hoy a tu siervo? Señor, Dios de Israel, si somos culpables yo o mi hijo Jonatán, salga cara; si es culpable tu pueblo Israel, salga cruz.
Cayó la suerte en Jonatán y Saúl, y la tropa quedó libre. </p>
<p style="style=text-align: justify;"><strong id="numeros">42. </strong>
Entonces dijo Saúl:
-Ahora echen la suerte entre mi hijo Jonatán y yo.
Le tocó a Jonatán. </p>
<p style="style=text-align: justify;"><strong id="numeros">43. </strong>
Y Saúl le preguntó:
-Dime lo que has hecho.
Jonatán le contó:
-Probé un poco de miel con la punta del palo que llevaba en la mano. ¡Y ahora me toca morir! </p>
<p style="style=text-align: justify;"><strong id="numeros">44. </strong>
Saúl le dijo:
-¡Que Dios me castigue si no mueres, Jonatán! </p>
<p style="style=text-align: justify;"><strong id="numeros">45. </strong>
Pero la tropa dijo a Saúl:
-¿Cómo va a morir Jonatán, que ha dado esta gran victoria a Israel? ¡De ningún modo! ¡Por la vida del Señor!, que no caerá a tierra ni un pelo de su cabeza; porque él ha actuado hoy con la ayuda de Dios.
Así salvaron la vida a Jonatán. </p>
<p style="style=text-align: justify;"><strong id="numeros">46. </strong>
Saúl dejó de perseguir a los filisteos, y éstos volvieron a sus casas. </p>
<p style="style=text-align: justify;"><strong id="numeros">47. </strong>
Después de ser proclamado rey de Israel, Saúl luchó contra todos sus enemigos de alrededor: Moab, los amonitas, Edom, el rey de Sobá, los filisteos, y vencía en todas sus campañas, </p>
<p style="style=text-align: justify;"><strong id="numeros">48. </strong>
haciendo proezas; derrotó a Amalec y libró a Israel de sus saqueadores. </p>
<p style="style=text-align: justify;"><strong id="numeros">49. </strong>
Sus hijos fueron: Jonatán, Isbaal, Malquisúa. De sus dos hijas, la mayor se llamaba Merab; la pequeña, Mical. </p>
<p style="style=text-align: justify;"><strong id="numeros">50. </strong>
Su mujer se llamaba Ajinoán, hija de Ajimás. El general de su ejército se llamaba Abner, hijo de Ner, tío de Saúl. </p>
<p style="style=text-align: justify;"><strong id="numeros">51. </strong>
Quis, padre de Saúl, y Ner, padre de Abner, eran hijos de Abiel. </p>
<p style="style=text-align: justify;"><strong id="numeros">52. </strong>
Durante todo el reinado de Saúl hubo guerra abierta contra los filisteos. A todo joven valiente y aguerrido que veía, Saúl lo enrolaba en su ejército. </p>
