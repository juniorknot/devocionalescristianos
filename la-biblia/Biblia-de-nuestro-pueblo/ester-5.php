 Y oró así, recordando todas las hazañas del Señor.
ñor, Señor, rey y dueño de todo, porque todo está bajo tu poder y no hay quien se oponga a tu voluntad de salvar a Israel.
Tú creaste el cielo y la tierra y todas las maravillas que hay bajo el cielo, y eres Señor de todo;
ni hay, Señor, quien se te pueda oponer.
Tú lo sabes todo. Si yo me niego a postrarme ante ese soberbio Amán, tú sabes bien, Señor, que no lo hago por arrogancia, orgullo o vanidad;
que por salvar a Israel, de buena gana le besaría yo la planta del pie.
Si me he negado a hacerlo es porque para mí Dios está por encima de cualquier hombre. Yo no me postro ante nadie si no es ante ti, Señor mío; no lo hago por orgullo.
Ahora, Señor, Dios rey, Dios de Abrahán, perdona a tu pueblo; porque traman nuestra muerte, han deseado aniquilar tu antigua herencia.
No desprecies la porción que te rescataste del país de Egipto;
escucha mi súplica, apiádate de tu herencia, cambia nuestro duelo en fiesta, para que vivamos celebrando tu nombre, Señor. No hagas enmudecer la boca de los que te alaban.
Ante la muerte inminente, todos los israelitas gritaban a Dios con todas sus fuerzas.
Oración de Ester
 La reina Ester, temiendo el peligro inminente, acudió al Señor.
Se despojó de sus ropas lujosas y se vistió de luto; en vez de perfumes refinados, se cubrió la cabeza de ceniza y basura, y se desfiguró por completo, cubriendo con sus cabellos revueltos aquel cuerpo que antes se complacía en adornar.
Luego rezó así al Señor, Dios de Israel:
Señor mío, único rey nuestro.
Protégeme, que estoy sola
y no tengo otro defensor fuera de ti,
porque yo misma
me he expuesto al peligro.
Desde mi infancia oí,
en el seno de mi familia,
cómo tú, Señor, escogiste
a Israel entre las naciones,
a nuestros padres
entre todos sus antepasados
para ser tu herencia perpetua,
y les cumpliste
lo que habías prometido.
Nosotros hemos pecado contra ti
dando culto a otros dioses;
por eso nos entregaste a
nuestros enemigos.
¡Eres justo, Señor!
Y no les basta
nuestro amargo cautiverio,
sino que se han comprometido
con sus ídolos,
jurando invalidar el pacto
salido de tus labios,
haciendo desaparecer tu herencia
y enmudecer a los que te alaban,
extinguiendo tu altar
y la gloria de tu templo
y abriendo los labios de los gentiles
para que den gloria a sus ídolos
y veneren eternamente
a un rey de carne.
No entregues, Señor, tu cetro
a los que no son nada.
Que no se burlen de nuestra caída.
Vuelve contra ellos sus planes,
que sirva de escarmiento
el que empezó a atacarnos.
Atiende, Señor,
muéstrate a nosotros
en la tribulación,
y dame valor, Señor,
rey de los dioses
y señor de poderosos.
Pon en mi boca
un discurso acertado
cuando tenga que hablar al león;
haz que cambie
y aborrezca a nuestro enemigo,
para que perezca
con todos sus cómplices.
A nosotros líbranos con tu mano,
y a mí, que no tengo otro auxilio
fuera de ti, protégeme tú, Señor,
que lo sabes todo,
y sabes que odio
la gloria de los impíos,
que me horroriza
el lecho de los incircuncisos
y de cualquier extranjero.
Tú conoces mi peligro.
Aborrezco este emblema
de grandeza
que llevo en mi frente
cuando aparezco en público.
Lo aborrezco
como un harapo inmundo,
y en privado no lo llevo.
Tu sierva no ha comido
a la mesa de Amán,
ni estimado el banquete del rey,
ni bebido vino de libaciones.
Desde el día de mi exaltación
hasta hoy,
tu sierva sólo se ha deleitado en ti,
Señor, Dios de Abrahán.
¡Oh Dios poderoso sobre todos!
Escucha el clamor
de los desesperados,
líbranos de las manos
de los malhechores
y a mí quítame el miedo.
Ester y Asuero
 Al tercer día, al acabar la oración, Ester se quitó la ropa de suplicante y se vistió con todo lujo.
Quedó esplendorosa. Luego, invocando al Dios y salvador que vela sobre todos, marchó con dos doncellas,
apoyándose suavemente en una con delicada elegancia,
mientras la otra la acompañaba llevando la cola del vestido.
Ester iba encendida, radiante de hermosura, con el rostro alegre, como una enamorada, pero con el corazón angustiado.
Atravesó todas las puertas, hasta quedar de pie ante el rey. Estaba sentado en su trono real, revestido de todos sus ornamentos majestuosos, de oro y piedras preciosas. El rey aparecía terrible.
Levantó su rostro encendido de majestad y, en un arrebato de ira, lanzó una mirada. La reina palideció y se apoyó en el hombro de la doncella, desmayándose.
Entonces Dios movió al rey a benevolencia; se inquietó, saltó de su trono y tomó a Ester en sus brazos, animándola con palabras tranquilizadoras mientras ella volvía en sí:
-¿Qué pasa, Ester? Soy tu esposo.
Ánimo, no morirás. Nuestra orden es sólo para nuestros súbditos.
Acércate.
Puso su cetro de oro sobre el cuello de Ester y la acarició, diciéndole:
-Háblame.
Ester le dijo:
-Te vi, señor, como a un ángel de Dios, y me atemoricé ante tanto esplendor.
Porque eres admirable, señor, y tu rostro fascina.
Mientras hablaba, se desmayó.
El rey se turbó, y todos los cortesanos intentaban reanimarla.
El rey y el virrey con Ester
 Al tercer día, Ester se puso sus vestidos de reina y llegó hasta el patio interior del palacio, frente al salón del trono. El rey estaba sentado en su trono real, en el salón, frente a la entrada.Cuando vio a la reina Ester, de pie en el patio, la miró complacido, extendió hacia ella el cetro de oro que tenía en la mano y Ester se acercó a tocar el extremo del cetro. - Biblia de nuestro Pueblo"/>
