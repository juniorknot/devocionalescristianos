-¿Qué te pasa, reina Ester? Pídemelo, y te daré hasta la mitad de mi reino. 4. Ester dijo:
-Si le agrada al rey, venga hoy con Amán al banquete que he preparado en su honor. 5. El rey dijo:
-Avisen inmediatamente a Amán, que acepte la invitación de Ester.
El rey y Amán fueron al banquete preparado por Ester. 6. Y en medio de los brindis, el rey dijo a Ester:
