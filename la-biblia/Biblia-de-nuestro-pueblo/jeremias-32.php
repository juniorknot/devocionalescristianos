Lv 25,25; Rut 3s
 Palabras que el Señor dirigió a Jeremías el año décimo del reinado de Sedecías en Judá, que corresponde al año dieciocho de Nabucodonosor. 2. Entonces sitiaba a Jerusalén el ejército del rey de Babilonia, y el profeta Jeremías estaba preso en el atrio de la guardia, en el palacio real de Judá. 3. Lo había encarcelado Sedecías, acusándole:
