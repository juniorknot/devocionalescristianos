<p style="style=text-align: justify;"><strong id="numeros">35. </strong>
Construían capillas a Baal, en el valle de Ben-Hinón, para pasar por el fuego a sus hijos e hijas, en honor de Moloc. Cosa que yo no mandé ni se me pasó por la cabeza. Hicieron prácticas idolátricas semejantes, haciendo pecar a Judá. </p>
<p style="style=text-align: justify;"><strong id="numeros">36. </strong>
El Señor ratifica las palabras del profeta
Y ahora así dice el Señor, Dios de Israel, a esta ciudad de la que ustedes dicen: Va a caer en manos del rey de Babilonia, por la espada, el hambre y la peste. </p>
<p style="style=text-align: justify;"><strong id="numeros">37. </strong>
Miren que yo los congregaré en todos los países por donde los dispersó mi ira y mi cólera y mi gran furor. Los traeré a este lugar, y los haré habitar tranquilos. </p>
<p style="style=text-align: justify;"><strong id="numeros">38. </strong>
Ellos serán mi pueblo y yo seré su Dios. </p>
<p style="style=text-align: justify;"><strong id="numeros">39. </strong>
Les daré un corazón entero y una conducta íntegra, para que me respeten toda la vida, para su bien y el de sus hijos que los sucedan. </p>
<p style="style=text-align: justify;"><strong id="numeros">40. </strong>
Haré con ellos alianza eterna, y no cesaré de hacerles bien. Les infundiré respeto a mí, para que no se aparten de mí. </p>
<p style="style=text-align: justify;"><strong id="numeros">41. </strong>
Gozaré haciéndoles el bien. Los plantaré de verdad en esta tierra, con todo mi corazón y toda mi alma. </p>
<p style="style=text-align: justify;"><strong id="numeros">42. </strong>
Porque así dice el Señor: Lo mismo que envié a este pueblo esta gran calamidad, también yo mismo les enviaré todos los bienes que les prometo. </p>
<p style="style=text-align: justify;"><strong id="numeros">43. </strong>
Se comprarán campos en esta tierra, de la que ustedes dicen: Está desolada, sin hombres ni ganado, y cae en manos de los caldeos. </p>
<p style="style=text-align: justify;"><strong id="numeros">44. </strong>
Se comprarán campos con dinero, ante testigos, se escribirá y sellará el contrato en el territorio de Benjamín y en el distrito de Jerusalén, en las poblaciones de Judá, de la Sierra, de la Sefela y del Negueb, porque cambiaré su suerte -oráculo del Señor-. </p>
