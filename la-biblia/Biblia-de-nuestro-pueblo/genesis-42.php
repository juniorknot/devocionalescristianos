<li class="hide-for-large"><a href="whatsapp://send?text=Génesis 42 (BNP)%0A%0A 1. Los hermanos de José: primer encuentro
 Al enterarse Jacob de que en Egipto había grano, dijo a sus hijos:
-¿Por qué se quedan ahí sin hacer nada? 2. He oído que hay grano en Egipto: Vayan allá y compren algo de grano para nosotros. Así viviremos y no moriremos. 3. Bajaron, entonces, diez hermanos de José a comprar grano en Egipto. 4. Jacob no envió con sus hermanos a Benjamín, hermano de José, no le fuera a suceder alguna desgracia. 5. Los hijos de Israel llegaron en medio de otros viajeros a comprar grano, porque en el país cananeo se pasaba hambre. 6. En el país mandaba José, él vendía el grano a todo el mundo; así que los hermanos de José llegaron y se postraron ante él rostro en tierra. 7. Al ver a sus hermanos, José los reconoció, pero disimuló y les habló con dureza:
-¿De dónde vienen?
Contestaron:
-De Canaán, a comprar alimentos. 8. José reconoció a sus hermanos, pero ellos no lo reconocieron. 9. Se acordó José de los sueños que había soñado sobre ellos y les dijo:
-¡Ustedes son espías! Han venido a inspeccionar las zonas desguarnecidas del país. 10. Le contestaron:
-¡De ningún modo, señor! Tus servidores han venido a comprar alimentos. 11. Somos todos hijos de un mismo padre, gente honrada; tus servidores no son espías. 12. Replicó:
-¿Cómo que no? Han venido a inspeccionar las zonas desguarnecidas del país. 13. Le dijeron:
-Éramos doce hermanos tus servidores, hijos del mismo padre, de Canaán. El menor se ha quedado con su padre, otro ha desaparecido. 14. Respondió José:
-Lo que yo decía: ustedes son espías. 15. Los pondré a prueba: no saldrán de aquí, ¡por vida del faraón!, si no viene acá su hermano menor. 16. Despachen a uno de ustedes por su hermano, mientras los demás quedarán presos. Así probarán ustedes que han dicho la verdad; de lo contrario, ¡por vida del faraón!, no habrá duda de que ustedes son espías. 17. Y los hizo encarcelar por tres días. 18. Al tercer día José les dijo:
-Hagan lo siguiente y quedarán con vida; porque yo respeto a Dios. 19. Si ustedes son gente honrada, uno de sus hermanos quedará aquí encarcelado y los demás irán a llevar grano a sus familias hambrientas. 20. Pero me traerán a su hermano menor. Así probarán que han dicho la verdad y no morirán.
Ellos estuvieron de acuerdo. 21. Y se decían:
-Estamos pagando el delito contra nuestro hermano: cuando lo veíamos suplicarnos angustiado y no le hicimos caso. Ahora nos toca a nosotros estar angustiados. 22. Les respondió Rubén:
-¿No les decía yo que no cometieran ese delito contra su hermano? Pero no me hicieron caso. Ahora nos piden cuentas de su sangre. 23. No sabía que José los entendía, porque había usado un traductor para hablar con ellos. 24. Él se retiró y lloró; después volvió para hablarles. Escogió a Simeón y lo hizo encadenar en su presencia. 25. José mandó que les llenaran las bolsas de grano, que metieran el dinero pagado en cada una de las bolsas y que les dieran provisiones para el viaje. Así se hizo. 26. Ellos cargaron el grano en los asnos y se marcharon. 27. En la posada uno de ellos abrió la bolsa para dar de comer a su asno y descubrió el dinero allí, en la boca de la bolsa. 28. Y dijo a sus hermanos:
-¡Me han devuelto el dinero!
Se les encogió el corazón del susto y se dijeron:
-¿Qué es lo que nos ha hecho Dios? 29. Llegados a casa de su padre Jacob, en Canaán, le contaron todo lo sucedido. 30. -El señor del país nos habló con dureza declarándonos espías de su tierra. 31. Le contestamos que somos gente honrada, que no somos espías. 32. Que éramos doce hermanos, hijos de un padre; que uno había desaparecido y el menor se había quedado con su padre en Canaán. 33. El señor del país nos contestó: Así sabré que son gente honrada: dejarán conmigo a uno de los hermanos, llevarán provisiones a sus familias hambrientas 34. y me traerán a su hermano menor. Así sabré que no son espías, sino gente honrada; entonces les devolveré a su hermano y podrán comerciar en mi país. 35. Cuando vaciaron las bolsas, encontró cada uno su dinero. Viendo el dinero, ellos y su padre se asustaron. 36. Jacob, su padre, les dijo:
