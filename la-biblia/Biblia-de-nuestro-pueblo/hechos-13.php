<li class="hide-for-large"><a href="whatsapp://send?text=Hechos 13 (BNP)%0A%0A 1. Misión de Pablo y Bernabé
 En la Iglesia de Antioquía había algunos profetas y doctores: Bernabé, Simeón el Negro, Lucio el Cireneo, Manajén, que se había criado con el tetrarca Herodes, y Saulo. 2. Un día, mientras celebraban el culto del Señor y ayunaban, el Espíritu Santo dijo:
-Sepárenme a Bernabé y a Saulo para la tarea a la que los tengo destinados. 3. Ayunaron, oraron, e imponiéndoles las manos, los despidieron. 4. Así, enviados por el Espíritu Santo, bajaron a Seleucia, de allí navegaron a Chipre y, 5. llegados a Salamina, anunciaban la Palabra de Dios en las sinagogas judías. Llevaban a Juan como colaborador. 6. Atravesando la isla, llegaron a Pafos, donde encontraron a un mago y falso profeta judío que se llamaba Barjesús. 7. Estaba en el séquito del gobernador Sergio Pablo, hombre inteligente, que había llamado a Bernabé y Saulo porque deseaba escuchar la Palabra de Dios. 8. Pero se les opuso el mago Elimas, que así se traduce su nombre, que procuraba apartar al gobernador de la fe. 9. Saulo, o sea Pablo, lleno de Espíritu Santo, lo miró fijamente 10. y le dijo:
-¡Gran embustero y embaucador, hijo del Diablo y enemigo de toda justicia! ¿Cuándo acabarás de retorcer los caminos rectos de Dios? 11. Mira, te herirá la mano de Dios y quedarás una temporada ciego sin ver el sol.
Al instante lo invadió una niebla oscura y andaba a tientas buscando a alguien que le diera la mano. 12. Al ver lo sucedido, el gobernador profundamente impresionado ante la enseñanza del Señor, abrazó la fe. 13. En Antioquía de Pisidia
 Navegando desde Pafos, Pablo y sus compañeros llegaron a Perge de Panfilia. Juan se separó de ellos y se volvió a Jerusalén. 14. Ellos continuaron desde Perge hasta Antioquía de Pisidia, y entrando un sábado en la sinagoga, tomaron asiento. 15. Terminada la lectura de la ley y los profetas, los jefes de la sinagoga les mandaron a decir:
-Hermanos, si tienen alguna palabra de aliento para el pueblo, pueden decirla. 16. Pablo se levantó y, pidiendo silencio con la mano, dijo:
-Israelitas y todos los que temen a Dios, escúchenme: 17. El Dios de este pueblo, el Dios de Israel eligió a nuestros padres y engrandeció al pueblo mientras residía en Egipto. Más tarde, con brazo poderoso los sacó de allí 18. y durante cuarenta años los condujo por el desierto. 19. Aniquiló a siete pueblos paganos de Canaán y entregó su territorio en heredad a Israel, 20. por cuatrocientos cincuenta años; les dio jueces hasta el profeta Samuel. 21. Entonces pidieron un rey y Dios les dio a Saúl, hijo de Quis, de la tribu de Benjamín, que reinó cuarenta años. 22. Lo depuso y nombró rey a David, de quien dio testimonio: Encontré a David, el de Jesé, un hombre a mi gusto, que cumplirá todos mis deseos. 23. De la descendencia de David, según la promesa, sacó Dios a Jesús como salvador de Israel. 24. Antes de su llegada Juan predicó un bautismo de penitencia a todo el pueblo de Israel. 25. Hacia el fin de su carrera mortal Juan dijo: Yo no soy el que ustedes creen; detrás de mí viene uno al que no tengo derecho a quitarle las sandalias de los pies. 26. Hermanos, descendientes de Abrahán, y todos los que temen a Dios: A ustedes se les envía este mensaje de salvación. 27. Los vecinos de Jerusalén y sus jefes no acogieron a Jesús ni entendieron las palabras de los profetas que se leen cada sábado. Pero, al juzgarlo, las cumplieron. 28. Pidieron a Pilato que lo condenara, aunque no encontraron causa para una sentencia de muerte. 29. Cuando se cumplió todo lo escrito de él lo descolgaron del madero y le dieron sepultura. 30. Pero Dios lo resucitó de la muerte 31. y se apareció durante muchos días a los que habían subido con él de Galilea a Jerusalén. Ellos son hoy sus testigos ante el pueblo. 32. Y nosotros, les anunciamos a ustedes esta Buena Noticia: la promesa que Dios hizo a nuestros padres 33. fue cumplida por él a sus descendientes, que somos nosotros, resucitando a Jesús, como está escrito en el salmo segundo: Tú eres mi hijo, yo te he engendrado hoy. 34. Y que lo ha resucitado para que nunca se someta a la corrupción está anunciado así: Cumpliré las santas promesas hechas a David, aquellas que no pueden fallar. 35. Y en otro lugar dice: No permitirás que tu fiel sufra la corrupción. 36. Ahora bien, David, después de haber cumplido la voluntad de Dios durante su propia generación, murió, fue sepultado y sufrió la corrupción. 37. En cambio, el que Dios resucitó no sufrió la corrupción. 38. Sépanlo, hermanos, se les anuncia el perdón de los pecados por medio de él, 39. y todo el que crea será perdonado de todo lo que no pudo perdonar la ley de Moisés. 40. ¡Tengan cuidado! Que no les suceda lo anunciado por los profetas: 41. Ustedes, los que desprecian,
llénense de estupor y ocúltense:
Porque en estos días
