<li class="hide-for-large"><a href="whatsapp://send?text=Ezequiel 20 (BNP)%0A%0A 1. Historia de una rebeldía
16; 23
 El año séptimo, el día décimo del quinto mes, vinieron algunos ancianos de Israel a consultar al Señor y se sentaron frente a mí. 2. Entonces me dirigió la palabra el Señor: 3. -Hijo de hombre, habla así a los ancianos de Israel: Esto dice el Señor: ¿Vienen a consultarme? ¡Por mi vida! Juro que no me dejaré consultar por ustedes -oráculo del Señor-. 4. ¡Júzgalos tú, júzgalos tú, Hijo de hombre! Dales a conocer las prácticas idolátricas de sus padres, 5. diciéndoles: Esto dice el Señor:
Cuando elegí a Israel,
juré con la mano en alto
a la descendencia de la casa de Jacob;
cuando me manifesté a ellos
en Egipto les dije con la mano en alto:
Yo soy el Señor, su Dios. 6. Aquel día les juré con la mano en alto
sacarlos de Egipto
y llevarlos a una tierra
que yo mismo les había explorado:
manaba leche y miel,
era la perla de las naciones. 7. Y les dije:
Arrojen sus detestables dioses
que atraen sus miradas
y no se contaminen
con los ídolos de Egipto.
Yo soy el Señor, su Dios. 8. Pero se rebelaron contra mí
y no quisieron obedecerme;
ninguno arrojó los detestables dioses
que atraían sus miradas
ni se deshizo de los ídolos de Egipto.
Entonces pensé derramar
mi cólera sobre ellos
para agotar en ellos mi ira
en territorio egipcio. 9. Pero actué por respeto a mi Nombre,
para que no fuera profanado
ante los paganos con los que vivían,
y en cuya presencia
me manifesté a ellos
para sacarlos de Egipto. 10. Los saqué de Egipto
y los llevé al desierto. 11. Les di mis preceptos
y les enseñé mis mandamientos,
que dan la vida al que los cumple. 12. Les di también mis sábados
como señal recíproca,
para que se supiera
que yo soy el Señor
que los santifico. 13. Pero se rebeló contra mí
la casa de Israel en el desierto:
no caminaron según mis preceptos,
rechazaron mis mandamientos,
que dan la vida al que los cumple,
y profanaron gravemente
mis sábados.
Entonces pensé derramar
mi cólera sobre ellos,
en el desierto, para exterminarlos. 14. Pero actué por respeto a mi Nombre,
para que no fuera profanado
ante los paganos,
en cuya presencia los había sacado. 15. No obstante, juré en el desierto,
con la mano en alto,
no llevarlos a la tierra
que les había asignado,
que manaba leche y miel
y era la perla de las naciones, 16. por haber rechazado
mis mandamientos,
por no haber caminado
según mis preceptos,
por haber profanado mis sábados,
porque se les iba el corazón
tras sus ídolos. 17. Pero compadecido de ellos,
no los aniquilé
ni acabé con ellos en el desierto. 18. A sus hijos les dije en el desierto:
No caminen según los preceptos
de sus padres,
ni guarden sus mandamientos,
ni se contaminen con sus ídolos. 19. Yo soy el Señor, su Dios:
caminen según mis preceptos,
guarden mis mandamientos
y cúmplanlos; 20. santifiquen mis sábados:
serán señal recíproca
para que se sepa
que soy el Señor, su Dios. 21. Pero sus hijos se rebelaron contra mí:
no caminaron según mis preceptos,
ni guardaron ni cumplieron
mis mandamientos,
que dan la vida al que los cumple,
y profanaron mis sábados.
Entonces pensé derramar
mi cólera sobre ellos
para agotar en ellos
mi ira en el desierto. 22. Pero retraje mi mano
y actué por respeto a mi Nombre
para que no fuera profanado
ante los paganos,
en cuya presencia los había sacado. 23. Con todo, juré en el desierto,
con la mano en alto,
dispersarlos por las naciones
y esparcirlos por los países, 24. por no haber cumplido
mis mandamientos,
por haber rechazado mis preceptos
y haber profanado mis sábados,
por haberse entregado
a los ídolos de sus padres. 25. ¿Acaso les di yo
preceptos no buenos,
mandamientos
que no les darían la vida? 26. ¿Los contaminé
con las ofrendas que hacían
sacrificando en el fuego
a sus hijos primogénitos?
¿Los horroricé para que así
supieran que yo soy el Señor? 27. Por tanto, Hijo de hombre,
habla así a la casa de Israel:
Esto dice el Señor:
Sus padres
encima me ofendieron
cometiendo esta traición: 28. Cuando los introduje en la tierra
que con la mano en alto
había jurado darles,
al ver una colina alta,
al ver un árbol frondoso,
allí hacían sus sacrificios,
allí depositaban su irritante ofrenda,
allí ponían sus oblaciones
