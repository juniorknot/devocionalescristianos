juro que no me dejaré consultar. </p>
<p style="style=text-align: justify;"><strong id="numeros">32. </strong>
Jamás se realizarán los planes
que están pensando:
Seremos como los demás pueblos,
como las razas de otros países,
que adoran al leño y a la piedra. </p>
<p style="style=text-align: justify;"><strong id="numeros">33. </strong>
¡Por mi vida!
-oráculo del Señor-,
juro que con mano poderosa,
con brazo extendido,
con cólera incontenible,
reinaré sobre ustedes </p>
<p style="style=text-align: justify;"><strong id="numeros">34. </strong>
y los sacaré de los países
y los reuniré de entre las naciones
por las que están dispersos,
con mano poderosa,
con brazo extendido,
con cólera incontenible. </p>
<p style="style=text-align: justify;"><strong id="numeros">35. </strong>
Y los llevaré
al desierto de los pueblos
para pleitear allí
con ustedes cara a cara. </p>
<p style="style=text-align: justify;"><strong id="numeros">36. </strong>
Igual que pleiteé
con sus padres
en el desierto de Egipto,
así pleitearé con ustedes
-oráculo del Señor-. </p>
<p style="style=text-align: justify;"><strong id="numeros">37. </strong>
Los haré pasar bajo el cayado
y los haré entrar uno a uno
por el aro de la alianza, </p>
<p style="style=text-align: justify;"><strong id="numeros">38. </strong>
y excluiré a los rebeldes
que se sublevan contra mí;
los sacaré del país de su destierro,
pero no entrarán
en la tierra de Israel.
Y sabrán que yo soy el Señor. </p>
<p style="style=text-align: justify;"><strong id="numeros">39. </strong>
A ustedes, casa de Israel,
esto les dice el Señor:
Cada uno que vaya
a servir a sus ídolos
si no quiere obedecerme,
pero que no siga profanando
mi santo Nombre
con sus ofrendas idolátricas. </p>
<p style="style=text-align: justify;"><strong id="numeros">40. </strong>
Porque en mi santo monte,
en el más alto monte de Israel
-oráculo del Señor-,
allí en la tierra, me servirá
la casa de Israel toda entera.
Allí los aceptaré,
allí les pediré sus tributos,
sus primicias
y sus dones sagrados. </p>
<p style="style=text-align: justify;"><strong id="numeros">41. </strong>
Como aroma que aplaca
los aceptaré
cuando los saque de los países
y los reúna de entre las naciones
en las que están dispersos
y muestre en ustedes mi santidad
a la vista de los paganos. </p>
<p style="style=text-align: justify;"><strong id="numeros">42. </strong>
Y sabrán que yo soy el Señor
cuando los lleve a la tierra de Israel,
al país que con la mano en alto
juré dar a sus padres. </p>
<p style="style=text-align: justify;"><strong id="numeros">43. </strong>
Allí, cuando se acuerden
de su conducta
y de las malas obras
con que se contaminaron
sentirán asco de ustedes mismos
por las maldades que cometieron. </p>
<p style="style=text-align: justify;"><strong id="numeros">44. </strong>
Y sabrán que yo soy el Señor
cuando los trate
como exige mi Nombre,
no según su mala conducta
y sus obras perversas,
casa de Israel -oráculo del Señor-. </p>
<h4><strong>Profecía contra el Neguev</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
El bosque en llamas
Me dirigió la palabra el Señor: </p>
<p style="style=text-align: justify;"><strong id="numeros">46. </strong>
-Hijo de hombre, ponte mirando al sur, vaticina al mediodía, profetiza así al bosque austral: </p>
<p style="style=text-align: justify;"><strong id="numeros">47. </strong>
¡Bosque austral, escucha la Palabra del Señor! Esto dice el Señor:
Voy a prenderte un fuego que devore
tus árboles verdes, tus árboles secos.
No se apagará la ardiente llamarada
que abrasará todos los terrenos,
desde el sur hasta el norte. </p>
<p style="style=text-align: justify;"><strong id="numeros">48. </strong>
Y verá todo mortal
que yo, el Señor, lo encendí,
y no se apagará. </p>
<p style="style=text-align: justify;"><strong id="numeros">49. </strong>
Yo entonces repliqué:
-¡Ay, Señor! Van diciendo de mí:
Es un charlatán. </p>
