<h4><strong>Mem</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">97. </strong>
¡Cómo amo tu voluntad!,
la medito todo el día. </p>
<p style="style=text-align: justify;"><strong id="numeros">98. </strong>
Tus mandatos me hacen
más hábil que mis enemigos,
siempre van conmigo. </p>
<p style="style=text-align: justify;"><strong id="numeros">99. </strong>
Soy más sagaz que todos mis maestros,
porque medito tus preceptos. </p>
<p style="style=text-align: justify;"><strong id="numeros">100. </strong>
Soy más sabio que los ancianos,
ya que observo tus decretos. </p>
<p style="style=text-align: justify;"><strong id="numeros">101. </strong>
Alejo mis pies de toda senda mala,
para observar tu palabra. </p>
<p style="style=text-align: justify;"><strong id="numeros">102. </strong>
No me aparto de tus mandamientos
porque tú me has instruido. </p>
<p style="style=text-align: justify;"><strong id="numeros">103. </strong>
¡Qué dulce es tu promesa al paladar,
más que miel a la boca! </p>
<p style="style=text-align: justify;"><strong id="numeros">104. </strong>
Reflexiono sobre tus decretos,
por eso odio toda senda falsa. </p>
<h4><strong>Nun</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">105. </strong>
Lámpara es tu palabra para mis pasos,
luz en mis senderos. </p>
<p style="style=text-align: justify;"><strong id="numeros">106. </strong>
He jurado, y lo ratifico:
cumpliré tus justos mandamientos. </p>
<p style="style=text-align: justify;"><strong id="numeros">107. </strong>
Estoy sumamente afligido,
vivifícame, Señor, según tu palabra. </p>
<p style="style=text-align: justify;"><strong id="numeros">108. </strong>
Acepta, Señor, las ofrendas de mi boca
y enséñame tus mandamientos. </p>
<p style="style=text-align: justify;"><strong id="numeros">109. </strong>
Mi vida está siempre en mis manos,
pero no olvido tu ley. </p>
<p style="style=text-align: justify;"><strong id="numeros">110. </strong>
Los malvados me ponen trampas,
yo no me desvío de tus decretos. </p>
<p style="style=text-align: justify;"><strong id="numeros">111. </strong>
Tus preceptos son mi herencia perpetua,
son el gozo de mi corazón. </p>
<p style="style=text-align: justify;"><strong id="numeros">112. </strong>
Inclino mi corazón a cumplir tus normas,
que son mi recompensa eterna. </p>
<h4><strong>Sámec</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">113. </strong>
Detesto a los inconstantes
y amo tu voluntad. </p>
<p style="style=text-align: justify;"><strong id="numeros">114. </strong>
Tú eres mi refugio y mi escudo:
confío en tu palabra. </p>
<p style="style=text-align: justify;"><strong id="numeros">115. </strong>
Apártense de mí, perversos,
y cumpliré los mandatos de mi Dios. </p>
<p style="style=text-align: justify;"><strong id="numeros">116. </strong>
Sostenme con tu promesa y viviré,
no defraudes mi esperanza. </p>
<p style="style=text-align: justify;"><strong id="numeros">117. </strong>
Respáldame y estaré a salvo
y me fijaré siempre en tus normas. </p>
<p style="style=text-align: justify;"><strong id="numeros">118. </strong>
Repudias a quienes
se apartan de tus normas,
porque falaz es la astucia. </p>
<p style="style=text-align: justify;"><strong id="numeros">119. </strong>
Rechazas como escoria
a todos los malvados de la tierra,
por eso amo tus preceptos. </p>
<p style="style=text-align: justify;"><strong id="numeros">120. </strong>
Mi cuerpo tiembla aterrorizado por ti
y me estremecen tus mandamientos. </p>
<h4><strong>Ayin</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">121. </strong>
Practico la justicia y el derecho:
no me entregues a mis opresores. </p>
<p style="style=text-align: justify;"><strong id="numeros">122. </strong>
Sal fiador por tu siervo,
que no me opriman los soberbios. </p>
<p style="style=text-align: justify;"><strong id="numeros">123. </strong>
Mis ojos se languidecen por tu salvación
y por tu promesa de justicia. </p>
<p style="style=text-align: justify;"><strong id="numeros">124. </strong>
Trata a tu siervo según tu amor
y enséñame tus normas. </p>
<p style="style=text-align: justify;"><strong id="numeros">125. </strong>
Soy tu siervo, instrúyeme,
y comprenderé tus preceptos. </p>
<p style="style=text-align: justify;"><strong id="numeros">126. </strong>
Es hora de actuar, Señor,
han quebrantado tu ley. </p>
<p style="style=text-align: justify;"><strong id="numeros">127. </strong>
¡Oh Dios altísimo y fiel,
yo amo tus mandatos
más que el oro puro! </p>
<p style="style=text-align: justify;"><strong id="numeros">128. </strong>
¡Oh Dios altísimo y fiel,
considero rectas todas tus normas
y detesto toda senda engañosa! </p>
<h4><strong>Pe</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">129. </strong>
Tus preceptos son admirables:
por eso los guarda mi alma. </p>
<p style="style=text-align: justify;"><strong id="numeros">130. </strong>
La explicación de tu palabra ilumina,
instruye a los inexpertos. </p>
<p style="style=text-align: justify;"><strong id="numeros">131. </strong>
Jadeo con la boca abierta,
anhelando tus mandatos. </p>
<p style="style=text-align: justify;"><strong id="numeros">132. </strong>
Vuélvete a mí con piedad,
como haces con quienes te aman. </p>
<p style="style=text-align: justify;"><strong id="numeros">133. </strong>
Afirma mis pasos según tu promesa,
que no me domine maldad alguna. </p>
<p style="style=text-align: justify;"><strong id="numeros">134. </strong>
Líbrame de la opresión de los hombres,
y guardaré tus decretos. </p>
<p style="style=text-align: justify;"><strong id="numeros">135. </strong>
Haz brillar tu rostro sobre tu siervo
y enséñame tus leyes. </p>
<p style="style=text-align: justify;"><strong id="numeros">136. </strong>
Ríos de lágrimas vierten mis ojos
porque no se guarda tu ley. </p>
<h4><strong>Tsade</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">137. </strong>
Tú eres justo, Señor,
y recto en tus juicios. </p>
<p style="style=text-align: justify;"><strong id="numeros">138. </strong>
Justamente prescribes preceptos,
sumamente estables. </p>
<p style="style=text-align: justify;"><strong id="numeros">139. </strong>
Me consumo de celo
porque mis enemigos olvidan tus palabras. </p>
<p style="style=text-align: justify;"><strong id="numeros">140. </strong>
Purísima es tu promesa,
y tu siervo la ama. </p>
<p style="style=text-align: justify;"><strong id="numeros">141. </strong>
Soy pequeño y despreciable,
mas no olvido tus decretos. </p>
<p style="style=text-align: justify;"><strong id="numeros">142. </strong>
Tu justicia es justicia eterna,
y tu ley es auténtica. </p>
<p style="style=text-align: justify;"><strong id="numeros">143. </strong>
Aunque me alcancen
la angustia y la opresión,
tus mandatos son mi delicia. </p>
<p style="style=text-align: justify;"><strong id="numeros">144. </strong>
Tus preceptos son justos por siempre;
instrúyeme y viviré. </p>
<h4><strong>Cof</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">145. </strong>
Clamo de todo corazón,
respóndeme, Señor,
y guardaré tus normas. </p>
<p style="style=text-align: justify;"><strong id="numeros">146. </strong>
Te invoco, sálvame,
y observaré tus preceptos. </p>
<p style="style=text-align: justify;"><strong id="numeros">147. </strong>
Me adelanto a la aurora pidiendo auxilio,
esperando tus palabras. </p>
<p style="style=text-align: justify;"><strong id="numeros">148. </strong>
Mis ojos se adelantan a las vigilias,
meditando tu promesa. </p>
<p style="style=text-align: justify;"><strong id="numeros">149. </strong>
Por tu amor escucha mi voz,
Señor, vivifícame según tu justicia. </p>
<p style="style=text-align: justify;"><strong id="numeros">150. </strong>
Me cercan los seguidores de los ídolos,
y se alejan de tu ley. </p>
<p style="style=text-align: justify;"><strong id="numeros">151. </strong>
Tú, Señor, estás cerca
y todos tus mandatos son auténticos. </p>
<p style="style=text-align: justify;"><strong id="numeros">152. </strong>
Desde hace tiempo estableciste
tus preceptos para siempre. </p>
<h4><strong>Resh</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">153. </strong>
Mira mi aflicción y líbrame,
pues no olvido tu voluntad. </p>
<p style="style=text-align: justify;"><strong id="numeros">154. </strong>
Defiende mi causa y rescátame,
vivifícame conforme a tu promesa. </p>
<p style="style=text-align: justify;"><strong id="numeros">155. </strong>
Tu salvación está lejos de los malvados,
porque no buscan tu ley. </p>
<p style="style=text-align: justify;"><strong id="numeros">156. </strong>
Grande es tu ternura, Señor,
vivifícame según tu justicia. </p>
<p style="style=text-align: justify;"><strong id="numeros">157. </strong>
Muchos son
mis perseguidores y adversarios,
pero yo no me aparto de tus preceptos. </p>
<p style="style=text-align: justify;"><strong id="numeros">158. </strong>
Veo a los renegados y siento asco,
porque no observan tus instrucciones. </p>
<p style="style=text-align: justify;"><strong id="numeros">159. </strong>
Mira cómo amo tus decretos;
Señor, vivifícame según tu amor. </p>
<p style="style=text-align: justify;"><strong id="numeros">160. </strong>
El compendio de tu palabra es la verdad,
son eternos tus justos mandamientos. </p>
<h4><strong>Sin</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">161. </strong>
Los poderosos me persiguen sin motivo;
mi corazón tiembla por tus palabras. </p>
<p style="style=text-align: justify;"><strong id="numeros">162. </strong>
Yo me alegro de tu promesa,
como el que obtiene un rico botín. </p>
<p style="style=text-align: justify;"><strong id="numeros">163. </strong>
Detesto y aborrezco la mentira,
amo tu voluntad. </p>
<p style="style=text-align: justify;"><strong id="numeros">164. </strong>
Siete veces al día te alabo
por tus justos mandamientos. </p>
<p style="style=text-align: justify;"><strong id="numeros">165. </strong>
Mucha paz tienen los que aman tu ley,
nada los hace tropezar. </p>
<p style="style=text-align: justify;"><strong id="numeros">166. </strong>
Espero tu salvación, Señor,
y cumplo tus mandatos. </p>
<p style="style=text-align: justify;"><strong id="numeros">167. </strong>
Yo observo tus preceptos,
los amo intensamente. </p>
<p style="style=text-align: justify;"><strong id="numeros">168. </strong>
Guardo tus preceptos y decretos,
¡todos mis caminos están ante ti! </p>
<h4><strong>Tau</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">169. </strong>
Llegue mi clamor a tu presencia, Señor,
instrúyeme con tu palabra. </p>
<p style="style=text-align: justify;"><strong id="numeros">170. </strong>
Llegue mi súplica a tu presencia:
líbrame según tu promesa. </p>
<p style="style=text-align: justify;"><strong id="numeros">171. </strong>
Brote de mis labios la alabanza,
pues me enseñaste tus normas. </p>
<p style="style=text-align: justify;"><strong id="numeros">172. </strong>
Proclame mi lengua tu promesa
pues todos tus mandatos son justos. </p>
<p style="style=text-align: justify;"><strong id="numeros">173. </strong>
Que tu mano me auxilie,
pues he elegido tus decretos. </p>
<p style="style=text-align: justify;"><strong id="numeros">174. </strong>
Anhelo tu salvación, Señor,
tu voluntad es mi delicia. </p>
<p style="style=text-align: justify;"><strong id="numeros">175. </strong>
Que yo viva para alabarte;
que tu mandamiento me auxilie. </p>
<p style="style=text-align: justify;"><strong id="numeros">176. </strong>
Si me extravié como oveja descarriada,
busca a tu siervo.
¡No. No olvido tus mandatos! </p>
