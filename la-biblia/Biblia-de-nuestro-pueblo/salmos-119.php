andan por sus caminos. 4. Tú mandaste que tus decretos
se observen exactamente. 5. Ojalá estén firmes mis caminos
para cumplir tus órdenes. 6. Entonces no quedaré defraudado
al fijarme en tus mandatos. 7. Te daré gracias con sincero corazón
cuando aprenda tus justos mandamientos. 8. Quiero cumplir tus órdenes
¡No me abandones,
oh Dios grande e inmortal! 9. ¿Cómo limpiará un joven su sendero?
-Observando tu palabra. 10. Te busco de todo corazón:
no me desvíes de tus mandatos. 11. Guardo en mi corazón tu promesa
para no pecar contra ti. 12. ¡Bendito eres, Señor!,
enséñame tus normas. 13. Mis labios recitarán
todo lo que manda tu boca. 14. En el camino de tus preceptos disfruto
más que con cualquier fortuna. 15. Voy a meditar tus decretos
y a fijarme en tus senderos. 16. Me complazco en tus órdenes:
no me olvido de tus palabras. 17. Cuida de tu servidor y viviré
para cumplir tu palabra. 18. Abre mis ojos y contemplaré
las maravillas de tu ley. 19. Soy peregrino en la tierra:
no me ocultes tus mandatos. 20. Mi vida se consume deseando
siempre tus mandamientos. 21. Amonesta a los malditos soberbios
que se apartan de tus mandatos. 22. Retira de mí el insulto y el desprecio,
porque guardo tus preceptos. 23. Aunque los poderosos conspiren contra mí,
tu siervo medita tus órdenes. 24. También tus preceptos son mi delicia,
son mis consejeros. 25. Estoy abatido en el polvo:
reanímame según tu palabra. 26. Te conté mis andanzas y me respondiste:
enséñame tus estatutos. 27. Indícame el camino de tus decretos,
y meditaré tus maravillas. 28. Mi cuerpo se encorva por la tristeza,
sostenme con tu palabra. 29. Aléjame del camino de la mentira
y dame la gracia de tu voluntad. 30. He escogido el camino de la lealtad,
he elegido tus mandamientos. 31. Me adhiero a tus preceptos, Señor,
no me defraudes. 32. Por el camino de tus mandatos correré
cuando me ensanches el corazón. 33. Muéstrame, Señor,
el camino de tus estatutos
y lo seguiré hasta el final. 34. Enséñame a cumplir tu voluntad
y a observarla de todo corazón. 35. Encamíname por la senda de tus mandatos,
porque en ella me deleito. 36. Inclina mi corazón hacia tus preceptos
y no a ganancias injustas. 37. No dejes que mis ojos se fijen en la mentira,
reanímame en tu camino. 38. Mantén a tu siervo la promesa
porque te reverencio de verdad. 39. Aleja el ultraje que me aterra;
pues tus mandamientos son buenos. 40. Mira cómo deseo tus decretos;
con tu justicia dame vida. 41. Señor, lleguen hasta mí tu amor
y tu salvación, según tu promesa, 42. así responderé al que me insulta
que confío en tu palabra. 43. No apartes de mi boca la palabra veraz
-oh Dios, grande e inmortal-,
pues espero en tus mandamientos. 44. Que cumpla tu voluntad, Dios eterno,
por siempre jamás; 45. y camine en libertad,
buscando tus decretos. 46. Que hable de tus preceptos ante reyes
sin sentir vergüenza, 47. y me deleite en tus mandatos
que tanto amo. 48. Alzaré las palmas
hacia tus amados mandatos
y meditaré tus normas. 49. Recuerda la palabra dada a tu siervo,
de la que hiciste mi esperanza. 50. Éste es mi consuelo en la aflicción:
que tu promesa me da vida. 51. Los soberbios me insultan,
-oh Dios, grande e inmortal-,
pero no me aparto de tu voluntad. 52. Recordando tus antiguos mandamientos,
Señor, quedé consolado. 53. Me enfurezco contra los malvados
que abandonan tu ley. 54. Tus normas eran mi música
en tierra extranjera. 55. De noche recuerdo tu Nombre, Señor,
en las vigilias, tu voluntad. 56. Ésta es mi tarea:
observar tus decretos. 57. He resuelto, Señor, que mi herencia
sea observar tus palabras. 58. Busco denodadamente tu rostro,
apiádate de mí según tu promesa. 59. He examinado mi proceder,
para retornar a tus preceptos. 60. Me doy prisa, no difiero
la observancia de tus mandatos. 61. Los lazos de los malvados me envolvían,
pero no olvidé tu ley. 62. A media noche
me levanto para darte gracias
por tus justos mandamientos. 63. Soy amigo de quienes te respetan,
de los que guardan tus decretos. 64. Señor, de tu amor está llena la tierra:
enséñame tus normas. 65. Trataste bien a tu siervo,
Señor, según tu palabra. 66. Enséñame a discernir y entender,
porque confío en tus mandatos. 67. Antes de la humillación, erraba
pero ahora cumplo tu instrucción. 68. Tú, que eres bueno y bienhechor,
enséñame tus leyes. 69. Unos soberbios me difaman con mentiras;
pero yo guardo de corazón tus decretos. 70. Como grasa se ha embotado su corazón,
pero yo me deleito en tu voluntad. 71. Me vino bien haber sido humillado,
así aprendí tus órdenes. 72. Es más valiosa la ley de tu boca
que mil monedas de oro y plata. 73. Tus manos me hicieron y me plasmaron,
instrúyeme y aprenderé tus mandatos. 74. Me miran
los que te respetan y se regocjan,
porque he confiado en tu palabra. 75. Señor, bien sé
que tus mandamientos son justos,
que con razón me humillaste. 76. Que tu amor sea mi consuelo
según prometiste a tu siervo. 77. Que me alcance tu compasión, y viviré,
porque tu ley es mi delicia. 78. Sean confundidos
los orgullosos que me calumnian,
yo meditaré tus decretos. 79. Vuelvan a mí los que te honran:
que conozcan tus preceptos. 80. Sea mi corazón íntegro en tus normas,
así no quedaré avergonzado. 81. Mi vida desfallece por tu salvación,
espero en tu palabra. 82. Mis ojos languidecen por tu promesa:
¿cuándo me consolarás? 83. Aunque era como un odre ahumado,
no olvidaba tus leyes. 84. ¿Cuántos serán aún los años de tu siervo?
¿Cuándo juzgarás a mis perseguidores? 85. Me han cavado una fosa los soberbios,
que no están de acuerdo con tu ley. 86. Todos tus mandatos son verdaderos;
sin causa me persiguen, socórreme. 87. Casi me eliminaron de la tierra,
pero no abandoné tus decretos. 88. Por tu amor dame vida
y guardaré la instrucción de tu boca. 89. Tu palabra, Señor, es eterna,
más estable que el cielo; 90. tu fidelidad, por generaciones,
afianzaste la tierra y está firme: 91. por tu disposición se mantienen hasta hoy,
pues todo está a tu servicio. 92. Si tu voluntad no fuera mi delicia,
habría perecido en mi aflicción. 93. Jamás olvidaré tus decretos,
pues con ellos me vivificas. 94. Tuyo soy, sálvame,
que busco tus normas. 95. Me acechan los malvados para perderme,
pero yo medito tus preceptos. 96. He visto límites en todo lo perfecto,
pero, ¡qué inmenso es tu mandato! 97. ¡Cómo amo tu voluntad!,
la medito todo el día. 98. Tus mandatos me hacen
más hábil que mis enemigos,
siempre van conmigo. 99. Soy más sagaz que todos mis maestros,
porque medito tus preceptos. 100. Soy más sabio que los ancianos,
ya que observo tus decretos. 101. Alejo mis pies de toda senda mala,
para observar tu palabra. 102. No me aparto de tus mandamientos
porque tú me has instruido. 103. ¡Qué dulce es tu promesa al paladar,
más que miel a la boca! 104. Reflexiono sobre tus decretos,
por eso odio toda senda falsa. 105. Lámpara es tu palabra para mis pasos,
luz en mis senderos. 106. He jurado, y lo ratifico:
cumpliré tus justos mandamientos. 107. Estoy sumamente afligido,
vivifícame, Señor, según tu palabra. 108. Acepta, Señor, las ofrendas de mi boca
y enséñame tus mandamientos. 109. Mi vida está siempre en mis manos,
pero no olvido tu ley. 110. Los malvados me ponen trampas,
yo no me desvío de tus decretos. 111. Tus preceptos son mi herencia perpetua,
son el gozo de mi corazón. 112. Inclino mi corazón a cumplir tus normas,
que son mi recompensa eterna. 113. Detesto a los inconstantes
y amo tu voluntad. 114. Tú eres mi refugio y mi escudo:
confío en tu palabra. 115. Apártense de mí, perversos,
y cumpliré los mandatos de mi Dios. 116. Sostenme con tu promesa y viviré,
no defraudes mi esperanza. 117. Respáldame y estaré a salvo
y me fijaré siempre en tus normas. 118. Repudias a quienes
se apartan de tus normas,
porque falaz es la astucia. 119. Rechazas como escoria
a todos los malvados de la tierra,
por eso amo tus preceptos. 120. Mi cuerpo tiembla aterrorizado por ti
y me estremecen tus mandamientos. 121. Practico la justicia y el derecho:
no me entregues a mis opresores. 122. Sal fiador por tu siervo,
que no me opriman los soberbios. 123. Mis ojos se languidecen por tu salvación
y por tu promesa de justicia. 124. Trata a tu siervo según tu amor
y enséñame tus normas. 125. Soy tu siervo, instrúyeme,
y comprenderé tus preceptos. 126. Es hora de actuar, Señor,
han quebrantado tu ley. 127. ¡Oh Dios altísimo y fiel,
yo amo tus mandatos
más que el oro puro! 128. ¡Oh Dios altísimo y fiel,
considero rectas todas tus normas
y detesto toda senda engañosa! 129. Tus preceptos son admirables:
por eso los guarda mi alma. 130. La explicación de tu palabra ilumina,
instruye a los inexpertos. 131. Jadeo con la boca abierta,
anhelando tus mandatos. 132. Vuélvete a mí con piedad,
como haces con quienes te aman. 133. Afirma mis pasos según tu promesa,
que no me domine maldad alguna. 134. Líbrame de la opresión de los hombres,
y guardaré tus decretos. 135. Haz brillar tu rostro sobre tu siervo
y enséñame tus leyes. 136. Ríos de lágrimas vierten mis ojos
porque no se guarda tu ley. 137. Tú eres justo, Señor,
y recto en tus juicios. 138. Justamente prescribes preceptos,
sumamente estables. 139. Me consumo de celo
porque mis enemigos olvidan tus palabras. 140. Purísima es tu promesa,
y tu siervo la ama. 141. Soy pequeño y despreciable,
mas no olvido tus decretos. 142. Tu justicia es justicia eterna,
y tu ley es auténtica. 143. Aunque me alcancen
la angustia y la opresión,
tus mandatos son mi delicia. 144. Tus preceptos son justos por siempre;
instrúyeme y viviré. 145. Clamo de todo corazón,
respóndeme, Señor,
y guardaré tus normas. 146. Te invoco, sálvame,
y observaré tus preceptos. 147. Me adelanto a la aurora pidiendo auxilio,
esperando tus palabras. 148. Mis ojos se adelantan a las vigilias,
meditando tu promesa. 149. Por tu amor escucha mi voz,
Señor, vivifícame según tu justicia. 150. Me cercan los seguidores de los ídolos,
y se alejan de tu ley. 151. Tú, Señor, estás cerca
y todos tus mandatos son auténticos. 152. Desde hace tiempo estableciste
tus preceptos para siempre. 153. Mira mi aflicción y líbrame,
pues no olvido tu voluntad. 154. Defiende mi causa y rescátame,
vivifícame conforme a tu promesa. 155. Tu salvación está lejos de los malvados,
porque no buscan tu ley. 156. Grande es tu ternura, Señor,
vivifícame según tu justicia. 157. Muchos son
mis perseguidores y adversarios,
pero yo no me aparto de tus preceptos. 158. Veo a los renegados y siento asco,
porque no observan tus instrucciones. 159. Mira cómo amo tus decretos;
Señor, vivifícame según tu amor. 160. El compendio de tu palabra es la verdad,
son eternos tus justos mandamientos. 161. Los poderosos me persiguen sin motivo;
mi corazón tiembla por tus palabras. 162. Yo me alegro de tu promesa,
como el que obtiene un rico botín. 163. Detesto y aborrezco la mentira,
amo tu voluntad. 164. Siete veces al día te alabo
por tus justos mandamientos. 165. Mucha paz tienen los que aman tu ley,
nada los hace tropezar. 166. Espero tu salvación, Señor,
y cumplo tus mandatos. 167. Yo observo tus preceptos,
los amo intensamente. 168. Guardo tus preceptos y decretos,
¡todos mis caminos están ante ti! 169. Llegue mi clamor a tu presencia, Señor,
instrúyeme con tu palabra. 170. Llegue mi súplica a tu presencia:
líbrame según tu promesa. 171. Brote de mis labios la alabanza,
pues me enseñaste tus normas. 172. Proclame mi lengua tu promesa
pues todos tus mandatos son justos. 173. Que tu mano me auxilie,
pues he elegido tus decretos. 174. Anhelo tu salvación, Señor,
tu voluntad es mi delicia. 175. Que yo viva para alabarte;
que tu mandamiento me auxilie. 176. Si me extravié como oveja descarriada,
busca a tu siervo.
