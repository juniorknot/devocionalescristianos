Naamán se puso en camino, llevando tres quintales de plata, seis mil monedas de oro y diez trajes. 6. Presentó al rey de Israel la carta, que decía así: Cuando recibas esta carta, verás que te envío a mi ministro Naamán para que lo libres de su enfermedad. 7. Cuando el rey de Israel leyó la carta, se rasgó las vestiduras, exclamando:
-¿Acaso soy yo un dios capaz de dar muerte o vida para que éste me encargue de librar a un hombre de su enfermedad? Fíjense bien y verán cómo está buscando un pretexto contra mí. 8. Eliseo, el hombre de Dios, se enteró de que el rey de Israel se había rasgado las vestiduras, y le envió este recado:
-¿Por qué te has rasgado las vestiduras? Que venga a mí y verá que hay un profeta en Israel. 9. Naamán llegó con sus caballos y su carro y se detuvo ante la puerta de Eliseo. 10. Eliseo mandó a uno a decirle:
-Ve a bañarte siete veces en el Jordán, y tu carne quedará limpia. 11. Naamán se enojó y decidió irse, comentando:
-Yo me imaginaba que saldría en persona a verme y que, puesto de pie, invocaría al Señor, su Dios, pasaría la mano sobre la parte enferma y me libraría de mi enfermedad. 12. ¿Es que los ríos de Damasco, el Abana y el Farfar, no valen más que toda el agua de Israel? ¿No puedo bañarme en ellos y quedar limpio?
Dio media vuelta y se marchaba furioso. 13. Pero sus servidores se le acercaron y le dijeron:
-Señor, si el profeta te hubiera mandado una cosa extraordinaria, ¿no la habrías hecho? Cuánto más si lo que te indica para quedar limpio es simplemente que te bañes. 14. Entonces Naamán bajó al Jordán y se bañó siete veces, como había ordenado el profeta, y su carne quedó limpia, como la de un niño. 15. Volvió con su comitiva y se presentó al hombre de Dios, diciendo:
-Ahora reconozco que no hay Dios en toda la tierra más que el de Israel. Acepta un regalo de tu servidor. 16. Eliseo contestó:
-¡Por la vida del Señor, a quien sirvo! No aceptaré nada.
Y aunque le insistía, lo rehusó. 17. Naamán dijo:
-Entonces que a tu servidor le dejen llevar tierra, la carga de un par de mulas; porque en adelante tu servidor no ofrecerá holocaustos ni sacrificios a otros dioses fuera del Señor. 18. Y que el Señor me perdone: si al entrar mi señor en el templo de Rimón para adorarlo se apoya en mi mano, y yo también me postro ante Rimón, que el Señor me perdone ese gesto. 19. Eliseo le dijo:
-Vete en paz.
Naamán se marchó. Y había caminado ya un buen trecho, 20. cuando Guejazí, criado del hombre de Dios Eliseo, pensó: Mi amo ha sido demasiado generoso con ese sirio, Naamán, no aceptando nada de lo que ofrecía. ¡Por la vida del Señor! Voy a correr detrás para que me dé algo. 21. Guejazí siguió a Naamán, y cuando éste lo vio correr tras él, bajó de la carroza para ir a su encuentro y lo saludó. Guejazí respondió al saludo, 22. y dijo:
-Mi amo me manda a decirte que precisamente en este momento se le han presentado dos muchachos de la serranía de Efraín, de la comunidad de los profetas; que hagas el favor de darme para ellos tres arrobas de plata y dos mudas de ropa. 23. Naamán dijo:
-Ten la bondad de tomar el doble.
