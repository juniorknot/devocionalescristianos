-Ya saben cómo me he comportado siempre con ustedes desde el primer día que pisé Asia. 19. He servido al Señor con toda humildad, con lágrimas y en todas las pruebas que me han causado las intrigas de los judíos. 20. No he dejado de hacer todo lo que pudiera ser útil: les prediqué y les enseñé tanto en público como en sus casas. 21. A judíos y griegos les he inculcado el arrepentimiento frente a Dios y la fe en nuestro Señor Jesús. 22. Ahora, encadenado por el Espíritu, me dirijo a Jerusalén sin saber lo que allí me sucederá. 23. Sólo sé que en cada ciudad el Espíritu Santo me asegura que me esperan cadenas y persecuciones. 24. Pero poco me importa la vida, con tal de completar mi carrera y el ministerio que recibí del Señor Jesús: anunciar la Buena Noticia de la gracia de Dios. 25. Ahora sé que ustedes, cuyo territorio he atravesado proclamando el reino, no volverán a verme. 26. Por eso hoy declaro que no soy responsable de la muerte de ninguno, 27. porque nunca dejé de anunciar plenamente el designio de Dios. 28. Cuídense ustedes y cuiden a todo el rebaño que el Espíritu Santo les encomendó como a pastores de la Iglesia de Dios, que Él adquirió pagando con su sangre. 29. Sé que después de mi partida se meterán entre ustedes lobos rapaces que no respetarán el rebaño. 30. Incluso de entre ustedes saldrán algunos que dirán cosas equivocadas para arrastrar tras de sí a los discípulos. 31. Por tanto, estén atentos y recuerden que durante tres años no he cesado de aconsejarlos con lágrimas ni de día ni de noche. 32. Ahora los encomiendo al Señor y al mensaje de su gracia, que tiene poder para hacerlos crecer y otorgar la herencia a todos los consagrados. 33. No he codiciado la plata ni el oro ni los vestidos de nadie. 34. Ustedes saben que con mis manos he atendido a las necesidades mías y de mis compañeros. 35. Les he enseñado siempre que, trabajando así, hay que ayudar a los débiles, recordando el dicho del Señor Jesús: más vale dar que recibir. 36. Dicho esto, se arrodilló con todos y oró. 37. Todos se pusieron a llorar; lo abrazaban y lo besaban afectuosamente, 38. entristecidos sobre todo por lo que había dicho, que no volverían a verlo.
