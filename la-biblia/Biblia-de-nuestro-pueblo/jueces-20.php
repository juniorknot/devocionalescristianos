<li class="hide-for-large"><a href="whatsapp://send?text=Jueces 20 (BNP)%0A%0A 1. La guerra
 Todos los israelitas, desde Dan hasta Berseba, incluido el país de Galaad, fueron como un solo hombre a reunirse en asamblea ante el Señor en Mispá. 2. Asistieron a la asamblea del pueblo de Dios los dignatarios del pueblo y todas las tribus de Israel: cuatrocientos mil soldados armados de espada. 3. Los benjaminitas se enteraron de que los israelitas habían ido a Mispá. Los israelitas empezaron:
-Ustedes dirán cómo se cometió ese crimen. 4. El levita, marido de la que había sido asesinada, respondió:
-Mi mujer y yo llegamos a Guibeá de Benjamín para pasar la noche. 5. Los del pueblo se levantaron contra mí, rodearon la casa de noche intentando matarme, y abusaron de mi mujer hasta hacerla morir. 6. Entonces tomé a la concubina, la despedacé y envié los trozos por toda la herencia de Israel, porque se había cometido un crimen infame en Israel. 7. Todos ustedes son israelitas: deliberen y tomen una decisión. 8. Todo el pueblo se puso en pie como un solo hombre, diciendo:
-Ninguno de nosotros marchará a su tienda ni se volverá a su casa. 9. Ahora vamos a actuar así contra Guibeá: sortearemos los que han de atacarla; 10. de todas las tribus de Israel tomaremos diez hombres de cada cien, cien de cada mil, mil de cada diez mil, para encargarse de los víveres del ejército que irá contra Guibeá de Benjamín a castigar como se merece esa infamia que han cometido en Israel. 11. Todos los israelitas, como un solo hombre, se reunieron contra la ciudad. 12. Entonces las tribus israelitas mandaron emisarios a la tribu de Benjamín a decirles:
-¿Qué explicación dan del crimen que se ha cometido entre ustedes? 13. Entreguen a esos pervertidos de Guibeá, para que los matemos y así se borre este crimen de en medio de Israel.
Pero los de Benjamín no quisieron hacer caso de sus hermanos los israelitas. 14. Desde sus ciudades se congregaron en Guibeá para ir a la guerra contra los israelitas. 15. De las ciudades de Benjamín se alistaron aquel día veintiséis mil hombres armados de espada, sin contar a los vecinos de Guibeá. 16. En todo aquel ejército se alistaron setecientos zurdos, hombres que manejaban tan bien la honda, que podían darle con la piedra a un cabello, sin fallar el tiro. 17. Los israelitas, excluidos los benjaminitas, alistaron cuatrocientos mil hombres armados de espada, todos ellos gente aguerrida. 18. Se pusieron en camino hacia Betel y consultaron a Dios:
-¿Quién de nosotros será el primero en subir a luchar contra los benjaminitas?
El Señor respondió:
-Judá será el primero. 19. Los israelitas se levantaron temprano y acamparon frente a Guibeá. 20. Salieron al combate contra Benjamín y formaron frente a Guibeá. 21. Pero los benjaminitas salieron de Guibeá y dejaron tendidos en tierra aquel día a veinte mil israelitas. 22. Los israelitas fueron a Betel a llorar ante el Señor hasta la tarde. Le consultaron:
-¿Volvemos a presentar batalla a nuestro hermano Benjamín?
El Señor respondió:
-Suban a atacarlo. 23. Entonces se rehicieron, volvieron a formar en orden de batalla en el mismo sitio que el día anterior y 24. se acercaron a los de Benjamín aquel segundo día. 25. Pero los de Benjamín salieron a su encuentro desde Guibeá aquel segundo día y dejaron tendidos en tierra otros dieciocho mil israelitas armados de espada. 26. Entonces subieron a Betel todos los israelitas, todo el ejército, a llorar allí, sentados ante el Señor. Ayunaron aquel día hasta la tarde, ofrecieron al Señor holocaustos y sacrificios de comunión 27. y le consultaron porque en aquella época estaba allí el arca de la alianza 28. y oficiaba Fineés, hijo de Eleazar, hijo de Aarón:
-¿Volvemos a salir al combate contra nuestro hermano Benjamín, o desistimos?
El Señor respondió:
-Ataquen, que mañana se lo entregaré. 29. Entonces pusieron emboscadas en torno a Guibeá 30. y marcharon contra Benjamín el tercer día, formando frente a Guibeá como las otras veces. 31. Los benjaminitas salieron a su encuentro, alejándose del pueblo, y como las otras veces, empezaron a destrozar y herir por los caminos, el que sube a Betel y el que va a Gabaón. Así mataron en campo abierto a unos treinta israelitas, 32. y comentaron:
-Ya están derrotados, como el primer día.
Pero es que los israelitas habían convenido:
-Emprenderemos la huida para alejarlos de la ciudad hacia los caminos. 33. El grueso del ejército se reorganizó en Baal-Tamar. Los que estaban emboscados salieron de sus posiciones desde el claro de Guibeá. 34. Diez mil hombres selectos de Israel llegaron delante de Guibeá, y se entabló un combate reñido, sin que los benjaminitas se dieran cuenta de que el desastre se les echaba encima. 35. El Señor los castigó ante Israel: aquel día los israelitas hicieron a Benjamín veinticinco mil cien bajas, todos soldados armados de espada. 36. Los benjaminitas se vieron derrotados. Los israelitas retrocedieron ante Benjamín, contando con la emboscada que habían tendido contra Guibeá. 37. Los de la emboscada asaltaron Guibeá rápidamente; fueron y pasaron a cuchillo a toda la población. 38. Los israelitas habían convenido con los de la emboscada en que, cuando hicieran subir una humareda desde el pueblo, 39. ellos presentarían batalla.
