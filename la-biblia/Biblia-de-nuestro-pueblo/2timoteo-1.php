<h4><strong>Salutación</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">1. </strong>
Saludo y acción de gracias
Pablo, apóstol de Cristo Jesús, por voluntad de Dios, según la promesa de vida cumplida en Cristo Jesús, </p>
<p style="style=text-align: justify;"><strong id="numeros">2. </strong>
al querido hijo Timoteo: Gracia, misericordia y paz de parte de Dios Padre y de Cristo Jesús Señor nuestro. </p>
<h4><strong>Testificando de Cristo</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">3. </strong>
Doy gracias al Dios de mis antepasados, a quien sirvo con conciencia limpia, siempre que te menciono en mis oraciones, noche y día. </p>
<p style="style=text-align: justify;"><strong id="numeros">4. </strong>
Me acuerdo siempre de las lágrimas que derramaste, y quisiera verte para llenarme de alegría. </p>
<p style="style=text-align: justify;"><strong id="numeros">5. </strong>
Recuerdo tu fe sincera, la que tuvo primero tu abuela Loide, después tu madre Eunice y ahora estoy seguro que también la tienes tú. </p>
<p style="style=text-align: justify;"><strong id="numeros">6. </strong>
Fiel a la Buena Noticia
Por eso te recuerdo que avives el don de Dios que recibiste por la imposición de mis manos. </p>
<p style="style=text-align: justify;"><strong id="numeros">7. </strong>
Porque el Espíritu que Dios nos ha dado no es un espíritu de cobardía, sino de fortaleza, amor y templanza. </p>
<p style="style=text-align: justify;"><strong id="numeros">8. </strong>
No te avergüences de dar testimonio de Dios, ni de mí, su prisionero; al contrario con la fuerza que Dios te da comparte conmigo los sufrimientos que es necesario padecer por la Buena Noticia. </p>
<p style="style=text-align: justify;"><strong id="numeros">9. </strong>
Él nos salvó y llamó, destinándonos a ser santos, no por mérito de nuestras obras, sino por su propia iniciativa y gracia, que se nos concede desde la eternidad en nombre de Cristo Jesús </p>
<p style="style=text-align: justify;"><strong id="numeros">10. </strong>
y que se manifiesta ahora por la aparición de nuestro salvador Cristo Jesús; quien ha destruido la muerte e iluminado la vida inmortal por medio de la Buena Noticia. </p>
<p style="style=text-align: justify;"><strong id="numeros">11. </strong>
De ella me han nombrado predicador, apóstol y maestro. </p>
<p style="style=text-align: justify;"><strong id="numeros">12. </strong>
Por esa causa padezco estas cosas, pero no me siento fracasado, porque sé en quién he puesto mi confianza y estoy convencido de que puede custodiar el bien que me ha encomendado hasta el último día. </p>
<p style="style=text-align: justify;"><strong id="numeros">13. </strong>
Consérvate fiel a las enseñanzas que me escuchaste, con la fe y el amor de Cristo Jesús. </p>
<p style="style=text-align: justify;"><strong id="numeros">14. </strong>
Y guarda el precioso depósito con la ayuda del Espíritu Santo que habita en nosotros. </p>
<p style="style=text-align: justify;"><strong id="numeros">15. </strong>
Estás enterado de que me han abandonado todos los de Asia, incluidos Figelo y Hermógenes. </p>
<p style="style=text-align: justify;"><strong id="numeros">16. </strong>
El Señor tenga piedad de la familia de Onesíforo, el cual muchas veces me alivió y no se avergonzó de visitar a un preso. </p>
<p style="style=text-align: justify;"><strong id="numeros">17. </strong>
Estando en Roma me buscó hasta encontrarme. </p>
<p style="style=text-align: justify;"><strong id="numeros">18. </strong>
El Señor le conceda alcanzar su misericordia en el día aquel. Tú conoces mejor que nadie los servicios que me prestó en Éfeso. </p>
