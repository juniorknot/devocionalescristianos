-Si al rey le agrada, que los judíos de Susa puedan prorrogar hasta mañana el cumplimiento del decreto. Y que cuelguen a los diez hijos de Amán. 14. El rey ordenó que se hiciese así: se prorrogó el decreto en Susa y colgaron a los diez hijos de Amán. 15. Así, los judíos de Susa se concentraron también el día catorce del mes de marzo. Mataron a otros trescientos hombres, pero no recogieron ningún botín. 16. Los demás judíos en las provincias del imperio se concentraron para defenderse, eliminando a sus enemigos; mataron a setenta y cinco mil adversarios, pero no tomaron botín. 17. Fiesta de Purim
 Eso fue el día trece del mes de marzo, y el día catorce descansaron, declarándolo día festivo. 18. En cambio, los judíos de Susa se reunieron los días trece y catorce; el día quince descansaron, declarándolo día festivo. 19. Por eso los judíos del campo, los que viven en los poblados, celebran como gran día festivo el catorce del mes de marzo, y se hacen regalos. 20. Mardoqueo puso todo esto por escrito, y mandó cartas a todos los judíos de todas las provincias del rey Asuero, próximos y lejanos, 21. encargándoles celebrar anualmente los días catorce y quince del mes de marzo, 22. por ser los días en los cuales los judíos quedaron libres de sus enemigos y el mes en que se les cambió la tristeza en alegría y el luto en fiesta. Que los declararan días festivos, que se hicieran regalos y dieran también a los pobres. 23. Los judíos, que ya habían empezado a hacerlo, aceptaron lo que les escribió Mardoqueo. 24. Porque Amán, hijo de Hamdatá, descendiente de Agag, el enemigo de los judíos, había hecho el sorteo, llamado pur, para eliminarlos y destruirlos; 25. pero cuando Ester se presentó al rey, el rey escribió un documento volviendo contra Amán el plan perverso que había tramado contra los judíos, y lo colgaron en la horca, a él y a sus hijos. 26. Por eso, esos días se llaman purim, de la palabra pur.
