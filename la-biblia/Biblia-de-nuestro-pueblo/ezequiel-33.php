El profeta como centinela
3,16-27 y 18
 Me dirigió la palabra el Señor: 2. -Hijo de hombre, habla así a tus compatriotas: Cuando yo envíe la espada contra una población y el vecindario escoja a uno del lugar y lo ponga de centinela; 3. si al divisar la espada que avanza contra la población da la alarma al vecindario a toque de trompeta, 4. el que oyendo el toque de trompeta no se ponga alerta, será responsable de su propia sangre cuando llegue la espada y lo arrebate. 5. Puesto que oyó el toque de trompeta y no se puso alerta, responderá de su propia sangre; si hubiera estado alerta, habría salvado la vida. 6. Pero si el centinela divisa la espada que avanza y no toca la trompeta, y el vecindario no se pone alerta, y llega la espada y arrebata a alguno de ellos, éstos mueren por su culpa, pero al centinela le pediré cuenta de la sangre. 7. A ti, Hijo de hombre, te he puesto de centinela en la casa de Israel; cuando escuches palabras de mi boca, les darás la alarma de mi parte. 8. Si yo digo al malvado: ¡Malvado, eres reo de muerte!, y tú no hablas poniendo en guardia al malvado para que cambie de conducta, el malvado morirá por su culpa, pero a ti te pediré cuenta de su sangre; 9. pero si tú pones en guardia al malvado para que cambie de conducta, y él no cambia de conducta, él morirá por su culpa y tú salvarás la vida. 10. Y tú, Hijo de hombre, dile a la casa de Israel: Ustedes piensan de este modo: Nuestros crímenes y nuestros pecados pesan sobre nosotros y por ellos nos consumimos, ¿podremos seguir con vida? 11. Pues diles: ¡Por mi vida! -oráculo del Señor-, juro que no quiero la muerte del malvado, sino que cambie de conducta y viva. ¡Conviértanse, cambien de conducta, malvados, y no morirán, casa de Israel! 12. Y tú, Hijo de hombre, di a tus compatriotas: Al justo no lo salvará su justicia si comete un delito, al malvado no lo condenará su maldad si se convierte de ella. Si el justo peca de nada le valdrá su buen comportamiento anterior. 13. Si digo al justo: vivirás, y él, confiado en su justicia, comete un delito, no se tendrá en cuenta su justicia, sino que morirá por el delito que cometió. 14. Si digo al malvado: morirás, y él se convierte de su pecado, practica el derecho y la justicia, 15. devuelve la prenda, restituye lo robado y sigue los preceptos de vida sin incurrir en delito, entonces vivirá y no morirá, 16. no se tendrá en cuenta ningún pecado de los que cometió; por haber practicado el derecho y la justicia vivirá. 17. Replicarán tus compatriotas: No es justo el proceder del Señor, cuando son ellos los que no proceden rectamente. 18. Si se pervierte el justo de su justicia y comete un delito, por él morirá. 19. Si el malvado se convierte de su maldad y practica la justicia y el derecho, por ellos vivirá. 20. ¿Insisten en decir que no es justo el proceder del Señor? A cada uno lo juzgaré según su conducta. 21. Llega el fugitivo
24,26-27
 El año duodécimo de nuestra deportación, el día cinco del mes décimo, se me presentó un fugitivo de Jerusalén y me dio esta noticia: Han destruido la ciudad. 22. La tarde anterior había venido sobre mí la mano del Señor, y permaneció hasta que el fugitivo se me presentó por la mañana; entonces se me abrió la boca y no volví a estar mudo. 23. En Jerusalén
