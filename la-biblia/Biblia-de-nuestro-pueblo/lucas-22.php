Y, en medio de la angustia, oraba más intensamente. Le corría el sudor como gotas de sangre cayendo al suelo.]] </p>
<p style="style=text-align: justify;"><strong id="numeros">45. </strong>
Se levantó de la oración, se acercó a sus discípulos y los encontró dormidos de tristeza; </p>
<p style="style=text-align: justify;"><strong id="numeros">46. </strong>
y les dijo:
-¿Por qué están dormidos? Levántense y oren para no sucumbir en la tentación. </p>
<h4><strong>Arresto de Jesús</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
Arresto de Jesús
Mt 26,47-56; Mc 14,43-52; cfr. Jn 18,2-12
Todavía estaba hablando, cuando llegó un gentío. El llamado Judas, uno de los Doce, se les adelantó, se acercó a Jesús y le besó. </p>
<p style="style=text-align: justify;"><strong id="numeros">48. </strong>
Jesús le dijo:
-Judas, ¿con un beso entregas al Hijo del Hombre? </p>
<p style="style=text-align: justify;"><strong id="numeros">49. </strong>
Viendo lo que iba a pasar, los que estaban con él dijeron:
-Señor, ¿usamos la espada? </p>
<p style="style=text-align: justify;"><strong id="numeros">50. </strong>
Uno de ellos dio un tajo al empleado del sumo sacerdote y le cortó la oreja derecha. </p>
<p style="style=text-align: justify;"><strong id="numeros">51. </strong>
Jesús le dijo:
-Ya basta.
Y tocándole la oreja, lo sanó. </p>
<p style="style=text-align: justify;"><strong id="numeros">52. </strong>
Después dijo Jesús a los sumos sacerdotes, guardias del templo y ancianos que habían venido a arrestarlo:
-¿Como si se tratara de un asaltante, han salido armados de espadas y palos? </p>
<p style="style=text-align: justify;"><strong id="numeros">53. </strong>
Diariamente estaba con ustedes en el templo y no me detuvieron. Pero ésta es la hora de ustedes, ahora son las tinieblas las que dominan. </p>
<h4><strong>Pedro niega a Jesús</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
Negaciones de Pedro
Mt 26,57s.69-75; Mc 14,53s.66-72;
cfr. Jn 18,12-18.25-27
Lo arrestaron, lo condujeron y lo metieron en casa del sumo sacerdote.
Pedro le seguía a distancia. </p>
<p style="style=text-align: justify;"><strong id="numeros">55. </strong>
Habían encendido fuego en medio del patio y estaban sentados alrededor; Pedro se sentó entre ellos. </p>
<p style="style=text-align: justify;"><strong id="numeros">56. </strong>
Una sirvienta lo vio sentado junto al fuego, lo miró fijamente y dijo:
-También éste estaba con él. </p>
<p style="style=text-align: justify;"><strong id="numeros">57. </strong>
Pedro lo negó diciendo:
-No lo conozco, mujer. </p>
<p style="style=text-align: justify;"><strong id="numeros">58. </strong>
Poco después otro lo vio y dijo:
-También tú eres uno de ellos.
Pedro respondió:
-No lo soy, hombre. </p>
<p style="style=text-align: justify;"><strong id="numeros">59. </strong>
Como una hora más tarde otro insistía:
-Realmente éste estaba con él, además, también es galileo. </p>
<p style="style=text-align: justify;"><strong id="numeros">60. </strong>
Pedro contestó:
-No sé lo que dices, hombre.
En ese momento, cuando aún estaba hablando, cantó el gallo. </p>
<p style="style=text-align: justify;"><strong id="numeros">61. </strong>
El Señor se volvió y miró a Pedro; éste recordó lo que le había dicho el Señor: Antes de que cante el gallo, me habrás negado tres veces. </p>
<p style="style=text-align: justify;"><strong id="numeros">62. </strong>
Salió afuera y lloró amargamente. </p>
<h4><strong>Jesús escarnecido y azotado</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">63. </strong>
Jesús ante el Consejo
Mt 26,57-68; Mc 14,53-65; cfr. Jn 18,19-24
Los que lo habían arrestado a Jesús se burlaban de él y lo golpeaban. </p>
<p style="style=text-align: justify;"><strong id="numeros">64. </strong>
Tapándole los ojos le decían:
-Adivina quién te ha pegado. </p>
<p style="style=text-align: justify;"><strong id="numeros">65. </strong>
Y le decían otras muchas injurias. </p>
<h4><strong>Jesús ante el concilio</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">66. </strong>
Al hacerse de día se reunieron los ancianos del pueblo, los sumos sacerdotes y letrados, lo condujeron ante el Consejo </p>
<p style="style=text-align: justify;"><strong id="numeros">67. </strong>
y le dijeron:
-Dinos si tú eres el Mesías.
Les respondió:
-Si se lo digo, no me creerán, </p>
<p style="style=text-align: justify;"><strong id="numeros">68. </strong>
y si pregunto, no me responderán. </p>
<p style="style=text-align: justify;"><strong id="numeros">69. </strong>
Pero en adelante el Hijo del Hombre estará sentado a la derecha de la Majestad de Dios. </p>
<p style="style=text-align: justify;"><strong id="numeros">70. </strong>
Dijeron todos:
-Entonces, ¿eres tú el Hijo de Dios?
Contestó:
-Tienen razón: Yo soy. </p>
<p style="style=text-align: justify;"><strong id="numeros">71. </strong>
Ellos dijeron:
-¿Qué falta nos hacen los testigos? Nosotros mismos lo hemos oído de su boca. </p>
