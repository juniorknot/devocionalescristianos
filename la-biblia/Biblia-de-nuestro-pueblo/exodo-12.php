<li class="hide-for-large"><a href="whatsapp://send?text=Éxodo 12 (BNP)%0A%0A 1. Pascua
Lv 23,5-8; Nm 9,1-14; Dt 16,1-8; Jos 5,10
 En aquellos días, el Señor dijo a Moisés y a Aarón en Egipto: 2. -Este mes será para ustedes el principal, será para ustedes el primer mes del año. 3. Digan a toda la asamblea de Israel: El diez de este mes cada uno se conseguirá un cordero o un cabrito para su familia, uno por casa. 4. Si la familia es demasiado pequeña para terminarlo, que se junte con el vecino de casa; el animal se repartirá según el número de comensales y lo que coma cada uno. 5. Será un animal sin defecto, macho, de un año, cordero o cabrito. 6. Lo guardarán hasta el día catorce del mes, y entonces toda la asamblea de Israel lo matará al atardecer. 7. Con algo de la sangre rociarán el marco de la puerta de la casa donde lo coman. 8. Esa noche comerán la carne, asada a fuego, acompañada de pan sin fermentar y verduras amargas. 9. No comerán de ella nada crudo ni cocido en agua, sino asado a fuego: con cabeza, patas y entrañas. 10. No dejarán restos para la mañana siguiente, y si sobra algo, lo quemarán. 11. Y lo comerán así: ceñidos con el cinturón, las sandalias en los pies, un bastón en la mano; y lo comerán rápidamente, porque es la Pascua del Señor. 12. Esa noche atravesaré todo el territorio egipcio dando muerte a todos sus primogénitos, de hombres y de animales; y daré un justo escarmiento a todos los dioses de Egipto. Yo soy el Señor. 13. La sangre será su contraseña de ustedes en las casas donde estén: cuando vea la sangre, pasaré de largo; no los tocará la plaga exterminadora cuando yo pase hiriendo a Egipto. 14. Este día será para ustedes memorable, en él celebrarán fiesta al Señor. Y lo harán de generación en generación como una ley perpetua. 15. Los Ázimos
Nm 9,11; 1 Cor 5,7s
 -Durante siete días comerán panes sin levadura; el día primero harán desaparecer de sus casas toda levadura, porque el que coma algo fermentado será excluido de Israel. Así del primero al séptimo día. 16. El día primero hay asamblea litúrgica y también el día séptimo: en esos días no trabajarán; solamente prepararán lo que haga falta a cada uno para comer. 17. Ustedes celebrarán la fiesta de los Ázimos, porque en ese día sacó el Señor a sus escuadrones de Egipto. Harán fiesta ese día: esto es ley perpetua para todas sus generaciones. 18. Desde la tarde del día catorce del mes primero a la tarde del día veintiuno comerán panes sin levadura; 19. durante siete días no habrá levadura en sus casas, porque quien coma algo fermentado será excluido de la asamblea de Israel, sea forastero o nativo. 20. Por lo tanto no coman nada fermentado, dondequiera que ustedes vivan, coman panes sin levadura. 21. Órdenes de Moisés
 Moisés llamó a todas las autoridades de Israel y les dijo:
-Elijan un cordero o un cabrito por familia y mátenlo para celebrar la pascua. 22. Tomen un manojo de ramas de hisopo, mójenlo en la sangre del plato y unten de sangre el marco de la puerta, y ninguno de ustedes salga por la puerta de casa hasta la mañana siguiente. 23. El Señor va a pasar hiriendo a Egipto, y cuando vea la sangre en el marco de la puerta, el Señor pasará de largo y no permitirá al exterminador entrar en sus casas para herir. 24. Cumplan este mandato del Señor: ésta es una ley perpetua para ustedes y sus hijos. 25. Y cuando entren en la tierra que el Señor les va a dar, según lo prometido, deberán seguir celebrando este rito. 26. Y cuando sus hijos les pregunten qué significa este rito, 27. les responderán: es el sacrificio de la Pascua del Señor. Él pasó en Egipto, junto a las casas de los israelitas, hiriendo a los egipcios y protegiendo nuestras casas. 28. El pueblo se inclinó en señal de adoración. Y los israelitas fueron y pusieron por obra lo que el Señor había mandado a Moisés y a Aarón. 29. Muerte de los primogénitos y salida de Israel
