-Espera, a ver si viene Elías a salvarlo. </p>
<p style="style=text-align: justify;"><strong id="numeros">50. </strong>
Jesús, lanzando un nuevo grito, entregó su espíritu. </p>
<p style="style=text-align: justify;"><strong id="numeros">51. </strong>
El velo del templo se rasgó en dos de arriba abajo, la tierra tembló, las piedras se partieron, </p>
<p style="style=text-align: justify;"><strong id="numeros">52. </strong>
los sepulcros se abrieron y muchos cadáveres de santos resucitaron. </p>
<p style="style=text-align: justify;"><strong id="numeros">53. </strong>
Y, cuando él resucitó, salieron de los sepulcros y se aparecieron a muchos en la Ciudad Santa. </p>
<p style="style=text-align: justify;"><strong id="numeros">54. </strong>
Al ver el terremoto y lo que sucedía, el centurión y la tropa que custodiaban a Jesús decían muy espantados:
-Realmente éste era Hijo de Dios. </p>
<p style="style=text-align: justify;"><strong id="numeros">55. </strong>
Estaban allí mirando a distancia muchas mujeres que habían acompañado y servido a Jesús desde Galilea. </p>
<p style="style=text-align: justify;"><strong id="numeros">56. </strong>
Entre ellas estaban María Magdalena, María, madre de Santiago y José, y la madre de los Zebedeos. </p>
<h4><strong>Jesús es sepultado</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
Sepultura de Jesús
Mc 15,42-47; Lc 23,50-56; cfr. Jn 19,38-42
Al atardecer llegó un hombre rico de Arimatea, llamado José, que también había sido discípulo de Jesús. </p>
<p style="style=text-align: justify;"><strong id="numeros">58. </strong>
Se presentó ante Pilato y le pidió el cadáver de Jesús. Pilato mandó que se lo entregaran. </p>
<p style="style=text-align: justify;"><strong id="numeros">59. </strong>
José lo tomó, lo envolvió en una sábana de lino limpia, </p>
<p style="style=text-align: justify;"><strong id="numeros">60. </strong>
y lo depositó en un sepulcro nuevo que se había excavado en la roca; después hizo rodar una gran piedra a la entrada del sepulcro y se fue. </p>
<p style="style=text-align: justify;"><strong id="numeros">61. </strong>
Estaban allí María Magdalena y la otra María sentadas frente al sepulcro. </p>
<h4><strong>La guardia ante la tumba</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">62. </strong>
Al día siguiente, el que sigue a la vigilia, se reunieron los sumos sacerdotes con los fariseos y fueron a Pilato </p>
<p style="style=text-align: justify;"><strong id="numeros">63. </strong>
a decirle:
-Señor, recordamos que aquel impostor dijo cuando aún vivía que resucitaría al tercer día. </p>
<p style="style=text-align: justify;"><strong id="numeros">64. </strong>
Manda que aseguren el sepulcro hasta el tercer día, no vayan a ir sus discípulos a robar el cadáver, para decir al pueblo que ha resucitado de entre los muertos. Este engaño sería peor que el primero. </p>
<p style="style=text-align: justify;"><strong id="numeros">65. </strong>
Les respondió Pilato:
-Ahí tienen una guardia: vayan y asegúrenlo como saben. </p>
<p style="style=text-align: justify;"><strong id="numeros">66. </strong>
Ellos aseguraron el sepulcro poniendo sellos en la piedra y colocando la guardia. </p>
