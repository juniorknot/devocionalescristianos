<li class="hide-for-large"><a href="whatsapp://send?text=Mateo 21 (BNP)%0A%0A 1. Entrada triunfal en Jerusalén
Mc 11,1-11; Lc 19,29-40; cfr. Jn 12,12-19
 Al llegar cerca de Jerusalén, entraron en Betfagé, junto al monte de los Olivos. Entonces Jesús envió a dos discípulos 2. diciéndoles:
-Vayan al pueblo de enfrente y enseguida encontrarán una burra atada y su cría junto a ella. Desátenla y tráiganla. 3. Si alguien les dice algo, ustedes le dirán que el Señor la necesita. Y enseguida los devolverá. 4. Esto sucedió para que se cumpliera lo anunciado por el profeta: 5. Digan a la ciudad de Sión:
mira a tu rey
que está llegando: humilde,
cabalgando un burrito, hijo de asna. 6. Fueron los discípulos y, siguiendo las instrucciones de Jesús, 7. le llevaron la burra y su cría. Echaron los mantos sobre ellos y el Señor se montó. 8. Una gran muchedumbre alfombraba con sus mantos el camino. Otros cortaban ramas de árbol y cubrían con ellas el camino. 9. La multitud, delante y detrás de él, aclamaba:
-¡Hosana al Hijo de David!
Bendito el que viene
en nombre del Señor.
¡Hosana en las alturas! 10. Cuando entró en Jerusalén, toda la población conmovida preguntaba:
-¿Quién es éste? 11. Y la multitud contestaba:
-Es el profeta Jesús, de Nazaret de Galilea. 12. Purifica el Templo
Mc 11,15-19; Lc 19,45-48; cfr. Jn 2,13-16
 Jesús entró en el templo y echó fuera a los que vendían y compraban en el templo, volcó las mesas de los cambistas y las sillas de los que vendían palomas. 13. Les dijo:
-Está escrito que mi casa será casa de oración, mientras que ustedes la han convertido en cueva de asaltantes. 14. En el templo se le acercaron ciegos y cojos y él los sanó. 15. Cuando los sumos sacerdotes y letrados vieron los milagros que hacía y a la gente gritando en el templo: ¡Hosana al Hijo de David!, se indignaron 16. y le dijeron:
-¿Oyes lo que están diciendo?
Jesús les contestó:
-Sí, ¿acaso nunca han oído aquel pasaje:
sacaré una alabanza de la boca
de criaturas y niños de pecho? 17. Dejándolos, salió de la ciudad y se dirigió a Betania, donde pasó la noche. 18. La higuera seca
Mc 11,12-14.20-25
 De mañana, cuando caminaba a la ciudad, sintió hambre, 19. al ver una higuera junto al camino, se acercó, pero no encontró más que hojas. Entonces le dijo:
-Jamás vuelvas a dar fruto.
En ese momento se secó la higuera. 20. Al verlo, los discípulos decían asombrados:
-¿Cómo es que la higuera se ha secado repentinamente? 21. Jesús les respondió:
-Les aseguro que, si tuvieran una fe firme, no sólo harían lo de la higuera, sino que podrían decir a ese monte que se quite de ahí y se tire al mar, y lo haría. 22. Y todo lo que pidan con fe lo recibirán. 23. La autoridad de Jesús
Mc 11,27-33; Lc 20,1-8
 Entró en el templo y se puso a enseñar. Se le acercaron los sumos sacerdotes y los ancianos del pueblo y le preguntaron:
-¿Con qué autoridad haces eso? ¿Quién te ha dado tal autoridad? 24. Jesús les contestó:
-Yo a mi vez les haré una pregunta, si me la responden, les diré con qué autoridad hago esto: 25. El bautismo de Juan, ¿de dónde procedía?, ¿del cielo o de los hombres?
Ellos discutían la cuestión: Si decimos que del cielo, nos dirá que por qué no le creímos; 26. si decimos que de los hombres, nos asusta la gente, porque todos tienen a Juan por profeta. 27. Así que respondieron a Jesús:
-No sabemos.
Él les replicó:
-Entonces yo tampoco les digo con qué autoridad lo hago. 28. Los dos hijos
 -A ver, ¿qué les parece? Un hombre tenía dos hijos. Se dirigió al primero y le dijo: Hijo, quiero que hoy vayas a trabajar a mi viña. 29. El hijo le respondió: No quiero; pero luego se arrepintió y fue. 30. Acercándose al segundo le dijo lo mismo. Éste respondió: Ya voy, señor; pero no fue. 31. ¿Cuál de los dos hizo la voluntad de su padre?
Le dijeron:
-El primero.
Y Jesús les contestó:
