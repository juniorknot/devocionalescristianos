<p style="style=text-align: justify;"><strong id="numeros">35. </strong>
recordaban que Dios era su Roca,
el Dios Altísimo, su Redentor. </p>
<p style="style=text-align: justify;"><strong id="numeros">36. </strong>
Lo adulaban con la boca,
le mentían con la lengua; </p>
<p style="style=text-align: justify;"><strong id="numeros">37. </strong>
su corazón no fue leal con él
ni fueron fieles a su alianza. </p>
<p style="style=text-align: justify;"><strong id="numeros">38. </strong>
Él, en cambio, enternecido,
perdonaba la culpa y no los destruía;
muchas veces reprimió su enojo
y no excitaba todo su furor, </p>
<p style="style=text-align: justify;"><strong id="numeros">39. </strong>
recordando que eran carne,
un aliento que se va y no retorna. </p>
<p style="style=text-align: justify;"><strong id="numeros">40. </strong>
¡Cómo se rebelaron en el desierto!
¡Cuánto lo irritaron en la estepa! </p>
<p style="style=text-align: justify;"><strong id="numeros">41. </strong>
Volvían a tentar a Dios,
irritando al Santo de Israel, </p>
<p style="style=text-align: justify;"><strong id="numeros">42. </strong>
sin acordarse de aquella mano
que un día los libró de la opresión, </p>
<p style="style=text-align: justify;"><strong id="numeros">43. </strong>
cuando hizo signos en Egipto
y portentos en la campiña de Soán. </p>
<p style="style=text-align: justify;"><strong id="numeros">44. </strong>
Él convirtió sus canales en sangre
y sus arroyos, para que no bebieran; </p>
<p style="style=text-align: justify;"><strong id="numeros">45. </strong>
les mandó tábanos que los picasen
y ranas que los destruyesen; </p>
<p style="style=text-align: justify;"><strong id="numeros">46. </strong>
entregó a la langosta su cosecha,
a saltamontes el fruto de su afán; </p>
<p style="style=text-align: justify;"><strong id="numeros">47. </strong>
asoló con granizo sus viñedos
y sus sicómoros con la escarcha; </p>
<p style="style=text-align: justify;"><strong id="numeros">48. </strong>
entregó sus ganados al pedrisco
y sus rebaños a los rayos; </p>
<p style="style=text-align: justify;"><strong id="numeros">49. </strong>
descargó sobre ellos su ira ardiente,
su enojo, su furor, su indignación:
una delegación de siniestros mensajeros, </p>
<p style="style=text-align: justify;"><strong id="numeros">50. </strong>
para prepararle el camino.
No salvó su vida de la muerte,
entregó sus vidas a la peste. </p>
<p style="style=text-align: justify;"><strong id="numeros">51. </strong>
Hirió a los primogénitos en Egipto,
primicias del vigor en las tiendas de Cam. </p>
<p style="style=text-align: justify;"><strong id="numeros">52. </strong>
Sacó como un rebaño a su pueblo,
los guió como un hato por el desierto; </p>
<p style="style=text-align: justify;"><strong id="numeros">53. </strong>
los condujo seguros, sin alarmas,
mientras el mar cubría a sus enemigos. </p>
<p style="style=text-align: justify;"><strong id="numeros">54. </strong>
Los llevó a su santa montaña,
al monte que su diestra conquistó. </p>
<p style="style=text-align: justify;"><strong id="numeros">55. </strong>
Expulsó ante ellos a los pueblos,
a cordel les asignó su heredad,
instaló en sus tiendas a las tribus de Israel. </p>
<p style="style=text-align: justify;"><strong id="numeros">56. </strong>
Pero ellos, rebeldes, tentaron al Dios Altísimo,
y no guardaron sus preceptos; </p>
<p style="style=text-align: justify;"><strong id="numeros">57. </strong>
desertaron, traidores como sus padres,
se torcieron como un arco mal tensado: </p>
<p style="style=text-align: justify;"><strong id="numeros">58. </strong>
lo irritaron con sus altozanos,
con sus ídolos excitaron sus celos. </p>
<p style="style=text-align: justify;"><strong id="numeros">59. </strong>
Lo oyó Dios y se indignó,
el Grande rechazó a Israel. </p>
<p style="style=text-align: justify;"><strong id="numeros">60. </strong>
Abandonó su morada de Siló,
la tienda plantada entre los humanos. </p>
<p style="style=text-align: justify;"><strong id="numeros">61. </strong>
Entregó su fortaleza a los conquistadores
y su ornato a la mano del adversario. </p>
<p style="style=text-align: justify;"><strong id="numeros">62. </strong>
Dejó su pueblo a merced de la espada,
indignado con su heredad. </p>
<p style="style=text-align: justify;"><strong id="numeros">63. </strong>
El fuego devoró a sus valientes,
y las doncellas no tuvieron cantos nupciales; </p>
<p style="style=text-align: justify;"><strong id="numeros">64. </strong>
sus sacerdotes caían a espada
y las viudas no cantaron lamentos fúnebres. </p>
<p style="style=text-align: justify;"><strong id="numeros">65. </strong>
Se despertó como de un sueño el Señor,
como soldado aturdido por el vino. </p>
<p style="style=text-align: justify;"><strong id="numeros">66. </strong>
Hirió al enemigo por la espalda
los dejó humillados para siempre. </p>
<p style="style=text-align: justify;"><strong id="numeros">67. </strong>
Rechazó la tienda de José
y no eligió a la tribu de Efraín; </p>
<p style="style=text-align: justify;"><strong id="numeros">68. </strong>
eligió a la tribu de Judá
y el monte Sión, su preferido. </p>
<p style="style=text-align: justify;"><strong id="numeros">69. </strong>
Se construyó un santuario como el cielo,
lo cimentó para siempre como la tierra. </p>
<p style="style=text-align: justify;"><strong id="numeros">70. </strong>
Eligió a David, su siervo,
sacándolo de los apriscos del rebaño; </p>
<p style="style=text-align: justify;"><strong id="numeros">71. </strong>
de andar tras las ovejas lo llevó
a pastorear a Jacob, su pueblo,
a Israel, su heredad. </p>
<p style="style=text-align: justify;"><strong id="numeros">72. </strong>
Los pastoreó con corazón íntegro,
los guió con mano experta. </p>
