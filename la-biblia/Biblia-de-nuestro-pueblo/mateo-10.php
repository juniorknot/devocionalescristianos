<li class="hide-for-large"><a href="whatsapp://send?text=Mateo 10 (BNP)%0A%0A 1. Los doce apóstoles
Mc 3,13-15; 6,7; Lc 9,1
 Y llamando a sus doce discípulos, les dio poder sobre los espíritus inmundos, para expulsarlos y para sanar toda clase de enfermedades y dolencias. 2. Mc 3,16-19; Lc 6,12-16; cfr. Hch 1,13
 Éstos son los nombres de los doce apóstoles: primero Simón, de sobrenombre Pedro, y Andrés su hermano; Santiago de Zebedeo y su hermano Juan; 3. Felipe y Bartolomé; Tomás y Mateo, el recaudador de impuestos; Santiago de Alfeo y Tadeo; 4. Simón el cananeo y Judas Iscariote, el que también le traicionó. 5. Misión de los doce apóstoles
 A estos doce los envió Jesús con las siguientes instrucciones:
-No se dirijan a países de paganos, no entren en ciudades de samaritanos; 6. vayan más bien a las ovejas descarriadas de la Casa de Israel. 7. Mc 6,8-13; Lc 9,2-6; 10,4-12
 Y de camino proclamen que el reino de los cielos está cerca. 8. Sanen a los enfermos, resuciten a los muertos, limpien a los leprosos, expulsen a los demonios. Gratuitamente han recibido, gratuitamente deben dar. 9. No lleven en el cinturón oro ni plata ni cobre, 10. ni provisiones para el camino ni dos túnicas ni sandalias ni bastón. Que el trabajador tiene derecho a su sustento. 11. Cuando entren en una ciudad o pueblo, pregunten por alguna persona respetable y quédense en su casa hasta que se vayan. 12. Al entrar en la casa, salúdenla invocando la paz; 13. si la casa lo merece, entrará en ella la paz; si no la merece, esa paz retornará a ustedes. 14. Si alguien no los recibe ni escucha el mensaje de ustedes, al salir de aquella casa o ciudad, sacúdanse el polvo de los pies. 15. Les aseguro que el día del juicio Sodoma y Gomorra serán tratadas con menos rigor que aquella ciudad. 16. Advertencia de persecuciones
Mc 13,9-13; Lc 21,12-19
 Miren, yo los envío como ovejas en medio de lobos: sean astutos como serpientes y sencillos como palomas. 17. ¡Cuidado con la gente!, porque los entregarán a los tribunales y los azotarán en sus sinagogas. 18. Los harán comparecer ante gobernadores y reyes por mi causa, para dar testimonio ante ellos y los paganos. 19. Cuando los entreguen, no se preocupen por lo que van a decir; 20. pues no serán ustedes los que hablen, sino el Espíritu de su Padre hablará por ustedes. 21. Un hermano entregará a la muerte a su hermano, un padre a su hijo; se rebelarán hijos contra padres y los matarán. 22. Serán odiados por todos a causa de mi nombre. Quien resista hasta el final se salvará. 23. Cuando los persigan en una ciudad, escapen a otra; les aseguro que no habrán recorrido todas las ciudades de Israel antes de que venga el Hijo del Hombre. 24. Lc 6,40; cfr. Jn 13,16; 15,20
 No está el discípulo por encima del maestro ni el sirviente por encima de su señor. 25. Al discípulo le basta ser como su maestro y al sirviente como su señor. Si al dueño de casa lo han llamado Belcebú, ¡cuánto más a los miembros de su casa! 26. Mc 4,22; Lc 12,2s; 8,17
 Por tanto no les tengan miedo. No hay nada encubierto que no se descubra, ni escondido que no se divulgue. 27. Lo que les digo de noche díganlo en pleno día; lo que escuchen al oído grítenlo desde los techos. 28. Lc 12,4-9
 No teman a los que matan el cuerpo y no pueden matar el alma; teman más bien al que puede arrojar cuerpo y alma en el infierno. 29. ¿No se venden dos gorriones por unas monedas? Sin embargo ni uno de ellos cae a tierra sin permiso del Padre de ustedes. 30. En cuanto a ustedes, hasta los pelos de su cabeza están contados. 31. Por tanto, no les tengan miedo, que ustedes valen más que muchos gorriones. 32. Al que me reconozca ante los hombres yo lo reconoceré ante mi Padre del cielo. 33. Pero el que me niegue ante los hombres, yo también lo negaré ante mi Padre del cielo. 34. La alternativa de Jesús
Lc 12,51-53
 No piensen que he venido a traer paz a la tierra. No vine a traer paz, sino espada. 35. Vine a enemistar a un hombre con su padre, a la hija con su madre, a la nuera con su suegra; 36. y así el hombre tendrá por enemigos a los de su propia casa. 37. Mc 8,34s; Lc 9,23s; 14,26s; 17,33; cfr. Jn 12,25
 Quien ame a su padre o a su madre más que a mí no es digno de mí; quien ame a su hijo o a su hija más que a mí no es digno de mí. 38. Quien no tome su cruz para seguirme no es digno de mí. 39. Quien se aferre a la vida la perderá, quien la pierda por mí la conservará. 40. Recompensas
