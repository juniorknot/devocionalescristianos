<li class="hide-for-large"><a href="whatsapp://send?text=Ester 4 (Junemann)%0A%0A 1. Consternación de los judíos. 
Mardoqueo y Ester deliberan. 
Pero Mardoqueo, sabiendo lo que pasaba, rasgó sus vestiduras y se vistió de saco y se espolvoreó con ceniza, y precipitándose por las calles de la ciudad; gritaba con voz grande: «Arrebátese a una gente que nada ha prevaricado». 2. Y vino hasta la puerta del rey, y detúvose; pues no le era lícito entrar en el aula teniendo saco y ceniza. 3. Y en toda región, donde se publicaban las letras, hubo vociferación y plañido y luto grande, para los judíos, y se echaron saco y ceniza. 4. Y entraron las doncellas y los eunucos de la reina, y anunciáronle; y conturbóse oyendo lo sucedido, y envió a que se vestimentara Mardoqueo y se quitara el saco; pero él no obedeció. 5. Mas Ester llamó a Acratayo, el eunuco de ella, que la asistía y envió a saber ella misma de Mardoqueo lo exacto. 6. --(a) 7. Y Mardoqueo le manifestó lo sucedido y la promesa que prometió Amán al rey: para el tesoro de talentos diez mil, para quien perdiese a los judíos; 8. y la copia del edicto publicado para perderlos diole para mostrar a Ester. Y díjole mandarla que entrando suplicase al rey a rogarle por el pueblo: «Acordándote de los días de tu humillación; cómo te criaste en mano mía; por esto, porque Amán, el segundo después del rey, ha hablado contra nosotros de muerte; invoca al Señor, y habla al rey acerca de nosotros, para librarnos de la muerte». 9. Y, entrando Acratayo le habló todas estas palabras. 10. Y dijo Ester a Acratayo: «Ve a Mardoqueo y di, 11. que las gentes todas del reino conocen que todo hombre o mujer que entrare al rey, en el aula interior, sin ser llamado, no tiene para él salvación, fuera del a quien extendiere el rey la áurea vara, porque sólo ese se salvará; y ya no he sido llamada a entrar al rey, en estos treinta días». 12. Y anunció Acratayo a Mardoqueo todas estas palabras de Ester. 13. Y dijo Mardoqueo a Acratayo: «Ve y dile»: «Ester, no te digas que salvarás sola en el reino, de entre todos los judíos. 14. Porque, si desoyeres en esta sazón, de otra parte vendrá auxilio y protección a los judíos; tú, empero, y la casa de tu padre, pereceréis. Y ¿quién sabe si para esta razón has sido entronizada?». 15. Y reenvió Ester al que llegó a ella, a Mardoqueo, diciendo: 16. «Caminando, congrega a los judíos, los de Susa, y ayunad por mí, y no comáis ni bebáis por tres días, noche y día; y yo y mis doncellas no comeremos, y entonces entraré al rey, contra la ley, aun cuando yo debiere perecer» 17. Y, caminando Mardoqueo, hizo cuanto le mandó Ester, y rogó a Señor acordándose de todas las obras del Señor y dijo:

