<li class="hide-for-large"><a href="whatsapp://send?text=Daniel 3 (Junemann)%0A%0A 1. La estatua áurea del rey. Los jóvenes en el horno. 
Favor del rey a los judíos. 
El año décimo octavo, Nabucodonosor rey, ordenando ciudades y regiones, y todos los habitantes, de India a Etiopía(a), hizo una imagen(b) áurea, la altura de ella de codos sesenta, y la anchura de ella de codos seis; y púsola en la llanura de la circunvecina(c) región de Babel. 2. Y Nabucodonosor, rey de reyes y dominador del orbe entero,envió a congregar todas las gentes, y tribus y lenguas: sátrapas, estrategos, gobernadores y soberanos, procuradores y los con poderes por la región; y a todos los por el orbe, para que viniesen a la dedicación de la imagen, la áurea, que puso Nabucodonosor, el rey. 3. Entonces congregáronse soberanos, estrategos, gobernadores, príncipes, soberanos grandes con poderes, y todos los potentados de las regiones para venir a la dedicación de la imagen que puso Nabucodonosor, el rey. Y pusiéronse los citados delante de la estatua que puso Nabucodonosor. 4. Y el pregonero pregonó a las turbas: «A vosotros se dice, gentes y regiones, pueblos y lenguas: 5. cuando oyereis la voz de la trompeta, flauta y cítara, arpa y salterio, sinfonía y todo linaje de músicas, postrándoos, adorad la imagen, la áurea que ha puesto Nabucodonosor, rey; 6. y todo el que no se postrare, no adorare, a la misma hora arrojado será en el horno del fuego, el encendido. 7. Y en aquel tiempo, cuando oyeron todas las gentes la voz de la trompeta, y flauta y cítara, y arpa y salterio y de todo son de músicas, postrándose todas las gentes, tribus y lenguas, adoraron la imagen, la áurea, que puso Nabucodonosor, el rey, en frente de ellas. 8. En aquel tiempo, acercándose varones caldeos, calumniaron a los judíos; 9. y empezando, dijeron a Nabucodonosor, el rey: «Señor, rey, ¡por el siglo vive! 10. Tú, rey, has ordenado y juzgado que todo hombre que oyere la voz de la trompeta, y flauta y arpa, cítara y salterio y de todo son de músicas, postrándose, adore la imagen, la áurea; 11. y el que no se postrare y adorare, será arrojado en el horno del fuego, el encendido. 12. Pero hay algunos varones judíos, que has constituido sobre la región de Babilonia: Sidrac, Misac, Abdenagó; esos varones no han temido tu mandato y tu numen no han venerado, y tu imagen, la áurea, que has puesto, no han adorado.» 13. Entonces Nabucodonosor, enfurecido de ira, mandó traer a Sidrac, Misac, Abdenagó; entonces los hombres fueron traídos al rey. 14. A los cuales mirando Nabucodonosor el rey, díjoles:

