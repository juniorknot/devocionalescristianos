<p style="style=text-align: justify;"><strong id="numeros">2. </strong>
El joven
Como un lirio entre los cardos es mi amada entre las jóvenes.</p>
<p style="style=text-align: justify;"><strong id="numeros">3. </strong>
La joven
Como el manzano más selecto del huerto es mi amante entre los jóvenes. Me siento bajo su sombra placentera y saboreo sus deliciosos frutos.</p>
<p style="style=text-align: justify;"><strong id="numeros">4. </strong>
Él me escolta hasta la sala de banquetes; es evidente lo mucho que me ama.</p>
<p style="style=text-align: justify;"><strong id="numeros">5. </strong>
Fortalézcanme con pasteles de pasas, refrésquenme con manzanas, porque desfallezco de amor.</p>
<p style="style=text-align: justify;"><strong id="numeros">6. </strong>
Su brazo izquierdo está debajo de mi cabeza, y su brazo derecho me abraza.</p>
<p style="style=text-align: justify;"><strong id="numeros">7. </strong>
Prométanme, oh mujeres de Jerusalén, por las gacelas y los ciervos salvajes, que no despertarán el amor hasta que llegue el momento apropiado.*</p>
<p style="style=text-align: justify;"><strong id="numeros">8. </strong>
¡Ay, oigo que viene mi amado! Viene saltando por las montañas, brincando por las colinas.</p>
<p style="style=text-align: justify;"><strong id="numeros">9. </strong>
Mi amante es como una gacela veloz o un venado joven. ¡Miren! Allí está, detrás del muro, asomándose por la ventana, mirando dentro de la habitación.</p>
<p style="style=text-align: justify;"><strong id="numeros">10. </strong>
Mi amante me dijo: «¡Levántate, amada mía! ¡Ven conmigo, mi bella mujer!</p>
<p style="style=text-align: justify;"><strong id="numeros">11. </strong>
Mira, el invierno se acabó y las lluvias ya pasaron.</p>
<p style="style=text-align: justify;"><strong id="numeros">12. </strong>
Las flores están brotando, ha llegado la temporada de los pájaros cantores;* y el arrullo de las tórtolas llena el aire.</p>
<p style="style=text-align: justify;"><strong id="numeros">13. </strong>
Las higueras comienzan a formar su fruto, y las vides fragantes están en flor. ¡Levántate, amada mía! ¡Ven conmigo, mi bella mujer!».</p>
<p style="style=text-align: justify;"><strong id="numeros">14. </strong>
El joven
Mi paloma se esconde detrás de las rocas, detrás de un peñasco en el precipicio. Déjame ver tu cara; déjame oír tu voz. Pues tu voz es agradable, y tu cara es hermosa.</p>
<p style="style=text-align: justify;"><strong id="numeros">15. </strong>
Las jóvenes de Jerusalén
Atrapen todos los zorros, esos zorros pequeños, antes de que arruinen el viñedo del amor, ¡porque las vides están en flor!</p>
<p style="style=text-align: justify;"><strong id="numeros">16. </strong>
La joven
Mi amado es mío, y yo soy suya. Él apacienta entre los lirios.</p>
<p style="style=text-align: justify;"><strong id="numeros">17. </strong>
Antes de que soplen las brisas del amanecer y huyan las sombras de la noche, regresa a mí, amor mío, como una gacela o un venado joven sobre montes empinados.*</p>
