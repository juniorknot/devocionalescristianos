SEÑOR, sé que tus ordenanzas son justas; me disciplinaste porque lo necesitaba.</p>
<p style="style=text-align: justify;"><strong id="numeros">76. </strong>
Ahora deja que tu amor inagotable me consuele, tal como le prometiste a este siervo tuyo.</p>
<p style="style=text-align: justify;"><strong id="numeros">77. </strong>
Rodéame con tus tiernas misericordias, para que viva, porque tus enseñanzas son mi deleite.</p>
<p style="style=text-align: justify;"><strong id="numeros">78. </strong>
Trae deshonra sobre los arrogantes que mintieron sobre mí; mientras tanto, me concentraré en tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">79. </strong>
Permite que esté unido a todos los que te temen, los que conocen tus leyes.</p>
<p style="style=text-align: justify;"><strong id="numeros">80. </strong>
Que sea intachable en guardar tus decretos; entonces nunca seré avergonzado.</p>
<h4><strong>Caf</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">81. </strong>
Caf
Estoy agotado de tanto esperar a que me rescates pero he puesto mi esperanza en tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">82. </strong>
Mis ojos se esfuerzan por ver cumplidas tus promesas, ¿cuándo me consolarás?</p>
<p style="style=text-align: justify;"><strong id="numeros">83. </strong>
Estoy arrugado como un odre ahumado, pero no me olvidé de obedecer tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">84. </strong>
¿Hasta cuándo tendré que esperar? ¿Cuándo castigarás a los que me persiguen?</p>
<p style="style=text-align: justify;"><strong id="numeros">85. </strong>
Estos arrogantes que odian tus enseñanzas cavaron hoyos profundos para atraparme.</p>
<p style="style=text-align: justify;"><strong id="numeros">86. </strong>
Todos tus mandatos son confiables. Protégeme de aquellos que me persiguen sin causa.</p>
<p style="style=text-align: justify;"><strong id="numeros">87. </strong>
Casi acaban conmigo, pero me negué a abandonar tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">88. </strong>
En tu amor inagotable, perdona mi vida; entonces podré continuar obedeciendo tus leyes.</p>
<h4><strong>Lámed</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">89. </strong>
Lámed
Tu eterna palabra, oh SEÑOR, se mantiene firme en el cielo.</p>
<p style="style=text-align: justify;"><strong id="numeros">90. </strong>
Tu fidelidad se extiende a cada generación, y perdura igual que la tierra que creaste.</p>
<p style="style=text-align: justify;"><strong id="numeros">91. </strong>
Tus ordenanzas siguen siendo verdad hasta el día de hoy, porque todo está al servicio de tus planes.</p>
<p style="style=text-align: justify;"><strong id="numeros">92. </strong>
Si tus enseñanzas no me hubieran sostenido con alegría, ya habría muerto en mi sufrimiento.</p>
<p style="style=text-align: justify;"><strong id="numeros">93. </strong>
Jamás olvidaré tus mandamientos, pues por medio de ellos me diste vida.</p>
<p style="style=text-align: justify;"><strong id="numeros">94. </strong>
Soy tuyo, ¡rescátame!, porque me he esforzado mucho en obedecer tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">95. </strong>
Aunque los malvados se escondan por el camino para matarme, con calma, mantendré mi mente puesta en tus leyes.</p>
<p style="style=text-align: justify;"><strong id="numeros">96. </strong>
Aun la perfección tiene sus límites, pero tus mandatos no tienen límite.</p>
<h4><strong>Mem</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">97. </strong>
Mem
¡Oh, cuánto amo tus enseñanzas! Pienso en ellas todo el día.</p>
<p style="style=text-align: justify;"><strong id="numeros">98. </strong>
Tus mandatos me hacen más sabio que mis enemigos, pues me guían constantemente.</p>
<p style="style=text-align: justify;"><strong id="numeros">99. </strong>
Así es, tengo mejor percepción que mis maestros, porque siempre pienso en tus leyes.</p>
<p style="style=text-align: justify;"><strong id="numeros">100. </strong>
Hasta soy más sabio que los ancianos, porque he obedecido tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">101. </strong>
Me negué a andar por cualquier mal camino, a fin de permanecer obediente a tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">102. </strong>
No me he apartado de tus ordenanzas, porque me has enseñado bien.</p>
<p style="style=text-align: justify;"><strong id="numeros">103. </strong>
¡Qué dulces son a mi paladar tus palabras!; son más dulces que la miel.</p>
<p style="style=text-align: justify;"><strong id="numeros">104. </strong>
Tus mandamientos me dan entendimiento; ¡con razón detesto cada camino falso de la vida!</p>
<h4><strong>Nun</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">105. </strong>
Nun
Tu palabra es una lámpara que guía mis pies y una luz para mi camino.</p>
<p style="style=text-align: justify;"><strong id="numeros">106. </strong>
Lo prometí una vez y volveré a prometerlo: obedeceré tus justas ordenanzas.</p>
<p style="style=text-align: justify;"><strong id="numeros">107. </strong>
He sufrido mucho, oh SEÑOR; restaura mi vida otra vez, como lo prometiste.</p>
<p style="style=text-align: justify;"><strong id="numeros">108. </strong>
SEÑOR, acepta mi ofrenda de alabanza y enséñame tus ordenanzas.</p>
<p style="style=text-align: justify;"><strong id="numeros">109. </strong>
Mi vida pende de un hilo constantemente, pero no dejaré de obedecer tus enseñanzas.</p>
<p style="style=text-align: justify;"><strong id="numeros">110. </strong>
Los malvados me han tendido sus trampas, pero no me apartaré de tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">111. </strong>
Tus leyes son mi tesoro; son el deleite de mi corazón.</p>
<p style="style=text-align: justify;"><strong id="numeros">112. </strong>
Estoy decidido a obedecer tus decretos hasta el final.</p>
<h4><strong>Sámec</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">113. </strong>
Sámec
Detesto a los que tienen divididas sus lealtades, pero amo tus enseñanzas.</p>
<p style="style=text-align: justify;"><strong id="numeros">114. </strong>
Tú eres mi refugio y mi escudo; tu palabra es la fuente de mi esperanza.</p>
<p style="style=text-align: justify;"><strong id="numeros">115. </strong>
Lárguense de mi vida, ustedes los de mente malvada, porque tengo la intención de obedecer los mandatos de mi Dios.</p>
<p style="style=text-align: justify;"><strong id="numeros">116. </strong>
¡SEÑOR, sostenme como prometiste para que viva! No permitas que se aplaste mi esperanza.</p>
<p style="style=text-align: justify;"><strong id="numeros">117. </strong>
Sostenme y seré rescatado; entonces meditaré continuamente en tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">118. </strong>
Pero has rechazado a todos los que se apartan de tus decretos, quienes no hacen más que engañarse a sí mismos.</p>
<p style="style=text-align: justify;"><strong id="numeros">119. </strong>
Desechas a los perversos de la tierra como si fueran desperdicios; ¡con razón me encanta obedecer tus leyes!</p>
<p style="style=text-align: justify;"><strong id="numeros">120. </strong>
Me estremezco en temor de ti; quedo en temor reverente ante tus ordenanzas.</p>
<h4><strong>Ayin</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">121. </strong>
Ayin
No me dejes a merced de mis enemigos, porque he hecho lo que es correcto y justo.</p>
<p style="style=text-align: justify;"><strong id="numeros">122. </strong>
Te ruego que me des seguridad de una bendición. ¡No permitas que los arrogantes me opriman!</p>
<p style="style=text-align: justify;"><strong id="numeros">123. </strong>
Mis ojos se esfuerzan por ver tu rescate, por ver la verdad de tu promesa cumplida.</p>
<p style="style=text-align: justify;"><strong id="numeros">124. </strong>
Soy tu siervo; trátame con tu amor inagotable y enséñame tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">125. </strong>
Da discernimiento a este siervo tuyo; entonces comprenderé tus leyes.</p>
<p style="style=text-align: justify;"><strong id="numeros">126. </strong>
SEÑOR, es tiempo de que actúes, porque esta gente malvada ha desobedecido tus enseñanzas.</p>
<p style="style=text-align: justify;"><strong id="numeros">127. </strong>
De verdad, amo tus mandatos más que el oro, incluso que el oro más fino.</p>
<p style="style=text-align: justify;"><strong id="numeros">128. </strong>
Cada uno de tus mandamientos es recto, por eso detesto todo camino falso.</p>
<h4><strong>Pe</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">129. </strong>
Pe
Tus leyes son maravillosas. ¡Con razón las obedezco!</p>
<p style="style=text-align: justify;"><strong id="numeros">130. </strong>
La enseñanza de tu palabra da luz, de modo que hasta los simples pueden entender.</p>
<p style="style=text-align: justify;"><strong id="numeros">131. </strong>
Abro la boca y jadeo anhelando tus mandatos.</p>
<p style="style=text-align: justify;"><strong id="numeros">132. </strong>
Ven y muéstrame tu misericordia, como lo haces con todos los que aman tu nombre.</p>
<p style="style=text-align: justify;"><strong id="numeros">133. </strong>
Guía mis pasos conforme a tu palabra, para que no me domine el mal.</p>
<p style="style=text-align: justify;"><strong id="numeros">134. </strong>
Rescátame de la opresión de la gente malvada, entonces podré obedecer tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">135. </strong>
Mírame con amor; enséñame tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">136. </strong>
Torrentes de lágrimas brotan de mis ojos, porque la gente desobedece tus enseñanzas.</p>
<h4><strong>Tsade</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">137. </strong>
Tsade
Oh SEÑOR, tú eres recto, y tus ordenanzas son justas.</p>
<p style="style=text-align: justify;"><strong id="numeros">138. </strong>
Tus leyes son perfectas y absolutamente confiables.</p>
<p style="style=text-align: justify;"><strong id="numeros">139. </strong>
La indignación me agobia, porque mis enemigos despreciaron tus palabras.</p>
<p style="style=text-align: justify;"><strong id="numeros">140. </strong>
Tus promesas fueron sometidas a una prueba rigurosa; por eso las amo tanto.</p>
<p style="style=text-align: justify;"><strong id="numeros">141. </strong>
Soy insignificante y despreciado, pero no olvido tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">142. </strong>
Tu justicia es eterna, y tus enseñanzas son totalmente ciertas.</p>
<p style="style=text-align: justify;"><strong id="numeros">143. </strong>
Cuando la presión y el estrés se me vienen encima, yo encuentro alegría en tus mandatos.</p>
<p style="style=text-align: justify;"><strong id="numeros">144. </strong>
Tus leyes siempre tienen razón; ayúdame a entenderlas para poder vivir.</p>
<h4><strong>Cof</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">145. </strong>
Cof
Oro con todo el corazón; ¡respóndeme, SEÑOR! Obedeceré tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">146. </strong>
A ti clamo; rescátame para que pueda obedecer tus leyes.</p>
<p style="style=text-align: justify;"><strong id="numeros">147. </strong>
Me levanto temprano, antes de que salga el sol; clamo en busca de ayuda y pongo mi esperanza en tus palabras.</p>
<p style="style=text-align: justify;"><strong id="numeros">148. </strong>
Me quedo despierto durante toda la noche, pensando en tu promesa.</p>
<p style="style=text-align: justify;"><strong id="numeros">149. </strong>
Oh SEÑOR, en tu fiel amor oye mi clamor, que el seguir tus ordenanzas me reanime.</p>
<p style="style=text-align: justify;"><strong id="numeros">150. </strong>
Los que no respetan la ley vienen a atacarme; viven alejados de tus enseñanzas.</p>
<p style="style=text-align: justify;"><strong id="numeros">151. </strong>
Pero tú estás cerca, oh SEÑOR, y todos tus mandatos son ciertos.</p>
<p style="style=text-align: justify;"><strong id="numeros">152. </strong>
Desde los primeros días sé que tus leyes durarán para siempre.</p>
<h4><strong>Resh</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">153. </strong>
Resh
Mira mi sufrimiento y rescátame, porque no me he olvidado de tus enseñanzas.</p>
<p style="style=text-align: justify;"><strong id="numeros">154. </strong>
¡Defiende mi caso, ponte de mi lado! Protege mi vida como lo prometiste.</p>
<p style="style=text-align: justify;"><strong id="numeros">155. </strong>
Los perversos están lejos de ser rescatados, porque no se interesan en tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">156. </strong>
SEÑOR, qué grande es tu misericordia; que el seguir tus ordenanzas me reanime.</p>
<p style="style=text-align: justify;"><strong id="numeros">157. </strong>
Muchos me persiguen y me molestan, sin embargo, no me he desviado de tus leyes.</p>
<p style="style=text-align: justify;"><strong id="numeros">158. </strong>
Ver a esos traidores me enferma el corazón, porque no les importa nada tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">159. </strong>
Mira cómo amo tus mandamientos, SEÑOR. Por tu amor inagotable, devuélveme la vida.</p>
<p style="style=text-align: justify;"><strong id="numeros">160. </strong>
La esencia misma de tus palabras es verdad; tus justas ordenanzas permanecerán para siempre.</p>
<h4><strong>Sin</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">161. </strong>
Shin
Gente poderosa me acosa sin razón, pero mi corazón tiembla sólo ante tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">162. </strong>
Me alegro en tu palabra como alguien que descubre un gran tesoro.</p>
<p style="style=text-align: justify;"><strong id="numeros">163. </strong>
Odio y detesto toda falsedad, pero amo tus enseñanzas.</p>
<p style="style=text-align: justify;"><strong id="numeros">164. </strong>
Te alabaré siete veces al día porque todas tus ordenanzas son justas.</p>
<p style="style=text-align: justify;"><strong id="numeros">165. </strong>
Los que aman tus enseñanzas tienen mucha paz y no tropiezan.</p>
<p style="style=text-align: justify;"><strong id="numeros">166. </strong>
Anhelo que me rescates, SEÑOR, por eso, he obedecido tus mandatos.</p>
<p style="style=text-align: justify;"><strong id="numeros">167. </strong>
Obedecí tus leyes, porque las amo mucho.</p>
<p style="style=text-align: justify;"><strong id="numeros">168. </strong>
Así es, obedezco tus leyes y tus mandamientos porque tú sabes todo lo que hago.</p>
<h4><strong>Tau</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">169. </strong>
Tau
Oh SEÑOR, escucha mi clamor; dame la capacidad de discernir que me prometiste.</p>
<p style="style=text-align: justify;"><strong id="numeros">170. </strong>
Escucha mi oración; rescátame como lo prometiste.</p>
<p style="style=text-align: justify;"><strong id="numeros">171. </strong>
Que la alabanza fluya de mis labios, porque me has enseñado tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">172. </strong>
Que mi lengua cante de tu palabra, porque todos tus mandatos son correctos.</p>
<p style="style=text-align: justify;"><strong id="numeros">173. </strong>
Tiéndeme una mano de ayuda, porque opté por seguir tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">174. </strong>
Oh SEÑOR, he anhelado que me rescates, y tus enseñanzas son mi deleite.</p>
<p style="style=text-align: justify;"><strong id="numeros">175. </strong>
Déjame vivir para que pueda alabarte, y que tus ordenanzas me ayuden.</p>
<p style="style=text-align: justify;"><strong id="numeros">176. </strong>
He andado descarriado como una oveja perdida; ven a buscarme, porque no me he olvidado de tus mandatos.</p>
