 Felices son los que en [su] camino están exentos de falta,  los que andan en la ley de Jehová. 2. Felices son los que observan sus recordatorios; con todo el corazón siguen buscándolo. 3. Realmente no han practicado ninguna injusticia.  En los caminos de él han andado. 4. Tú mismo has dado imperativamente tus órdenes para que se guarden cuidadosamente. 5. ¡Oh, que mis caminos fueran firmemente establecidos para guardar tus disposiciones reglamentarias! 6. En tal caso no quedaría avergonzado, cuando mirara a todos tus mandamientos. 7. Te elogiaré en rectitud de corazón, cuando aprenda tus decisiones judiciales justas. 8. Tus disposiciones reglamentarias continúo guardando.  Oh, no me dejes enteramente. 9. (ב[Behth])
 ¿Cómo limpiará un joven su senda? Manteniéndose alerta conforme a tu palabra. 10. Con todo mi corazón te he buscado.  No me descarríes de tus mandamientos. 11. En mi corazón he guardado cual tesoro tu dicho, a fin de no pecar contra ti. 12. Bendito eres, oh Jehová.  Enséñame tus disposiciones reglamentarias. 13. Con mis labios he declarado todas las decisiones judiciales de tu boca. 14. En el camino de tus recordatorios me he alborozado, así como por toda otra cosa valiosa. 15. En tus órdenes ciertamente me interesaré intensamente, y ciertamente miraré atento a tus sendas. 16. En cuanto a tus estatutos, mostraré tenerles cariño.  No olvidaré tu palabra. 17. (ג[Guí·mel])
 Obra apropiadamente para con tu siervo, para que yo viva y para que guarde tu palabra. 18. Destapa mis ojos, para que mire las cosas maravillosas procedentes de tu ley. 19. Solamente soy residente forastero en la tierra.  No ocultes de mí tus mandamientos. 20. Aplastada está mi alma de ansiar tus decisiones judiciales todo el tiempo. 21. Has reprendido a los malditos presuntuosos, que se descarrían de tus mandamientos. 22. Haz rodar de sobre mí el oprobio y el desprecio, porque he observado tus propios recordatorios. 23. Aun príncipes se han sentado; contra mí han hablado los unos con los otros.  En cuanto a tu siervo, él se interesa intensamente en tus disposiciones reglamentarias. 24. También, tus recordatorios son aquello con lo que estoy encariñado, como hombres de mi consejo. 25. (ד [Dá·leth])
 Mi alma ha estado pegada al mismísimo polvo.  Consérvame vivo conforme a tu palabra. 26. He declarado mis propios caminos, para que me respondas.  Enséñame tus disposiciones reglamentarias. 27. Hazme entender el camino de tus propias órdenes, para que me preocupe con tus obras maravillosas. 28. Mi alma se ha desvelado de desconsuelo.  Levántame conforme a tu palabra. 29. Aparta de mí aun el camino falso, y favoréceme con tu propia ley. 30. El camino de la fidelidad he escogido.  Tus decisiones judiciales he considerado apropiadas. 31. Me he apegado a tus recordatorios.  Oh Jehová, no me avergüences. 32. Correré por el mismísimo camino de tus mandamientos, porque haces que mi corazón tenga el espacio. 33. (ה [He’])
 Instrúyeme, oh Jehová, en el camino de tus disposiciones reglamentarias, para que lo observe hasta lo último. 34. Hazme entender, para que observe tu ley, y para que la guarde con todo el corazón. 35. Hazme pisar en el sendero de tus mandamientos, porque en él me he deleitado. 36. Inclina mi corazón a tus recordatorios, y no a las ganancias. 37. Haz que mis ojos pasen adelante para que no vean lo que es inútil; consérvame vivo en tu propio camino. 38. Realiza para con tu siervo tu dicho que [propende] al temor de ti. 39. Haz pasar mi oprobio, del cual he estado asustado, porque tus decisiones judiciales son buenas. 40. ¡Mira! He ansiado tus órdenes.  En tu justicia consérvame vivo. 41. (ו [Waw])
 Y vengan a mí tus bondades amorosas, oh Jehová, tu salvación conforme a tu dicho, 42. para que pueda responder con una palabra al que me vitupera, porque he confiado en tu palabra. 43. Y no quites de mi boca la palabra de verdad enteramente, porque he esperado tu propia decisión judicial. 44. Y ciertamente guardaré tu ley constantemente, hasta tiempo indefinido, aun para siempre. 45. Y ciertamente andaré de acá para allá en un lugar espacioso, porque he buscado aun tus órdenes. 46. También hablaré de seguro de tus recordatorios enfrente de reyes, y no me avergonzaré. 47. Y mostraré tener cariño a tus mandamientos que he amado. 48. Y alzaré las palmas de las manos a tus mandamientos que he amado, y ciertamente me interesaré intensamente en tus disposiciones reglamentarias. 49. (ז [Zá·yin])
 Acuérdate de la palabra a tu siervo, la cual me has hecho esperar. 50. Esta es mi consuelo en mi aflicción, porque tu propio dicho me ha conservado vivo. 51. Los presuntuosos mismos me han escarnecido hasta el extremo.  De tu ley no me he desviado. 52. Me he acordado de tus decisiones judiciales desde tiempo indefinido, oh Jehová, y hallo consuelo para mí mismo. 53. Un calor furioso mismo se ha apoderado de mí a causa de los inicuos, que están dejando tu ley. 54. Melodías han llegado a ser para mí tus disposiciones reglamentarias en la casa de mis residencias como forastero. 55. En la noche me he acordado de tu nombre, oh Jehová, para guardar tu ley. 56. Aun esto ha llegado a ser mío, porque tus órdenes he observado. 57. (ח [Jehth])
 Jehová es la parte que me corresponde; he prometido guardar tus palabras. 58. He ablandado tu rostro con todo [mi] corazón.  Muéstrame favor conforme a tu dicho. 59. He considerado mis caminos, para volver mis pies a tus recordatorios. 60. Me apresuré, y no me dilaté en guardar tus mandamientos. 61. Las mismas sogas de los inicuos me cercaron.  Tu ley no olvidé. 62. A medianoche me levanto para darte gracias por tus justas decisiones judiciales. 63. Soy socio de todos los que de veras te temen, y de los que guardan tus órdenes. 64. Tu bondad amorosa, oh Jehová, ha llenado la tierra.  Enséñame tus propias disposiciones reglamentarias.  65. (ט [Tehth])
 Realmente has tratado bien con tu siervo, oh Jehová, conforme a tu palabra. 66. Enséñame bondad, la sensatez y el conocimiento mismos, porque en tus mandamientos he ejercido fe. 67. Antes de estar bajo aflicción estuve pecando por equivocación, pero ahora he guardado tu mismísimo dicho. 68. Tú eres bueno y estás haciendo el bien.  Enséñame tus disposiciones reglamentarias. 69. Los presuntuosos me han embadurnado de falsedad.  En cuanto a mí, con todo [mi] corazón observaré tus órdenes. 70. El corazón de ellos se ha hecho insensible tal como grasa.  Yo, por mi parte, he estado encariñado con tu propia ley. 71. Bueno es para mí el que se me haya afligido, a fin de que aprenda tus disposiciones reglamentarias. 72. La ley de tu boca es buena para mí, en mayor grado que miles de piezas de oro y plata. 73. (י [Yohdh])
 Tus propias manos me han hecho, y procedieron a fijarme sólidamente.  Hazme entender, para que aprenda tus mandamientos. 74. Los que te temen son los que me ven y se regocijan, porque he esperado tu propia palabra. 75. Bien sé, oh Jehová, que tus decisiones judiciales son justicia, y que con fidelidad me has afligido. 76. Sirva tu bondad amorosa, por favor, para consolarme, según el dicho tuyo a tu siervo. 77. Vengan a mí tus misericordias, para que siga viviendo; porque con tu ley estoy encariñado. 78. Queden avergonzados los presuntuosos, porque sin causa me han extraviado.  En cuanto a mí, yo me intereso intensamente en tus órdenes. 79. Vuélvanse a mí los que te temen, los que también conocen tus recordatorios. 80. Resulte mi corazón exento de falta en tus disposiciones reglamentarias, a fin de que no quede avergonzado. 81. (כ [Kaf])
 Se ha consumido mi alma en su vivo deseo por tu salvación; he esperado tu palabra. 82. Se han consumido mis ojos en su vivo deseo por tu dicho, mientras digo: “¿Cuándo me consolarás?”. 83. Porque me he hecho como un odre en el humo.  No he olvidado tus disposiciones reglamentarias. 84. ¿Cuántos son los días de tu siervo? ¿Cuándo ejecutarás juicio contra los que me persiguen? 85. Los presuntuosos han excavado hoyos para atraparme, aquellos que no están en armonía con tu ley. 86. Todos tus mandamientos son fidelidad misma.  Sin causa me han perseguido. Oh, ayúdame. 87. En poco tiempo me hubieran exterminado en la tierra; pero yo mismo no dejé tus órdenes. 88. Conforme a tu bondad amorosa consérvame vivo, para que guarde el recordatorio de tu boca. 89. (ל [Lá·medh])
 Hasta tiempo indefinido, oh Jehová, tu palabra está estacionada en los cielos. 90. Tu fidelidad es para generación tras generación.  Has fijado sólidamente la tierra, para que siga subsistiendo. 91. Conforme a tus decisiones judiciales han subsistido [hasta] hoy, porque todos son siervos tuyos. 92. Si tu ley no hubiera sido aquello con lo que estoy encariñado, entonces habría perecido en mi aflicción. 93. Hasta tiempo indefinido no olvidaré tus órdenes, porque por ellas me has conservado vivo. 94. Tuyo soy. Oh sálvame, porque he buscado tus propias órdenes. 95. Me han esperado los inicuos, para destruirme.  Sigo mostrándome atento a tus recordatorios. 96. A toda perfección he visto fin.  Tu mandamiento es muy amplio. 97. (מ [Mem])
 ¡Cómo amo tu ley, sí! Todo el día ella es mi interés intenso. 98. Tu mandamiento me hace más sabio que mis enemigos, porque hasta tiempo indefinido es mío. 99. He llegado a tener más perspicacia que todos mis maestros, porque tus recordatorios me son de interés intenso. 100. Me porto con más entendimiento que hombres de más edad, porque he observado tus propias órdenes. 101. De toda senda mala he restringido mis pies, con el propósito de guardar tu palabra. 102. De tus decisiones judiciales no me he desviado, porque tú mismo me has instruido. 103. ¡Cuán suaves a mi paladar han sido tus dichos, más que la miel a mi boca! 104. Debido a tus órdenes me porto con entendimiento.  Por eso he odiado toda senda falsa. 105. (נ [Nun])
 Tu palabra es una lámpara para mi pie, y una luz para mi vereda. 106. He hecho una declaración jurada —y ciertamente la llevaré a cabo— de guardar tus justas decisiones judiciales. 107. He sido afligido en gran grado.  Oh Jehová, consérvame vivo conforme a tu palabra. 108. Por favor, complácete en las ofrendas voluntarias de mi boca, oh Jehová, y enséñame tus propias decisiones judiciales. 109. Mi alma está constantemente en la palma de mi mano; pero tu ley no he olvidado. 110. Los inicuos me han tendido una trampa, pero no me he desviado de tus órdenes. 111. He tomado tus recordatorios como posesión hasta tiempo indefinido, porque son el alborozo de mi corazón. 112. He inclinado mi corazón a poner por obra tus disposiciones reglamentarias hasta tiempo indefinido, hasta lo último. 113. (ס [Sá·mekj])
 A los de corazón irresoluto he odiado, pero tu ley he amado. 114. Tú eres mi escondrijo y mi escudo.  Tu palabra he esperado. 115. Apártense de mí, malhechores, para que yo observe los mandamientos de mi Dios. 116. Sosténme conforme a tu dicho, para que siga viviendo, y no me hagas quedar avergonzado por mi esperanza. 117. Susténtame, para que sea salvo, y fijaré la vista constantemente en tus disposiciones reglamentarias. 118. Has echado a un lado a todos los que se descarrían de tus disposiciones reglamentarias; porque su proceder mañoso es falsedad. 119. Como escoria espumajosa has hecho cesar a todos los inicuos de la tierra.  Por eso he amado tus recordatorios. 120. Por el pavor de ti mi carne ha tenido sensación de hormigueo; y a causa de tus decisiones judiciales he tenido miedo. 121. (ע ['Á·yin])
 He ejecutado juicio y justicia.  ¡Oh, no me abandones a los que me defraudan! 122. Sirve de fianza a tu siervo para lo que es bueno.  No me defrauden los presuntuosos. 123. Mis ojos mismos se han consumido del vivo deseo por tu salvación y por tu justo dicho. 124. Haz con tu siervo conforme a tu bondad amorosa, y enséñame tus propias disposiciones reglamentarias. 125. Soy tu siervo. Hazme entender, para que conozca tus recordatorios. 126. Es el tiempo para que Jehová obre.  Han quebrantado tu ley. 127. Por eso he amado tus mandamientos más que el oro, aun oro refinado. 128. Por eso he considerado rectas todas las órdenes respecto de todas las cosas; toda senda falsa he odiado. 129. (פ [Pe’])
 Tus recordatorios son maravillosos.  Por eso mi alma los ha observado. 130. La manifestación misma de tus palabras da luz, y hace entender a los inexpertos. 131. Mi boca he abierto bien, para poder jadear, porque tus mandamientos he anhelado. 132. Dirígete a mí y muéstrame favor, conforme a [tu] decisión judicial para con los que aman tu nombre. 133. Fija mis propios pasos sólidamente en tu dicho, y no se enseñoree dominantemente de mí ninguna clase de cosa perjudicial. 134. Redímeme de cualquier defraudador de la humanidad, y ciertamente guardaré tus órdenes. 135. Haz brillar tu propio rostro sobre tu siervo, y enséñame tus disposiciones reglamentarias. 136. Corrientes de agua han bajado corriendo de mis ojos debido al hecho de que no se ha guardado tu ley. 137. (צ [Tsa·dhéh])
Tú eres justo, oh Jehová, y tus decisiones judiciales son rectas. 138. Has ordenado tus recordatorios en justicia y en suma fidelidad. 139. Mi ardor me ha acabado, porque mis adversarios han olvidado tus palabras. 140. Tu dicho es muy refinado, y tu propio siervo lo ama. 141. Yo soy insignificante y despreciable.  Tus órdenes no he olvidado. 142. Tu justicia es una justicia hasta tiempo indefinido, y tu ley es la verdad. 143. Angustia y dificultad mismas me hallaron.  Con tus mandamientos estaba encariñado. 144. La justicia de tus recordatorios es hasta tiempo indefinido.  Hazme entender, para que siga viviendo. 145. (ק [Qohf])
 He llamado con todo [mi] corazón. Respóndeme, oh Jehová.  Tus disposiciones reglamentarias ciertamente observaré. 146. Te he invocado. ¡Oh sálvame! Y ciertamente guardaré tus recordatorios. 147. Me he levantado temprano en el crepúsculo matutino, para poder clamar por ayuda.  Tus palabras he esperado. 148. Mis ojos se han anticipado a las vigilias de la noche, [para que] me interese intensamente en tu dicho. 149. Oh, de veras oye mi propia voz conforme a tu bondad amorosa.  Oh Jehová, conforme a tu decisión judicial consérvame vivo. 150. Los que siguen tras conducta relajada se han acercado; se han alejado mucho de tu propia ley. 151. Tú estás cerca, oh Jehová, y todos tus mandamientos son la verdad. 152. Hace mucho que he conocido algunos de tus recordatorios, porque hasta tiempo indefinido los has fundado. 153. (ר [Rehsch])
 Oh, ve mi aflicción, y líbrame; porque no he olvidado tu propia ley. 154. Oh, de veras conduce mi causa judicial y recóbrame; consérvame vivo de acuerdo con tu dicho. 155. La salvación está lejos de los inicuos, porque no han buscado tus propias disposiciones reglamentarias. 156. Muchas son tus misericordias, oh Jehová.  Conforme a tus decisiones judiciales, oh consérvame vivo. 157. Mis perseguidores y mis adversarios son muchos.  De tus recordatorios no me he desviado. 158. He visto a los que son traicioneros en los tratos, y de veras siento asco, porque no han guardado tu propio dicho. 159. Oh, ve que yo he amado tus propias órdenes.  Oh Jehová, conforme a tu bondad amorosa consérvame vivo. 160. La sustancia de tu palabra es verdad, y toda justa decisión judicial tuya es hasta tiempo indefinido. 161. (ש [Sin] o [Schin])
 Los príncipes mismos me han perseguido sin causa alguna, pero mi corazón ha sentido pavor ante tus propias palabras. 162. Ando alborozado a causa de tu dicho, tal como uno hace al hallar mucho despojo. 163. La falsedad he odiado, y de veras sigo detestándola.  Tu ley he amado. 164. Siete veces al día te he alabado a causa de tus justas decisiones judiciales. 165. Paz abundante pertenece a los que aman tu ley, y no hay para ellos tropiezo. 166. He esperado de ti la salvación, oh Jehová, y he puesto por obra tus propios mandamientos. 167. Mi alma ha guardado tus recordatorios, y los amo en sumo grado. 168. He guardado tus órdenes y tus recordatorios, porque todos mis caminos están enfrente de ti. 169. (ת [Taw])
 Que mi clamor rogativo se acerque delante de ti, oh Jehová.  Conforme a tu palabra, oh hazme entender. 170. Entre mi petición de favor delante de ti.  Conforme a tu dicho, oh líbrame. 171. Que mis labios hagan salir burbujeando alabanza, pues tú me enseñas tus disposiciones reglamentarias. 172. Cante mi lengua tu dicho, porque todos tus mandamientos son justicia. 173. Sirva tu mano para ayudarme, porque tus órdenes he escogido. 174. He ansiado tu salvación, oh Jehová, y con tu ley estoy encariñado. 175. Siga mi alma viviendo y alabándote, y ayúdenme tus propias decisiones judiciales. 176. He andado errante como una oveja perdida. Oh busca a tu siervo, porque no he olvidado tus propios mandamientos. ... %0A%0Ahttp://bt3.me/eFs"><span class="icon-whatsapp"></span> WHATSAPP</a></li>
