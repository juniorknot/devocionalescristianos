 Envió de nuevo otros siervos,  más que los primeros;  e hicieron con ellos lo mismo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 Finalmente les envió su hijo,  diciendo:  "Tendrán respeto a mi hijo". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 Pero los labradores,  cuando vieron al hijo,  dijeron entre sí:  "Este es el heredero;  venid,  matémoslo y apoderémonos de su heredad". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 Y tomándolo,  lo echaron fuera de la viña y lo mataron.[24] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
 Cuando venga,  pues,  el señor de la viña,  ¿qué hará a aquellos labradores? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 Le dijeron:  
 --A los malos destruirá sin misericordia,  y arrendará su viña a otros labradores que le paguen el fruto a su tiempo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 Jesús les preguntó:  
 --¿Nunca leísteis en las Escrituras:  
 ""La piedra que desecharonlos edificadores 
 ha venido a ser cabeza del ángulo.  
 El Señor ha hecho esto,  
 y es cosa maravillosa a nuestros ojos?"[25]   </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 "Por tanto,  os digo que el reino de Dios será quitado de vosotros y será dado a gente que produzca los frutos de él. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 El que caiga sobre esta piedra será quebrantado,  y sobre quien ella caiga será desmenuzado.[26] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 Al oir sus parábolas,  los principales sacerdotes y los fariseos entendieron que hablaba de ellos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 Pero al buscar cómo echarle mano,  temían al pueblo,  porque este lo tenía por profeta. </p>
                                                        
