 tu Rey viene a ti,  
 manso y sentado sobre un asno,  
 sobre un pollino,hijo de animal de carga".[5] 6. Entonces los discípulos fueron e hicieron como Jesús les mandó. 7. Trajeron el asna y el pollino;  pusieron sobre ellos sus mantos,  y él se sentó encima. 8. La multitud,  que era muy numerosa,  tendía sus mantos en el camino;  otros cortaban ramas de los árboles y las tendían en el camino.[6] 9. Y la gente que iba delante y la que iba detrás aclamaba,  diciendo:  "¡Hosana[7]  al Hijo de David!  ¡Bendito el que viene en el nombre del Señor!  ¡Hosana en las alturas!" 10. Cuando entró él en Jerusalén,  toda la ciudad se agitó,  diciendo:  
 --¿Quién es este? 11. Y la gente decía:  
 --Este es Jesús,  el profeta,  el de Nazaret de Galilea. 12. Entró Jesús en el templo de Dios y echó fuera a todos los que vendían y compraban en el templo;  volcó las mesas de los cambistas y las sillas de los que vendían palomas,[8] 13. y les dijo:  "Escrito está:  "Mi casa,  casa de oración será llamada",[9]  pero vosotros la habéis hecho cueva de ladrones".[10] 14. Y en el templo se le acercaron ciegos y cojos,  y los sanó. 15. Pero los principales sacerdotes y los escribas,  viendo las maravillas que hacía y a los muchachos aclamando en el templo y diciendo:  "¡Hosana al Hijo de David!",  se enojaron 16. y le dijeron:  
 --¿Oyes lo que estos dicen?  
 Jesús les dijo:  
 --Sí.  ¿Nunca leísteis:  
 ""De la boca de los niñosy de los que aún maman,  
 perfeccionaste la alabanza"?[11] 17. Y dejándolos,  salió fuera de la ciudad,  a Betania,[12]  y se quedó allí. 18. Por la mañana,  volviendo a la ciudad,  tuvo hambre. 19. Viendo una higuera cerca del camino,  se acercó,  pero no halló nada en ella,  sino hojas solamente,[13]  y le dijo:  
 --¡Nunca jamás nazca de ti fruto!  
 Y al instante la higuera se secó. 20. Al ver esto los discípulos,  decían asombrados:  
 --¿Cómo es que se secó en seguida la higuera? 21. Respondiendo Jesús,  les dijo:  
