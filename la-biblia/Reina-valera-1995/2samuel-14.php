 --Habla --respondió el rey. 13. La mujer añadió:  
 --¿Por qué,  pues,  has pensado tú cosa semejante contra el pueblo de Dios?  Porque diciendo el rey estas cosas se confiesa culpable él mismo,  por cuanto el rey no deja volver a su desterrado. 14. Todos de cierto morimos y somos como agua derramada en tierra que no puede volver a recogerse.  Ni Dios quita la vida,  sino que provee medios para que el desterrado no siga alejado de él. 15. Si yo he venido ahora para decir esto al rey,  mi señor,  es porque el pueblo me ha atemorizado.  Y tu sierva pensó:  "Hablaré ahora al rey;  quizá haga lo que su sierva le diga, 16. pues el rey me oirá y librará a su sierva de manos del hombre que quiere extirparme a mí,  junto con mi hijo,  de la heredad de Dios". 17. Tu sierva dice pues:  "Sea ahora de consuelo la respuesta de mi señor,  el rey,  pues mi señor,  el rey,  es como un ángel de Dios para discernir entre lo bueno y lo malo.  Y que Jehová,  tu Dios,  sea contigo". 18. Entonces David respondió a la mujer:  
