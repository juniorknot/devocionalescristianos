 --Dejad a los niños venir a mí y no se lo impidáis,  porque de los tales es el reino de Dios. 17. De cierto os digo que el que no recibe el reino de Dios como un niño,  no entrará en él.[15] 18. Un dignatario[16]  le preguntó,  diciendo:  
 --Maestro bueno,  ¿qué haré para heredar la vida eterna?[17] 19. Jesús le dijo:  
 --¿Por qué me llamas bueno?  Nadie es bueno,  sino solo Dios. 20. Los mandamientos sabes:  "No adulterarás;  no matarás;  no hurtarás;  no dirás falso testimonio;  honra a tu padre y a tu madre".[18] 21. Él dijo:  
 --Todo esto lo he guardado desde mi juventud. 22. Al oir esto,  Jesús le dijo:  
 --Aún te falta una cosa:  vende todo lo que tienes y dalo a los pobres,  y tendrás tesoro en el cielo;  y ven,  sígueme.[19] 23. Entonces él,  oyendo esto,  se puso muy triste porque era muy rico. 24. Al ver Jesús que se había entristecido mucho,  dijo:  
 --¡Cuán difícilmente entrarán en el reino de Dios los que tienen riquezas![20] 25. Porque es más fácil que pase un camello por el ojo de una aguja que un rico entre en el reino de Dios.[21] 26. Los que oyeron esto dijeron:  
 --¿Quién,  pues,  podrá ser salvo? 27. Él les dijo:  
 --Lo que es imposible para los hombres,  es posible para Dios. 28. Entonces Pedro dijo:  
