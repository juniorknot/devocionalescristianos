 Mientras oraba,  la apariencia de su rostro cambió[22]  y su vestido se volvió blanco y resplandeciente. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">30. </strong>
 Y dos varones hablaban con él,  los cuales eran Moisés y Elías.[23] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">31. </strong>
 Estos aparecieron rodeados de gloria;  y hablaban de su partida,[24]  que Jesús iba a cumplir en Jerusalén. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">32. </strong>
 Pedro y los que lo acompañaban estaban rendidos de sueño;  pero,  permaneciendo despiertos,  vieron la gloria de Jesús y a los dos varones que estaban con él. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">33. </strong>
 Y sucedió que,  mientras estos se alejaban de él,  Pedro dijo a Jesús:  
 --Maestro,  bueno es para nosotros estar aquí.  Hagamos tres enramadas,  una para ti,  una para Moisés y una para Elías.  
 Pero no sabía lo que decía. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">34. </strong>
 Mientras él decía esto,  vino una nube que los cubrió;  y tuvieron temor al entrar en la nube.[25] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">35. </strong>
 Y vino una voz desde la nube,  que decía:  "Este es mi Hijo amado;  a él oíd".[26] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
 Cuando cesó la voz,  Jesús se encontraba solo.[27]  Ellos callaron,  y por aquellos días no dijeron nada a nadie de lo que habían visto. </p>
                                                    <h4><strong>Jesús sana a un muchacho endemoniado</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 Al día siguiente,  cuando descendieron del monte,  una gran multitud les salió al encuentro. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 Y un hombre de la multitud clamó diciendo:  
 --Maestro,  te ruego que veas a mi hijo,  pues es el único que tengo; </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 y sucede que un espíritu lo toma y,  de repente,  lo hace gritar,  lo sacude con violencia,  lo hace echar espuma y,  estropeándolo,  a duras penas se aparta de él.[28] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
 Rogué a tus discípulos que lo echaran fuera,  pero no pudieron. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 Respondiendo Jesús,  dijo:  
 --¡Generación incrédula y perversa![29]  ¿Hasta cuándo he de estar con vosotros y os he de soportar?  Trae acá a tu hijo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 Mientras se acercaba el muchacho,  el demonio lo derribó y lo sacudió con violencia;  pero Jesús reprendió al espíritu impuro,  sanó al muchacho y se lo devolvió a su padre. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 Y todos se admiraban de la grandeza de Dios.  
 Estando todos maravillados de todas las cosas que hacía,  dijo a sus discípulos: </p>
                                                    <h4><strong>Jesús anuncia otra vez su muerte</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 --Haced que os penetren bien en los oídos estas palabras,  porque acontecerá que el Hijo del hombre será entregado en manos de hombres.[30] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 Pero ellos no entendían estas palabras,  pues les estaban veladas para que no las entendieran;  y temían preguntarle sobre esas palabras. </p>
                                                    <h4><strong>¿Quién es el mayor?</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 Entonces entraron en discusión sobre quién de ellos sería el mayor.[31] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
 Jesús,  percibiendo los pensamientos de sus corazones,  tomó a un niño,[32]  lo puso junto a sí </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 y les dijo:  
 --Cualquiera que reciba a este niño en mi nombre,  a mí me recibe;  y cualquiera que me recibe a mí,  recibe al que me envió,[33]  porque el que es más pequeño entre todos vosotros,  ese es el más grande. </p>
                                                    <h4><strong>El que no es contra nosotros, por nosotros es</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 Entonces respondiendo Juan,  dijo:  
 --Maestro,  hemos visto a uno que echaba fuera demonios en tu nombre;  y se lo prohibimos,  porque no sigue con nosotros. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 Jesús le dijo:  
 --No se lo prohibáis,  porque el que no está contra nosotros,  por nosotros está.[34] </p>
                                                    <h4><strong>Jesús reprende a Jacobo y a Juan</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 [35]  Cuando se cumplió el tiempo en que él había de ser recibido arriba,  afirmó su rostro para ir a Jerusalén. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 Y envió mensajeros delante de él,  los cuales fueron y entraron en una aldea de los samaritanos para hacerle preparativos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 Pero no lo recibieron,  porque su intención era ir a Jerusalén.[36] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 Al ver esto,  Jacobo y Juan,  sus discípulos,  le dijeron:  
 --Señor,  ¿quieres que mandemos que descienda fuego del cielo,[37]  como hizo Elías,[38]  y los consuma? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 Entonces,  volviéndose él,  los reprendió diciendo:[39]  
 --Vosotros no sabéis de qué espíritu sois, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 porque el Hijo del hombre no ha venido para perder las almas de los hombres,  sino para salvarlas.  
 Y se fueron a otra aldea. </p>
                                                    <h4><strong>Los que querían seguir a Jesús</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 Yendo por el camino,  uno le dijo:  
 --Señor,  te seguiré adondequiera que vayas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
 Jesús le dijo:  
 --Las zorras[40]  tienen guaridas y las aves de los cielos nidos,  pero el Hijo del hombre no tiene donde recostar la cabeza. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">59. </strong>
 Y dijo a otro:  
 --Sígueme.  
 Él le respondió:  
 --Señor,  déjame que primero vaya y entierre a mi padre. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">60. </strong>
 Jesús le dijo:  
 --Deja que los muertos entierren a sus muertos;[41]  pero tú vete a anunciar el reino de Dios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">61. </strong>
 Entonces también dijo otro:  
 --Te seguiré,  Señor;  pero déjame que me despida primero de los que están en mi casa. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">62. </strong>
 Jesús le contestó:  
 --Ninguno que,  habiendo puesto su mano en el arado,  mira hacia atrás[42]  es apto para el reino de Dios. </p>
                                                        
