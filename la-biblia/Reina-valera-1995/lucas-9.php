 --No toméis nada para el camino:  ni bastón,  ni alforja,  ni pan,  ni dinero;  ni llevéis dos túnicas.[2] 4. En cualquier casa donde entréis,  quedad allí,  y de allí salid. 5. Dondequiera que no os reciban,  salid de aquella ciudad y sacudid el polvo de vuestros pies[3]  en testimonio contra ellos. 6. Y saliendo,  pasaban por todas las aldeas anunciando el evangelio y sanando por todas partes. 7. Herodes,[4]  el tetrarca,  oyó de todas las cosas que hacía Jesús,  y estaba perplejo,  porque decían algunos:  "Juan ha resucitado de los muertos"; 8. otros:  "Elías ha aparecido";  y otros:  "Algún profeta de los antiguos ha resucitado".[5] 9. Y dijo Herodes:  
 --A Juan yo lo hice decapitar;  ¿quién,  pues,  es este de quien oigo tales cosas?  
 Y procuraba verlo.[6] 10. [7]  Al regresar[8]  los apóstoles,  le contaron todo lo que habían hecho.  Y tomándolos,  se retiró aparte,  a un lugar desierto de la ciudad llamada Betsaida.[9] 11. Cuando la gente lo supo,  lo siguió;  y él los recibió,  les hablaba del reino de Dios y sanaba a los que necesitaban ser curados. 12. Pero el día comenzaba a declinar.  Acercándose los doce,  le dijeron:  
 --Despide a la gente,  para que vayan a las aldeas y campos de alrededor y se alojen y encuentren alimentos,  porque aquí estamos en lugar desierto. 13. Él les dijo:  
 --Dadles vosotros de comer.  
 Dijeron ellos:  
 --No tenemos más que cinco panes y dos peces,  a no ser que vayamos nosotros a comprar alimentos para toda esta multitud. 14. Eran como cinco mil hombres.  Entonces dijo a sus discípulos:  
 --Hacedlos sentar en grupos de cincuenta. 15. Así lo hicieron,  haciéndolos sentar a todos. 16. Y tomando los cinco panes y los dos peces,  levantó los ojos al cielo,  los bendijo,[10]  los partió y dio a sus discípulos para que los pusieran delante de la gente. 17. Comieron todos y se saciaron;  y recogieron lo que les sobró:  doce cestas de pedazos.[11] 18. Aconteció que mientras Jesús oraba aparte,[12]  estaban con él los discípulos;  y les preguntó,  diciendo:  
 --¿Quién dice la gente que soy yo? 19. Ellos respondieron:  
 --Unos,  Juan el Bautista;  otros,  Elías;  y otros,  que algún profeta de los antiguos ha resucitado.[13] 20. Él les dijo:  
 --¿Y vosotros,  quién decís que soy?  
 Entonces,  respondiendo Pedro,  dijo:  
 --El Cristo de Dios.[14] 21. Pero él les mandó que a nadie dijeran esto,[15]  encargándoselo rigurosamente, 22. y diciendo:  
 --Es necesario que el Hijo del hombre padezca muchas cosas y sea desechado por los ancianos,  por los principales sacerdotes y por los escribas,  y que sea muerto y resucite al tercer día.[16] 23. Y decía a todos:  
 --Si alguno quiere venir en pos de mí,  niéguese a sí mismo,  tome su cruz cada día y sígame. 24. Todo el que quiera salvar su vida,  la perderá;  y todo el que pierda su vida por causa de mí,  este la salvará,[17] 25. pues,  ¿qué aprovecha al hombre si gana todo el mundo y se destruye o se pierde a sí mismo?, 26. porque el que se averg@uence de mí y de mis palabras,  de este se avergonzará el Hijo del hombre cuando venga en su gloria,  y en la del Padre y de los santos ángeles.[18] 27. Pero en verdad os digo que hay algunos de los que están aquí que no gustarán la muerte hasta que vean el reino de Dios.[19] 28. [20]  Como ocho días después de estas palabras,  Jesús tomó a Pedro,  a Juan y a Jacobo,  y subió al monte a orar.[21] 29. Mientras oraba,  la apariencia de su rostro cambió[22]  y su vestido se volvió blanco y resplandeciente. 30. Y dos varones hablaban con él,  los cuales eran Moisés y Elías.[23] 31. Estos aparecieron rodeados de gloria;  y hablaban de su partida,[24]  que Jesús iba a cumplir en Jerusalén. 32. Pedro y los que lo acompañaban estaban rendidos de sueño;  pero,  permaneciendo despiertos,  vieron la gloria de Jesús y a los dos varones que estaban con él. 33. Y sucedió que,  mientras estos se alejaban de él,  Pedro dijo a Jesús:  
 --Maestro,  bueno es para nosotros estar aquí.  Hagamos tres enramadas,  una para ti,  una para Moisés y una para Elías.  
 Pero no sabía lo que decía. 34. Mientras él decía esto,  vino una nube que los cubrió;  y tuvieron temor al entrar en la nube.[25] 35. Y vino una voz desde la nube,  que decía:  "Este es mi Hijo amado;  a él oíd".[26] 36. Cuando cesó la voz,  Jesús se encontraba solo.[27]  Ellos callaron,  y por aquellos días no dijeron nada a nadie de lo que habían visto. 37. Al día siguiente,  cuando descendieron del monte,  una gran multitud les salió al encuentro. 38. Y un hombre de la multitud clamó diciendo:  
 --Maestro,  te ruego que veas a mi hijo,  pues es el único que tengo; 39. y sucede que un espíritu lo toma y,  de repente,  lo hace gritar,  lo sacude con violencia,  lo hace echar espuma y,  estropeándolo,  a duras penas se aparta de él.[28] 40. Rogué a tus discípulos que lo echaran fuera,  pero no pudieron. 41. Respondiendo Jesús,  dijo:  
