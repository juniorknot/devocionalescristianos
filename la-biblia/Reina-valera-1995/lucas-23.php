<li class="hide-for-large"><a href="whatsapp://send?text=Lucas 23 (RV1995)%0A%0A 1. Levantándose entonces todos,  llevaron a Jesús a Pilato.[1] 2. Y comenzaron a acusarlo,  diciendo:  
 --Hemos encontrado que este pervierte a la nación,  y que prohíbe dar tributo a César[2]  diciendo que él mismo es el Cristo,  un Rey.[3] 3. Entonces Pilato le preguntó,  diciendo:  
 --¿Eres tú el Rey de los judíos?  
 Respondiéndole él,  dijo:  
 --Tú lo dices.[4] 4. Pilato dijo a los principales sacerdotes y a la gente:  
 --Ningún delito hallo en este hombre. 5. Pero ellos porfiaban,  diciendo:  
 --Alborota al pueblo,  enseñando por toda Judea,  comenzando desde Galilea hasta aquí. 6. Entonces Pilato,  cuando oyó decir  "Galilea",[5]  preguntó si el hombre era galileo. 7. Y al saber que era de la jurisdicción de Herodes,[6]  lo remitió a Herodes,  que en aquellos días también estaba en Jerusalén. 8. Herodes,  al ver a Jesús,  se alegró mucho,  porque hacía tiempo que deseaba verlo,  porque había oído muchas cosas acerca de él y esperaba verlo hacer alguna señal.[7] 9. Le hizo muchas preguntas,  pero él nada le respondió.[8] 10. Estaban los principales sacerdotes y los escribas acusándolo con gran vehemencia. 11. Entonces Herodes con sus soldados lo menospreció y se burló de él,  vistiéndolo con una ropa espléndida;  y volvió a enviarlo a Pilato.[9] 12. Y aquel día,  Pilato y Herodes,  que estaban enemistados,  se hicieron amigos. 13. Entonces Pilato,  convocando a los principales sacerdotes,  a los gobernantes y al pueblo, 14. les dijo:  
 --Me habéis presentado a este como un hombre que perturba al pueblo;  pero,  habiéndolo interrogado yo delante de vosotros,  no he hallado en él delito alguno de aquellos de que lo acusáis. 15. Ni tampoco Herodes,  porque os remití a él.  Nada digno de muerte ha hecho este hombre, 16. así que lo soltaré después de castigarlo. 17. Tenía necesidad de soltarles uno en cada fiesta.[10] 18. Pero toda la multitud gritó a una,[11]  diciendo:  
 --¡Fuera con ese;  suéltanos a Barrabás! 19. Este había sido echado en la cárcel por rebelión en la ciudad y por un homicidio. 20. Les habló otra vez Pilato,  queriendo soltar a Jesús; 21. pero ellos volvieron a gritar,  diciendo:  
 --¡Crucifícalo,  crucifícalo![12] 22. Él les dijo por tercera vez:  
 --¿Pues qué mal ha hecho este?  Ningún delito digno de muerte he hallado en él;  lo castigaré y lo soltaré. 23. Pero ellos insistían a gritos,  pidiendo que fuera crucificado;  y las voces de ellos y de los principales sacerdotes se impusieron.[13] 24. Entonces Pilato sentenció que se hiciera lo que ellos pedían. 25. Les soltó a aquel que había sido echado en la cárcel por rebelión y homicidio,  a quien habían pedido,  y entregó a Jesús a la voluntad de ellos. 26. Cuando lo llevaban,  tomaron a cierto Simón de Cirene,  que venía del campo,  y le pusieron encima la cruz para que la llevara tras Jesús.[14] 27. Lo seguía una gran multitud del pueblo,  y de mujeres que lloraban y hacían lamentación por él. 28. Pero Jesús,  volviéndose hacia ellas,  les dijo:  
 --Hijas de Jerusalén,  no lloréis por mí,  sino llorad por vosotras mismas y por vuestros hijos,[15] 29. porque vendrán días en que dirán:  "Bienaventuradas las estériles y los vientres que no concibieron y los pechos que no criaron".[16] 30. Entonces comenzarán a decir a los montes:  "Caed sobre nosotros",  y a los collados:  "Cubridnos",[17] 31. porque si en el árbol verde hacen estas cosas,  ¿en el seco,  qué no se hará?[18] 32. Llevaban también con él a otros dos,  que eran malhechores,  para ser ejecutados. 33. Cuando llegaron al lugar llamado de la Calavera,[19]  lo crucificaron allí,  y a los malhechores,  uno a la derecha y otro a la izquierda. 34. Jesús decía:  
 --Padre,  perdónalos,  porque no saben lo que hacen.[20]  
 Y repartieron entre sí sus vestidos,  echando suertes.[21] 35. El pueblo estaba mirando,  y aun los gobernantes se burlaban de él diciendo:  
 --A otros salvó;  sálvese a sí mismo,  si este es el Cristo,  el escogido de Dios. 36. Los soldados también se burlaban de él,  y se acercaban ofreciéndole vinagre[22] 37. y diciendo:  
 --Si tú eres el Rey de los judíos,  sálvate a ti mismo. 38. Había también sobre él un título escrito con letras griegas,  latinas y hebreas:  "Este es el Rey de los judíos". 39. Uno de los malhechores que estaban colgados lo insultaba diciendo:  
 --Si tú eres el Cristo,  sálvate a ti mismo y a nosotros. 40. Respondiendo el otro,  lo reprendió,  diciendo:  
 --¿Ni siquiera estando en la misma condenación temes tú a Dios? 41. Nosotros,  a la verdad,  justamente padecemos,  porque recibimos lo que merecieron nuestros hechos;  pero este ningún mal hizo. 42. Y dijo a Jesús:  
 --Acuérdate de mí cuando vengas en tu Reino. 43. Entonces Jesús le dijo:  
 --De cierto te digo que hoy estarás conmigo en el paraíso.[23] 44. Cuando era como la hora sexta,  hubo tinieblas sobre toda la tierra hasta la hora novena.[24] 45. El sol se oscureció y el velo[25]  del templo se rasgó por la mitad. 46. Entonces Jesús,  clamando a gran voz,  dijo:  
