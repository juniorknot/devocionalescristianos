<li class="hide-for-large"><a href="whatsapp://send?text=Hechos 19 (RV1995)%0A%0A 1. Aconteció que entre tanto que Apolos estaba en Corinto,[1]  Pablo,  después de recorrer las regiones superiores,[2]  vino a Éfeso,  y hallando a ciertos discípulos, 2. les preguntó:  
 --¿Recibisteis el Espíritu Santo cuando creísteis?[3]  
 Ellos le dijeron:  
 --Ni siquiera habíamos oído que hubiera Espíritu Santo. 3. Entonces dijo:  
 --¿En qué,  pues,  fuisteis bautizados?  
 Ellos dijeron:  
 --En el bautismo de Juan.[4] 4. Dijo Pablo:  
 --Juan bautizó con bautismo de arrepentimiento,  diciendo al pueblo que creyeran en aquel que vendría después de él,  esto es,  en Jesús el Cristo.[5] 5. Cuando oyeron esto,  fueron bautizados en el nombre del Señor Jesús. 6. Y habiéndoles impuesto Pablo las manos,  vino sobre ellos el Espíritu Santo;[6]  y hablaban en lenguas y profetizaban.[7] 7. Eran entre todos unos doce hombres. 8. Entrando Pablo en la sinagoga,  habló con valentía por espacio de tres meses,  discutiendo y persuadiendo acerca del reino de Dios. 9. Pero como algunos se rehusaban a creer y maldecían el Camino[8]  delante de la multitud,  Pablo se apartó de ellos y separó a los discípulos,  discutiendo cada día en la escuela de uno llamado Tiranno.[9] 10. Así continuó por espacio de dos años,  de manera que todos los que habitaban en Asia,  judíos y griegos,  oyeron la palabra del Señor Jesús.[10] 11. Y hacía Dios milagros extraordinarios por mano de Pablo, 12. de tal manera que hasta los pañuelos o delantales que habían tocado su cuerpo eran llevados a los enfermos,  y las enfermedades se iban de ellos,  y los espíritus malos salían.[11] 13. Pero algunos de los judíos,  exorcistas ambulantes,  intentaron invocar el nombre del Señor Jesús sobre los que tenían espíritus malos,  diciendo:  "¡Os conjuro por Jesús,  el que predica Pablo!"[12] 14. Había siete hijos de un tal Esceva,  judío,  jefe de los sacerdotes,[13]  que hacían esto. 15. Pero respondiendo el espíritu malo,  dijo:  "A Jesús conozco y sé quién es Pablo,  pero vosotros,  ¿quiénes sois?" 16. El hombre en quien estaba el espíritu malo,  saltando sobre ellos y dominándolos,  pudo más que ellos,  de tal manera que huyeron de aquella casa desnudos y heridos. 17. Esto fue notorio a todos los que habitaban en Éfeso,  así judíos como griegos;  y tuvieron temor todos ellos,  y era glorificado el nombre del Señor Jesús. 18. Muchos de los que habían creído venían,  confesando y dando cuenta de sus hechos. 19. Asimismo muchos de los que habían pr