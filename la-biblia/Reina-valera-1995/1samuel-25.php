                                                                                <p style="style=text-align: justify;"><strong id="numeros">29. </strong>
 Aunque alguien se haya levantado para perseguirte y atentar contra tu vida,  con todo,  la vida de mi señor será atada al haz de los que viven delante de Jehová tu Dios,  mientras que él arrojará las vidas de tus enemigos como quien las tira con el cuenco de una honda. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">30. </strong>
 Cuando Jehová haga con mi señor conforme a todo el bien que ha hablado de ti,  y te establezca como príncipe sobre Israel, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">31. </strong>
 entonces,  señor mío,  no tendrás motivo de pena ni remordimientos por haber derramado sangre sin causa,  o por haberte vengado con tu propia mano.  Guárdese,  pues,  mi señor,  y cuando Jehová haya favorecido a mi señor,  acuérdate de tu sierva. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">32. </strong>
 Entonces David dijo a Abigail:  
 --Bendito sea Jehová,  Dios de Israel,  que te envió para que hoy me encontraras. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">33. </strong>
 Bendito sea tu razonamiento y bendita tú,  que me has impedido hoy derramar sangre y vengarme por mi propia mano. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">34. </strong>
 Porque,  ¡vive Jehová,  Dios de Israel!,  que me ha impedido hacerte mal,  que de no haberte dado prisa en venir a mi encuentro,  mañana por la mañana no le habría quedado con vida a Nabal ni un solo hombre. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">35. </strong>
 David recibió de sus manos lo que le había traído,  y le dijo:  
 --Sube en paz a tu casa,  pues he escuchado tu petición y te la he concedido. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
 Cuando Abigail volvió adonde estaba Nabal,  este estaba celebrando en su casa un banquete como de rey.  Nabal estaba alegre y completamente ebrio,  por lo cual ella no le dijo absolutamente nada hasta el día siguiente. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 Pero por la mañana,  cuando ya a Nabal se le habían pasado los efectos del vino,  le contó su mujer estas cosas;  entonces se le apretó el corazón en el pecho,  y se quedó como una piedra. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 Diez días después,  Jehová hirió a Nabal,  y este murió. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 Luego de oir David que Nabal había muerto,  dijo:  
 "Bendito sea Jehová,  que juzgó la causa de la afrenta que recibí de manos de Nabal,  y ha preservado del mal a su siervo.  Jehová ha hecho caer la maldad de Nabal sobre su propia cabeza".  
 Después mandó David a decir a Abigail que quería tomarla por mujer. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
 Los siervos de David se presentaron ante Abigail en Carmel y le hablaron diciendo:  
 --David nos envía para tomarte por mujer. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 Ella se levantó,  se postró rostro en tierra,  y dijo:  
 --Aquí tienes a tu sierva,  que será una sierva para lavar los pies de los siervos de mi señor. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 Se levantó luego Abigail y,  acompañada de las cinco doncellas que la servían,  montó en un asno,  siguió a los mensajeros de David,  y fue su mujer. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 También tomó David a Ahinoam de Jezreel,[10]  y ambas fueron sus mujeres. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 Porque Saúl había dado a su hija Mical,  mujer de David,  a Palti hijo de Lais,  que era de Galim. </p>
                                                        
