 --¿Quién es David,  quién es el hijo de Isaí?  Muchos siervos hay hoy que huyen de sus señores. 11. ¿He de tomar yo ahora mi pan,  mi agua y la carne que he preparado para mis esquiladores,  y darla a hombres que no sé de dónde son? 12. Los jóvenes que había enviado David,  dando media vuelta,  tomaron el camino de regreso.  Cuando llegaron a donde estaba David,  le dijeron todas estas cosas. 13. Entonces David dijo a sus hombres:  
 "Cíñase cada uno su espada".  
 Cada uno se ciñó su espada y también David se ciñó la suya.  Subieron tras David unos cuatrocientos hombres,  y dejaron doscientos con el bagaje. 14. Pero uno de los criados avisó a Abigail,  mujer de Nabal,  diciendo:  "Mira que David ha enviado mensajeros del desierto para saludar a nuestro amo,  y él los ha despreciado. 15. Aquellos hombres han sido muy buenos con nosotros,  y cuando estábamos en el campo nunca nos trataron mal,  ni nos faltó nada en todo el tiempo que anduvimos con ellos. 16. Muro fueron para nosotros de día y de noche,  todos los días que hemos estado con ellos apacentando las ovejas.[6] 17. Ahora,  pues,  reflexiona y mira lo que has de hacer,  porque ya está decidida la ruina de nuestro amo y de toda su casa;  pues él es un hombre tan perverso,  que no hay quien pueda hablarle". 18. Tomó Abigail a toda prisa doscientos panes,  dos cueros de vino,  cinco ovejas guisadas,  cinco medidas de grano tostado,  cien racimos de uvas pasas y doscientos panes de higos secos,  y lo cargó todo sobre unos asnos. 19. Luego dijo a sus criados:  
 "Id delante de mí,  y yo os seguiré luego".  
