<li class="hide-for-large"><a href="whatsapp://send?text=Génesis 41 (RV1995)%0A%0A 1. [1]  Aconteció,  pasados dos años,  que el faraón tuvo un sueño.  Le parecía que estaba junto al río, 2. y que del río subían siete vacas[2]  hermosas a la vista,  muy gordas,  y que pacían en el prado. 3. Tras ellas subían del río otras siete vacas de feo aspecto y enjutas de carne,  que se pararon cerca de las vacas hermosas a la orilla del río; 4. y las vacas de feo aspecto y enjutas de carne devoraban a las siete vacas hermosas y muy gordas.  
 El faraón se despertó, 5. pero se durmió de nuevo,  y soñó la segunda vez:  Siete espigas llenas y hermosas crecían de una sola caña, 6. y después de ellas salían otras siete espigas menudas y quemadas por el viento del este;[3] 7. y las siete espigas menudas devoraban a las siete espigas gruesas y llenas.  
 El faraón se despertó y vio que era un sueño. 8. Sucedió que por la mañana estaba agitado su espíritu,  y envió llamar a todos los magos de Egipto y a todos sus sabios.  Les contó sus sueños,  pero no había quien se los pudiera interpretar al faraón. 9. Entonces el jefe de los coperos dijo al faraón:  
 --Me acuerdo hoy de mis faltas. 10. Cuando el faraón se enojó contra sus siervos,  nos echó a la prisión de la casa del capitán de la guardia a mí y al jefe de los panaderos. 11. Él y yo tuvimos un sueño en la misma noche,  y cada sueño tenía su propio significado. 12. Estaba allí con nosotros un joven hebreo,  siervo del capitán de la guardia.  Se lo contamos,  y él nos interpretó nuestros sueños y declaró a cada uno conforme a su sueño. 13. Y aconteció que como él nos los interpretó,  así ocurrió:  yo fui restablecido en mi puesto y el otro fue colgado. 14. Entonces el faraón envió a llamar a José;  lo sacaron apresuradamente de la cárcel,  se afeitó,  mudó sus vestidos y vino ante el faraón. 15. El faraón dijo a José:  
 --Yo he tenido un sueño,  y no hay quien lo interprete;  pero he oído decir de ti que oyes sueños para interpretarlos. 16. Respondió José al faraón:  
