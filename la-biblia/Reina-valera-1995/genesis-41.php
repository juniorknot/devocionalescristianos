 También las siete vacas flacas y feas que subían tras ellas son siete años,  y las siete espigas menudas y quemadas por el viento solano siete años serán de hambre. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">28. </strong>
 Esto es lo que respondo al faraón.  Lo que Dios va a hacer,  lo ha mostrado al faraón. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">29. </strong>
 Vienen siete años de gran abundancia en toda la tierra de Egipto. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">30. </strong>
 Tras ellos seguirán siete años de hambre:  toda la abundancia será olvidada en la tierra de Egipto,  y el hambre consumirá la tierra. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">31. </strong>
 Y aquella abundancia no se echará de ver,  a causa del hambre que la seguirá,  la cual será gravísima. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">32. </strong>
 Y que el faraón haya tenido el sueño dos veces significa que la cosa es firme de parte de Dios,  y que Dios se apresura a hacerla. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">33. </strong>
 "Por tanto,  es necesario que el faraón se provea de un hombre prudente y sabio,  y que lo ponga sobre la tierra de Egipto. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">34. </strong>
 Haga esto el faraón:  ponga gobernadores sobre el país,  que recojan la quinta parte de las cosechas de Egipto en los siete años de la abundancia. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">35. </strong>
 Junten toda la provisión de estos buenos años que vienen,  recojan el trigo bajo la mano del faraón para mantenimiento de las ciudades y guárdenlo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
 Y esté aquella provisión en depósito para el país,  para los siete años de hambre que habrá en la tierra de Egipto;  y el país no perecerá de hambre. </p>
                                                    <h4><strong>José, gobernador de Egipto</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 El asunto pareció bien al faraón y a sus siervos, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 y dijo el faraón a sus siervos:  
 --¿Acaso hallaremos a otro hombre como este,  en quien esté el espíritu de Dios?[4] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 Y dijo el faraón a José:  
 --Después de haberte dado a conocer Dios todo esto,  no hay entendido ni sabio como tú. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
 Tú estarás sobre mi casa y por tu palabra se gobernará todo mi pueblo;  solamente en el trono seré yo mayor que tú.[5] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 Dijo además el faraón a José:  
 --Yo te he puesto sobre toda la tierra de Egipto. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 Entonces el faraón se quitó el anillo de su mano y lo puso en la mano de José;  lo hizo vestir de ropas de lino finísimo y puso un collar de oro en su cuello. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 Lo hizo subir en su segundo carro,  y pregonaban delante de él:  "¡Doblad la rodilla!"[6]  Así quedó José sobre toda la tierra de Egipto. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 Luego dijo el faraón a José:  
 --Yo soy el faraón;  pero sin ti nadie alzará su mano ni su pie en toda la tierra de Egipto. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 El faraón puso a José el nombre de Zafnat-panea,[7]  y le dio por mujer a Asenat,  hija de Potifera,  sacerdote de On.[8]  Así quedó José al frente de toda la tierra de Egipto. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 Era José de edad de treinta años cuando fue presentado delante del faraón,  el rey de Egipto;  y salió José de delante del faraón y recorrió toda la tierra de Egipto. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
 En aquellos siete años de abundancia la tierra produjo en gran cantidad. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 Y él recogió todo el alimento de los siete años de abundancia que hubo en la tierra de Egipto,  y almacenó alimento en las ciudades,  poniendo en cada ciudad el alimento de los campos de alrededor. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 Recogió José trigo como si fuera arena del mar;  tanto que no se podía contar,  porque era incalculable. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 Antes que llegara el primer año de hambre,  le nacieron a José dos hijos,  los cuales le dio a luz Asenat,  hija de Potifera,  sacerdote de On. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 Llamó José al primogénito Manasés,  porque dijo:  "Dios me hizo olvidar todos mis sufrimientos,  y a toda la casa de mi padre". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 Al segundo lo llamó Efraín,  porque dijo:  "Dios me hizo fructificar en la tierra de mi aflicción".[9] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 Se cumplieron así los siete años de abundancia que hubo en la tierra de Egipto, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 y comenzaron a llegar los siete años de hambre,  como José había predicho.  Hubo hambre en todos los países,  pero en toda la tierra de Egipto había pan. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 Cuando se sintió el hambre en toda la tierra de Egipto,  el pueblo clamó por pan al faraón.  Y dijo el faraón a todos los egipcios:  "Id a José,  y haced lo que él os diga". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 Cuando el hambre se extendió por todo el país,  abrió José todos los graneros donde estaba el trigo,  y lo vendía a los egipcios,  porque había crecido el hambre en la tierra de Egipto. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 Y de todos los países venían a Egipto para comprar grano a José,  porque por toda la tierra había crecido el hambre. </p>
                                                        
