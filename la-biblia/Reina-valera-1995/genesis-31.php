 y puso en camino todo su ganado y todo cuanto había adquirido (el ganado de la ganancia que había obtenido en Padan-aram),  para volverse a Isaac,  su padre,  en la tierra de Canaán. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">19. </strong>
 Como Labán había ido a trasquilar sus ovejas,  Raquel hurtó los ídolos de su padre;[4] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">20. </strong>
 y Jacob engañó a Labán,  el arameo,  no diciéndole que se iba. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">21. </strong>
 Huyó,  pues,  con todo lo que tenía;  se levantó,  pasó el Éufrates y se dirigió a los montes de Galaad.[5] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">22. </strong>
 Al tercer día le dijeron a Labán que Jacob había huido. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">23. </strong>
 Entonces Labán tomó consigo a sus parientes,  y fue tras Jacob.  Siete días después lo alcanzó en los montes de Galaad. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">24. </strong>
 Pero aquella noche vino Dios en sueños a Labán,  el arameo,  y le dijo:  "Cuídate de no hablarle a Jacob descomedidamente".[6] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">25. </strong>
 Alcanzó,  pues,  Labán a Jacob,  que había fijado su tienda en el monte;  y acampó Labán con sus parientes en los montes de Galaad. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">26. </strong>
 Entonces dijo Labán a Jacob:  
 --¿Qué has hecho?  ¿Por qué me has engañado y te has llevado a mis hijas como prisioneras de guerra? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">27. </strong>
 ¿Por qué te escondiste para huir,  y me engañaste,  y no me lo hiciste saber para que yo te despidiera con alegría y con cantares,  con tamborín y arpa? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">28. </strong>
 Pues ni aun me dejaste besar a mis hijos y a mis hijas.  Esta vez has obrado locamente. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">29. </strong>
 Poder hay en mi mano para haceros daño;  pero el Dios de tu padre me habló anoche diciendo:  "Cuídate de no hablarle a Jacob descomedidamente". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">30. </strong>
 Y ya que te ibas,  pues añorabas la casa de tu padre,  ¿por qué hurtaste mis dioses?[7] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">31. </strong>
 Respondió Jacob a Labán:  
 --Porque tuve miedo,  pues pensé que quizá me quitarías por fuerza tus hijas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">32. </strong>
 Aquel en cuyo poder halles tus dioses,  ¡que no viva!  Reconoce delante de nuestros hermanos lo que yo tenga tuyo,  y llévatelo.  
 Ciertamente Jacob no sabía que Raquel los había hurtado. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">33. </strong>
 Entró Labán en la tienda de Jacob,  en la tienda de Lea y en la tienda de las dos siervas,[8]  y no los halló.  Salió de la tienda de Lea y entró en la tienda de Raquel. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">34. </strong>
 Pero Raquel tomó los ídolos y los puso en la montura de un camello,  y se sentó sobre ellos.  Labán rebuscó por toda la tienda y no los encontró. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">35. </strong>
 Entonces ella dijo a su padre:  
 --No se enoje mi señor,  si no me puedo levantar delante de ti,  pues estoy con el período de las mujeres.  
 Como Labán siguió rebuscando sin hallar los ídolos, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
 Jacob se enojó y riñó con Labán,  diciéndole:  
 --¿Qué falta cometí?  ¿Cuál es mi pecado,  para que con tanto ardor hayas venido en mi persecución? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 Al registrar todas mis cosas,  ¿qué has hallado de todos los enseres de tu casa?  Ponlo aquí delante de mis hermanos y de los tuyos,  y juzguen entre nosotros. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 Estos veinte años he estado contigo;  tus ovejas y tus cabras nunca abortaron,  ni yo comí carnero de tus ovejas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 Nunca te traje lo arrebatado por las fieras:  yo pagaba el daño;  lo hurtado,  así de día como de noche,  a mí me lo cobrabas.[9] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
 De día me consumía el calor y de noche la helada,  y el sueño huía de mis ojos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 Así he estado veinte años en tu casa:  catorce años te serví por tus dos hijas y seis años por tu ganado,  y has cambiado mi salario diez veces. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 Si el Dios de mi padre,  Dios de Abraham y Terror de Isaac,[10]  no estuviera conmigo,  de cierto me enviarías ahora con las manos vacías;  pero Dios ha visto mi aflicción y el trabajo de mis manos,  y anoche te reprendió. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 Respondió Labán y dijo a Jacob:  
 --Las hijas son hijas mías;  los hijos,  hijos míos son;  las ovejas son mis ovejas,  y todo lo que tú ves es mío:  ¿qué les puedo yo hacer hoy a estas mis hijas,  o a los hijos que ellas han dado a luz? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 Ven ahora,  pues,  y hagamos pacto tú y yo,  y sirva por testimonio entre nosotros dos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 Entonces Jacob tomó una piedra y la levantó por señal. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 Y dijo Jacob a sus hermanos:  
 --Recoged piedras.  
 Tomaron,  pues,  piedras e hicieron un montón,  y comieron allí sobre aquel montón.[11] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
 Labán lo llamó  "Jegar Sahaduta";  y Jacob lo llamó  "Galaad".[12] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 Entonces Labán dijo:  
 --Este montón de piedras es testigo hoy entre nosotros dos.  
 Por eso fue llamado su nombre Galaad; </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 y también Mizpa,[13]  por cuanto dijo:  
 --Vigile Jehová entre tú y yo cuando nos apartemos el uno del otro. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 Si maltratas a mis hijas o si tomas otras mujeres además de mis hijas,  aunque nadie esté con nosotros,  mira,  Dios es testigo entre nosotros dos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 Dijo más Labán a Jacob:  
 --Mira este montón de piedras y esta señal que he erigido entre tú y yo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 Testigo sea este montón de piedras y testigo sea esta señal,  que ni yo pasaré de este montón de piedras para ir contra ti ni tú pasarás de este montón ni de esta señal para ir contra mí,  para nada malo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 Que el Dios del padre de nuestros padres,  el Dios de Abraham y el Dios de Nacor,  juzgue entre nosotros.  
 Jacob juró por aquel a quien temía Isaac,  su padre. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 Luego Jacob inmoló víctimas en el monte,  y llamó a sus hermanos a comer pan.  Ellos comieron pan y durmieron aquella noche en el monte. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 Se levantó Labán de mañana y besó a sus hijos y a sus hijas;  los bendijo,  partió y se volvió a su lugar. </p>
                                                        
