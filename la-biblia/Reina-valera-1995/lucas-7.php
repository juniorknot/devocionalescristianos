<li class="hide-for-large"><a href="whatsapp://send?text=Lucas 7 (RV1995)%0A%0A 1. [1]  Después que terminó todas sus palabras al pueblo que lo oía,  entró en Capernaúm. 2. Y el siervo[2]  de un centurión,[3]  a quien este quería mucho,  estaba enfermo y a punto de morir. 3. Cuando el centurión oyó hablar de Jesús,  le envió unos ancianos[4]  de los judíos,  rogándole que viniera y sanara a su siervo. 4. Ellos se acercaron a Jesús y le rogaron con solicitud,  diciéndole:  
 --Es digno de que le concedas esto, 5. porque ama a nuestra nación y nos edificó una sinagoga.[5] 6. Jesús fue con ellos.  Pero cuando ya no estaban lejos de la casa,  el centurión envió a él unos amigos,  diciéndole:  
 --Señor,  no te molestes,  pues no soy digno de que entres bajo mi techo, 7. por lo que ni aun me tuve por digno de ir a ti;  pero di la palabra y mi siervo será sanado, 8. pues también yo soy hombre puesto bajo autoridad,  y tengo soldados bajo mis órdenes,  y digo a este:  "Ve",  y va;  y al otro:  "Ven",  y viene;  y a mi siervo:  "Haz esto",  y lo hace. 9. Al oir esto,  Jesús se maravilló de él y,  volviéndose,  dijo a la gente que lo seguía:  
 --Os digo que ni aun en Israel he hallado tanta fe. 10. Y al regresar a casa los que habían sido enviados,  hallaron sano al siervo que había estado enfermo. 11. [6]  Aconteció después,  que él iba a la ciudad que se llama Naín,[7]  e iban con él muchos de sus discípulos y una gran multitud. 12. Cuando llegó cerca de la puerta de la ciudad,  llevaban a enterrar a un difunto,  hijo único de su madre,  que era viuda;  y había con ella mucha gente de la ciudad. 13. Cuando el Señor la vio,  se compadeció de ella y le dijo:  
 --No llores. 14. Acercándose,  tocó el féretro;[8]  y los que lo llevaban se detuvieron.  Y dijo:  
 --Joven,  a ti te digo,  levántate. 15. Entonces se incorporó el que había muerto y comenzó a hablar.  Y lo dio a su madre. 16. Todos tuvieron miedo,  y glorificaban a Dios diciendo:  "Un gran profeta se ha levantado entre nosotros"  y  "Dios ha visitado a su pueblo".[9] 17. Y se extendió la fama de él por toda Judea[10]  y por toda la región de alrededor. 18. Los discípulos de Juan le dieron las nuevas de todas estas cosas.[11]  Y llamó Juan a dos de sus discípulos, 19. y los envió a Jesús para preguntarle:  "¿Eres tú el que había de venir[12]  o esperaremos a otro?" 20. Cuando,  pues,  los hombres vinieron a él,  le dijeron:  
 --Juan el Bautista nos ha enviado a ti para preguntarte:  "¿Eres tú el que había de venir o esperaremos a otro?" 21. En esa misma hora sanó a muchos de enfermedades,  plagas y espíritus malos,  y a muchos ciegos les dio la vista. 22. Respondiendo Jesús,  les dijo:  
 --Id,  haced saber a Juan lo que habéis visto y oído:  los ciegos ven,  los cojos andan,  los leprosos son limpiados,  los sordos oyen,[13]  los muertos son resucitados y a los pobres es anunciado el evangelio;[14] 23. y bienaventurado es aquel que no halle tropiezo en mí. 24. Cuando se fueron los mensajeros de Juan,  comenzó a hablar de Juan a la gente:  
 --¿Qué salisteis a ver al desierto?  ¿Una caña sacudida por el viento? 25. ¿O qué salisteis a ver?  ¿A un hombre cubierto de vestiduras delicadas?  Pero los que tienen vestidura preciosa[15]  y viven en deleites,  en los palacios de los reyes están. 26. Entonces  ¿qué salisteis a ver?  ¿A un profeta?  Sí,  os digo,  y más que profeta. 27. Este es de quien está escrito:  
 ""Yo envío mi mensajerodelante de tu faz,  
 el cual preparará tu caminodelante de ti".[16] 28. "Os digo que entre los nacidos de mujeres no hay mayor profeta que Juan el Bautista;  y,  sin embargo,  el más pequeño en el reino de Dios es mayor que él. 29. El pueblo entero que lo escuchó,  incluso los publicanos,  justificaron a Dios,  bautizándose con el bautismo de Juan. 30. Pero los fariseos y los intérpretes de la Ley desecharon los designios de Dios respecto de sí mismos,  y no quisieron ser bautizados por Juan.[17] 31. Agregó el Señor:  
 --¿A qué,  pues,  compararé a los hombres de esta generación?  ¿A qué son semejantes? 32. Semejantes son a los muchachos sentados en la plaza,  que se gritan unos a otros y dicen:  "Os tocamos flauta,  y no bailasteis;  os entonamos canciones de duelo y no llorasteis".[18] 33. Vino Juan el Bautista,  que ni comía pan ni bebía vino,[19]  y decís:  "Demonio tiene". 34. Vino el Hijo del hombre,  que come y bebe,  y decís:  "Este es un hombre comilón y bebedor de vino,  amigo de publicanos y de pecadores".[20] 35. Pero la sabiduría es justificada por todos sus hijos.[21] 36. [22]  Uno de los fariseos[23]  rogó a Jesús que comiera con él.  Y habiendo entrado en casa del fariseo,  se sentó a la mesa. 37. Entonces una mujer de la ciudad,  que era pecadora,[24]  al saber que Jesús estaba a la mesa en casa del fariseo,  trajo un frasco de alabastro[25]  con perfume; 38. y estando detrás de él a sus pies,[26]  llorando,  comenzó a regar con lágrimas sus pies,  y los secaba con sus cabellos;  y besaba sus pies y los ungía con el perfume. 39. Cuando vio esto el fariseo que lo había convidado,  dijo para sí:  "Si este fuera profeta,  conocería quién y qué clase de mujer es la que lo toca,  porque es pecadora". 40. Entonces,  respondiendo Jesús,  le dijo:  
 --Simón,[27]  una cosa tengo que decirte.  
 Y él le dijo:  
 --Di,  Maestro. 41. --Un acreedor tenía dos deudores:  uno le debía quinientos denarios y el otro,  cincuenta.[28] 42. No teniendo ellos con qué pagar,  perdonó a ambos.  Di,  pues,  ¿cuál de ellos lo amará más?[29] 43. Respondiendo Simón,  dijo:  
 --Pienso que aquel a quien perdonó más.  
