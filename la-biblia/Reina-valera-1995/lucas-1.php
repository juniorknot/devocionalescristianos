                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 pues nada hay imposible para Dios.[25] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 Entonces María dijo:  
 --Aquí está la sierva del Señor;  hágase conmigo conforme a tu palabra.  
 Y el ángel se fue de su presencia. </p>
                                                    <h4><strong>María visita a Elisabet</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 En aquellos días,  levantándose María,  fue de prisa a la montaña,  a una ciudad de Judá; </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
 entró en casa de Zacarías y saludó a Elisabet. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 Y aconteció que cuando oyó Elisabet la salutación de María,  la criatura saltó en su vientre,  y Elisabet,  llena del Espíritu Santo, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 exclamó a gran voz:  
 --Bendita tú entre las mujeres y bendito el fruto de tu vientre.[26] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 ¿Por qué se me concede esto a mí,  que la madre de mi Señor venga a mí?, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 porque tan pronto como llegó la voz de tu salutación a mis oídos,  la criatura saltó de alegría en mi vientre. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 Bienaventurada la que creyó,  porque se cumplirá[27]  lo que le fue dicho de parte del Señor. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 Entonces María dijo:  
 "Engrandece mi alma al Señor[28] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
 y mi espíritu se regocijaen Dios mi Salvador,[29] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 porque ha mirado la bajeza de su sierva,[30]  
 pues desde ahora me diránbienaventurada todas las generaciones, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 porque me ha hecho grandes cosasel Poderoso.[31]  
 ¡Santo es su nombre, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 y su misericordia es de generaciónen generación 
 a los que le temen![32] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 Hizo proezas con su brazo;[33]  
 esparció a los soberbiosen el pensamiento de sus corazones. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 Quitó de los tronos a los poderosos[34]  
 y exaltó a los humildes. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 A los hambrientos colmó de bienes 
 y a los ricos envió vacíos.[35] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 Socorrió a Israel,  su siervo,  
 acordándose de su misericordia[36] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 --de la cual habló a nuestros padres-- 
 para con Abraham[37]  y su descendenciapara siempre".[38] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 Se quedó María con ella como tres meses;  después se volvió a su casa. </p>
                                                    <h4><strong>Nacimiento de Juan el Bautista</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 Cuando a Elisabet se le cumplió el tiempo de su alumbramiento,  dio a luz un hijo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
 Al oir los vecinos y los parientes que Dios había engrandecido para con ella su misericordia,  se regocijaron con ella. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">59. </strong>
 Aconteció que al octavo día vinieron para circuncidar al niño,[39]  y lo llamaban con el nombre de su padre,  Zacarías; </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">60. </strong>
 pero su madre dijo:  
 --¡No!  Se llamará Juan. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">61. </strong>
 Le dijeron:  
 --¿Por qué?  No hay nadie en tu parentela que se llame con ese nombre. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">62. </strong>
 Entonces preguntaron por señas a su padre cómo lo quería llamar. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">63. </strong>
 Él,  pidiendo una tablilla,  escribió:  "Juan es su nombre".  Y todos se maravillaron. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">64. </strong>
 En ese momento fue abierta su boca y suelta su lengua,  y comenzó a bendecir a Dios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">65. </strong>
 Se llenaron de temor todos sus vecinos,  y en todas las montañas de Judea se divulgaron todas estas cosas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">66. </strong>
 Los que las oían las guardaban en su corazón,  diciendo:  "¿Quién,  pues,  será este niño?"  Y la mano del Señor estaba con él. </p>
                                                    <h4><strong>Profecía de Zacarías</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">67. </strong>
 Zacarías,  su padre,  fue lleno del Espíritu Santo y profetizó,  diciendo: </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">68. </strong>
 "Bendito el Señor Dios de Israel,[40]  
 que ha visitado y redimido[41]  a su pueblo, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">69. </strong>
 y nos levantó un poderoso Salvador[42]  
 en la casa de David,  su siervo[43] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">70. </strong>
 --como habló por boca de sus santosprofetas que fuerondesde el principio--, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">71. </strong>
 salvación de nuestros enemigos y de lamano de todos los que nos odiaron, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">72. </strong>
 para hacer misericordiacon nuestros padres 
 y acordarse de su santo pacto,[44] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">73. </strong>
 del juramento que hizoa Abraham,  nuestro padre,  
 que nos había de conceder[45] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">74. </strong>
 que,  librados de nuestros enemigos,  
 sin temor lo serviríamos[46] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">75. </strong>
 en santidad y en justicia delante de éltodos nuestros días.[47] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">76. </strong>
 Y tú,  niño,  profeta del Altísimoserás llamado,  
 porque irás delante de la presencia delSeñor para preparar sus caminos,[48] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">77. </strong>
 para dar conocimiento de salvacióna su pueblo,  
 para perdón de sus pecados,[49] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">78. </strong>
 por la entrañable misericordiade nuestro Dios,  
 con que nos visitódesde lo alto la aurora,[50] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">79. </strong>
 para dar luz a los que habitanen tinieblas[51]  y en sombra de muerte,  
 para encaminar nuestros piespor camino de paz".[52] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">80. </strong>
 El niño crecía y se fortalecía en espíritu,  y estuvo en lugares desiertos hasta el día de su manifestación a Israel.[53] </p>
                                                        
