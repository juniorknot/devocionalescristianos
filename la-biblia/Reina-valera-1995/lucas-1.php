<li class="hide-for-large"><a href="whatsapp://send?text=Lucas 1 (RV1995)%0A%0A 1. [1]  Puesto que ya muchos han tratado de poner en orden la historia de las cosas que entre nosotros han sido ciertísimas, 2. tal como nos las enseñaron los que desde el principio las vieron con sus ojos y fueron ministros de la palabra, 3. me ha parecido también a mí,  después de haber investigado con diligencia todas las cosas desde su origen,  escribírtelas por orden,  excelentísimo Teófilo,[2] 4. para que conozcas bien la verdad de las cosas en las cuales has sido instruido.[3] 5. Hubo en los días de Herodes,[4]  rey de Judea,  un sacerdote llamado Zacarías,  de la clase de Abías;[5]  su mujer era de las hijas de Aarón[6]  y se llamaba Elisabet. 6. Ambos eran justos delante de Dios y andaban irreprensibles en todos los mandamientos y ordenanzas del Señor. 7. Pero no tenían hijos,  porque Elisabet era estéril.  Ambos eran ya de edad avanzada.[7] 8. Aconteció que ejerciendo Zacarías el sacerdocio delante de Dios,  según el orden de su clase, 9. le tocó en suerte entrar,  conforme a la costumbre del sacerdocio,  en el santuario del Señor para ofrecer el incienso.[8] 10. Toda la multitud del pueblo estaba fuera orando a la hora del incienso. 11. Entonces se le apareció un ángel del Señor puesto de pie a la derecha del altar del incienso. 12. Al verlo,  Zacarías se turbó y lo sobrecogió temor. 13. Pero el ángel le dijo:  
 --Zacarías,  no temas,  porque tu oración ha sido oída y tu mujer Elisabet dará a luz un hijo,  y le pondrás por nombre Juan. 14. Tendrás gozo y alegría,  y muchos se regocijarán por su nacimiento, 15. porque será grande delante de Dios.  No beberá vino ni sidra,[9]  y será lleno del Espíritu Santo[10]  aun desde el vientre de su madre. 16. Hará que muchos de los hijos de Israel se conviertan al Señor,  su Dios. 17. E irá delante de él con el espíritu y el poder de Elías,  para hacer volver los corazones de los padres a los hijos[11]  y de los rebeldes a la prudencia de los justos,  para preparar al Señor un pueblo bien dispuesto. 18. Zacarías preguntó al ángel:  
 --¿En qué conoceré esto?,  porque yo soy viejo y mi mujer es de edad avanzada. 19. Respondiendo el ángel,  le dijo:  
 --Yo soy Gabriel,[12]  que estoy delante de Dios,  y he sido enviado a hablarte y darte estas buenas nuevas. 20. Ahora,  por cuanto no creíste mis palabras,  las cuales se cumplirán a su tiempo,  quedarás mudo y no podrás hablar hasta el día en que esto suceda. 21. El pueblo,  entretanto,  estaba esperando a Zacarías,  y se extrañaba de que se demorara en el santuario. 22. Cuando salió,  no les podía hablar;  entonces comprendieron que había tenido una visión en el santuario.  Él les hablaba por señas,  y permaneció mudo. 23. Cumplidos los días de su ministerio,  se fue a su casa.[13] 24. Después de aquellos días concibió su mujer Elisabet,  y se recluyó en casa por cinco meses,  diciendo: 25. "Así ha hecho conmigo el Señor en los días en que se dignó quitar mi afrenta entre los hombres".[14] 26. Al sexto mes,[15]  el ángel Gabriel fue enviado por Dios a una ciudad de Galilea llamada Nazaret,[16] 27. a una virgen desposada con un varón que se llamaba José,[17]  de la casa de David;  y el nombre de la virgen era María. 28. Entrando el ángel a donde ella estaba,  dijo:  
 --¡Salve,  muy favorecida![18]  El Señor es contigo;  bendita tú entre las mujeres.[19] 29. Pero ella,  cuando lo vio,  se turbó por sus palabras,  y pensaba qué salutación sería esta. 30. Entonces el ángel le dijo:  
 --María,  no temas,  porque has hallado gracia delante de Dios. 31. Concebirás en tu vientre y darás a luz un hijo,  y llamarás su nombre Jesús.[20] 32. Este será grande,  y será llamado Hijo del Altísimo.  El Señor Dios le dará el trono de David,  su padre; 33. reinará sobre la casa de Jacob para siempre y su Reino no tendrá fin.[21] 34. Entonces María preguntó al ángel:  
 --¿Cómo será esto?,  pues no conozco varón.[22] 35. Respondiendo el ángel,  le dijo:  
 --El Espíritu Santo vendrá sobre ti[23]  y el poder del Altísimo te cubrirá con su sombra;  por lo cual también el Santo Ser que va a nacer será llamado Hijo de Dios.[24] 36. Y he aquí también tu parienta Elisabet,  la que llamaban estéril,  ha concebido hijo en su vejez y este es el sexto mes para ella, 37. pues nada hay imposible para Dios.[25] 38. Entonces María dijo:  
 --Aquí está la sierva del Señor;  hágase conmigo conforme a tu palabra.  
 Y el ángel se fue de su presencia. 39. En aquellos días,  levantándose María,  fue de prisa a la montaña,  a una ciudad de Judá; 40. entró en casa de Zacarías y saludó a Elisabet. 41. Y aconteció que cuando oyó Elisabet la salutación de María,  la criatura saltó en su vientre,  y Elisabet,  llena del Espíritu Santo, 42. exclamó a gran voz:  
 --Bendita tú entre las mujeres y bendito el fruto de tu vientre.[26] 43. ¿Por qué se me concede esto a mí,  que la madre de mi Señor venga a mí?, 44. porque tan pronto como llegó la voz de tu salutación a mis oídos,  la criatura saltó de alegría en mi vientre. 45. Bienaventurada la que creyó,  porque se cumplirá[27]  lo que le fue dicho de parte del Señor. 46. Entonces María dijo:  
 "Engrandece mi alma al Señor[28] 47. y mi espíritu se regocijaen Dios mi Salvador,[29] 48. porque ha mirado la bajeza de su sierva,[30]  
 pues desde ahora me diránbienaventurada todas las generaciones, 49. porque me ha hecho grandes cosasel Poderoso.[31]  
 ¡Santo es su nombre, 50. y su misericordia es de generaciónen generación 
 a los que le temen![32] 51. Hizo proezas con su brazo;[33]  
 esparció a los soberbiosen el pensamiento de sus corazones. 52. Quitó de los tronos a los poderosos[34]  
 y exaltó a los humildes. 53. A los hambrientos colmó de bienes 
