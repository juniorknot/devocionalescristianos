<li class="hide-for-large"><a href="whatsapp://send?text=Génesis 37 (RV1995)%0A%0A 1. [1]  Jacob habitó en la tierra donde había vivido su padre,  en la tierra de Canaán.[2] 2. Esta es la historia de la familia de Jacob:[3]  José tenía diecisiete años y apacentaba las ovejas con sus hermanos;  el joven estaba con los hijos de Bilha y con los hijos de Zilpa,  mujeres de su padre;  e informaba José a su padre de la mala fama de ellos. 3. Israel amaba a José más que a todos sus hijos,  porque lo había tenido en su vejez;  y le hizo una túnica de diversos colores.[4] 4. Al ver sus hermanos que su padre lo amaba más que a todos ellos,  lo aborrecían y no podían hablarle pacíficamente.[5] 5. Tuvo José un sueño y lo contó a sus hermanos,  y ellos llegaron a aborrecerlo más todavía. 6. Él les dijo:  
 --Oíd ahora este sueño que he tenido: 7. estábamos atando manojos en medio del campo,  y mi manojo se levantaba y se quedaba derecho,  y vuestros manojos estaban alrededor y se inclinaban ante el mío. 8. Entonces le respondieron sus hermanos:  
 --¿Reinarás tú sobre nosotros,  o dominarás sobre nosotros?  
 Y lo aborrecieron aún más a causa de sus sueños y sus palabras. 9. Después tuvo otro sueño y lo contó a sus hermanos.  Les dijo:  
 --He tenido otro sueño.  Soñé que el sol,  la luna y once estrellas se inclinaban hacia mí. 10. Y lo contó a su padre y a sus hermanos;  su padre le reprendió,  y le dijo:  
 --¿Qué sueño es este que tuviste?  ¿Acaso vendremos yo,  tu madre y tus hermanos a postrarnos en tierra ante ti? 11. Sus hermanos le tenían envidia,[6]  pero su padre meditaba en esto.[7] 12. Un día,  sus hermanos fueron a apacentar las ovejas de su padre en Siquem.[8] 13. Entonces Israel dijo a José:  
 --Tus hermanos apacientan las ovejas en Siquem.  Ven,  y te enviaré a ellos.  
 --Aquí estoy --respondió él. 14. --Ve ahora,  mira cómo están tus hermanos y cómo están las ovejas,  y tráeme la noticia --dijo Israel.  
 Lo envió,  pues,  desde el valle del Hebrón,  y José llegó a Siquem. 15. Lo halló un hombre,  andando él errante por el campo;  y aquel hombre le preguntó:  
 --¿Qué buscas? 16. --Busco a mis hermanos;  te ruego que me muestres dónde están apacentando --respondió José. 17. --Ya se han ido de aquí;  pero yo les oí decir:  "Vamos a Dotán"  --dijo el hombre.  
 Entonces José fue tras sus hermanos y los halló en Dotán.[9] 18. Cuando ellos lo vieron de lejos,  antes que llegara cerca de ellos conspiraron contra él para matarlo. 19. Se dijeron el uno al otro:  
 --¡Ahí viene el soñador! 20. Ahora pues,  venid,  matémoslo y echémoslo en una cisterna,  y diremos:  "Alguna mala bestia lo devoró".  Veremos entonces qué será de sus sueños. 21. Cuando Rubén oyó esto,  lo libró de sus manos.  Dijo:  
 --No lo matemos. 22. Y añadió:  
 --No derraméis sangre;  echadlo en esta cisterna que está en el desierto,  pero no le pongáis las manos encima.  
