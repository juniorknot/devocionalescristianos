 --¡Lleno de todo engaño y de toda maldad,  hijo del diablo,[12]  enemigo de toda justicia!  ¿No cesarás de trastornar los caminos rectos del Señor?[13] 11. Ahora,  pues,  la mano del Señor está contra ti,  y quedarás ciego y no verás el sol por algún tiempo.  
