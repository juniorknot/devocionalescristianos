                                                                                <p style="style=text-align: justify;"><strong id="numeros">27. </strong>
 porque los habitantes de Jerusalén y sus gobernantes,  que no conocían a Jesús ni las palabras de los profetas que se leen todos los sábados,  las cumplieron al condenarlo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">28. </strong>
 Sin hallar en él causa digna de muerte,  pidieron a Pilato que se le matara.[32] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">29. </strong>
 Y cuando cumplieron todas las cosas que de él estaban escritas,  lo bajaron del madero y lo pusieron en el sepulcro.[33] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">30. </strong>
 Pero Dios lo levantó de los muertos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">31. </strong>
 Y él se apareció durante muchos días a los que habían subido juntamente con él de Galilea a Jerusalén,[34]  los cuales ahora son sus testigos ante el pueblo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">32. </strong>
 "Nosotros también os anunciamos el evangelio de aquella promesa hecha a nuestros padres, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">33. </strong>
 la cual Dios nos ha cumplido a nosotros,  sus hijos,  resucitando a Jesús;  como está escrito también en el salmo segundo:  "Mi hijo eres tú,  yo te he engendrado hoy".[35] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">34. </strong>
 Y en cuanto a que lo levantó de los muertos para nunca más volver a corrupción,  lo dijo así:  "Os daré las misericordias fieles de David".[36] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">35. </strong>
 Por eso dice también en otro salmo:  "No permitirás que tu Santo[37]  vea corrupción". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
 Y a la verdad David,  habiendo servido a su propia generación según la voluntad de Dios,  durmió y fue reunido con sus padres,  y vio corrupción. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 Pero aquel a quien Dios levantó,  no vio corrupción. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 Sabed,  pues,  esto,  hermanos:  que por medio de él se os anuncia perdón de pecados, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 y que de todo aquello de que no pudisteis ser justificados por la Ley de Moisés,  en él es justificado todo aquel que cree.[38]  [39] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
 Mirad,  pues,  que no venga sobre vosotros lo que está dicho en los profetas: </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 " "Mirad,  menospreciadores,  
 asombraos y desapareced,  
 porque yo hago una obra en vuestros días,  
 obra que no creeréis,si alguien os la cuenta".[40] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 Cuando salieron ellos de la sinagoga de los judíos,  los gentiles les rogaron que el siguiente sábado[41]  les hablaran de estas cosas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 Y despedida la congregación,  muchos de los judíos y de los prosélitos piadosos siguieron a Pablo y a Bernabé,  quienes hablándoles los persuadían a que perseveraran en la gracia de Dios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 El siguiente sábado se juntó casi toda la ciudad para oir la palabra de Dios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 Pero viendo los judíos la muchedumbre,  se llenaron de celos y rebatían lo que Pablo decía,  contradiciendo y blasfemando. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 Entonces Pablo y Bernabé,  hablando con valentía,  dijeron:  
 --A vosotros,  a la verdad,  era necesario que se os hablara primero la palabra de Dios;  pero puesto que la desecháis y no os juzgáis dignos de la vida eterna,  nos volvemos a los gentiles,[42] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
 porque así nos ha mandado el Señor,  diciendo:  
 ""Te he puesto para luz de los gentiles,  
 a fin de que seas para salvación hasta lo último de la tierra".[43] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 Los gentiles,  oyendo esto,  se regocijaban y glorificaban la palabra del Señor,  y creyeron todos los que estaban ordenados para vida eterna.[44] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 Y la palabra del Señor se difundía por toda aquella provincia. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 Pero los judíos instigaron a mujeres piadosas y distinguidas,  y a los principales de la ciudad,  y levantaron persecución contra Pablo y Bernabé,  y los expulsaron de sus límites. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 Ellos,  entonces,  sacudiendo contra ellos el polvo de sus pies,[45]  llegaron a Iconio. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 Y los discípulos estaban llenos de gozo y del Espíritu Santo. </p>
                                                        
