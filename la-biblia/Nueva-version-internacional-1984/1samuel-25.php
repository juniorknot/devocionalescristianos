 ¡Bendito sea el Señor,  Dios de Israel,  que te ha enviado hoy a mi encuentro! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">33. </strong>
 ¡Y bendita seas tú por tu buen juicio,  pues me has impedido derramar sangre y vengarme con mis propias manos! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">34. </strong>
 El Señor,  Dios de Israel,  me ha impedido hacerte mal;  pero te digo que si no te hubieras dado prisa en venir a mi encuentro,  para mañana no le habría quedado vivo a Nabal ni uno solo de sus hombres.  ¡Tan cierto como que el Señor vive! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">35. </strong>
 Dicho esto,  David aceptó lo que ella le había traído.  
 Vuelve tranquila a tu casa añadió.  Como puedes ver,  te he hecho caso:  te concedo lo que me has pedido.[8] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
 Cuando Abigaíl llegó a la casa,  Nabal estaba dando un regio banquete.  Se encontraba alegre y muy borracho,  así que ella no le dijo nada hasta el día siguiente. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 Por la mañana,  cuando a Nabal ya se le había pasado la borrachera,  su esposa le contó lo sucedido.  Al oírlo,  Nabal sufrió un ataque al corazón y quedó paralizado. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 Unos diez días después,  el Señor hirió a Nabal,  y así murió. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 Cuando David se enteró de que Nabal había muerto,  exclamó:  "¡Bendito sea el Señor,  que me ha hecho *justicia por la afrenta que recibí de Nabal!  El Señor libró a este siervo suyo de hacer mal,  pero hizo recaer sobre Nabal su propia maldad."  
 Entonces David envió un mensaje a Abigaíl,  proponiéndole matrimonio. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
 Cuando los criados llegaron a Carmel,  hablaron con Abigaíl y le dijeron:  
 David nos ha enviado para pedirle a usted que se case con él. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 Ella se inclinó,  y postrándose rostro en tierra dijo:  
 Soy la sierva de David,  y estoy para servirle.  Incluso estoy dispuesta a lavarles los pies a sus criados. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 Sin perder tiempo,  Abigaíl se dispuso a partir.  Se montó en un asno y,  acompañada de cinco criadas,  se fue con los mensajeros de David.  Después se casó con él. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 David también se había casado con Ajinoán de Jezrel,  así que ambas fueron sus esposas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 Saúl,  por su parte,  había entregado su hija Mical,  esposa de David,  a Paltiel[9] hijo de Lais,  oriundo de Galín. </p>
                                                        
