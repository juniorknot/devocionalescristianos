                                                                                <p style="style=text-align: justify;"><strong id="numeros">34. </strong>
 Pero  ¿cuántos años de vida me quedan?  respondió Barzilay.  ¿Para qué subir con el rey a Jerusalén? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">35. </strong>
 Ya tengo ochenta años,  y apenas puedo distinguir lo bueno de lo malo,  o saborear lo que como y bebo,  o aun apreciar las voces de los cantores y las cantoras.  ¿Por qué ha de ser este servidor una carga más para mi señor el rey? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
 ¿Y por qué quiere Su Majestad recompensarme de este modo,  cuando tan sólo voy a acompañarlo a cruzar el Jordán? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 Déjeme usted regresar a mi propio pueblo,  para que pueda morir allí y ser enterrado en la tumba de mis padres.  Pero aquí le dejo a Quimán para que sirva a Su Majestad y lo acompañe a cruzar el río.  Haga usted por él lo que haría por mí. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 Está bien respondió el rey,  Quimán irá conmigo,  y haré por él lo que me pides.  Y a ti te daré todo lo que quieras. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 La gente y el rey cruzaron el Jordán.  Luego el rey le dio un beso a Barzilay y lo bendijo,  y Barzilay volvió a su pueblo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
 El rey,  acompañado de Quimán y escoltado por las tropas de Judá y la mitad de las tropas de Israel,  siguió hasta Guilgal. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 Por eso los israelitas fueron a ver al rey y le reclamaron:  
 ¿Cómo es que nuestros hermanos de Judá se han adueñado del rey al cruzar el Jordán,  y lo han escoltado a él,  a su familia y a todas sus tropas? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 Los de Judá respondieron:  
 ¿Y a qué viene ese enojo?  ¡El rey es nuestro pariente cercano!  ¿Acaso hemos vivido a costillas del rey?  ¿Acaso nos hemos aprovechado de algo? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 Pero los israelitas insistieron:  
 ¿Por qué nos tratan con tanto desprecio?  ¡Nosotros tenemos diez veces más derecho que ustedes sobre el rey David!  Además,  ¿no fuimos nosotros los primeros en pedirle que volviera?  
 Entonces los de Judá les contestaron aun con más severidad. </p>
                                                        
