 Los israelitas,  mientras tanto,  habían huido a sus hogares, 9. y por todas las tribus de Israel se hablaba de la situación.  Decían:  "El rey nos rescató del poder de nuestros enemigos;  él nos libró del dominio de los filisteos.  Por causa de Absalón tuvo que huir del país. 10. Pero ahora Absalón,  al que habíamos ungido como rey,  ha muerto en la batalla.  ¿Qué nos impide pedirle al rey que vuelva?" 11. Entonces el rey David mandó este mensaje a los sacerdotes Sadoc y Abiatar:  "Hablen con los *ancianos de Judá y díganles:  El rey se ha enterado de lo que se habla por todo Israel.  ¿Serán ustedes los últimos en pedirme a mí,  el rey,  que regrese a mi palacio? 12. Ustedes son mis hermanos,  ¡son de mi propia sangre!  ¿Por qué han de ser los últimos en llamarme? 13. Díganle también a Amasá:  ¿Acaso no eres de mi propia sangre?  Tú serás de por vida el general de mi ejército,  en lugar de Joab.  ¡Que Dios me castigue sin piedad si no lo cumplo!  " 14. Así el rey se ganó el aprecio de todos los de Judá,  quienes a una voz le pidieron que regresara con todas sus tropas, 15. de modo que el rey emprendió el viaje y llegó hasta el Jordán.  Los de Judá se dirigieron entonces a Guilgal para encontrarse con el rey y acompañarlo a cruzar el río. 16. Pero el benjaminita Simí hijo de Guerá,  oriundo de Bajurín,  se apresuró a bajar con los de Judá para recibir al rey David. 17. Con él iban mil benjaminitas,  e incluso Siba,  que había sido administrador de la familia de Saúl,  con sus quince hijos y veinte criados.  Éstos llegaron al Jordán antes que el rey 18. y vadearon el río para ponerse a las órdenes del rey y ayudar a la familia real a cruzar el Jordán.  Cuando el rey estaba por cruzarlo,  Simí hijo de Guerá se inclinó ante él 19. y le dijo:  
 Ruego a mi señor el rey que no tome en cuenta mi delito ni recuerde el mal que hizo este servidor suyo el día en que Su Majestad salió de Jerusalén.  Le ruego a Su Majestad que olvide eso. 20. Reconozco que he pecado,  y por eso hoy,  de toda la tribu de José,  he sido el primero en salir a recibir a mi señor el rey. 21. Pero Abisay hijo de Sarvia exclamó:  
 ¡Simí maldijo al *ungido del Señor,  y merece la muerte! 22. David respondió:  
 Hijos de Sarvia,  esto no es asunto de ustedes,  sino mío.  Están actuando como si fueran mis adversarios.  ¿Cómo va a morir hoy alguien del pueblo,  cuando precisamente en este día vuelvo a ser rey de Israel? 23. Y dirigiéndose a Simí,  el rey le juró:  
 ¡No morirás! 24. También Mefiboset,  el nieto de Saúl,  salió a recibir al rey.  No se había lavado los pies ni la ropa,  ni se había recortado el bigote,  desde el día en que el rey tuvo que irse hasta que regresó sano y salvo. 25. Cuando llegó de Jerusalén para recibir al rey,  éste le preguntó:  
