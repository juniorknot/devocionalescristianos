 Dime el secreto de tu tremenda fuerza,  y cómo se te puede atar y dominar. 7. Sansón le respondió:  
 Si se me ata con siete cuerdas de arco[2] que todavía no estén secas,  me debilitaré y seré como cualquier otro hombre. 8. Los jefes de los filisteos le trajeron a ella siete cuerdas de arco que aún no se habían secado,  y Dalila lo ató con ellas. 9. Estando unos hombres al acecho en el cuarto,  ella le gritó:  
 ¡Sansón,  los filisteos se lanzan sobre ti!  
 Pero él rompió las cuerdas como quien rompe un pedazo de cuerda chamuscada.  De modo que no se descubrió el secreto de su fuerza. 10. Dalila le dijo a Sansón:  
 ¡Te burlaste de mí!  ¡Me dijiste mentiras!  Vamos,  dime cómo se te puede atar. 11. Si se me ata firmemente con sogas nuevas,  sin usar le dijo él,  me debilitaré y seré como cualquier otro hombre. 12. Mientras algunos filisteos estaban al acecho en el cuarto,  Dalila tomó sogas nuevas y lo ató,  y luego le gritó:  
 ¡Sansón,  los filisteos se lanzan sobre ti!  
 Pero él rompió las sogas que ataban sus brazos,  como quien rompe un hilo. 13. Entonces Dalila le dijo a Sansón:  
 ¡Hasta ahora te has burlado de mí,  y me has dicho mentiras!  Dime cómo se te puede atar.  
