<li class="hide-for-large"><a href="whatsapp://send?text=Josué 15 (NVI1984)%0A%0A 1. El territorio asignado a los clanes de la tribu de Judá abarcaba las tierras comprendidas hasta la frontera de Edom,  incluyendo el desierto de Zin en el sur. 2. La frontera sur,  que partía de la bahía ubicada al extremo sur del Mar Muerto, 3. salía hacia el sur de la cuesta de Acrabín,  cruzaba hacia el desierto de Zin y continuaba hacia Cades Barnea,  al sur.  De allí seguía por Jezrón,  subía hacia Adar,  daba la vuelta hacia Carcá, 4. continuaba por Asmón y salía hacia el arroyo de Egipto,  para terminar en el Mediterráneo.  Ésta es la frontera sur de Judá.[1] 5. La frontera oriental la formaba el Mar Muerto hasta la desembocadura del río Jordán.  
 La frontera norte se iniciaba en la bahía de la desembocadura del Jordán 6. y subía por Bet Joglá,  continuando al norte de Bet Arabá,  hasta la peña de Bohán hijo de Rubén. 7. Subía luego hacia Debir desde el valle de Acor,  y giraba hacia el norte en dirección a Guilgal,  al frente de la pendiente de Adumín,  al sur del valle.  Seguía bordeando las aguas de Ensemes y llegaba a Enroguel. 8. Continuaba hacia el valle de Ben Hinón al sur de la cuesta de la ciudad jebusea,  es decir,  Jerusalén.  Ascendía a la cumbre de la loma al oeste del valle de Hinón,  al norte del valle de Refayin. 9. De aquella cumbre la frontera se dirigía hacia el manantial de Neftóaj,  seguía por las ciudades del monte Efrón y descendía hacia Balá,  también llamada Quiriat Yearín. 10. De allí giraba al oeste de Balá y se dirigía hacia el monte Seír,  bordeaba por el norte las laderas del monte Yearín,  llamado también Quesalón,  y descendía hacia Bet Semes,  pasando por Timná. 11. Después seguía por la parte norte las cuestas de Ecrón,  giraba hacia Sicrón,  rodeaba el monte Balá y llegaba hasta Jabnel.  La línea fronteriza terminaba en el mar Mediterráneo. 12. La frontera occidental la formaba la costa del mar Mediterráneo.  
 Éstas son las fronteras de los territorios asignados a la tribu de Judá y sus clanes. 13. De acuerdo con lo ordenado por el Señor,  Josué le dio a Caleb hijo de Jefone una porción del territorio asignado a Judá.  Esa porción es Quiriat Arbá,  es decir,  Hebrón  (Arbá fue un ancestro de los *anaquitas). 14. Caleb expulsó de Hebrón a tres descendientes de Anac:  Sesay,  Ajimán y Talmay. 15. De allí subió para atacar a los habitantes de Debir,  ciudad que antes se llamaba Quiriat Séfer. 16. Y dijo:  "Le daré mi hija Acsa como esposa al hombre que ataque y conquiste la ciudad de Quiriat Séfer." 17. Entonces Otoniel hijo de Quenaz y sobrino de Caleb capturó Quiriat Séfer y se casó con Acsa. 18. Cuando ella llegó,  Otoniel la convenció[2] de que le pidiera un terreno a su padre.  Al bajar Acsa del asno,  Caleb le preguntó:  
 ¿Qué te pasa? 19. Concédeme un gran favor respondió ella.  Ya que me has dado tierras en el Néguev,  dame también manantiales.  
 Fue así como Caleb le dio a su hija manantiales en las zonas altas y en las bajas. 20. Ésta es la lista de los territorios que recibieron como herencia los clanes de la tribu de Judá: 21. Las ciudades sureñas de la tribu,  ubicadas en el Néguev,  cerca de la frontera con Edom:  
 Cabsel,  Edar,  Jagur, 22. Quiná,  Dimoná,  Adadá, 23. Cedes,  Jazor,  Itnán, 24. Zif,  Telén,  Bealot, 25. Jazor Jadatá,  Queriot,  Jezrón  (conocida también como Jazor), 26. Amán,  Semá,  Moladá, 27. Jazar Gadá,  Hesmón,  Bet Pelet, 28. Jazar Súal,  Berseba,  con sus poblados, 29. [3]  Balá,  Iyín,  Esen, 30. Eltolad,  Quesil,  Jormá, 31. Siclag,  Madmana,  Sansaná, 32. Lebaot,  Siljín,  Ayin y Rimón,  es decir,  un total de veintinueve ciudades con sus pueblos. 33. En la llanura:  
