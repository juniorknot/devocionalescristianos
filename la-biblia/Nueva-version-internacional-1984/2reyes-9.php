 Tengo un mensaje para el capitán.  
 ¿Para cuál de todos nosotros?  preguntó Jehú.  
 Para usted,  mi capitán respondió. 6. Jehú se levantó y entró en la casa.  Entonces el profeta lo ungió con el aceite y declaró:  
 "Así dice el Señor,  Dios de Israel:  Ahora te unjo como rey sobre mi pueblo Israel. 7. Destruirás a la familia de Acab,  tu señor,  y así me vengaré de la sangre de mis siervos los profetas;  castigando a Jezabel,  vengaré la sangre de todos mis siervos. 8. Toda la familia de Acab perecerá,  pues de sus descendientes en Israel exterminaré hasta el último varón,[1] esclavo o libre. 9. Haré con ellos lo mismo que hice con la familia de Jeroboán hijo de Nabat y con la familia de Basá hijo de Ahías. 10. Y en cuanto a Jezabel,  los perros se la comerán en el campo de Jezrel,  y nadie le dará sepultura.  "  
 Acto seguido,  el profeta abrió la puerta y huyó. 11. Cuando Jehú salió para volver a reunirse con los capitanes,  uno de ellos le preguntó:  
 ¿Todo bien?  ¿Qué quería ese loco?  
 Ustedes ya lo conocen respondió,  y saben cómo habla. 12. ¡Pamplinas!  replicaron.  Dinos la verdad.  
 Jehú admitió:  
 Esto es lo que me declaró,  palabra por palabra:  Así dice el Señor:  ‘Ahora te unjo como rey de Israel. 13. Dicho esto,  todos se apresuraron a tender sus mantos sobre los escalones,  a los pies de Jehú.  Luego tocaron la trompeta y gritaron:  "¡Viva el rey Jehú!" 14. Entonces Jehú,  hijo de Josafat y nieto de Nimsi,  conspiró contra Jorán.  Sucedió que Jorán,  con todo el ejército israelita,  había estado defendiendo Ramot de Galaad contra Jazael,  rey de *Siria, 15. pero tuvo que regresar a Jezrel para reponerse de las heridas que había recibido de los sirios en la batalla.  Así que Jehú les dijo a sus partidarios:  "Si ustedes quieren que yo sea rey,  no dejen que nadie salga de la ciudad para ir a Jezrel con el informe." 16. Luego se montó en su carro de combate y fue a Jezrel,  pues allí se estaba recuperando Jorán,  a quien también Ocozías,  rey de Judá,  había ido a visitar. 17. Cuando el centinela que vigilaba desde la torre de Jezrel vio que las tropas de Jehú se acercaban,  gritó:  
 ¡Se acercan unas tropas!  
