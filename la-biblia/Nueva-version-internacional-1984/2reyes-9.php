 Cuando Ocozías,  rey de Judá,  vio lo que pasaba,  huyó en dirección a Bet Hagán.[4] Pero Jehú lo persiguió,  y ordenó:  
 ¡Mátenlo a él también!  
 Y lo hirieron[5] en su carro cuando iba por la cuesta de Gur,  cerca de Ibleam,  pero logró escapar y llegar a Meguido.  Allí murió. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">28. </strong>
 Luego sus siervos trasladaron el cuerpo a Jerusalén,  la Ciudad de David,  donde lo sepultaron en su tumba,  junto a sus antepasados. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">29. </strong>
 Ocozías había ascendido al trono en el undécimo año del reinado de Jorán hijo de Acab. </p>
                                                    <h4><strong>Muerte de Jezabel</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">30. </strong>
 Cuando Jezabel se enteró de que Jehú estaba regresando a Jezrel,  se sombreó los ojos,  se arregló el cabello y se asomó a la ventana. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">31. </strong>
 Al entrar Jehú por la *puerta de la ciudad,  ella le preguntó:  
 ¿Cómo estás,  Zimri,  asesino de tu señor?[6] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">32. </strong>
 Levantando la vista hacia la ventana,  Jehú gritó:  
 ¿Quién está de mi parte?  ¿Quién?  
 Entonces se asomaron dos o tres oficiales, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">33. </strong>
 [7]  y Jehú les ordenó:  
 ¡Arrójenla de allí!  
 Así lo hicieron,  y su sangre salpicó la pared y a los caballos que la pisotearon. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">34. </strong>
 Luego Jehú se sentó a comer y beber,  y dio esta orden:  
 Ocúpense de esa maldita mujer;  denle sepultura,  pues era hija de un rey. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">35. </strong>
 Pero cuando fueron a enterrarla,  no encontraron más que el cráneo,  los pies y las manos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
 Así que volvieron para informarle a Jehú,  y éste comentó:  
 Se ha cumplido la palabra que el Señor dio a conocer por medio de su siervo Elías el tisbita,  que dijo:  En el campo de Jezrel los perros se comerán a Jezabel. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 [8]  De hecho,  el cadáver de Jezabel será como estiércol en el campo de Jezrel,  y nadie podrá identificarla ni decir:  Ésta era Jezabel. </p>
                                                        
