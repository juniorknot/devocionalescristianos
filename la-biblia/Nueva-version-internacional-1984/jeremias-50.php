                                                                                <p style="style=text-align: justify;"><strong id="numeros">19. </strong>
 Haré que Israel vuelva a su prado 
 y que se alimente en el Carmelo y en Basán. 
 en las montañas de Efraín y Galaad. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">20. </strong>
 En aquellos días se buscará la iniquidad de Israel, 
 pero ya no se encontrará. 
 En aquel tiempo se buscarán los pecados de Judá, 
 pero ya no se hallarán, 
 porque yo perdonaré a los que deje con vida 
 afirma el Señor. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">21. </strong>
 "¡Ataca el país de Meratayin 
 y a los que viven en Pecod! 
 ¡Mátalos, *destrúyelos por completo! 
 ¡Cumple con todas mis órdenes! 
 afirma el Señor. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">22. </strong>
 ¡En el país hay estruendo de guerra 
 y de impresionante destrucción! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">23. </strong>
 ¡Cómo ha sido quebrado y derribado 
 el martillo de toda la tierra! 
 ¡Babilonia ha quedado desolada 
 en medio de las naciones! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">24. </strong>
 Te tendí una trampa, y en ella caíste 
 antes de que te dieras cuenta. 
 Fuiste sorprendida y apresada, 
 porque te opusiste al Señor. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">25. </strong>
 El Señor ha abierto su arsenal, 
 y ha sacado las armas de su ira; 
 el Señor omnipotente, el Todopoderoso, 
 tiene una tarea que cumplir 
 en el país de los babilonios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">26. </strong>
 ¡Atáquenla desde los confines de la tierra! 
 ¡Abran sus graneros! 
 ¡Amontónenla como a las gavillas! 
 ¡Destrúyanla por completo! 
 ¡Que no quede nada de ella! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">27. </strong>
 ¡Maten a todos sus novillos! 
 ¡Llévenlos al matadero! 
 ¡Ay de ellos, pues les ha llegado el día, 
 el día de su castigo! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">28. </strong>
 Se oye la voz de los fugitivos, 
 de los que escaparon de Babilonia; 
 vienen a anunciar en Sión 
 la venganza del Señor, nuestro Dios, 
 la venganza por su templo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">29. </strong>
 "Recluten contra Babilonia a los arqueros, 
 a todos los que tensan el arco; 
 acampen a su alrededor, 
 y que no escape ninguno. 
 Retribúyanle según sus obras, 
 páguenle con la misma moneda. 
 Porque ella ha desafiado al Señor, 
 al *Santo de Israel. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">30. </strong>
 Por eso en aquel día 
 caerán sus jóvenes en las calles 
 y perecerán todos sus soldados 
 afirma el Señor. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">31. </strong>
 "Estoy contra ti, nación arrogante 
 afirma el Señor, el Señor Todopoderoso; 
 al fin ha llegado el día, 
 el día de tu castigo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">32. </strong>
 El arrogante tropezará y caerá, 
 y no habrá quien lo ayude a levantarse. 
 Prenderé fuego a todas sus ciudades, 
 fuego que consumirá cuanto le rodea." </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">33. </strong>
 Así dice el Señor Todopoderoso: 

 "Israel y Judá son pueblos oprimidos; 
 sus enemigos los tienen apresados, 
 no los dejan en libertad. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">34. </strong>
 Pero su redentor es fuerte, 
 su *nombre es el Señor Todopoderoso. 
 Con vigor defenderá su causa; 
 traerá la *paz a la tierra, 
 pero a Babilonia, el terror. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">35. </strong>
 "¡Muerte a[1] los babilonios! 
 ¡Muerte a sus jefes y sabios! 
 afirma el Señor. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
 ¡Muerte a sus falsos profetas! 
 ¡Que pierdan la razón! 
 ¡Muerte a sus guerreros! 
 ¡Que queden aterrorizados! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 ¡Muerte a sus caballos y carros! 
 ¡Muerte a todos sus mercenarios! 
 ¡Que se vuelvan como mujeres! 
 ¡Muerte a sus tesoros! 
 ¡Que sean saqueados! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 ¡Muerte a sus aguas! 
 ¡Que queden secas! 
 Porque Babilonia es un país de ídolos, 
 de ídolos terribles que provocan la locura. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 "Por eso las fieras del desierto 
 vivirán allí con las hienas; 
 también los avestruces harán allí su morada. 
 Nunca más volverá a ser habitada; 
 quedará despoblada para siempre. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
 Será como cuando Dios destruyó a Sodoma y Gomorra, 
 y a sus ciudades vecinas; 
 allí nadie volverá a vivir, 
 ni la habitará ningún *ser humano 
 afirma el Señor. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 "Del norte viene un ejército; 
 desde los confines de la tierra 
 se preparan una gran nación y muchos reyes. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 Vienen armados con arcos y lanzas; 
 son crueles y desalmados. 
 Vienen montados a caballo; 
 su estruendo es como el bramido del mar. 
 Contra ti, bella Babilonia, contra ti 
 marchan en formación de combate, 
 alineados como un solo hombre. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 El rey de Babilonia ha escuchado la noticia, 
 y sus brazos flaquean; 
 de él se apodera la angustia 
 y le vienen dolores de parto. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 Como león que sale de los matorrales del Jordán 
 hacia praderas de verdes pastos, 
 en un instante espantaré de su tierra a los de Babilonia, 
 y sobre ellos nombraré a mi elegido. 
 Porque, ¿quién como yo? 
 ¿Quién me puede desafiar? 
 ¿Qué pastor se me puede oponer?" </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 Por eso, escuchen el plan que el Señor 
 ha diseñado contra Babilonia, 
 escuchen lo que tiene proyectado 
 en contra del país de los babilonios: 
 los más pequeños del rebaño; 
 por causa de ellos, 
 sus praderas quedarán asoladas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 Tiembla la tierra 
 por la estruendosa caída de Babilonia; 
 resuenan sus gritos 
 en medio de las naciones. </p>
                                                        
