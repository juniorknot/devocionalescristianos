<h2 class="text-center"> Colosenses 3 - Arcas-Fernandez (Nuevo Testamento)</h2>
<p style="style=text-align: justify;"><strong id="numeros">1. </strong>
¡Habéis resucitado con Cristo! Orientad, pues, vuestra vida hacia el cielo, donde está Cristo sentado al lado de Dios, en el lugar de honor. </p>
<p style="style=text-align: justify;"><strong id="numeros">2. </strong>
Poned el corazón en las realidades celestiales y no en las de la tierra. </p>
<p style="style=text-align: justify;"><strong id="numeros">3. </strong>
Muertos al mundo, vuestra vida está escondida con Cristo en Dios. </p>
<p style="style=text-align: justify;"><strong id="numeros">4. </strong>
Cuando Cristo, vida vuestra, se manifieste, también vosotros apareceréis, junto a él, llenos de gloria. </p>
<h4><strong>La vida antigua y la nueva</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">5. </strong>
Destruid lo mucho de mundano que hay en vosotros: la lujuria, la impureza , las pasiones desenfrenadas, los malos deseos y la avaricia, que es una especie de idolatría. </p>
<p style="style=text-align: justify;"><strong id="numeros">6. </strong>
Esto es lo que enciende la ira de Dios sobre quienes se niegan a obedecerle, y </p>
<p style="style=text-align: justify;"><strong id="numeros">7. </strong>
lo que en otro tiempo constituyó vuestra norma de conducta y de vida. </p>
<p style="style=text-align: justify;"><strong id="numeros">8. </strong>
Ahora es preciso que renunciéis a todo eso: a la ira, el rencor, la malquerencia, la calumnia y la grosería. </p>
<p style="style=text-align: justify;"><strong id="numeros">9. </strong>
No andéis engañándoos unos a otros. Despojaos de la vieja y pecadora condición humana </p>
<p style="style=text-align: justify;"><strong id="numeros">10. </strong>
y convertíos en hombres nuevos, hombres que van renovándose sin cesar a imagen de su Creador, en busca de un conocimiento cada vez más profundo. </p>
<p style="style=text-align: justify;"><strong id="numeros">11. </strong>
Ya no hay fronteras de raza, religión, cultura o posición social, sino que Cristo es todo en todos. </p>
<p style="style=text-align: justify;"><strong id="numeros">12. </strong>
Sois elegidos de Dios; él os ha consagrado y os ha dado su amor. Sed, pues, profundamente compasivos, benignos, humildes, pacientes y comprensivos. </p>
<p style="style=text-align: justify;"><strong id="numeros">13. </strong>
Soportaos mutuamente, y así como el Señor os perdonó, perdonaos también vosotros, si alguno tiene quejas contra otro. </p>
<p style="style=text-align: justify;"><strong id="numeros">14. </strong>
Y, por encima de todo, practicad el amor, que es la cumbre de la perfección. </p>
<p style="style=text-align: justify;"><strong id="numeros">15. </strong>
Que la paz de Cristo reine en vuestras vida; a ella os ha llamado Dios para formar un solo cuerpo. Sed agradecidos. </p>
<p style="style=text-align: justify;"><strong id="numeros">16. </strong>
El mensaje de Cristo llene con toda su riqueza vuestros corazones, y sed de veras maestros y consejeros los unos de los otros. Con un corazón profundamente agradecido, cantad a Dios salmos, himnos y canciones inspiradas. </p>
<p style="style=text-align: justify;"><strong id="numeros">17. </strong>
En fin, cuanto hagáis o digáis, hacedlo en nombre de Jesús, el Señor, dando gracias a Dios Padre por medio de él. </p>
<h4><strong>Deberes sociales de la nueva vida</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">18. </strong>
Esposas, respetad la autoridad de vuestros maridos; tal es vuestro deber como cristianas. </p>
<p style="style=text-align: justify;"><strong id="numeros">19. </strong>
Maridos, amad a vuestras mujeres y nunca las tratéis con aspereza. </p>
<p style="style=text-align: justify;"><strong id="numeros">20. </strong>
De vosotros, hijos, el Señor espera que obedezcáis puntualmente a vuestros padres. </p>
<p style="style=text-align: justify;"><strong id="numeros">21. </strong>
Por vuestra parte, padres, educad con tacto a vuestros hijos, para que no se desalienten. </p>
<p style="style=text-align: justify;"><strong id="numeros">22. </strong>
Esclavos, acatad en todo momento con respeto las órdenes de los amos temporales. No en plan adulador o como alguien que se siente vigilado, sino con la nobleza de los que temen al Señor. </p>
<p style="style=text-align: justify;"><strong id="numeros">23. </strong>
Poned el corazón en lo que hagáis, como si lo dedicaseis al Señor y no a los hombres. </p>
<p style="style=text-align: justify;"><strong id="numeros">24. </strong>
Sabed que el Señor os dará, al fin y al cabo, la herencia eterna como premio y que sois esclavos de Cristo, el Señor. </p>
<p style="style=text-align: justify;"><strong id="numeros">25. </strong>
En cuanto al malvado, Dios le dará su merecido sin favoritismo alguno. </p>
