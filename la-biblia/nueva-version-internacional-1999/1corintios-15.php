<li class="hide-for-large"><a href="whatsapp://send?text=1 Corintios 15 (NVI)%0A%0A 1. La resurrección de Cristo
 Ahora, hermanos, quiero recordarles el evangelio que les prediqué, el mismo que recibieron y en el cual se mantienen firmes. 2. Mediante este evangelio son salvos, si se aferran a la palabra que les prediqué. De otro modo, habrán creído en vano. 3. Porque ante todo[q]  les transmití a ustedes lo que yo mismo recibí: que Cristo murió por nuestros pecados según las Escrituras, 4. que fue sepultado, que resucitó al tercer día según las Escrituras, 5. y que se apareció a Cefas, y luego a los doce. 6. Después se apareció a más de quinientos hermanos a la vez, la mayoría de los cuales vive todavía, aunque algunos han muerto. 7. Luego se apareció a Jacobo, más tarde a todos los apóstoles, 8. y por último, como a uno nacido fuera de tiempo, se me apareció también a mí. 9. Admito que yo soy el más insignificante de los apóstoles y que ni siquiera merezco ser llamado apóstol, porque perseguí a la iglesia de Dios. 10. Pero por la gracia de Dios soy lo que soy, y la gracia que él me concedió no fue infructuosa. Al contrario, he trabajado con más tesón que todos ellos, aunque no yo sino la gracia de Dios que está conmigo. 11. En fin, ya sea que se trate de mí o de ellos, esto es lo que predicamos, y esto es lo que ustedes han creído. 12. La resurrección de los muertos
 Ahora bien, si se predica que Cristo ha sido levantado de entre los muertos, ¿cómo dicen algunos de ustedes que no hay resurrección? 13. Si no hay resurrección, entonces ni siquiera Cristo ha resucitado. 14. Y si Cristo no ha resucitado, nuestra predicación no sirve para nada, como tampoco la fe de ustedes. 15. Aún más, resultaríamos falsos testigos de Dios por haber testificado que Dios resucitó a Cristo, lo cual no habría sucedido, si en verdad los muertos no resucitan. 16. Porque si los muertos no resucitan, tampoco Cristo ha resucitado. 17. Y si Cristo no ha resucitado, la fe de ustedes es ilusoria y todavía están en sus pecados. 18. En este caso, también están perdidos los que murieron en Cristo. 19. Si la esperanza que tenemos en Cristo fuera sólo para esta vida, seríamos los más desdichados de todos los mortales. 20. Lo cierto es que Cristo ha sido levantado de entre los muertos, como primicias de los que murieron. 21. De hecho, ya que la muerte vino por medio de un hombre, también por medio de un hombre viene la resurrección de los muertos. 22. Pues así como en Adán todos mueren, también en Cristo todos volverán a vivir, 23. pero cada uno en su debido orden: Cristo, las primicias; después, cuando él venga, los que le pertenecen. 24. Entonces vendrá el fin, cuando él entregue el reino a Dios el Padre, luego de destruir todo dominio, autoridad y poder. 25. Porque es necesario que Cristo reine hasta poner a todos sus enemigos debajo de sus pies. 26. El último enemigo que será destruido es la muerte, 27. pues Dios «ha sometido todo a su dominio».[r]  Al decir que «todo» ha quedado sometido a su dominio, es claro que no se incluye a Dios mismo, quien todo lo sometió a Cristo. 28. Y cuando todo le sea sometido, entonces el Hijo mismo se someterá a aquel que le sometió todo, para que Dios sea todo en todos. 29. Si no hay resurrección, ¿qué sacan los que se bautizan por los muertos? Si en definitiva los muertos no resucitan, ¿por qué se bautizan por ellos? 30. Y no