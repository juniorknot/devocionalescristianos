 12:1-8-Mr 2:23-28; Lc 6:1-5
 12:9-14-Mr 3:1-6; Lc 6:6-11
 Por aquel tiempo pasaba Jesús por los sembrados en sábado. Sus discípulos tenían hambre, así que comenzaron a arrancar algunas espigas de trigo y comérselas. 2. Al ver esto, los fariseos le dijeron: -¡Mira! Tus discípulos están haciendo lo que está prohibido en sábado. 3. Él les contestó: -¿No han leído lo que hizo David en aquella ocasión en que él y sus compañeros tuvieron hambre? 4. Entró en la casa de Dios, y él y sus compañeros comieron los panes consagrados a Dios, lo que no se les permitía a ellos sino sólo a los sacerdotes. 5. ¿O no han leído en la ley que los sacerdotes en el templo profanan el sábado sin incurrir en culpa? 6. Pues yo les digo que aquí está uno más grande que el templo. 7. Si ustedes supieran lo que significa: “Lo que pido de ustedes es misericordia y no sacrificios”,[s]  no condenarían a los que no son culpables. 8. Sepan que el Hijo del hombre es Señor del sábado. 9. Pasando de allí, entró en la sinagoga, 10. donde había un hombre que tenía una mano paralizada. Como buscaban un motivo para acusar a Jesús, le preguntaron: -¿Está permitido sanar en sábado? 11. Él les contestó: -Si alguno de ustedes tiene una oveja y en sábado se le cae en un hoyo, ¿no la agarra y la saca? 12. ¡Cuánto más vale un hombre que una oveja! Por lo tanto, está permitido hacer el bien en sábado. 13. Entonces le dijo al hombre: -Extiende la mano. Así que la extendió y le quedó restablecida, tan sana como la otra. 14. Pero los fariseos salieron y tramaban cómo matar a Jesús. 15. El siervo escogido por Dios
 Consciente de esto, Jesús se retiró de aquel lugar. Muchos lo siguieron, y él sanó a todos los enfermos, 16. pero les ordenó que no dijeran quién era él. 17. Esto fue para que se cumpliera lo dicho por el profeta Isaías: 18. «Éste es mi siervo, a quien he escogido, mi amado, en quien estoy muy complacido; sobre él pondré mi Espíritu, y proclamará justicia a las naciones. 19. No disputará ni gritará; nadie oirá su voz en las calles. 20. No acabará de romper la caña quebrada ni apagará la mecha que apenas arde, hasta que haga triunfar la justicia. 21. Y en su nombre pondrán las naciones su esperanza.»[t] 22. Jesús y Beelzebú
 12:25-29-Mr 3:23-27; Lc 11:17-22
 Un día le llevaron un endemoniado que estaba ciego y mudo, y Jesús lo sanó, de modo que pudo ver y hablar. 23. Toda la gente se quedó asombrada y decía: «¿No será éste el Hijo de David?» 24. Pero al oírlo los fariseos, dijeron: «Éste no expulsa a los demonios sino por medio de Beelzebú, príncipe de los demonios.» 25. Jesús conocía sus pensamientos, y les dijo: «Todo reino dividido contra sí mismo quedará asolado, y toda ciudad o familia dividida contra sí misma no se mantendrá en pie. 26. Si Satanás expulsa a Satanás, está dividido contra sí mismo. ¿Cómo puede, entonces, mantenerse en pie su reino? 27. Ahora bien, si yo expulso a los demonios por medio de Beelzebú, ¿los seguidores de ustedes por medio de quién los expulsan? Por eso ellos mismos los juzgarán a ustedes. 28. En cambio, si expulso a los demonios por medio del Espíritu de Dios, eso significa que el reino de Dios ha llegado a ustedes. 29. »¿O cómo puede entrar alguien en la casa de un hombre fuerte y arrebatarle sus bienes, a menos que primero lo ate? Sólo entonces podrá robar su casa. 30. »El que no está de mi parte, está contra mí; y el que conmigo no recoge, esparce. 31. Por eso les digo que a todos se les podrá perdonar todo pecado y toda blasfemia, pero la blasfemia contra el Espíritu no se le perdonará a nadie. 32. A cualquiera que pronuncie alguna palabra contra el Hijo del hombre se le perdonará, pero el que hable contra el Espíritu Santo no tendrá perdón ni en este mundo ni en el venidero. 33. »Si tienen un buen árbol, su fruto es bueno; si tienen un mal árbol, su fruto es malo. Al árbol se le reconoce por su fruto. 34. Camada de víboras, ¿cómo pueden ustedes que son malos decir algo bueno? De la abundancia del corazón habla la boca. 35. El que es bueno, de la bondad que atesora en el corazón saca el bien, pero el que es malo, de su maldad saca el mal. 36. Pero yo les digo que en el día del juicio todos tendrán que dar cuenta de toda palabra ociosa que hayan pronunciado. 37. Porque por tus palabras se te absolverá, y por tus palabras se te condenará.» 38. La señal de Jonás
