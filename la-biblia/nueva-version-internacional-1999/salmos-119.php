<p style="style=text-align: justify;"><strong id="numeros">83. </strong>Parezco un odre ennegrecido por el humo, pero no me olvido de tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">84. </strong>¿Cuánto más vivirá este siervo tuyo? ¿Cuándo juzgarás a mis perseguidores?</p>
<p style="style=text-align: justify;"><strong id="numeros">85. </strong>Me han cavado trampas los insolentes, los que no viven conforme a tu ley.</p>
<p style="style=text-align: justify;"><strong id="numeros">86. </strong>Todos tus mandamientos son fidedignos; ¡ayúdame!, pues falsos son mis perseguidores.</p>
<p style="style=text-align: justify;"><strong id="numeros">87. </strong>Por poco me borran de la tierra, pero yo no abandono tus preceptos.</p>
<p style="style=text-align: justify;"><strong id="numeros">88. </strong>Por tu gran amor, dame vida y cumpliré tus estatutos.</p>
<h4><strong>Lámed</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">89. </strong>Lámed
Tu palabra, Señor, es eterna, y está firme en los cielos.</p>
<p style="style=text-align: justify;"><strong id="numeros">90. </strong>Tu fidelidad permanece para siempre; estableciste la tierra, y quedó firme.</p>
<p style="style=text-align: justify;"><strong id="numeros">91. </strong>Todo subsiste hoy, conforme a tus decretos, porque todo está a tu servicio.</p>
<p style="style=text-align: justify;"><strong id="numeros">92. </strong>Si tu ley no fuera mi regocijo, la aflicción habría acabado conmigo.</p>
<p style="style=text-align: justify;"><strong id="numeros">93. </strong>Jamás me olvidaré de tus preceptos, pues con ellos me has dado vida.</p>
<p style="style=text-align: justify;"><strong id="numeros">94. </strong>¡Sálvame, pues te pertenezco y escudriño tus preceptos!</p>
<p style="style=text-align: justify;"><strong id="numeros">95. </strong>Los impíos me acechan para destruirme, pero yo me esfuerzo por entender tus estatutos.</p>
<p style="style=text-align: justify;"><strong id="numeros">96. </strong>He visto que aun la perfección tiene sus límites; ¡sólo tus mandamientos son infinitos!</p>
<h4><strong>Mem</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">97. </strong>Mem
¡Cuánto amo yo tu ley! Todo el día medito en ella.</p>
<p style="style=text-align: justify;"><strong id="numeros">98. </strong>Tus mandamientos me hacen más sabio que mis enemigos porque me pertenecen para siempre.</p>
<p style="style=text-align: justify;"><strong id="numeros">99. </strong>Tengo más discernimiento que todos mis maestros porque medito en tus estatutos.</p>
<p style="style=text-align: justify;"><strong id="numeros">100. </strong>100 Tengo más entendimiento que los ancianos porque obedezco tus preceptos.</p>
<p style="style=text-align: justify;"><strong id="numeros">101. </strong>101 Aparto mis pies de toda mala senda para cumplir con tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">102. </strong>102 No me desvío de tus juicios porque tú mismo me instruyes.</p>
<p style="style=text-align: justify;"><strong id="numeros">103. </strong>103 ¡Cuán dulces son a mi paladar tus palabras! ¡Son más dulces que la miel a mi boca!</p>
<p style="style=text-align: justify;"><strong id="numeros">104. </strong>104 De tus preceptos adquiero entendimiento; por eso aborrezco toda senda de mentira.</p>
<h4><strong>Nun</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">105. </strong>Nun
105 Tu palabra es una lámpara a mis pies; es una luz en mi sendero.</p>
<p style="style=text-align: justify;"><strong id="numeros">106. </strong>106 Hice un juramento, y lo he confirmado: que acataré tus rectos juicios.</p>
<p style="style=text-align: justify;"><strong id="numeros">107. </strong>107 Señor, es mucho lo que he sufrido; dame vida conforme a tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">108. </strong>108 Señor, acepta la ofrenda que brota de mis labios; enséñame tus juicios.</p>
<p style="style=text-align: justify;"><strong id="numeros">109. </strong>109 Mi vida pende de un hilo,[l] pero no me olvido de tu ley.</p>
<p style="style=text-align: justify;"><strong id="numeros">110. </strong>110 Los impíos me han tendido una trampa, pero no me aparto de tus preceptos.</p>
<p style="style=text-align: justify;"><strong id="numeros">111. </strong>111 Tus estatutos son mi herencia permanente; son el regocijo de mi corazón.</p>
<p style="style=text-align: justify;"><strong id="numeros">112. </strong>112 Inclino mi corazón a cumplir tus decretos para siempre y hasta el fin.</p>
<h4><strong>Sámec</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">113. </strong>Sámej
113 Aborrezco a los hipócritas, pero amo tu ley.</p>
<p style="style=text-align: justify;"><strong id="numeros">114. </strong>114 Tú eres mi escondite y mi escudo; en tu palabra he puesto mi esperanza.</p>
<p style="style=text-align: justify;"><strong id="numeros">115. </strong>115 ¡Malhechores, apártense de mí, que quiero cumplir los mandamientos de mi Dios!</p>
<p style="style=text-align: justify;"><strong id="numeros">116. </strong>116 Sosténme conforme a tu promesa, y viviré; no defraudes mis esperanzas.</p>
<p style="style=text-align: justify;"><strong id="numeros">117. </strong>117 Defiéndeme, y estaré a salvo; siempre optaré por tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">118. </strong>118 Tú rechazas a los que se desvían de tus decretos, porque sólo maquinan falsedades.</p>
<p style="style=text-align: justify;"><strong id="numeros">119. </strong>119 Tú desechas como escoria a los impíos de la tierra; por eso amo tus estatutos.</p>
<p style="style=text-align: justify;"><strong id="numeros">120. </strong>120 Mi cuerpo se estremece por el temor que me inspiras; siento reverencia por tus leyes.</p>
<h4><strong>Ayin</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">121. </strong>Ayin
121 Yo practico la justicia y el derecho; no me dejes en manos de mis opresores.</p>
<p style="style=text-align: justify;"><strong id="numeros">122. </strong>122 Garantiza el bienestar de tu siervo; que no me opriman los arrogantes.</p>
<p style="style=text-align: justify;"><strong id="numeros">123. </strong>123 Mis ojos se consumen esperando tu salvación, esperando que se cumpla tu justicia.</p>
<p style="style=text-align: justify;"><strong id="numeros">124. </strong>124 Trata a tu siervo conforme a tu gran amor; enséñame tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">125. </strong>125 Tu siervo soy: dame entendimiento y llegaré a conocer tus estatutos.</p>
<p style="style=text-align: justify;"><strong id="numeros">126. </strong>126 Señor, ya es tiempo de que actúes, pues tu ley está siendo quebrantada.</p>
<p style="style=text-align: justify;"><strong id="numeros">127. </strong>127 Sobre todas las cosas amo tus mandamientos, más que el oro, más que el oro refinado.</p>
<p style="style=text-align: justify;"><strong id="numeros">128. </strong>128 Por eso tomo en cuenta todos tus preceptos[m] y aborrezco toda senda falsa.</p>
<h4><strong>Pe</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">129. </strong>Pe
129 Tus estatutos son maravillosos; por eso los obedezco.</p>
<p style="style=text-align: justify;"><strong id="numeros">130. </strong>130 La exposición de tus palabras nos da luz, y da entendimiento al sencillo.</p>
<p style="style=text-align: justify;"><strong id="numeros">131. </strong>131 Jadeante abro la boca porque ansío tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">132. </strong>132 Vuélvete a mí, y tenme compasión como haces siempre con los que aman tu nombre.</p>
<p style="style=text-align: justify;"><strong id="numeros">133. </strong>133 Guía mis pasos conforme a tu promesa; no dejes que me domine la iniquidad.</p>
<p style="style=text-align: justify;"><strong id="numeros">134. </strong>134 Líbrame de la opresión humana, pues quiero obedecer tus preceptos.</p>
<p style="style=text-align: justify;"><strong id="numeros">135. </strong>135 Haz brillar tu rostro sobre tu siervo; enséñame tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">136. </strong>136 Ríos de lágrimas brotan de mis ojos, porque tu ley no se obedece.</p>
<h4><strong>Tsade</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">137. </strong>Tsade
137 Señor, tú eres justo, y tus juicios son rectos.</p>
<p style="style=text-align: justify;"><strong id="numeros">138. </strong>138 Justos son los estatutos que has ordenado, y muy dignos de confianza.</p>
<p style="style=text-align: justify;"><strong id="numeros">139. </strong>139 Mi celo me consume, porque mis adversarios pasan por alto tus palabras.</p>
<p style="style=text-align: justify;"><strong id="numeros">140. </strong>140 Tus promesas han superado muchas pruebas, por eso tu siervo las ama.</p>
<p style="style=text-align: justify;"><strong id="numeros">141. </strong>141 Insignificante y menospreciable como soy, no me olvido de tus preceptos.</p>
<p style="style=text-align: justify;"><strong id="numeros">142. </strong>142 Tu justicia es siempre justa; tu ley es la verdad.</p>
<p style="style=text-align: justify;"><strong id="numeros">143. </strong>143 He caído en la angustia y la aflicción, pero tus mandamientos son mi regocijo.</p>
<p style="style=text-align: justify;"><strong id="numeros">144. </strong>144 Tus estatutos son siempre justos; dame entendimiento para poder vivir.</p>
<h4><strong>Cof</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">145. </strong>Qof
145 Con todo el corazón clamo a ti, Señor; respóndeme, y obedeceré tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">146. </strong>146 A ti clamo: «¡Sálvame!» Quiero cumplir tus estatutos.</p>
<p style="style=text-align: justify;"><strong id="numeros">147. </strong>147 Muy de mañana me levanto a pedir ayuda; en tus palabras he puesto mi esperanza.</p>
<p style="style=text-align: justify;"><strong id="numeros">148. </strong>148 En toda la noche no pego los ojos,[n] para meditar en tu promesa.</p>
<p style="style=text-align: justify;"><strong id="numeros">149. </strong>149 Conforme a tu gran amor, escucha mi voz; conforme a tus juicios, Señor, dame vida.</p>
<p style="style=text-align: justify;"><strong id="numeros">150. </strong>150 Ya se acercan mis crueles perseguidores, pero andan muy lejos de tu ley.</p>
<p style="style=text-align: justify;"><strong id="numeros">151. </strong>151 Tú, Señor, también estás cerca, y todos tus mandamientos son verdad.</p>
<p style="style=text-align: justify;"><strong id="numeros">152. </strong>152 Desde hace mucho conozco tus estatutos, los cuales estableciste para siempre.</p>
<h4><strong>Resh</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">153. </strong>Resh
153 Considera mi aflicción, y líbrame, pues no me he olvidado de tu ley.</p>
<p style="style=text-align: justify;"><strong id="numeros">154. </strong>154 Defiende mi causa, rescátame; dame vida conforme a tu promesa.</p>
<p style="style=text-align: justify;"><strong id="numeros">155. </strong>155 La salvación está lejos de los impíos, porque ellos no buscan tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">156. </strong>156 Grande es, Señor, tu compasión; dame vida conforme a tus juicios.</p>
<p style="style=text-align: justify;"><strong id="numeros">157. </strong>157 Muchos son mis adversarios y mis perseguidores, pero yo no me aparto de tus estatutos.</p>
<p style="style=text-align: justify;"><strong id="numeros">158. </strong>158 Miro a esos renegados y me dan náuseas, porque no cumplen tus palabras.</p>
<p style="style=text-align: justify;"><strong id="numeros">159. </strong>159 Mira, Señor, cuánto amo tus preceptos; conforme a tu gran amor, dame vida.</p>
<p style="style=text-align: justify;"><strong id="numeros">160. </strong>160 La suma de tus palabras es la verdad; tus rectos juicios permanecen para siempre.</p>
<h4><strong>Sin</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">161. </strong>Shin
161 Gente poderosa[ñ] me persigue sin motivo, pero mi corazón se asombra ante tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">162. </strong>162 Yo me regocijo en tu promesa como quien halla un gran botín.</p>
<p style="style=text-align: justify;"><strong id="numeros">163. </strong>163 Aborrezco y repudio la falsedad, pero amo tu ley.</p>
<p style="style=text-align: justify;"><strong id="numeros">164. </strong>164 Siete veces al día te alabo por tus rectos juicios.</p>
<p style="style=text-align: justify;"><strong id="numeros">165. </strong>165 Los que aman tu ley disfrutan de gran bienestar, y nada los hace tropezar.</p>
<p style="style=text-align: justify;"><strong id="numeros">166. </strong>166 Yo, Señor, espero tu salvación y practico tus mandamientos.</p>
<p style="style=text-align: justify;"><strong id="numeros">167. </strong>167 Con todo mi ser cumplo tus estatutos. ¡Cuánto los amo!</p>
<p style="style=text-align: justify;"><strong id="numeros">168. </strong>168 Obedezco tus preceptos y tus estatutos, porque conoces todos mis caminos.</p>
<h4><strong>Tau</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">169. </strong>Tav
169 Que llegue mi clamor a tu presencia; dame entendimiento, Señor, conforme a tu palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">170. </strong>170 Que llegue a tu presencia mi súplica; líbrame, conforme a tu promesa.</p>
<p style="style=text-align: justify;"><strong id="numeros">171. </strong>171 Que rebosen mis labios de alabanza, porque tú me enseñas tus decretos.</p>
<p style="style=text-align: justify;"><strong id="numeros">172. </strong>172 Que entone mi lengua un cántico a tu palabra, pues todos tus mandamientos son justos.</p>
<p style="style=text-align: justify;"><strong id="numeros">173. </strong>173 Que acuda tu mano en mi ayuda, porque he escogido tus preceptos.</p>
<p style="style=text-align: justify;"><strong id="numeros">174. </strong>174 Yo, Señor, ansío tu salvación. Tu ley es mi regocijo.</p>
<p style="style=text-align: justify;"><strong id="numeros">175. </strong>175 Déjame vivir para alabarte; que vengan tus juicios a ayudarme.</p>
<p style="style=text-align: justify;"><strong id="numeros">176. </strong>176 Cual oveja perdida me he extraviado; ven en busca de tu siervo, porque no he olvidado tus mandamientos.</p>
