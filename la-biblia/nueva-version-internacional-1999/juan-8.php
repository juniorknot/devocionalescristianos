<p style="style=text-align: justify;"><strong id="numeros">45. </strong>Y sin embargo a mí, que les digo la verdad, no me creen.</p>
<p style="style=text-align: justify;"><strong id="numeros">46. </strong>¿Quién de ustedes me puede probar que soy culpable de pecado? Si digo la verdad, ¿por qué no me creen?</p>
<p style="style=text-align: justify;"><strong id="numeros">47. </strong>El que es de Dios escucha lo que Dios dice. Pero ustedes no escuchan, porque no son de Dios.</p>
<h4><strong>La preexistencia de Cristo</strong></h4> <p style="style=text-align: justify;"><strong id="numeros">48. </strong>Declaración de Jesús acerca de sí mismo
-¿No tenemos razón al decir que eres un samaritano, y que estás endemoniado? -replicaron los judíos.</p>
<p style="style=text-align: justify;"><strong id="numeros">49. </strong>-No estoy poseído por ningún demonio -contestó Jesús-. Tan sólo honro a mi Padre; pero ustedes me deshonran a mí.</p>
<p style="style=text-align: justify;"><strong id="numeros">50. </strong>Yo no busco mi propia gloria; pero hay uno que la busca, y él es el juez.</p>
<p style="style=text-align: justify;"><strong id="numeros">51. </strong>Ciertamente les aseguro que el que cumple mi palabra, nunca morirá.</p>
<p style="style=text-align: justify;"><strong id="numeros">52. </strong>-¡Ahora estamos convencidos de que estás endemoniado! -exclamaron los judíos-. Abraham murió, y también los profetas, pero tú sales diciendo que si alguno guarda tu palabra, nunca morirá.</p>
<p style="style=text-align: justify;"><strong id="numeros">53. </strong>¿Acaso eres tú mayor que nuestro padre Abraham? Él murió, y también murieron los profetas. ¿Quién te crees tú?</p>
<p style="style=text-align: justify;"><strong id="numeros">54. </strong>-Si yo me glorifico a mí mismo -les respondió Jesús-, mi gloria no significa nada. Pero quien me glorifica es mi Padre, el que ustedes dicen que es su Dios,</p>
<p style="style=text-align: justify;"><strong id="numeros">55. </strong>aunque no lo conocen. Yo, en cambio, sí lo conozco. Si dijera que no lo conozco, sería tan mentiroso como ustedes; pero lo conozco y cumplo su palabra.</p>
<p style="style=text-align: justify;"><strong id="numeros">56. </strong>Abraham, el padre de ustedes, se regocijó al pensar que vería mi día; y lo vio y se alegró.</p>
<p style="style=text-align: justify;"><strong id="numeros">57. </strong>-Ni a los cincuenta años llegas -le dijeron los judíos-, ¿y has visto a Abraham?</p>
<p style="style=text-align: justify;"><strong id="numeros">58. </strong>-Ciertamente les aseguro que, antes de que Abraham naciera, ¡yo soy!</p>
<p style="style=text-align: justify;"><strong id="numeros">59. </strong>Entonces los judíos tomaron piedras para arrojárselas, pero Jesús se escondió y salió inadvertido del templo.[ñ]</p>
