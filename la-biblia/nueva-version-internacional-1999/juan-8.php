<li class="hide-for-large"><a href="whatsapp://send?text=Juan 8 (NVI)%0A%0A 1. La mujer sorprendida en adulterio
 Pero Jesús se fue al monte de los Olivos. 2. Al amanecer se presentó de nuevo en el templo. Toda la gente se le acercó, y él se sentó a enseñarles. 3. Los maestros de la ley y los fariseos llevaron entonces a una mujer sorprendida en adulterio, y poniéndola en medio del grupo 4. le dijeron a Jesús: -Maestro, a esta mujer se le ha sorprendido en el acto mismo de adulterio. 5. En la ley Moisés nos ordenó apedrear a tales mujeres. ¿Tú qué dices? 6. Con esta pregunta le estaban tendiendo una trampa, para tener de qué acusarlo. Pero Jesús se inclinó y con el dedo comenzó a escribir en el suelo. 7. Y como ellos lo acosaban a preguntas, Jesús se incorporó y les dijo: -Aquel de ustedes que esté libre de pecado, que tire la primera piedra. 8. E inclinándose de nuevo, siguió escribiendo en el suelo. 9. Al oír esto, se fueron retirando uno tras otro, comenzando por los más viejos, hasta dejar a Jesús solo con la mujer, que aún seguía allí. 10. Entonces él se incorporó y le preguntó: -Mujer, ¿dónde están?[l]  ¿Ya nadie te condena? 11. -Nadie, Señor. -Tampoco yo te condeno. Ahora vete, y no vuelvas a pecar. 12. Validez del testimonio de Jesús
 Una vez más Jesús se dirigió a la gente, y les dijo: -Yo soy la luz del mundo. El que me sigue no andará en tinieblas, sino que tendrá la luz de la vida. 13. -Tú te presentas como tu propio testigo -alegaron los fariseos-, así que tu testimonio no es válido. 14. -Aunque yo sea mi propio testigo -repuso Jesús-, mi testimonio es válido, porque sé de dónde he venido y a dónde voy. Pero ustedes no saben de dónde vengo ni a dónde voy. 15. Ustedes juzgan según criterios humanos; yo, en cambio, no juzgo a nadie. 16. Y si lo hago, mis juicios son válidos porque no los emito por mi cuenta sino en unión con el Padre que me envió. 17. En la ley de ustedes está escrito que el testimonio de dos personas es válido. 18. Uno de mis testigos soy yo mismo, y el Padre que me envió también da testimonio de mí. 19. -¿Dónde está tu padre? -Si supieran quién soy yo, sabrían también quién es mi Padre. 20. Estas palabras las dijo Jesús en el lugar donde se depositaban las ofrendas, mientras enseñaba en el templo. Pero nadie le echó mano porque aún no había llegado su tiempo. 21. Yo no soy de este mundo
 De nuevo Jesús les dijo: -Yo me voy, y ustedes me buscarán, pero en su pecado morirán. Adonde yo voy, ustedes no pueden ir. 22. Comentaban, por tanto, los judíos: «¿Acaso piensa suicidarse? ¿Será por eso que dice: “Adonde yo voy, ustedes no pueden ir”?» 23. -Ustedes son de aquí abajo -continuó Jesús-; yo soy de allá arriba. Ustedes son de este mundo; yo no soy de este mundo. 24. Por eso les he dicho que morirán en sus pecados, pues si no creen que yo soy el que afirmo ser,[m]  en sus pecados morirán. 25. -¿Quién eres tú? -le preguntaron. -En primer lugar, ¿qué tengo que explicarles?[n]  -contestó Jesús-. 26. Son muchas las cosas que tengo que decir y juzgar de ustedes. Pero el que me envió es veraz, y lo que le he oído decir es lo mismo que le repito al mundo. 27. Ellos no entendieron que les hablaba de su Padre. 28. Por eso Jesús añadió: -Cuando hayan levantado al Hijo del hombre, sabrán ustedes que yo soy, y que no hago nada por mi propia cuenta, sino que hablo conforme a lo que el Padre me ha enseñado. 29. El que me envió está conmigo; no me ha dejado solo, porque siempre hago lo que le agrada. 30. Mientras aún hablaba, muchos creyeron en él. 31. Los hijos de Abraham
