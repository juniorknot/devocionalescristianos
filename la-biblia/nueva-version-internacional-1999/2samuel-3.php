<li class="hide-for-large"><a href="whatsapp://send?text=2 Samuel 3 (NVI)%0A%0A 1. La guerra entre las familias de Saúl y David se prolongó durante mucho tiempo. David consolidaba más y más su reino, en tanto que el de Saúl se iba debilitando. 2. Hijos de David nacidos en Hebrón
 3:2-5-1Cr 3:1-4
 Mientras estuvo en Hebrón, David tuvo los siguientes hijos: Su primogénito fue Amnón hijo de Ajinoán la jezrelita; 3. el segundo, Quileab hijo de Abigaíl, viuda de Nabal de Carmel; el tercero, Absalón hijo de Macá, la hija del rey Talmay de Guesur; 4. el cuarto, Adonías hijo de Jaguit; el quinto, Sefatías hijo de Abital; 5. el sexto, Itreán hijo de Eglá, que era otra esposa de David. Éstos son los hijos que le nacieron a David mientras estuvo en Hebrón. 6. Abner hace un pacto con David
 Durante la guerra entre las familias de Saúl y David, Abner fue consolidando su posición en el reino de Saúl, 7. aunque Isboset le reclamó a Abner el haberse acostado con Rizpa hija de Ayá, que había sido concubina de Saúl. 8. A Abner le molestó mucho el reclamo, así que replicó: -¿Acaso soy un perro de Judá? Hasta el día de hoy me he mantenido fiel a la familia de tu padre Saúl, incluso a sus parientes y amigos, y conste que no te he entregado en manos de David. ¡Y ahora me sales con que he cometido una falta con esa mujer! 9. Que Dios me castigue sin piedad si ahora yo no procedo con David conforme a lo que el Señor le juró: 10. Voy a quitarle el reino a la familia de Saúl y a establecer el trono de David sobre Israel y Judá, desde Dan hasta Berseba. 11. Isboset no se atrevió a responderle a Abner ni una sola palabra, pues le tenía miedo. 12. Entonces Abner envió unos mensajeros a decirle a David: «¿A quién le pertenece la tierra, si no a usted? Haga un pacto conmigo, y yo lo apoyaré para hacer que todo Israel se ponga de su parte.» 13. «Muy bien -respondió David-. Haré un pacto contigo, pero con esta condición: Cuando vengas a verme, trae contigo a Mical hija de Saúl. De lo contrario, no te recibiré.» 14. Además, David envió unos mensajeros a decirle a Isboset hijo de Saúl: «Devuélveme a mi esposa Mical, por la que di a cambio cien prepucios de filisteos.» 15. Por tanto, Isboset mandó que se la quitaran a Paltiel hijo de Lais, que era su esposo, 16. pero Paltiel se fue tras ella, llorando por todo el camino hasta llegar a Bajurín. Allí Abner le ordenó que regresara, y Paltiel obedeció. 17. Luego Abner habló con los ancianos de Israel. «Hace tiempo que ustedes quieren hacer rey a David -les dijo-. 18. Ya pueden hacerlo, pues el Señor le ha prometido: “Por medio de ti, que eres mi siervo, libraré a mi pueblo Israel del poder de los filisteos y de todos sus enemigos.”» 19. Abner habló también con los de Benjamín, y más tarde fue a Hebrón para contarle a David todo 