 9:2-8-Lc 9:28-36
 9:2-13-Mt 17:1-13
 Seis días después Jesús tomó consigo a Pedro, a Jacobo y a Juan, y los llevó a una montaña alta, donde estaban solos. Allí se transfiguró en presencia de ellos. 3. Su ropa se volvió de un blanco resplandeciente como nadie en el mundo podría blanquearla. 4. Y se les aparecieron Elías y Moisés, los cuales conversaban con Jesús. Tomando la palabra, 5. Pedro le dijo a Jesús: -Rabí, ¡qué bien que estemos aquí! Podemos levantar tres albergues: uno para ti, otro para Moisés y otro para Elías. 6. No sabía qué decir, porque todos estaban asustados. 7. Entonces apareció una nube que los envolvió, de la cual salió una voz que dijo: «Éste es mi Hijo amado. ¡Escúchenlo!» 8. De repente, cuando miraron a su alrededor, ya no vieron a nadie más que a Jesús. 9. Mientras bajaban de la montaña, Jesús les ordenó que no contaran a nadie lo que habían visto hasta que el Hijo del hombre se levantara de entre los muertos. 10. Guardaron el secreto, pero discutían entre ellos qué significaría eso de «levantarse de entre los muertos». 11. -¿Por qué dicen los maestros de la ley que Elías tiene que venir primero? -le preguntaron. 12. -Sin duda Elías ha de venir primero para restaurar todas las cosas -respondió Jesús-. Pero entonces, ¿cómo es que está escrito que el Hijo del hombre tiene que sufrir mucho y ser rechazado? 13. Pues bien, les digo que Elías ya ha venido, y le hicieron todo lo que quisieron, tal como está escrito de él. 14. Jesús sana a un muchacho endemoniado
 9:14-28, 9-Mt 17:14-19, 22-23; Lc 9:37-45
 Cuando llegaron adonde estaban los otros discípulos, vieron[y]  que a su alrededor había mucha gente y que los maestros de la ley discutían con ellos. 15. Tan pronto como la gente vio a Jesús, todos se sorprendieron y corrieron a saludarlo. 16. -¿Qué están discutiendo con ellos? -les preguntó. 17. -Maestro -respondió un hombre de entre la multitud-, te he traído a mi hijo, pues está poseído por un espíritu que le ha quitado el habla. 18. Cada vez que se apodera de él, lo derriba. Echa espumarajos, cruje los dientes y se queda rígido. Les pedí a tus discípulos que expulsaran al espíritu, pero no lo lograron. 19. -¡Ah, generación incrédula! -respondió Jesús-. ¿Hasta cuándo tendré que estar con ustedes? ¿Hasta cuándo tendré que soportarlos? Tráiganme al muchacho. 20. Así que se lo llevaron. Tan pronto como vio a Jesús, el espíritu sacudió de tal modo al muchacho que éste cayó al suelo y comenzó a revolcarse echando espumarajos. 21. -¿Cuánto tiempo hace que le pasa esto? -le preguntó Jesús al padre. -Desde que era niño -contestó-. 22. Muchas veces lo ha echado al fuego y al agua para matarlo. Si puedes hacer algo, ten compasión de nosotros y ayúdanos. 23. -¿Cómo que si puedo? Para el que cree, todo es posible. 24. -¡Sí creo! -exclamó de inmediato el padre del muchacho-. ¡Ayúdame en mi poca fe! 25. Al ver Jesús que se agolpaba mucha gente, reprendió al espíritu maligno. -Espíritu sordo y mudo -dijo-, te mando que salgas y que jamás vuelvas a entrar en él. 26. El espíritu, dando un alarido y sacudiendo violentamente al muchacho, salió de él. Éste quedó como muerto, tanto que muchos decían: «Ya se murió.» 27. Pero Jesús lo tomó de la mano y lo levantó, y el muchacho se puso de pie. 28. Cuando Jesús entró en casa, sus discípulos le preguntaron en privado: -¿Por qué nosotros no pudimos expulsarlo? 29. -Esta clase de demonios sólo puede ser expulsada a fuerza de oración[z]  -respondió Jesús. 30. Dejaron aquel lugar y pasaron por Galilea. Pero Jesús no quería que nadie lo supiera, 31. porque estaba instruyendo a sus discípulos. Les decía: «El Hijo del hombre va a ser entregado en manos de los hombres. Lo matarán, y a los tres días de muerto resucitará.» 32. Pero ellos no entendían lo que quería decir con esto, y no se atrevían a preguntárselo. 33. ¿Quién es el más importante?
 9:33-37-Mt 18:1-5; Lc 9:46-48
 Llegaron a Capernaúm. Cuando ya estaba en casa, Jesús les preguntó: -¿Qué venían discutiendo por el camino? 34. Pero ellos se quedaron callados, porque en el camino habían discutido entre sí quién era el más importante. 35. Entonces Jesús se sentó, llamó a los doce y les dijo: -Si alguno quiere ser el primero, que sea el último de todos y el servidor de todos. 36. Luego tomó a un niño y lo puso en medio de ellos. Abrazándolo, les dijo: 37. -El que recibe en mi nombre a uno de estos niños, me recibe a mí; y el que me recibe a mí, no me recibe a mí sino al que me envió. 38. El que no está contra nosotros está a favor de nosotros
