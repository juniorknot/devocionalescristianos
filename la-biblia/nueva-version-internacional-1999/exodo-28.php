<li class="hide-for-large"><a href="whatsapp://send?text=Éxodo 28 (NVI)%0A%0A 1. Las vestiduras sacerdotales
 »Haz que comparezcan ante ti tu hermano Aarón y sus hijos Nadab, Abiú, Eleazar e Itamar. De entre todos los israelitas, ellos me servirán como sacerdotes. 2. Hazle a tu hermano Aarón vestiduras sagradas que le confieran honra y dignidad. 3. Habla con todos los expertos a quienes he dado habilidades especiales, para que hagan las vestiduras de Aarón, y así lo consagre yo como mi sacerdote. 4. »Las vestiduras que le harás son las siguientes: un pectoral, un efod, un manto, una túnica bordada, un turbante y una faja. Estas vestiduras sagradas se harán para tu hermano Aarón y para sus hijos, a fin de que me sirvan como sacerdotes. 5. Al efecto se usará oro, púrpura, carmesí, escarlata y lino. 6. El efod
 28:6-14-Éx 39:2-7
 »El efod se bordará artísticamente con oro, púrpura, carmesí, escarlata y lino fino. 7. En sus dos extremos tendrá hombreras con cintas, para que pueda sujetarse. 8. El cinturón bordado con el que se sujeta el efod deberá ser del mismo material, es decir, de oro, púrpura, carmesí, escarlata y lino fino, y formará con el efod una sola pieza. 9. »Toma dos piedras de ónice, y graba en ellas los nombres de los doce hijos de Israel 10. por orden de nacimiento, seis nombres en una piedra, y seis en la otra. 11. Un joyero grabará los nombres en las dos piedras, como los orfebres graban sellos: engarzará las piedras en filigrana de oro 12. y las sujetará a las hombreras del efod. Así Aarón llevará en sus hombros los nombres de los hijos de Israel, para recordarlos ante el Señor. 13. Haz también engastes en filigrana de oro, 14. y dos cadenillas de oro puro, a manera de cordón, para fijar las cadenillas en los engastes. 15. El pectoral
 28:15-28-Éx 39:8-21
