--¿No han leído en la Biblia lo que hizo el rey David cuando él y sus compañeros tuvieron hambre? 4. Entraron en la casa de Dios y comieron el pan sagrado. Ni a David ni a sus compañeros les estaba permitido comer ese pan. Sólo los sacerdotes podían comerlo. 5. ¿Tampoco han leído los libros de la ley de Moisés? ¿No saben que los sacerdotes pueden trabajar en el templo en el día de descanso, sin que nadie los acuse de nada? 6. Pues les aseguro que aquí hay algo más importante que el templo. 7. Ustedes no entienden esto que Dios dijo: "No quiero que me sacrifiquen animales, sino que amen y ayuden a los demás". Si lo entendieran, no estarían acusando a gente inocente. 8. Porque yo, el Hijo del hombre, soy quien decide lo que puede hacerse en el día de descanso, y lo que no puede hacerse.


Jesús sana a un hombre en sábado 9. Jesús se fue y entró en la sinagoga del lugar. 10. Allí había un hombre que tenía una mano tullida. Como los fariseos buscaban la manera de acusar de algo malo a Jesús, le preguntaron:
--¿Permite nuestra ley sanar a una persona en el día de descanso? 11. Jesús les respondió:
--Si a uno de ustedes se le cae una oveja en un pozo en el día de descanso, ¿la sacaría de allí? ¡Por supuesto que sí! 12. ¡Pues una persona vale mucho más que una oveja! Por eso está permitido hacer el bien en ese día. 13. Luego Jesús le dijo al hombre que no podía mover la mano: "Extiende tu mano". El hombre la extendió; y la mano le quedó tan sana como la otra. 14. Entonces los fariseos salieron de la sinagoga y comenzaron a hacer planes para matar a Jesús.


Jesús, servidor de Dios 15. Al enterarse Jesús de lo que planeaban los fariseos, se fue de allí, y mucha gente lo siguió. Jesús sanó a todos los que estaban enfermos 16. y les ordenó que no contaran a nadie nada acerca de él. 17. Así se cumplió lo que Dios había dicho por medio del profeta Isaías: 18. "Aquí está mi servidor,
yo lo elegí.
Lo amo mucho
y estoy muy contento con él.
Yo le daré mi Espíritu,
y él hablará de la justicia
a todo el mundo. 19. No discutirá con nadie,
ni gritará.
¡Nadie escuchará su voz
en las calles! 20. Tratará bien a los débiles,
que son como cañas quebradas,
como mechas que apenas echan humo.
Actuará así hasta que la justicia
reine en todo el mundo. 21. ¡Todas las personas del mundo
confiarán en él!"


Jesús y el jefe de los demonios 22. Unas personas llevaron un hombre a Jesús para que lo sanara. Era ciego y mudo porque tenía un demonio. Jesús lo sanó, y el hombre pudo ver y hablar. 23. La gente estaba asombrada de lo que Jesús hacía, y se preguntaba: "¿Será Jesús el Mesías que Dios prometió para salvarnos?" 24. Pero algunos de los fariseos oyeron a la gente y pensaron: "Jesús libera de los demonios a la gente, porque Beelzebú, el jefe de los demonios, le da poder para hacerlo". 25. Jesús se dio cuenta de lo que ellos pensaban y les dijo:
"Si los habitantes de un país se pelean entre ellos, el país se destruirá. Si los habitantes de una ciudad se pelean unos contra otros, la ciudad se destruirá. Y si los miembros de una familia se pelean entre ellos, la familia también se destruirá. 26. Si Satanás lucha contra sí mismo, destruirá su propio reino. 27. Según ustedes, yo expulso los demonios por el poder de Satanás. Si eso fuera cierto, entonces ¿quién les da poder a los discípulos de ustedes para echar fuera los demonios? Si ustedes me responden que Dios les da ese poder, eso demuestra que ustedes están equivocados. 28. Y si yo echo fuera los demonios con el poder del Espíritu de Dios, eso demuestra que el reino de Dios ya está aquí. 29. "Si alguien quiere robar lo que hay en la casa de un hombre fuerte, primero tiene que atar al hombre. Después puede robarle todo. 30. "El que no está de mi parte, está contra mí. El que no me ayuda a traer a otros para que me sigan, es como si los estuviera ahuyentando. 31. "Les aseguro que Dios les perdonará cualquier pecado y todo lo malo que digan. Aun si dicen algo contra mí, que soy el Hijo del hombre, Dios los perdonará. Pero lo que no les perdonará es que hablen contra el Espíritu Santo. ¡Eso no lo perdonará, ni ahora, ni nunca! 33. "Un buen árbol produce buenos frutos, y un mal árbol produce malos frutos. Para saber si un árbol es bueno o malo, sólo hay que fijarse en sus frutos. Lo mismo sucede con las personas: para saber si son buenas o malas, sólo hay que fijarse en las cosas que hacen. 34. Lo que ustedes enseñan es tan malo como el veneno de una serpiente. ¡Claro! ¿Cómo van a decir cosas buenas, si ustedes son malos? Porque si alguien es bueno, siempre habla cosas buenas, y si es malo, siempre habla cosas malas. 36. Les aseguro que en el día del juicio final todos tendrán que explicar por qué usaron su boca y su lengua para hacer mal a los demás. 37. Dios juzgará a cada uno de acuerdo con sus palabras: si dijeron cosas buenas se salvarán, pero si dijeron cosas malas serán castigados".


Una señal milagrosa 38. Entonces algunos fariseos y maestros de la Ley le dijeron a Jesús:
--Maestro, queremos que hagas algo que nos pruebe que tú fuiste enviado por Dios. 39. Pero Jesús les contestó:
--Ustedes, que son malos y no confían en Dios, me piden darles una prueba. Pero la única prueba que les daré será la del profeta Jonás: 40. Así como Jonás estuvo tres días dentro del gran pez, así yo, el Hijo del hombre, estaré muerto durante tres días. 41. En el juicio final, la gente de la ciudad de Nínive se levantará y hablará contra ustedes para que Dios los castigue. Porque esa gente sí cambió de vida cuando oyó el mensaje que le dio Jonás. Pero ustedes oyen mi mensaje y no cambian, aunque yo soy más importante que Jonás. 42. "La reina del Sur también se levantará en el día del juicio, y hablará contra ustedes. Porque ella vino desde muy lejos a escuchar las sabias enseñanzas del rey Salomón. Pero ustedes no quieren escuchar mis enseñanzas, aunque yo soy más importante que Salomón.


