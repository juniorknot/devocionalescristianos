Una trampa para Jesús 20. Los enemigos de Jesús querían arrestarlo y entregarlo al gobernador romano. Pero como no tenían de qué acusarlo, enviaron unos espías para que se hicieran pasar por personas buenas y vigilaran en qué momento Jesús decía algo malo. 21. Los espías le dijeron a Jesús:
--Maestro, sabemos que siempre dices la verdad. Tú enseñas que todos deben obedecer a Dios, y tratas a todos por igual. 22. Por eso te preguntamos: ¿Está bien que paguemos impuestos al emperador de Roma o no? 23. Como Jesús sabía que ellos querían ponerle una trampa, les respondió: 24. --Muéstrenme una moneda. ¿De quién es la cara dibujada en la moneda? ¿De quién es el nombre que tiene escrito?
Ellos contestaron:
--Del emperador de Roma. 25. Jesús les dijo:
--Pues denle al Emperador lo que es del Emperador, y a Dios lo que es de Dios. 26. Los espías no lograron que Jesús cayera en la trampa. Quedaron sorprendidos por su respuesta y no supieron decir nada más.


Los saduceos hablan con Jesús 27. Después, unos saduceos fueron a ver a Jesús. Como ellos no creían que los muertos pueden volver a vivir, 28. le preguntaron:
--Maestro, Moisés escribió que si un hombre muere sin tener hijos con su esposa, el hermano de ese hombre debe casarse con esa mujer y tener hijos con ella. De acuerdo con la Ley, esos hijos le pertenecen al hermano muerto y llevan su nombre. 29. "Pues bien, aquí vivían siete hermanos. El hermano mayor se casó y, tiempo más tarde, murió sin tener hijos. 30. El segundo hermano se casó con la misma mujer, pero tiempo después también él murió sin tener hijos. 31. Lo mismo sucedió con el tercer hermano y con el resto de los siete hermanos. 32. El tiempo pasó y la mujer también murió. 33. "Ahora bien, cuando Dios haga que todos los muertos vuelvan a vivir, ¿de quién será esposa esta mujer, si estuvo casada con los siete? 34. Jesús contestó:
--Ahora los hombres y las mujeres se casan. 35. Pero Dios decidirá quiénes merecen volver a vivir, y cuando eso suceda nadie se casará 36. ni morirá. Todos serán como los ángeles, y serán hijos de Dios porque han vuelto a vivir. 37. Hasta Moisés mismo nos demuestra que los muertos vuelven a vivir. En la historia del arbusto que ardía, Moisés dijo que Dios es el Dios de sus antepasados Abraham, Isaac y Jacob. 38. Con eso, Moisés estaba demostrando que Dios no es Dios de muertos, sino de vivos, pues para Dios todos ellos están vivos. 39. Algunos maestros de la Ley que estaban allí dijeron:
--¡Maestro, diste una buena respuesta! 40. Después de esto, ya nadie se atrevía a hacerle más preguntas.


