Los fariseos quieren arrestar a Jesús 32. Los fariseos oyeron lo que la gente decía. Entonces ellos y los sacerdotes principales enviaron a unos guardias del templo para que arrestaran a Jesús. 33. Pero Jesús dijo: "Sólo estaré con ustedes un poco más de tiempo. Luego volveré a donde está el que me envió. 34. Ustedes me buscarán, pero no me encontrarán, porque no pueden ir a donde yo voy". 35. Los jefes judíos comenzaron a preguntarse entre ellos:
"¿Y a dónde podrá ir, que no podamos encontrarlo? ¿Acaso piensa ir a vivir entre los judíos de otros países, y enseñar también a los que no son judíos? 36. ¿Qué quiere decir con eso de que: "Me buscarán, pero no me encontrarán, porque no pueden ir a donde yo voy?""


