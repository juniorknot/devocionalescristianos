¿Quién es Jesús? 25. Algunos de los que vivían en Jerusalén empezaron a preguntar:
"¿No es este al que andan buscando para matarlo? 26. Pues ahí está, hablando con la gente; ¡y nadie le dice nada! ¿No será que nuestros gobernantes creen de verdad que él es el Mesías? 27. Pero ¡no puede ser! Porque, cuando venga el Mesías, nadie sabrá de dónde viene; en cambio, nosotros sabemos de dónde viene este hombre". 28. Jesús estaba enseñando en el templo y dijo con voz fuerte:
