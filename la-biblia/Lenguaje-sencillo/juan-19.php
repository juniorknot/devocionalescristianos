La muerte de Jesús 28. Jesús sabía que ya había hecho todo lo que Dios le había ordenado. Por eso, y para que se cumpliera lo que dice la Biblia, dijo: "Tengo sed". 29. Había allí un jarro lleno de vinagre. Entonces empaparon una esponja en el vinagre, la ataron a una rama, y la acercaron a la boca de Jesús. 30. Él probó el vinagre y dijo: "Todo está cumplido". Luego inclinó su cabeza y murió.


