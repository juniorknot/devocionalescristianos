Jesús es clavado en la cruz 26. Los soldados se llevaron a Jesús para clavarlo en una cruz. En el camino detuvieron a un hombre llamado Simón, y lo obligaron a llevar la cruz detrás de Jesús. Simón era del pueblo de Cirene, y en ese momento volvía del campo. 27. Muchas personas seguían a Jesús, y entre ellas había muchas mujeres que gritaban y lloraban de tristeza por él. 28. Jesús se volvió y les dijo:
"¡Mujeres de Jerusalén! No lloren por mí. Más bien, lloren por ustedes y por sus hijos. 29. Porque llegará el momento en que la gente dirá: "¡Dichosas las mujeres que no pueden tener hijos! ¡Dichosas las que nunca fueron madres, ni tuvieron niños que alimentar!" 30. La gente deseará que una montaña les caiga encima y las mate. 31. Porque si a mí, que no he hecho nada malo, me matan así, ¿qué le pasará a los que hacen lo malo?" 32. También llevaron a dos malvados, para matarlos junto con Jesús. 33. Cuando llegaron al lugar llamado La Calavera, los soldados clavaron a Jesús en la cruz. También clavaron a los dos criminales; uno a la derecha y el otro a la izquierda de Jesús. 34. Poco después, Jesús dijo: "¡Padre, perdona a toda esta gente! ¡Ellos no saben lo que hacen!"
Mientras los soldados echaban suertes para saber cuál de ellos se quedaría con la ropa de Jesús, 35. la gente miraba todo lo que pasaba. Los líderes del pueblo, entre tanto, se burlaban de Jesús y decían: "Él salvó a otros, y si de verdad es el Mesías que Dios eligió, que se salve a sí mismo". 36. Los soldados también se burlaban de él. Le ofrecieron vinagre para que lo bebiera 37. y le dijeron: "¡Si en verdad eres el Rey de los judíos, sálvate a ti mismo!" 38. Sobre la cabeza de Jesús había un letrero que decía: "Este es el Rey de los judíos". 39. Uno de los criminales que estaban clavados junto a Jesús también lo insultaba:
--¿No que tú eres el Mesías? Sálvate tú, y sálvanos a nosotros también. 40. Pero el otro hombre lo reprendió:
--¿No tienes miedo de Dios? ¿Acaso no estás sufriendo el mismo castigo? 41. Nosotros sí merecemos el castigo, porque hemos sido muy malos; pero este hombre no ha hecho nada malo para merecerlo. 42. Luego, le dijo a Jesús:
--Jesús, no te olvides de mí cuando comiences a reinar. 43. Jesús le dijo:
--Te aseguro que hoy estarás conmigo en el paraíso.


