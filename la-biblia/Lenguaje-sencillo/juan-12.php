El plan para matar a Lázaro 9. Muchos de los judíos que vivían en Jerusalén se enteraron de que Jesús estaba en Betania; así que fueron allá, no sólo para verlo sino para ver también a Lázaro, a quien Jesús había resucitado. 10. Cuando los sacerdotes principales se enteraron de esto, planearon matar también a Lázaro, 11. pues por su culpa muchos judíos ya no querían nada con los sacerdotes y se habían vuelto seguidores de Jesús.


Jesús entra en Jerusalén 12. Mucha gente había ido a la ciudad de Jerusalén para la fiesta de la Pascua. Al día siguiente, cuando algunos escucharon que Jesús iba a llegar a la ciudad, 13. cortaron ramas de palmera y salieron a encontrarlo, gritando:
"¡Sálvanos, oh Dios!
¡Bendito el que viene de parte de Dios!
¡Bendito sea el Rey de Israel!" 14. Jesús, por su parte se montó en un burrito que encontró en el camino. Así cumplió con lo que anunciaba la Biblia: 15. "¡No tengan miedo
habitantes de Jerusalén!
"¡Ya viene su Rey!
¡Viene montado en un burrito!" 16. Los que estuvieron presentes en Betania cuando Jesús resucitó a Lázaro habían contado en Jerusalén este milagro. Por eso la gente salió al encuentro de Jesús. Pero los fariseos se decían unos a otros: "Miren, ¡todos lo siguen! No vamos a poder hacer nada".
Al principio, los discípulos de Jesús no entendían lo que estaba pasando; pero después de que Jesús murió y resucitó, se acordaron de que todo lo que le habían hecho a Jesús ya estaba anunciado en la Biblia.


Jesús anuncia su muerte 20. Entre las personas que habían ido a Jerusalén para la fiesta de la Pascua, había unos griegos. 21. Ellos fueron a un pueblo de Galilea para ver a Felipe, uno de los discípulos de Jesús, y le dijeron:
--Señor, queremos ver a Jesús.
Felipe, que era de Betsaida, 22. fue a contárselo a Andrés, y los dos fueron a decírselo a Jesús. 23. Él les dijo:
--Ha llegado el momento de que todos sepan de verdad quién es el Hijo del hombre. 24. Ustedes saben que si un grano de trigo cae en la tierra y no muere, no produce nada. Pero si muere, da una cosecha abundante. 25. Si ustedes consideran que su vida es más importante que obedecerme, no tendrán vida eterna. Pero si consideran que su vida en este mundo no es importante y me obedecen, entonces tendrán vida eterna. 26. Si alguno de ustedes quiere servirme, tiene que obedecerme. Donde yo esté, ahí también estarán los que me sirven, y mi Padre los premiará. 27. "En este momento estoy sufriendo mucho y me encuentro confundido. Quisiera decirle a mi Padre que no me deje sufrir así. Pero no lo haré, porque yo vine al mundo precisamente para hacer lo que él me mandó. 28. Más bien le diré a mi Padre: "Muéstrale al mundo tu poder".
Luego, desde el cielo se oyó una voz que decía: "Ya he mostrado mi poder, y volveré a hacerlo de nuevo". 29. Los que estaban allí decían que habían oído un trueno. Otros decían: "Un ángel le ha hablado a Jesús". 30. Pero Jesús les dijo:
"El propósito de la voz que ustedes oyeron es para ayudarlos a confiar en mí. 31. Ahora es cuando la gente de este mundo va a ser juzgada; y el que manda en este mundo, que es el diablo, será echado fuera. 32. Pero, cuando me cuelguen en la cruz, haré que todos crean en mí". 33. Cuando Jesús dijo que lo colgarían en la cruz, se refería al modo en que iba a morir. 34. La gente le preguntó:
--¿Por qué dices tú que al Hijo del hombre lo van colgar en una cruz? ¿Quién es este Hijo del hombre? La Biblia dice que el Mesías vivirá para siempre. 35. Jesús les contestó:
--Yo soy la luz, y estaré con ustedes poco tiempo. Crean en mí mientras aún estoy aquí. Creer en mí significa andar por el camino mientras todavía hay luz, para no ser sorprendido por la noche; porque el que camina en la oscuridad no sabe por dónde va.
Después de decir esto, Jesús se apartó de todos y se fue a un lugar donde no lo pudieran encontrar.


La gente no creía en Jesús 37. Jesús había hecho muchos milagros delante de esa gente, pero aun así nadie creía en él. 38. Esto sucedió porque tenía que cumplirse lo que había escrito el profeta Isaías:
