"El que siembra la buena semilla de trigo soy yo, el Hijo del hombre. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 El terreno es el mundo, y las buenas semillas de trigo son todos los que obedecen las leyes del reino de Dios. Las semillas de cizaña son los que obedecen al diablo, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 que fue quien las sembró en el mundo. El tiempo de la cosecha es el juicio final, y los trabajadores que recogen la cosecha son los ángeles. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
 Así como se arranca la mala hierba y se quema, así sucederá cuando Dios juzgue a todos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 Yo, el Hijo del hombre, enviaré a mis ángeles para que saquen de mi reino a todos los que hacen lo malo y obligan a otros a hacerlo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 Los ángeles echarán a esas personas en el infierno, y allí tendrán tanto miedo que llorarán y rechinarán los dientes. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 Pero los que obedecen a Dios brillarán como el sol en su reino. ¡Ustedes, si en verdad tienen oídos, presten atención!


El tesoro escondido </p>
                                                    <h4><strong>El tesoro escondido</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 "Con el reino de Dios pasa lo mismo que cuando alguien encuentra un tesoro escondido en un terreno, y lo vuelve a esconder. Después va muy alegre a vender todo lo que tiene para comprar el terreno y quedarse con el tesoro.


La joya fina </p>
                                                    <h4><strong>La perla de gran precio</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 "El reino de Dios también se parece a un comerciante que compra joyas finas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 Cuando encuentra una joya muy valiosa, vende todo lo que tiene y va y la compra.


La red de pescar </p>
                                                    <h4><strong>La red</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
 "El reino de Dios se parece a una red de pescar. Los pescadores echan la red al mar, y en ella recogen toda clase de peces. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 Cuando la red ya está llena, la sacan a la orilla y se sientan a separar los pescados. Guardan los buenos en una canasta y tiran los malos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 Así también sucederá cuando llegue el fin del mundo: Los ángeles saldrán a separar a las personas buenas de las malas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 A las malas las echarán en el infierno, y allí tendrán tanto horror que llorarán y rechinarán los dientes".


Cosas nuevas y viejas </p>
                                                    <h4><strong>Tesoros nuevos y viejos</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 Jesús les preguntó a sus discípulos:
--¿Entienden ustedes todas estas enseñanzas?
Ellos contestaron:
--Sí, las entendemos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 Jesús les dijo:
--Todo maestro de la Ley que se convierte en discípulo del reino de Dios, se parece al que va a su bodega y de allí saca cosas nuevas y cosas viejas.


Jesús viaja a Nazaret </p>
                                                    <h4><strong>Jesús en Nazaret</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 Cuando Jesús terminó de enseñar con estos ejemplos, se fue de allí. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 Llegó a su pueblo y comenzó a enseñar en la sinagoga. La gente estaba tan sorprendida que algunos decían: "¿Dónde aprendió este hombre tantas cosas? ¿Cómo puede hacer esos milagros?"
Otros decían: </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 "Pero, ¡si es Jesús, el hijo de José, el carpintero! María es su madre, y sus hermanos son Santiago, José, Simón y Judas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 Sus hermanas aún viven aquí. ¿Cómo es que Jesús sabe tanto y puede hacer estos milagros?" </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 Ninguno de los que estaban allí quiso aceptar las enseñanzas de Jesús. Entonces él dijo: "A un profeta se le respeta en todas partes, menos en su propio pueblo y en su propia familia". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
 Jesús no hizo muchos milagros en aquel lugar, porque la gente no creía en él. </p>
                                                        
