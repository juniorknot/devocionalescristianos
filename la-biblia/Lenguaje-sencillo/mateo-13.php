<li class="hide-for-large"><a href="whatsapp://send?text=Mateo 13 (BLS)%0A%0A 1. Ese mismo día, Jesús salió de la casa donde estaba, fue a la orilla del Lago de Galilea, y allí se sentó para enseñar. 2. Como mucha gente llegó a escucharlo, tuvo que subir a una barca y sentarse para enseñar desde allí. La gente permaneció de pie en la playa. 3. Jesús les enseñó muchas cosas por medio de ejemplos y comparaciones. Les puso esta comparación: 4. "Un agricultor salió a sembrar trigo. Mientras sembraba, algunas semillas cayeron en el camino. Poco después vinieron unos pájaros y se las comieron. 5. "Otras semillas cayeron en un terreno con muchas piedras y poca tierra. Allí pronto brotaron plantas de trigo, pues la tierra era poco profunda. 6. Pero las plantas no vivieron mucho tiempo porque no tenían buenas raíces, y se quemaron cuando salió el sol. 7. "Otras semillas cayeron entre espinos. Cuando los espinos crecieron, apretaron las espigas de trigo y no las dejaron crecer. 8. "Pero otras semillas cayeron en tierra buena y produjeron una cosecha excelente. En algunos casos, las semillas sembradas produjeron espigas con cien semillas, otras produjeron espigas con sesenta semillas, y otras produjeron espigas con treinta semillas. 9. ¿Por qué Jesús enseña con ejemplos? 
"¡Ustedes, si en verdad tienen oídos, presten mucha atención!"


¿Por qué Jesús enseña con ejemplos? 10. Los discípulos se acercaron a Jesús y le preguntaron:
--¿Por qué pones ejemplos para enseñar a la gente? 11. Jesús les dijo:
"Yo les he explicado a ustedes los secretos del reino de Dios, pero no a los demás. 12. Porque a los que saben algo acerca de los secretos del reino, se les hará saber mucho más. Pero a los que no saben de los secretos del reino, Dios hará que olviden aun lo poquito que saben. 13. Yo enseño a la gente por medio de ejemplos; así, por más que miren no verán nada, y por más que oigan no entenderán. 14. Así se cumple lo que Dios había dicho por medio del profeta Isaías:
"Esta gente,
por más que escuche,
nunca entenderá;
y por más que mire,
nunca verá. 15. Pues no aprende, ni piensa;
cierra los ojos para no ver,
y se tapa los oídos para no oír.
Si no fuera así,
entendería mi mensaje,
cambiaría su manera de vivir,
¡y yo la salvaría!" 16. "Pero a ustedes, mis discípulos, Dios los ha bendecido, porque ven y escuchan mi mensaje. 17. Muchos profetas y mucha gente buena hubieran querido ver lo que ustedes ven y oyen, pero no pudieron.


Jesús explica el ejemplo de las semillas 18. "Ahora, pongan atención y les diré lo que significa el ejemplo del agricultor. 19. Hay algunos que escuchan el mensaje del reino de Dios, pero como no lo entienden, el diablo viene y hace que lo olviden. Estos son como las semillas que cayeron junto al camino. 20. "Las semillas que cayeron entre piedras representan a los que oyen el mensaje del reino de Dios y lo aceptan rápidamente y con gran alegría. 21. Pero como no entendieron muy bien el mensaje, su alegría dura muy poco. Cuando tienen problemas, o los maltratan por ser obedientes a Dios, en seguida se olvidan del mensaje. 22. "Luego están las semillas que cayeron entre los espinos. Estas semillas representan a los que oyen el mensaje, pero no dejan que este cambie sus vidas. Sólo piensan en lo que necesitan y en cómo hacerse ricos. 23. "Finalmente, las semillas que cayeron en buena tierra representan a los que oyen y entienden el mensaje. Estos sí cambian sus vidas y hacen lo bueno. Son como esas semillas que produjeron espigas con cien, con sesenta, y hasta con treinta semillas".


La mala hierba y el trigo 24. Jesús les puso este otro ejemplo:
"En el reino de Dios sucede lo mismo que le pasó a uno que sembró en su terreno muy buenas semillas de trigo. 25. Mientras todos dormían, llegó su enemigo y, junto a las semillas de trigo, sembró unas semillas de una mala hierba llamada cizaña, y después se fue. 26. "Cuando las semillas de trigo produjeron espigas, los trabajadores se dieron cuenta de que también había crecido cizaña. 27. Entonces fueron adonde estaba el dueño del terreno y le dijeron: "Señor, si usted sembró buenas semillas de trigo, ¿por qué creció también la cizaña?" 28. "El dueño les dijo: "Esto lo hizo mi enemigo".
"Los trabajadores le preguntaron: "¿Quieres que vayamos a quitar la mala hierba?" 29. "El dueño les dijo: "¡No! A lo mejor ustedes van y arrancan el trigo junto con la cizaña, porque el trigo y la cizaña se parecen mucho. 30. Cuando llegue el tiempo de la cosecha podremos distinguir entre el trigo y la cizaña. Dejen que las dos plantas crezcan juntas. Cuando llegue el tiempo de la cosecha, enviaré a los trabajadores para que arranquen primero la cizaña, la amontonen y la quemen. Luego recogerán el trigo y lo llevarán a mi granero"".


La semilla de mostaza 31. Jesús también les hizo esta comparación:
"Con el reino de Dios pasa algo parecido a lo que sucede con la semilla de mostaza que un hombre siembra en su terreno. 32. A pesar de ser la más pequeña de todas las semillas, cuando crece se convierte en la más grande de las plantas del huerto. Llega a ser tan grande como un árbol, y hasta los pájaros hacen nidos en sus ramas".


La levadura 33. Jesús les puso esta otra comparación:
"Con el reino de Dios pasa lo mismo que cuando una mujer pone un poquito de levadura en un montón de harina. Ese poquito hace crecer toda la masa".


Jesús cumple lo dicho por medio de un profeta 34. Jesús le enseñó todo esto a la gente por medio de ejemplos y comparaciones, y sólo así les enseñaba. 35. De esa manera, Jesús cumplió lo que Dios había dicho por medio del profeta:
"Hablaré a la gente
por medio de ejemplos,
y contaré cosas
que Dios ha tenido en secreto
desde que hizo el mundo".


Jesús explica el ejemplo de la cizaña 36. Jesús dejó a la gente allí y se fue a la casa. Sus discípulos fueron y le dijeron:
--Explícanos qué significa el ejemplo de la mala hierba en el terreno. 37. Jesús les dijo:
"El que siembra la buena semilla de trigo soy yo, el Hijo del hombre. 38. El terreno es el mundo, y las buenas semillas de trigo son todos los que obedecen las leyes del reino de Dios. Las semillas de cizaña son los que obedecen al diablo, 39. que fue quien las sembró en el mundo. El tiempo de la cosecha es el juicio final, y los trabajadores que recogen la cosecha son los ángeles. 40. Así como se arranca la mala hierba y se quema, así sucederá cuando Dios juzgue a todos. 41. Yo, el Hijo del hombre, enviaré a mis ángeles para que saquen de mi reino a todos los que hacen lo malo y obligan a otros a hacerlo. 42. Los ángeles echarán a esas personas en el infierno, y allí tendrán tanto miedo que llorarán y rechinarán los dientes. 43. Pero los que obedecen a Dios brillarán como el sol en su reino. ¡Ustedes, si en verdad tienen oídos, presten atención!


El tesoro escondido 44. "Con el reino de Dios pasa lo mismo que cuando alguien encuentra un tesoro escondido en un terreno, y lo vuelve a esconder. Después va muy alegre a vender todo lo que tiene para comprar el terreno y quedarse con el tesoro.


La joya fina 45. "El reino de Dios también se parece a un comerciante que compra joyas finas. 46. Cuando encuentra una joya muy valiosa, vende todo lo que tiene y va y la compra.


La red de pescar 47. "El reino de Dios se parece a una red de pescar. Los pescadores echan la red al mar, y en ella recogen toda clase de peces. 48. Cuando la red ya está llena, la sacan a la orilla y se sientan a separar los pescados. Guardan los buenos en una canasta y tiran los malos. 49. Así también sucederá cuando llegue el fin del mundo: Los ángeles saldrán a separar a las personas buenas de las malas. 50. A las malas las echarán en el infierno, y allí tendrán tanto horror que llorarán y rechinarán los dientes".


Cosas nuevas y viejas 51. Jesús les preguntó a sus discípulos:
--¿Entienden ustedes todas estas enseñanzas?
Ellos contestaron:
--Sí, las entendemos. 52. Jesús les dijo:
