Un día, Lázaro se enfermó 3. y sus hermanas le mandaron este mensaje a Jesús: "Señor, tu querido amigo Lázaro está enfermo". 4. Cuando Jesús recibió el mensaje, dijo: "Esta enfermedad no terminará en muerte. Servirá para mostrar el poder de Dios, y el poder que tengo yo, el Hijo de Dios". 5. Jesús amaba a Marta, a María y a Lázaro. 6. Pero cuando recibió la noticia de que Lázaro estaba enfermo, decidió quedarse dos días más en donde estaba. 7. Al tercer día les dijo a sus discípulos:
--Regresemos a la región de Judea. 8. Los discípulos le dijeron:
--Maestro, algunos de los judíos de esa región trataron de matarte hace poco. ¿Aún así quieres regresar allá? 9. Jesús les respondió:
--Cada día, el sol brilla durante doce horas. Si uno camina de día, no tropieza con nada, porque la luz del sol le alumbra el camino. 10. Pero si camina de noche tropieza, porque le hace falta la luz. Nuestro amigo Lázaro está dormido, y yo voy a despertarlo. 12. Los discípulos le dijeron:
--Señor, si Lázaro está dormido para qué te preocupas. 13. Lo que Jesús quería darles a entender era que Lázaro había muerto, pero los discípulos entendieron que estaba descansando. 14. Por eso Jesús les explicó:
--Lázaro ha muerto, 15. y me alegro de no haber estado allí, porque ahora ustedes tendrán oportunidad de confiar en mí. Vayamos adonde está él. 16. Entonces Tomás, al que llamaban el Gemelo, les dijo a los otros discípulos: "Vayamos también nosotros, para morir con Jesús".


Jesús es la vida 17. Como el pueblo de Betania estaba a unos tres kilómetros de la ciudad de Jerusalén, muchos de los judíos que vivían cerca fueron a visitar a Marta y a María, para consolarlas por la muerte de su hermano. Cuando Jesús llegó a Betania, se enteró de que habían sepultado a Lázaro cuatro días antes. 20. Al enterarse Marta de que Jesús había llegado, salió a recibirlo, y María se quedó en la casa. 21. Entonces Marta le dijo a Jesús:
--Señor, si tú hubieras estado aquí, mi hermano no habría muerto. 22. Pero a pesar de todo lo que ha pasado, Dios hará lo que tú le pidas. De eso estoy segura. 23. Jesús le contestó:
--Tu hermano volverá a vivir. 24. Y Marta le dijo:
--Claro que sí, cuando llegue el fin, todos los muertos volverán a vivir. 25. A esto Jesús respondió:
--Yo soy el que da la vida y el que hace que los muertos vuelvan a vivir. Quien pone su confianza en mí, aunque muera, vivirá. 26. Los que todavía viven y confían en mí, nunca morirán para siempre. ¿Puedes creer esto? 27. Marta le respondió:
--Sí, Señor. Yo creo que tú eres el Mesías, el Hijo de Dios, que debía venir al mundo.


Jesús llora por su amigo 28. Después de decir esto, Marta llamó a María y le dijo en secreto: "El Maestro ha llegado, y te llama". 29. María se levantó en seguida y fue a verlo. 30. Jesús no había llegado todavía a la casa, sino que estaba en el lugar donde Marta lo había encontrado. 31. Al ver que María se levantó y salió rápidamente, los judíos que estaban consolándola en su casa la siguieron. Ellos pensaban que María iba a llorar ante la tumba de su hermano. 32. Cuando María llegó adonde estaba Jesús, se arrodilló delante de él y le dijo:
--Señor, si hubieras estado aquí, mi hermano no habría muerto. 33. Cuando Jesús vio que María y los judíos que habían ido con ella lloraban mucho, se sintió muy triste y les tuvo compasión. 34. Les preguntó:
--¿Dónde sepultaron a Lázaro?
Ellos le dijeron:
--Ven Señor; aquí está. 35. Jesús se puso a llorar, 36. y los judíos que estaban allí dijeron: "Se ve que Jesús amaba mucho a su amigo Lázaro". 37. Pero otros decían: "Jesús hizo que el ciego pudiera ver. También pudo haber hecho algo para que Lázaro no muriera".


Lázaro vuelve a vivir 38. Todavía con lágrimas en los ojos, Jesús se acercó a la cueva donde habían puesto el cuerpo de Lázaro, y ordenó que quitaran la piedra que cubría la entrada. Pero Marta le dijo:
--Señor, hace cuatro días que murió Lázaro. Seguramente ya huele mal. 40. Jesús le contestó:
--¿No te dije que si confías en mí verás el poder de Dios? 41. La gente quitó la piedra de la entrada. Luego, Jesús miró al cielo y dijo:
"Padre, te doy gracias porque me has escuchado. 42. Yo sé que siempre me escuchas, pero lo digo por el bien de todos los que están aquí, para que crean que tú me enviaste". 43. Después de decir esto, Jesús gritó: "¡Lázaro, sal de ahí!" 44. Lázaro salió de la cueva envuelto totalmente en las vendas de lino con que lo habían sepultado. Su cara estaba envuelta con un pañuelo. Por eso Jesús le dijo a los que estaban allí: "Quítenle todas las vendas, y déjenlo libre".


El plan para matar a Jesús 45. Muchos de los judíos que habían ido al pueblo de Betania para acompañar a María vieron lo que Jesús hizo, y creyeron en él. 46. Pero otros fueron a ver a los fariseos, y les contaron lo que Jesús había hecho. 47. Los sacerdotes principales y los fariseos reunieron a la Junta Suprema, y dijeron:
