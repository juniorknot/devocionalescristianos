                                                                                <p style="style=text-align: justify;"><strong id="numeros">28. </strong>
 Esto es mi sangre, y con ella Dios hace un trato con todos ustedes. Esa sangre servirá para perdonar los pecados de mucha gente. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">29. </strong>
 Esta es la última vez que bebo de este vino con ustedes. Pero cuando estemos juntos otra vez en el reino de mi Padre, entonces beberemos del vino nuevo". </p>
                                                    <h4><strong>Jesús anuncia la negación de Pedro</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">30. </strong>
 Después cantaron un himno y se fueron al Monte de los Olivos.


Pedro promete no dejar a Jesús </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">31. </strong>
 Cuando llegaron al Monte de los Olivos, Jesús les dijo a los discípulos:
--Esta noche ustedes van a perder su confianza en mí. Porque la Biblia dice:
"Heriré al pastor
y las ovejas huirán
por todos lados". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">32. </strong>
 "Pero cuando Dios me devuelva la vida, iré a Galilea antes que ustedes. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">33. </strong>
 Entonces Pedro le dijo:
--Aunque todos te abandonen, yo no te abandonaré. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">34. </strong>
 Jesús le respondió:
--Pedro, no estés muy seguro de eso; antes de que el gallo cante, tres veces dirás que no me conoces. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">35. </strong>
 Pedro le contestó:
--Aunque tenga que morir contigo, yo nunca diré que no te conozco.
Los demás discípulos dijeron lo mismo.


Jesús ora con mucha tristeza </p>
                                                    <h4><strong>Jesús ora en Getsemaní</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
 Después, Jesús fue con sus discípulos a un lugar llamado Getsemaní, y les dijo: "Quédense aquí, mientras yo voy allí a orar". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
 Jesús invitó a Pedro, a Santiago y a Juan para que lo acompañaran. Luego empezó a sentirse muy, pero muy triste, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
 y les dijo: "Estoy muy triste, y siento que me voy a morir; quédense aquí conmigo y no se duerman". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
 Jesús se alejó un poco de ellos, se arrodilló y se inclinó hasta tocar el suelo con la frente, y oró a Dios: "Padre, ¡cómo deseo que me libres de este sufrimiento! Pero que no suceda lo que yo quiero, sino lo que quieras tú". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
 Jesús regresó a donde estaban los tres discípulos, y los encontró durmiendo. Entonces le dijo a Pedro: "¿No han podido quedarse despiertos conmigo ni una hora? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
 No se duerman; oren para que puedan resistir la prueba que se acerca. Ustedes están dispuestos a hacer lo bueno, pero no pueden hacerlo con sus propias fuerzas". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
 Jesús se fue a orar otra vez, y decía:
--Padre, si tengo que pasar por este sufrimiento, estoy dispuesto a obedecerte. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
 Jesús regresó de nuevo a donde estaban los tres discípulos, y otra vez los encontró bien dormidos, pues estaban muy cansados. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
 Nuevamente se apartó de ellos y oró por tercera vez, repitiendo las mismas palabras con que había orado antes. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
 Luego volvió Jesús a donde estaban los tres discípulos y les dijo: "¿Siguen descansando y durmiendo? Ya vienen los malvados para apresarme a mí, el Hijo del hombre. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
 ¡Levántense y vengan conmigo, que allí viene el que me va a entregar!"


Los enemigos apresan a Jesús </p>
                                                    <h4><strong>Arresto de Jesús</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
 Todavía estaba hablando Jesús cuando llegó Judas, uno de los doce discípulos. Con él venían muchos hombres armados con cuchillos y palos. Los sacerdotes principales y los líderes del país los habían enviado. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 Judas ya les había dicho: "Al que yo bese, ese es Jesús; ¡arréstenlo!" </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 Judas se acercó a Jesús y le dijo:
--¡Hola, Maestro!
Y lo besó. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 Jesús le dijo:
--Amigo, ya que estás aquí, haz pronto lo que tienes que hacer.
Los hombres arrestaron a Jesús. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 Entonces uno de los que acompañaban a Jesús sacó su espada y le cortó una oreja al sirviente del jefe de los sacerdotes. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 Pero Jesús le dijo:
"Guarda tu espada, porque al que mata con espada, con espada lo matarán. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 ¿No sabes que yo puedo pedirle ayuda a mi Padre, y de inmediato me enviaría más de doce ejércitos de ángeles para defenderme? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 Deja que las cosas pasen como están sucediendo ahora; sólo así puede cumplirse lo que dice la Biblia". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 Jesús se volvió a la gente y le preguntó:
"¿Por qué han venido con cuchillos y palos, como si yo fuera un criminal? Todos los días estuve enseñando en el templo, y allí nunca me apresaron. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 Pero todo esto debe suceder para que se cumpla lo que anunciaron los profetas".
En ese momento, todos los discípulos abandonaron a Jesús y huyeron.


El juicio contra Jesús </p>
                                                    <h4><strong>Jesús ante el concilio</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 Pedro siguió a Jesús desde lejos y llegó hasta el patio del palacio. Allí se sentó con los guardias para no perderse de nada. Los que arrestaron a Jesús lo llevaron al palacio de Caifás, el jefe de los sacerdotes. Allí estaban reunidos los maestros de la Ley y los líderes del pueblo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">59. </strong>
 Los sacerdotes principales y todos los de la Junta Suprema buscaban gente que mintiera contra Jesús, para poder condenarlo a muerte. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">60. </strong>
 Sin embargo, aunque muchos vinieron con mentiras, no pudieron condenarlo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">61. </strong>
 Por fin, hubo dos que dijeron: "Este hombre dijo que es capaz de destruir el templo de Dios, y de construirlo de nuevo en tres días". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">62. </strong>
 El jefe de los sacerdotes dijo a Jesús:
--¿Oíste bien de qué te acusan? ¿Qué puedes decir para defenderte? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">63. </strong>
 Pero Jesús no respondió nada. Entonces el jefe de los sacerdotes le dijo:
--Dinos por Dios, quien vive para siempre, si eres tú el Mesías, el Hijo de Dios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">64. </strong>
 Jesús le respondió:
--Tú lo has dicho. Y déjame decirte que dentro de poco tiempo ustedes verán cuando yo, el Hijo del hombre, venga en las nubes del cielo con el poder y la autoridad que me da Dios todopoderoso. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">65. </strong>
 Al escuchar esto, el jefe de los sacerdotes rompió sus ropas para mostrar su enojo, y dijo:
--¡Ha insultado a Dios! Ya no necesitamos más pruebas. Dice que él es Dios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">66. </strong>
 ¿Qué les parece?
--¡Que muera! --contestaron todos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">67. </strong>
 Entonces algunos le escupieron en la cara y otros lo golpearon. Aun otros le pegaban en la cara, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">68. </strong>
 y le decían: "Mesías, ¡adivina quién te pegó!"


Pedro niega conocer a Jesús </p>
                                                    <h4><strong>Pedro niega a Jesús</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">69. </strong>
 Mientras sucedía todo esto, Pedro estaba sentado en el patio del palacio. De pronto, una sirvienta se le acercó y le dijo:
--Tú siempre estabas con Jesús, el de Galilea. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">70. </strong>
 Y delante de todos, Pedro le contestó:
--Eso no es cierto; ¡no sé de qué me hablas! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">71. </strong>
 Pedro salió por la puerta del patio, pero otra sirvienta lo vio y dijo a los que estaban allí:
--Este también estaba con Jesús, el que vino de Nazaret. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">72. </strong>
 Pedro lo negó de nuevo y dijo:
--¡Les juro que no conozco a ese hombre! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">73. </strong>
 Un poco más tarde, algunos de los que estaban por allí se acercaron a Pedro y le dijeron:
--Estamos seguros de que tú eres uno de los seguidores de Jesús; hablas como los de Galilea. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">74. </strong>
 Pedro les contestó con más fuerza:
--¡Ya les dije que no conozco a ese hombre! ¡Que Dios me castigue si no estoy diciendo la verdad!
En ese momento un gallo cantó, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">75. </strong>
 y Pedro se acordó de lo que Jesús le había dicho: "Antes de que el gallo cante, vas a decir tres veces que no me conoces". Pedro salió de aquel lugar y se puso a llorar con mucha tristeza. </p>
                                                        
