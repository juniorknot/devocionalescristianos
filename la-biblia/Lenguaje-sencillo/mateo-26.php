                                <li class="hide-for-large"><a href="whatsapp://send?text=Mateo 26 (BLS)%0A%0A 1. Cuando Jesús terminó de enseñar, dijo a sus discípulos: 2. "Ustedes saben que dentro de dos días se celebrará la fiesta de la Pascua. Durante la fiesta, yo, el Hijo del hombre, seré apresado y moriré clavado en una cruz". 3. En esos días, los sacerdotes principales y los líderes del país se reunieron en el palacio del jefe de los sacerdotes, llamado Caifás. 4. Todos se pusieron de acuerdo para ponerle una trampa a Jesús, apresarlo y matarlo. 5. Pero algunos decían: "No lo hagamos durante la fiesta, para que la gente no se enoje contra nosotros ni se arme un gran alboroto".


Una mujer perfuma a Jesús 6. Jesús estaba en el pueblo de Betania, en casa de Simón, el que había tenido lepra. 7. Mientras Jesús comía, llegó una mujer con un frasco de perfume muy caro. Se acercó a él y le echó el perfume sobre la cabeza. 8. Los discípulos se enojaron y dijeron:
--¡Qué desperdicio! 9. Ese perfume pudo haberse vendido, y con el dinero hubiéramos ayudado a muchos pobres. 10. Jesús los escuchó y en seguida les dijo:
--No critiquen a esta mujer. Ella me ha tratado con bondad. 11. Siempre habrá gente pobre cerca de ustedes, pero muy pronto ya no estaré aquí con ustedes. 12. Esta mujer echó perfume sobre mi cabeza, sin saber que estaba preparando mi cuerpo para mi entierro. 13. Les aseguro que esto que ella hizo se recordará en todos los lugares donde se anuncien las buenas noticias de Dios.


Judas traiciona a Jesús 14. Ese mismo día, Judas Iscariote, uno de los doce discípulos de Jesús, fue a ver a los sacerdotes principales 15. y les dijo: "¿Cuánto me pagarán si les ayudo a atrapar a Jesús?"
Ellos le ofrecieron treinta monedas de plata. 16. Y desde ese momento, Judas buscó una buena oportunidad para entregarles a Jesús.


Una cena inolvidable 17. El primer día de la fiesta de los Panes sin levadura, los discípulos se acercaron a Jesús y le dijeron:
--¿Dónde quieres que preparemos la cena de Pascua? 18. Jesús les respondió:
--Vayan a la ciudad, busquen al amigo que ustedes ya conocen, y denle este mensaje: "El Maestro dice: yo sé que pronto moriré; por eso quiero celebrar la Pascua en tu casa, con mis discípulos". 19. Los discípulos fueron y prepararon todo, tal y como Jesús les mandó. 20. Al anochecer, mientras Jesús y sus discípulos comían, 21. él les dijo:
--Uno de ustedes me va a entregar a mis enemigos. 22. Los discípulos se pusieron muy tristes, y cada uno le dijo:
--Señor, no estarás acusándome a mí, ¿verdad? 23. Jesús respondió:
--El que ha mojado su pan en el mismo plato que yo, ese va a traicionarme. 24. La Biblia dice claramente que yo, el Hijo del hombre, tengo que morir. Sin embargo, al que me traiciona va a pasarle algo muy terrible. ¡Más le valdría no haber nacido! 25. Judas, el que después entregó a Jesús, también le preguntó:
--Maestro, ¿soy yo?
Jesús le contestó:
--Tú lo has dicho. 26. Mientras estaban comiendo, Jesús tomó un pan y dio gracias a Dios. Luego lo partió, lo dio a sus discípulos y les dijo:
"Tomen y coman; esto es mi cuerpo". 27. Después tomó una copa llena de vino y dio gracias a Dios. Luego la pasó a sus discípulos y les dijo:
"Beban todos ustedes de este vino. 28. Esto es mi sangre, y con ella Dios hace un trato con todos ustedes. Esa sangre servirá para perdonar los pecados de mucha gente. 29. Esta es la última vez que bebo de este vino con ustedes. Pero cuando estemos juntos otra vez en el reino de mi Padre, entonces beberemos del vino nuevo". 30. Después cantaron un himno y se fueron al Monte de los Olivos.


Pedro promete no dejar a Jesús 31. Cuando llegaron al Monte de los Olivos, Jesús les dijo a los discípulos:
--Esta noche ustedes van a perder su confianza en mí. Porque la Biblia dice:
"Heriré al pastor
y las ovejas huirán
por todos lados". 32. "Pero cuando Dios me devuelva la vida, iré a Galilea antes que ustedes. 33. Entonces Pedro le dijo:
--Aunque todos te abandonen, yo no te abandonaré. 34. Jesús le respondió:
--Pedro, no estés muy seguro de eso; antes de que el gallo cante, tres veces dirás que no me conoces. 35. Pedro le contestó:
--Aunque tenga que morir contigo, yo nunca diré que no te conozco.
Los demás discípulos dijeron lo mismo.


Jesús ora con mucha tristeza 36. Después, Jesús fue con sus discípulos a un lugar llamado Getsemaní, y les dijo: "Quédense aquí, mientras yo voy allí a orar". 37. Jesús invitó a Pedro, a Santiago y a Juan para que lo acompañaran. Luego empezó a sentirse muy, pero muy triste, 38. y les dijo: "Estoy muy triste, y siento que me voy a morir; quédense aquí conmigo y no se duerman". 39. Jesús se alejó un poco de ellos, se arrodilló y se inclinó hasta tocar el suelo con la frente, y oró a Dios: "Padre, ¡cómo deseo que me libres de este sufrimiento! Pero que no suceda lo que yo quiero, sino lo que quieras tú". 40. Jesús regresó a donde estaban los tres discípulos, y los encontró durmiendo. Entonces le dijo a Pedro: "¿No han podido quedarse despiertos conmigo ni una hora? 41. No se duerman; oren para que puedan resistir la prueba que se acerca. Ustedes están dispuestos a hacer lo bueno, pero no pueden hacerlo con sus propias fuerzas". 42. Jesús se fue a orar otra vez, y decía:
--Padre, si tengo que pasar por este sufrimiento, estoy dispuesto a obedecerte. 43. Jesús regresó de nuevo a donde estaban los tres discípulos, y otra vez los encontró bien dormidos, pues estaban muy cansados. 44. Nuevamente se apartó de ellos y oró por tercera vez, repitiendo las mismas palabras con que había orado antes. 45. Luego volvió Jesús a donde estaban los tres discípulos y les dijo: "¿Siguen descansando y durmiendo? Ya vienen los malvados para apresarme a mí, el Hijo del hombre. 46. ¡Levántense y vengan conmigo, que allí viene el que me va a entregar!"


Los enemigos apresan a Jesús 47. Todavía estaba hablando Jesús cuando llegó Judas, uno de los doce discípulos. Con él venían muchos hombres armados con cuchillos y palos. Los sacerdotes principales y los líderes del país los habían enviado. 48. Judas ya les había dicho: "Al que yo bese, ese es Jesús; ¡arréstenlo!" 49. Judas se acercó a Jesús y le dijo:
--¡Hola, Maestro!
Y lo besó. 50. Jesús le dijo:
--Amigo, ya que estás aquí, haz pronto lo que tienes que hacer.
