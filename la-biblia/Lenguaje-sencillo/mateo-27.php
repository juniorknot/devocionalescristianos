 A esa hora, Jesús gritó con mucha fuerza: "¡Elí, Elí!, ¿lemá sabactani?"
Eso quiere decir: "¡Dios mío, Dios mío! ¿Por qué me has abandonado?" </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
 Algunos de los que estaban allí lo oyeron y dijeron: "Está llamando al profeta Elías". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
 Uno de ellos consiguió de inmediato una esponja, la empapó con vinagre, la ató en el extremo de un palo largo y se la acercó a Jesús para que bebiera. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
 Los demás que observaban le dijeron: "Déjalo, vamos a ver si Elías viene a salvarlo". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
 Jesús dio otro fuerte grito y murió. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
 En aquel momento, la cortina del templo se partió en dos pedazos de arriba abajo, la tierra tembló y las rocas se partieron; </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
 las tumbas se abrieron, y muchos de los que confiaban en Dios y ya habían muerto, volvieron a vivir. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
 Después de que Jesús resucitó, esas personas entraron en Jerusalén y mucha gente las vio. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
 El oficial romano y los soldados que vigilaban a Jesús sintieron el terremoto y vieron todo lo que pasaba. Temblando de miedo dijeron: "¡Es verdad, este hombre era el Hijo de Dios!" </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
 Había allí muchas mujeres que miraban desde lejos. Ellas habían seguido y ayudado a Jesús durante su viaje desde Galilea. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
 Entre esas mujeres estaban María Magdalena, María la madre de Santiago y de José, y la esposa de Zebedeo.


El entierro de Jesús </p>
                                                    <h4><strong>Jesús es sepultado</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
 Al anochecer, un hombre rico llamado José se acercó al lugar. Era del pueblo de Arimatea y se había hecho seguidor de Jesús. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
 José le pidió a Pilato que le permitiera llevarse el cuerpo de Jesús para enterrarlo. Pilato ordenó que se lo dieran. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">59. </strong>
 José tomó el cuerpo de Jesús, lo envolvió en una sábana limpia </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">60. </strong>
 y lo puso en una tumba. Era una tumba nueva, que hacía poco tiempo él había ordenado construir en una gran roca. José tapó la entrada de la tumba con una piedra muy grande, y se fue. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">61. </strong>
 Frente a la tumba se quedaron sentadas María Magdalena y la otra María. </p>
                                                    <h4><strong>La guardia ante la tumba</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">62. </strong>
 El día siguiente era sábado, el día de descanso de los judíos. Los sacerdotes principales y los fariseos fueron a ver a Pilato </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">63. </strong>
 y le dijeron:
--Señor, nos acordamos que cuando ese mentiroso de Jesús aún vivía, dijo: "Tres días después de que me maten volveré a vivir". </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">64. </strong>
 Ahora sus discípulos pueden robar el cuerpo y empezar a decir a la gente que Jesús resucitó. Ese engaño sería peor que cuando él dijo que era el Mesías. Para que no pase esto, ordene usted que unos guardias vigilen cuidadosamente la tumba hasta después del tercer día. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">65. </strong>
 Pilato les dijo:
--Ustedes tienen soldados a su servicio; vayan y protejan la tumba lo mejor que puedan. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">66. </strong>
 Entonces ellos fueron a la tumba y ataron la piedra que tapaba la entrada para que no se moviera. También dejaron allí a los soldados para que vigilaran. </p>
                                                        
