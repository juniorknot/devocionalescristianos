envíen a Kedar [hijo de Yishmael] y observen atentamente;
miren si algo como esto ha sucedido anteriormente; 11. ¿acaso alguna nación ha cambiado sus dioses
(¡y los de ellos no son dioses en lo absoluto!)? °
Sin embargo, mi pueblo cambió su Gloria por algo sin valor alguno. ° 12. ¡Sorpréndanse por esto, ustedes cielos! °
¡Tiemblen en absoluto horror!" Dice YAHWEH. 13. "¡Porque mi pueblo ha cometido dos males:
ellos me han abandonado, la fuente de agua viviente, °
y han cavado para sí mismos cisternas, cisternas rotas, que no retienen agua! 14. ¿Es Yisra'el un esclavo, nacido para servidumbre? °
si no ¿Por qué se ha convertido en botín? 15. Los leones le están rugiendo, ¡y qué alto ellos rugen!
