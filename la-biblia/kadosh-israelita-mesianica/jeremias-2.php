y convertirse ellos mismos en vanos?" ° 6. Ellos no preguntaron: "¿Dónde está YAHWEH,
quien nos sacó de la tierra de Mitzrayim, °
quien nos condujo por el desierto, °
a través de una tierra despoblada y de barrancos,
