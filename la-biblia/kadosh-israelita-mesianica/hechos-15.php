mos salvos, y es lo mismo con ellos." 12. Por lo cual toda la asamblea se mantuvo quieta mientras escuchaban a Bar-Nabba y a Shaúl contar las señales y milagros que YAHWEH había hecho por medio de ellos entre los Goyim. 13. Ya'akov rompió el silencio para responder, y dijo: "Hermanos, oigan lo que tengo que decir. 14. Shimeon les ha contado en detalle lo que YAHWEH hizo cuando comenzó a mostrar su interés en tomar un pueblo entre los Goyim para llevar Su Nombre.[100] 15. Y las palabras de los Profetas están en armonía con esto, pues está escrito: 16. "Después de esto regresaré;
