'Por más que permanezcan escuchando, nunca entenderán,
por más que permanezcan mirando, nunca percibirán, 15. porque el corazón de esta gente está entumecido;
con sus oídos escasamente oyen,
han cerrado sus ojos,
como para no ver con ellos,
ni oír con sus oídos,
para no entender con sus corazones,
y hacer teshuvah,
para que Yo los pueda sanar.'[75] ° 16. "Pero ustedes, ¡bendecidos son sus ojos, porque ellos ven; y sus oídos, porque ellos oyen! 17. ¡Sí, en verdad! Les aseguro que muchos profetas y muchos tzaddikim quisieron haber visto esto que ustedes están viendo, y no lo vieron; quisieron haber oído esto que ustedes están oyendo, y no lo oyeron. [76] 18. "Escuchen, pues, lo que quiere decir la parábola del sembrador: 19. Los que oyen el mensaje del Reino y no lo entienden, son como la semilla que cayó en el camino; viene el maligno, y les quita el mensaje sembrado en su corazón. 20. La semilla que cayó entre las piedras, representa a los que oyen el mensaje, y lo aceptan inmediatamente con alegría; 21. pero no tienen raíz en ellos, no se mantienen firmes; apenas surgen pruebas o persecución por causa del mensaje, se van apartando rápidamente. 22. La semilla que cayó entre espinos, representa a los que oyen el mensaje, pero es ahogado por las preocupaciones de este mundo y el amor a las riquezas, así que no produjo nada. 23. Sin embargo, la semilla sembrada en buena tierra, es el que oye el mensaje y lo entiende; dicha persona seguramente dará buen fruto, cien, sesenta, treinta veces más de lo que se sembró."[77] 24. Yahshúa les contó otra parábola: "El Reino de YAHWEH es como un hombre que sembró buena semilla en su campo; 25. pero cuando todos estaban durmiendo, su enemigo sembró mala hierba[78] entre el trigo, y después se retiró. 26. Cuando el trigo brotó y se formó la espiga con grano, la mala hierba también brotó. 27. Entonces los trabajadores fueron a decirle al dueño: 'Adón, ¿no sembraste buena semilla en tu campo? ¿De dónde ha salido la mala hierba?' 28. El respondió: 'Algún enemigo ha hecho esto.' Los trabajadores le preguntaron: '¿Quieres que vayamos a arrancar la mala hierba?' 29. El les dijo: 'No, porque al arrancar la mala hierba, pueden arrancar algo del trigo al mismo tiempo. 30. Dejen que crezcan juntos hasta la cosecha; entonces mandaré a los que recojan la 