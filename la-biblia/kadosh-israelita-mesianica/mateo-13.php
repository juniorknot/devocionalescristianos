También les contó, además, otra parábola: "El Reino de YAHWEH es semejante a la levadura que una mujer tomó y mezcló con tres medidas de harina, después esperó hasta que toda la masa fue leudada."[81] </p>
                                                    <h4><strong>El uso que Jesús hace de las parábolas</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">34. </strong>
Todas estas cosas, Yahshúa contó a la gente por medio de parábolas; en verdad no dijo nada sin usar parábolas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">35. </strong>
Esto era para que se cumpliera lo dicho por el profeta: "Abriré mi boca en parábolas,
diré lo que ha estado oculto
desde la creación del universo"[82] ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
Entonces Yahshúa dejó la multitud, y entró en la casa. Sus talmidim se le acercaron, y dijeron: "Explícanos la parábola de las malas hierbas en el campo." </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
Y les dijo: "El que siembra la buena semilla es el Ben Ha Adam; </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
el campo es el mundo, la buena semilla representa las personas que pertenecen al Reino, y las malas hierbas son las personas que pertenecen al maligno. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
El enemigo que sembró la mala hierba es ha satán, la cosecha es el fin del mundo, y los que recogen la cosecha son los malajim. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
Tal como la mala hierba se recoge, y se echa al fuego para quemarla, así sucederá en el fin del mundo.[83] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
El Ben Ha Adam enviará a sus malajim a recoger y desechar de su Reino todas las cosas que ofenden [apóstatas], y a los rechazan la Toráh; [84] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
y los echarán en el ardiente horno, donde la gente se lamentará y rechinarán sus dientes. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
Entonces los justos brillarán como el sol en el Reino de su Padre. ¡Los que tienen oídos, que oigan! </p>
                                                    <h4><strong>El tesoro escondido</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
"El Reino de YAHWEH es semejante a un tesoro escondido en un campo. Un hombre lo encontró, lo escondió de nuevo y después, con mucha alegría fue, vendió todo lo que tenía, y compró el campo. </p>
                                                    <h4><strong>La perla de gran precio</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
"También el Reino de YAHWEH es como un comerciante que andaba buscando perlas finas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
Cuando encontró una perla de mucho valor, fue y vendió todo lo que tenía, y la compró. </p>
                                                    <h4><strong>La red</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
"También sucede que el Reino de YAHWEH es como la red que se tiró al lago y recogió toda clase de peces. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
Cuando estaba llena, los pescadores la sacaron a la orilla, se sentaron a recoger el pescado bueno en canastas, pero el malo lo tiraron fuera.[85] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
Así también sucederá al cerrar ésta época; los malajim saldrán adelante, para separar a los que no guardan la Toráh de los justos, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
y echarán a los que no guardan la Toráh en el ardiente horno, donde se lamentarán y rechinarán los dientes. [86] </p>
                                                    <h4><strong>Tesoros nuevos y viejos</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
"¿Han entendido ustedes todas éstas cosas? "Sí," respondieron ellos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
Y les dijo: "Entonces todo maestro de la Toráh, quien ha sido hecho un talmid para el Reino de YAHWEH es como el dueño de una casa, que saca de su almacén ambas cosas nuevas y viejas."[87] </p>
                                                    <h4><strong>Jesús en Nazaret</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
Cuando Yahshúa terminó de contar estas parábolas se fue, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
y se dirigió a su pueblo natal. Entonces empezó a enseñar en la sinagoga en una forma que los asombraba, así que preguntaron: "¿De dónde viene toda la sabiduría y milagros de éste? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
¿No es éste, el hijo del carpintero?[88] ¿No se llama su madre Miryam? ¿No se llaman sus hermanos Ya'akov, Yosef, Shimeon y Yahudáh? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
Y sus hermanas, ¿no están todas ellas con nosotros?[89] Así que, ¿de dónde saca El todo esto?" </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
Y se sentían ofendidos por El, pero Yahshúa les dijo: "En el único lugar que la gente no respeta a un profeta es en su pueblo natal y en su propia casa." </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
Entonces hizo pocos milagros allí, porque aquella gente no tenía confianza en El.</p>
                                                        
