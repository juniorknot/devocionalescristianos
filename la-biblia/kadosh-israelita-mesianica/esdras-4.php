tras naciones las cuales el gran noble Asnapar[16] removió y asentó en Shomron, y el resto [de ellos] del otro lado del Río [Eufrates]." 11. (Este es el texto de la carta que ellos enviaron a él,)
"A Artajshashta el rey de sus siervos el pueblo del otro lado de Río: 12. "Sepa el rey que los Yahudim que te dejaron para subir a nosotros en Yerushalayim están edificando esta ciudad rebelde y perversa. Ellos han terminado los muros y ahora ellos han establecido los cimientos. 13. Y ahora, que sea conocido por el rey que si esta ciudad es edificada y los muros son terminados, tú no tendrás tributos, tampoco ellos pagarán [nada], y esto daña al rey. 14. No es legal para nosotros ver la deshonra hacia el rey,[17] nosotros, por lo tanto, hemos enviado a informar al rey 15. para que una búsqueda pueda ser hecha en los archivos de tus padres; en estos archivos encontrarás y comprobarás que esta ciudad es de cierto una ciudad rebelde, y hace daño a reyes y países, y que en medio de ella hay refugio para esclavos [fugitivos]; por lo tanto, esta ciudad ha sido desolada. 16. Nosotros sometemos al rey que si esta ciudad es edificada y los muros son terminados, no tendrías Shalom." 17. El rey envió la respuesta:
