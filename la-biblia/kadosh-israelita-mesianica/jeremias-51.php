En la tierra de los Kasdim los muertos caerán, aquellos atravesados caerán fuera. ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">5. </strong>
Yisra'el y Yahudáh no se han olvidado de su Elohim, YAHWEH-HaElyon; °
pero su tierra [de los Kasdim] esta llena de iniquidad delante del HaKadosh de Yisra'el." </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">6. </strong>
¡Huyan de Bavel, que cada uno salve su alma! No perezcan en la iniquidad de ella.
Porque el tiempo ha llegado para la retribución de YAHWEH; °
El le recompensará por lo que ella merece. ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">7. </strong>
Bavel fue una copa de oro en las manos de YAHWEH; °
hizo que toda la tierra se emborrachara – las naciones bebieron su vino;
a causa de esto las naciones han perdido sus sentidos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">8. </strong>
Bavel ha caído repentinamente. Ella está quebrada en pedazos; lloren por ella. °
Traigan ungüento para su herida mortal; quizás ella pueda ser sanada. ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">9. </strong>
"Nosotros tratamos de sanar a Bavel, pero ella no puede ser sanada. °
Así que déjenla estar, y cada uno de nosotros regresará a su país." °
Porque el juicio contra ella sube hasta el cielo y llega hasta las estrellas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">10. </strong>
 YAHWEH ha traído Su juicio. ¡Vengan, vamos a proclamar en Tziyon
la obra de YAHWEH ELOHEINU! ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">11. </strong>
¡Afilen las saetas! ¡Llenen las aljabas!
YAHWEH provocó el ruaj de los reyes de los Medos, porque su ira está contra Bavel.
Esta es la venganza de YAHWEH, venganza sobre Su Templo.[247] ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">12. </strong>
¡Levanten bandera contra las murallas de Bavel! ¡Levanten la guardia! °
¡Preparen las aljabas, preparen las armas! Porque YAHWEH 
ha ambos planeado y ejecutado lo que El habló contra los habitantes de Bavel. ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">13. </strong>
Tú que habitas cerca de mucha agua, tan rico en tesoro –
¡tu fin ha llegado, tu momento de ser cortado! ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">14. </strong>
 YAHWEH ha jurado por Su Brazo: "¡Te llenaré con hombres como con langostas;
ellos levantarán sobre ti un grito de triunfo!" ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">15. </strong>
 YAHWEH hizo la tierra por Su Poder, estableció el mundo por Su Sabiduría,
extendió los cielos por Su entendimiento. ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">16. </strong>
Cuando El truena, las aguas del cielo rugen, El trae nubes desde los confines de la tierra,
El hace que el relámpago resplandezca en la lluvia, y trae la luz de sus almacenes. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">17. </strong>
¡A esto todo hombre perdió el entendimiento! °
¡Todos los orfebres son puestos en vergüenza por sus ídolos!
Las figuras que funden son un fraude, no hay aliento en ellos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">18. </strong>
Ellos son obras vanas, objetos de desdeño; °
en tiempo que su visitación llegue, ellos perecerán. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">19. </strong>
La porción de Ya'akov no es así, porque El es el que formó todas las cosas, °
Yisra'el es la vara de Su herencia. YAHWEH es Su Nombre. ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">20. </strong>
"Tú eres mi garrote de guerra y mis armas de guerra – contigo disperso naciones;
contigo Yo destruyo reinos; ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">21. </strong>
contigo disperso caballos y sus jinetes; contigo disperso carruajes y los que los guían; </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">22. </strong>
contigo disperso juventud y sirvientas; contigo disperso hombres y mujeres; ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">23. </strong>
contigo disperso pastores y sus rebaños, contigo disperso labradores y sus yuntas;
contigo disperso líderes y sus capitanes. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">24. </strong>
"Pero Yo pagaré a Bavel y a todos los Kasdim que habitanallí por todo el mal °
ellos hicieron en Tziyon," dice YAHWEH, "delante de tus ojos [Yahudáh]." ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">25. </strong>
"Yo estoy contra ti montaña destructora, °
destruyendo toda la tierra," dice YAHWEH. "Yo extenderé Mi Brazo hacia ti,
para lanzarte rodando de las hendiduras de las rocas y te haré una montaña quemada. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">26. </strong>
Nadie tomará piedras angulares o piedras de cimiento de ti otra vez;
sino que estarás desolada para siempre," dice YAHWEH.[248] ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">27. </strong>
Levanten bandera en La Tierra, suenen el shofar entre las naciones. °
Preparen a las naciones para la guerra contra ella. Convoquen reinos contra ella –
Ararat, Minni, y Ashkenaz. Nombren un oficial contra ella;
traigan caballos como multitud de langostas.[249] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">28. </strong>
Traigan las naciones contra ella, °
Los reyes de los Medos, sus gobernantes y capitanes, y toda la tierra que él controla. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">29. </strong>
La tierra tiembla y se retuerce, y los designios de Elohim contra Bavel son cumplidos,
hacer de la tierra de Bavel una ruina, con nadie habitando allí. ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">30. </strong>
El guerrero de Bavel se ha rendido ante la batalla; ellos permanecen en sus fortalezas;
Su valentía ha fallado; ahora son como mujeres. Sus moradas están incendiadas,
los cerrojos de las puertas, quebrados.[250] ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">31. </strong>
Un corredor corre para alcanzar a otro, mensajero para alcanzar al mensajero,
para reportar al rey de Bavel, que todas las partes de su ciudad están tomadas, ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">32. </strong>
todos los riachuelos están ocupados, y sus cisternas han sido quemadas con fuego,
mientras los guerreros están sobrecogidos con pánico. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">33. </strong>
Aquí está lo que YAHWEH-Tzevaot, el Elohim de Yisra'el, dice:
"La hija de Bavel es como era en tiempo de vendimia, °
sólo un poco de tiempo más, y el tiempo de cosecharla a ella llegará." ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">34. </strong>
Nevujadretzar rey de Bavel me ha devorado, me ha machacado.
El me dejó como una olla vacía; como un monstruo me tragó entero.
Con mis delicadezas se llenó su panza. ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">35. </strong>
Pero uno que vive en Tziyon dirá: "Que mi carne rasgada sea vengada sobre Bavel,"
y Yerushalayim dirá: "Que mi sangre sea vengada sobre los Kasdim." ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
Por lo tanto aquí está lo que YAHWEH dice: "Yo defenderé tu causa. °
Yo tomaré venganza por ti. Yo secaré su río y haré que sus manantiales se sequen. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
Bavel se convertirá en un montón de ruinas, un lugar para que los chacales vivan,
un objeto de horror y burla, con nadie habitando allí. ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
Juntos ellos rugen como leones, gruñen como cachorros de león. ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">39. </strong>
Cuando estén calientes con deseo, les prepararé una bebida. °
Yo los pondré tan borrachos que tendrán convulsiones,
dormirán para siempre y nunca se despertarán," dice YAHWEH. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">40. </strong>
"Yo los arrastraré como a ovejas al matadero, como carneros y machos cabríos." ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">41. </strong>
¡Sheshaj ha sido capturada, el orgullo de toda la tierra, tomada!
¡Bavel se ha convertido en desolación entre todas las naciones! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">42. </strong>
El mar ha inundado a Bavel, la ha sobrecogido con sus olas rabiosas. ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">43. </strong>
Sus ciudades están desoladas – tierra agrietada, árida,
una tierra donde nadie habita; nadie pasa por allí. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">44. </strong>
"Yo castigaré a Bel en Bavel y lo haré vomitar lo que se ha tragado. °
Las naciones ya no fluirán a él. La muralla de Bavel caerá. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">45. </strong>
¡Salgan de ahí, pueblo mío![251] ¡Cada uno, sálvese de la ira furiosa de YAHWEH! ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">46. </strong>
No se descorazonen, no tengan miedo, de los rumores que se oyen en la tierra. °
Un año un rumor viene, el año próximo otros °
rumores de violencia en la tierra y reyes peleando contra reyes. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">47. </strong>
¡Por lo tanto, escuchen! Los días vienen cuando dictaré juicio sobre los ídolos de Bavel.
Toda su tierra será avergonzada, según todos sus muertos caen en tierra de casa. ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">48. </strong>
Entonces cielo y tierra y todo lo que en ellos haycantarán de alegría sobre Bavel;
porque los saqueadores del norte están viniendo a ella," dice YAHWEH. ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">49. </strong>
Así como Bavel causó que los muertos de Yisra'el cayeran,
así en Bavel caerán los muertos de toda la tierra. ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">50. </strong>
¡Tú que escapaste la espada, vete! ¡No te quedes parado ahí! °
Recuérdate de YAHWEH, y que Yerushalayim venga a sus mentes. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">51. </strong>
"El oprobio que hemos oído nos ha avergonzado, desgracia sobre nuestros rostros;
porque extranjeros han entrado en los Lugares Kadoshim de la casa de YAHWEH." </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">52. </strong>
"Por lo tanto," dice YAHWEH, "los días están viniendo
cuando tomaré venganza sobre sus ídolos, y los heridos gemirán en toda su tierra.[252] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">53. </strong>
Aun si Bavel escala a los cielos o refuerza su fortaleza majestuosa,
saqueadores vendrán a ella de Mí," dice YAHWEH. ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">54. </strong>
¡Un grito agonizante es oído desde Bavel! ¡Gran destrucción en la tierra de los Kasdim! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">55. </strong>
Porque YAHWEH está saqueando a Bavel y silenciando su ruidoso clamor –
sus olas rugen como el océano furioso, su clamor suena y resuena. ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">56. </strong>
Sí, la aflicción ha caído sobre ella, ha caído sobre Bavel. °
Sus guerreros son capturados, sus arcos son quebrados. °
Porque YAHWEH es un Elohim de retribución; Elohim los recompensa. ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">57. </strong>
"Yo intoxicaré a sus príncipes y a sus sabios, sus líderes, capitanes. °
Completamente borrachos, ellos dormirán para siempre y nunca despertarán,"
dice el Rey, cuyo Nombre es YAHWEH-Elohim Tzevaot. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">58. </strong>
Así dice YAHWEH: "Los muros anchos de Bavel serán arrasados hasta la tierra,
sus majestuosas puertas serán incendiadas.[253] Las gentes están trabajando en vano,
la labor de la nación se va en el fuego, y todos están exhaustos." ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">59. </strong>
Esta es la orden que el profeta Yirmeyah dio a Serayah el hijo de Neriyah, el hijo de Majseyah, cuando él fue a Bavel con Tzidkiyah el rey de Yahudáh en el cuarto año de su reinado. Serayah era intendente del ejército. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">60. </strong>
Yirmeyah había escrito en un rollo aparte todas las palabras arriba mencionadas describiendo el desastre que iba a caer sobre Bavel. ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">61. </strong>
Yirmeyah dijo a Serayah: "Cuando llegues a Bavel ocúpate de leer todas estas palabras en voz alta. ° Después di: </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">62. </strong>
' YAHWEH, Tú has prometido destruir este lugar, que nadie vivirá aquí, ni hombre ni animal, sino que será desolado para siempre.' ° </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">63. </strong>
Cuando termines de leer el rollo, átalo a una roca, tíralo en el medio del Eufrates,[254] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">64. </strong>
y di: 'Como ésta, Bavel se hundirá, para nunca flotar otra vez, a causa del desastre que estoy trayendo sobre ella; y ellos se afligirán.'" ° (Hasta aquí han sido las palabras de Yirmeyah.)</p>
                                                        
