porque he confiado en Tu Palabra. 43. No retires completamente mi poder de hablar la verdad;
porque pongo mi esperanza en Tus juicios; 44. y guardaré Tu Toráh siempre,
por siempre y para siempre. 45. Iré dondequiera que quiera,
porque he buscado Tus mandamientos. 46. Hablé de Tus instrucciones aun a los reyes
sin estar avergonzado.[342] 47. Me deleitaré en Tus mitzvot,
los cuales he amado. 48. Alzaré mis manos a Tus mitzvot, los cuales amo;[343]
y meditaré en Tus ordenanzas. 49. ז Zain
 Recuerda Tus palabras a tu siervo,
por ellas Tú me has dado esperanza. 50. En mi aflicción mi consuelo es éste:
Que Tu oráculo me da vida. 51. Los arrogantes han transgredido completamente;
pero yo no me he apartado de tu Toráh. 52. Tengo en mi mente Tus juicios de la antigüedad;
O YAHWEH, en ellos tomo consuelo. 53. El desánimo se apoderó de mí,
a causa de los pecadores que abandonan Tu Toráh. 54. Tus ordenanzas son mis canciones
donde quiera que asiente mi hogar. 55. De noche me acuerdo de Tu Nombre, O YAHWEH;
y observo tu Toráh. 56. Este (consuelo) ha llegado a mí,
porque diligentemente observo Tus ordenanzas. 57. ח Jet
 Tú eres mi porción, O YAHWEH,
yo dije que observaría Tu Toráh. 58. Suplico Tu misericordia con todo mi corazón;
muestra piedad de mí, según Tu Palabra. 59. He pensado acerca de mis caminos
y he vuelto mis pies hacia Tu instrucción. 60. Me he preparado (y no atemorizado),
para observar tus mitzvot. 61. Las trampas de los pecadores me enredan,
pero no me olvido de Tu Toráh. 62. A medianoche me levanto a darte gracias
por Tus justos juicios. 63. Soy amigo de todos los que te temen
de aquellos que observan Tus mandamientos. 64. La tierra, O YAHWEH, esta llena de Tu misericordia;
enséñame Tus ordenanzas. 65. ט Tet
 Has tratado bien a tu siervo.
O YAHWEH, conforme a Tu Palabra. 66. Enséñame bondad, juicio y conocimiento,
porque he creído en Tus mitzvot. 67. Antes que fuera yo humillado, yo transgredí;
mas ahora observo Tu Palabra. 68. Tú eres bueno, O YAHWEH, por lo tanto,
enséñame Tus ordenanzas. 69. Los arrogantes me están calumniando,
pero yo buscaré Tus mandamientos de todo corazón. 70. Sus corazones son tan gruesos como el sebo,
pero yo he meditado en Tu Toráh. 71. Es por mi bien haber sido humillado,
fue para que aprendiera Tus ordenanzas. 72. La Toráh de Tu Boca significa más para mí
que una fortuna de oro y plata. 73. י Yod
 Tus manos me hicieron y me formaron;
instrúyeme, para así poder aprender Tus mitzvot. 74. Los que te temen se alegran de verme,
porque he puesto mi esperanza en Tus Palabras. 75. Conozco, O YAHWEH, que Tus juicios son justos
y Tú en verdad me has afligido. 76. Por favor que Tu misericordia me consuele,
conforme a Tu Palabra a tu siervo. 77. Muéstrame piedad, y viviré,
porque Tu Toráh es mi meditación. 78. Sean los soberbios avergonzados, porque transgreden contra mí;
pero yo meditaré en Tus mandamientos. 79. Aquellos que te temen se vuelvan a mí,
junto con los que conocen Tu instrucción. 80. Sea mi corazón puro en Tus ordenanzas,
para que no sea avergonzado. 81. כ Kaf
 Mi alma de desmaya por Tu Salvación,
mi esperanza es en Tu Palabra. 82. Mis ojos desfallecen de contemplar Tu Palabra;
pregunto: "¿Cuándo me consolarás?" 83. Porque he temblado como el odre ahumado;
pero, aún no olvido Tus ordenanzas. 84. ¿Cuántos son los días de tu siervo?
¿Cuándo harás juicio contra los que me persiguen? 85. Los transgresores me dijeron [dichos vanos];
¡pero no me aparté de Tu Toráh! 86. Todos Tus mitzvot (muestran tu) verdad;
ellos me persiguieron injustamente; ¡ayúdame! 87. Casi han terminado con mi vida en la tierra,
pero no he abandonado Tus mandamientos. 88. Conforme a Tu misericordia revíveme;
y observaré las instrucciones de Tu Boca. 89. ל Lamed
 Tu Palabra continúa para siempre, O YAHWEH,
firme; fijada en los cielos; 90. Tu verdad por todas las generaciones,
Tú estableciste la tierra, y ella permanece. 91. Sí, permanece hoy conforme a Tus juicios,
pues todas las cosas son tus siervos. 92. Si Tu Toráh no hubiera sido mi meditación,
hubiera perecido en mi aflicción, 93. Nunca olvidaré Tus ordenanzas,
porque con ellas me has vivificado. 94. Soy tuyo; sálvame
porque he buscado Tus ordenanzas. 95. Los pecadores añoran destruirme,
pero yo persevero en Tu instrucción. 96. Veo el fin de toda perfección,
pero Tu mitzvah no tiene barreras. 97. מ Mem
 ¡Cómo amo Tu Toráh, O YAHWEH!
Medito en ella todo el día. 98. Tú me has hecho más sabio que mis enemigos,
porque Tus mitzvot son míos para siempre. 99. Tengo más entendimiento que todos mis maestros
porque Tu instrucción es mi meditación, 100. Entiendo más que los ancianos,
porque guardo Tus mandamientos. 101. Aparto mis pies de todo camino maligno,
para guardar Tus Palabras. 102. No me aparto de Tus juicios,
porque Tú me has instruido. 103. ¡Qué dulce a mi garganta son Tus oráculos,
verdaderamente más dulce que miel en mi boca! 104. De Tus mandamientos adquiero entendimiento;
por esto odio todo camino de injusticia.[344] 105. נ Nun
 Lámpara a mis pies es Tu Toráh
y luz en mi senda.[345] 106. He jurado un pacto y lo he confirmado,
observaré Tus justos juicios. 107. He estado afligido en gran manera,
YAHWEH, dame vida, conforme a Tu Palabra. 108. Por favor acepta la ofrenda voluntaria de mi boca,
YAHWEH, y enséñame Tus juicios. 109. Mi alma está continuamente en Tus manos,
mas no he olvidado de Tu Toráh. 110. Los pecadores me han tendido trampa
mas no me he extraviado de Tus mandamientos. 111. Tengo tu instrucción como heredad permanente,
porque es la alegría de mi corazón. 112. He inclinado mi corazón a obedecer Tus ordenanzas
para siempre, porque son la alegría de mi corazón 113. ס Samej
 He odiado a los transgresores,
pero he amado Tu Toráh.[346] 114. Tú eres mi refugio y mi escudo.
pongo mi esperanza en Tus Palabras. 115. Déjenme estar, ustedes, obreros de maldad,
para que pueda buscar los mitzvot de mi Elohim. 116. Sostenme de acuerdo a Tu Palabra, y yo viviré;
no me desilusiones en mi esperanza. 117. Ayúdame; y yo seré salvo,
y meditaré en Tus ordenanzas continuamente. 118. Tú rechazas a todos los que ponen a nada Tus ordenanzas.
porque su pensamiento interno es falso. 119. Tú desecharás al perverso de la tierra como transgresores;
por esto amo Tu instrucción. 120. Mi cuerpo tiembla por temor de ti;
Tus juicios me dan miedo. 121. ע Ayin
 He hecho lo que es justo y recto;
no me abandones a mis opresores. 122. Garantiza el bienestar de tu siervo;
no permitas que el arrogante me acuse falsamente. 123. Mis ojos desfallecen por Tu Salvación
y por la palabra de Tu justicia. 124. Trata con tu siervo conforme a Tu misericordia
y enséñame Tus ordenanzas. 125. Soy tu siervo; dame entendimiento,
para así poder conocer Tu instrucción.[347] 126. El tiempo para YAHWEH actuar ha llegado;
porque ellos han quebrantando Tu Toráh. 127. Por lo tanto yo amo Tus mitzvot,
más que el oro, o el topacio. 128. Por eso dirijo mis pasos por Tus mandamientos;
odio todo camino injusto. 129. פ Pei
 Tu instrucción es una maravilla,
por esto mi alma la ha buscado. 130. La manifestación de Tu Palabra alumbrará;
dando instrucción a los descuidados. 131. Mi boca está completamente abierta mientras suspiro
con anhelo por Tus mitzvot. 132. Vuélvete a mí y muéstrame Tu misericordia,
conforme a Tus juicios para aquellos que aman Tu Nombre. 133. Ordena mis pasos por Tus Palabras;
no permitas que ninguna iniquidad domine en mí. 134. Redímeme de la opresión de los humanos,
y observaré Tus mandamientos. 135. Haz que Tu Rostro resplandezca sobre tu siervo,
y enséñame Tus ordenanzas. 136. Ríos de lágrimas fluyen de mis ojos
porque no observé Tu Toráh. 137. צ Tzadik
 Tú eres justo, O YAHWEH;
y Tus juicios son rectos 138. Tú has ordenado Tus instrucciones
en justicia y gran fidelidad. 139. Mi celo me está destruyendo,
porque mis enemigos han olvidado Tus Palabras. 140. Tu Palabra está refinada a completa pureza,
y tu siervo la ama. 141. Puedo ser joven y despreciado,
pero no me olvido de Tus ordenanzas. 142. Tu justicia es justicia eterna
y Tu Toráh es verdad. 143. Aflicción y angustia me han sobrecogido,
pero Tus mitzvot son mi meditación. 144. Tú instrucción es justa para siempre;
dame entendimiento, y yo viviré. 145. ק Kuf
 De todo corazón clamo a ti;
respóndeme, O YAHWEH; yo guardaré Tus ordenanzas. 146. Clamo a ti; sálvame
y observaré Tu instrucción. 147. Me levanto antes del alba y clamo por tu ayuda;
pongo mi esperanza en Tu Palabra. 148. Mis ojos están abiertos antes de las vigilias de la noche,
para que pueda meditar en Tus oráculos. 149. En Tu misericordia, oye mi voz;
YAHWEH, conforme a Tu justicia, revíveme. 150. Los que persiguen la carnalidad se acercan;
se están distanciando de Tu Toráh. 151. Cerca estás Tú, YAHWEH;
y todos Tus mitzvot son verdad. 152. Hace ya mucho aprendí de Tu instrucción,
que estableciste para siempre. 153. ר Reish
 Mira mi aflicción y rescátame,
porque no me olvido de Tu Toráh. 154. Intercede por mi causa, y redímeme;
conforme a Tu Palabra, revíveme. 155. La Salvación está muy lejos de los pecadores,
porque no buscan Tus ordenanzas. 156. Grande es Tu misericordia, O YAHWEH;
conforme a Tus juicios, revíveme. 157. A pesar de que mis perseguidores y enemigos,
son muchos, no me he apartado de Tu instrucción. 158. Miro a los traidores con disgusto,
porque no guardan Tus oráculos. 159. Mira como amo Tus mandamientos, YAHWEH;
conforme a Tu misericordia, revíveme. 160. Lo principal de Tu Palabra es que es verdad;
y todos Tus juicios permanecen para siempre.[348] 161. ש Shin
 Los príncipes me persiguen, sin razón alguna;
pero mi corazón tiene temor de Tus Palabras. 162. Me regocijo en Tus oráculos;
como alguien que encuentra mucho botín. 163. Odio la injusticia, la detesto;
pero amo Tu Toráh. 164. Te alabo siete veces al día,
a causa de tus justos juicios. 165. Aquellos que aman Tu Toráh tienen una gran Shalom;
nada les hace tropezar.[349] 166. Tengo esperanza en Tu Salvación, O YAHWEH;
y he amado Tus mitzvot. 167. Mi alma observa Tu instrucción,
¡Y yo la amo en gran manera! 168. Observo Tus mandamientos e instrucción, O YAHWEH,
porque todos mis caminos yacen al descubierto ante ti. 169. ת Tav
 Mi llanto llegue ante ti, O YAHWEH;
instrúyeme de acuerdo a Tus oráculos. 170. Mi oración llegue ante ti, O YAHWEH;
conforme a Tus oráculos, rescátame. 171. Mis labios rebocen de alabanza;
porque Tú me enseñas Tus ordenanzas. 172. Mi lengua pronuncie Tus oráculos,
porque todos Tus mitzvot son justos. 173. Tu mano esté lista para Salvarme,
porque escojo Tus mandamientos. 174. Añoro Tu salvación, O YAHWEH,
y Tu Toráh es mi meditación. 175. Mi alma vivirá, y te alabaré;
