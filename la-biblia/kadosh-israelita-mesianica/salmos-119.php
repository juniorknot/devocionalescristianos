Las trampas de los pecadores me enredan,
pero no me olvido de Tu Toráh. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">62. </strong>
A medianoche me levanto a darte gracias
por Tus justos juicios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">63. </strong>
Soy amigo de todos los que te temen
de aquellos que observan Tus mandamientos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">64. </strong>
La tierra, O YAHWEH, esta llena de Tu misericordia;
enséñame Tus ordenanzas. </p>
                                                    <h4><strong>Tet</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">65. </strong>

ט Tet
 Has tratado bien a tu siervo.
O YAHWEH, conforme a Tu Palabra. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">66. </strong>
Enséñame bondad, juicio y conocimiento,
porque he creído en Tus mitzvot. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">67. </strong>
Antes que fuera yo humillado, yo transgredí;
mas ahora observo Tu Palabra. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">68. </strong>
Tú eres bueno, O YAHWEH, por lo tanto,
enséñame Tus ordenanzas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">69. </strong>
Los arrogantes me están calumniando,
pero yo buscaré Tus mandamientos de todo corazón. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">70. </strong>
Sus corazones son tan gruesos como el sebo,
pero yo he meditado en Tu Toráh. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">71. </strong>
Es por mi bien haber sido humillado,
fue para que aprendiera Tus ordenanzas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">72. </strong>
La Toráh de Tu Boca significa más para mí
que una fortuna de oro y plata. </p>
                                                    <h4><strong>Yod</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">73. </strong>

י Yod
 Tus manos me hicieron y me formaron;
instrúyeme, para así poder aprender Tus mitzvot. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">74. </strong>
Los que te temen se alegran de verme,
porque he puesto mi esperanza en Tus Palabras. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">75. </strong>
Conozco, O YAHWEH, que Tus juicios son justos
y Tú en verdad me has afligido. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">76. </strong>
Por favor que Tu misericordia me consuele,
conforme a Tu Palabra a tu siervo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">77. </strong>
Muéstrame piedad, y viviré,
porque Tu Toráh es mi meditación. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">78. </strong>
Sean los soberbios avergonzados, porque transgreden contra mí;
pero yo meditaré en Tus mandamientos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">79. </strong>
Aquellos que te temen se vuelvan a mí,
junto con los que conocen Tu instrucción. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">80. </strong>
Sea mi corazón puro en Tus ordenanzas,
para que no sea avergonzado. </p>
                                                    <h4><strong>Caf</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">81. </strong>

כ Kaf
 Mi alma de desmaya por Tu Salvación,
mi esperanza es en Tu Palabra. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">82. </strong>
Mis ojos desfallecen de contemplar Tu Palabra;
pregunto: "¿Cuándo me consolarás?" </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">83. </strong>
Porque he temblado como el odre ahumado;
pero, aún no olvido Tus ordenanzas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">84. </strong>
¿Cuántos son los días de tu siervo?
¿Cuándo harás juicio contra los que me persiguen? </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">85. </strong>
Los transgresores me dijeron [dichos vanos];
¡pero no me aparté de Tu Toráh! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">86. </strong>
Todos Tus mitzvot (muestran tu) verdad;
ellos me persiguieron injustamente; ¡ayúdame! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">87. </strong>
Casi han terminado con mi vida en la tierra,
pero no he abandonado Tus mandamientos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">88. </strong>
Conforme a Tu misericordia revíveme;
y observaré las instrucciones de Tu Boca. </p>
                                                    <h4><strong>Lámed</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">89. </strong>

ל Lamed
 Tu Palabra continúa para siempre, O YAHWEH,
firme; fijada en los cielos; </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">90. </strong>
Tu verdad por todas las generaciones,
Tú estableciste la tierra, y ella permanece. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">91. </strong>
Sí, permanece hoy conforme a Tus juicios,
pues todas las cosas son tus siervos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">92. </strong>
Si Tu Toráh no hubiera sido mi meditación,
hubiera perecido en mi aflicción, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">93. </strong>
Nunca olvidaré Tus ordenanzas,
porque con ellas me has vivificado. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">94. </strong>
Soy tuyo; sálvame
porque he buscado Tus ordenanzas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">95. </strong>
Los pecadores añoran destruirme,
pero yo persevero en Tu instrucción. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">96. </strong>
Veo el fin de toda perfección,
pero Tu mitzvah no tiene barreras. </p>
                                                    <h4><strong>Mem</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">97. </strong>

מ Mem
 ¡Cómo amo Tu Toráh, O YAHWEH!
Medito en ella todo el día. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">98. </strong>
Tú me has hecho más sabio que mis enemigos,
porque Tus mitzvot son míos para siempre. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">99. </strong>
Tengo más entendimiento que todos mis maestros
porque Tu instrucción es mi meditación, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">100. </strong>
Entiendo más que los ancianos,
porque guardo Tus mandamientos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">101. </strong>
Aparto mis pies de todo camino maligno,
para guardar Tus Palabras. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">102. </strong>
No me aparto de Tus juicios,
porque Tú me has instruido. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">103. </strong>
¡Qué dulce a mi garganta son Tus oráculos,
verdaderamente más dulce que miel en mi boca! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">104. </strong>
De Tus mandamientos adquiero entendimiento;
por esto odio todo camino de injusticia.[344] </p>
                                                    <h4><strong>Nun</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">105. </strong>

נ Nun
 Lámpara a mis pies es Tu Toráh
y luz en mi senda.[345] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">106. </strong>
He jurado un pacto y lo he confirmado,
observaré Tus justos juicios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">107. </strong>
He estado afligido en gran manera,
YAHWEH, dame vida, conforme a Tu Palabra. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">108. </strong>
Por favor acepta la ofrenda voluntaria de mi boca,
YAHWEH, y enséñame Tus juicios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">109. </strong>
Mi alma está continuamente en Tus manos,
mas no he olvidado de Tu Toráh. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">110. </strong>
Los pecadores me han tendido trampa
mas no me he extraviado de Tus mandamientos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">111. </strong>
Tengo tu instrucción como heredad permanente,
porque es la alegría de mi corazón. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">112. </strong>
He inclinado mi corazón a obedecer Tus ordenanzas
para siempre, porque son la alegría de mi corazón </p>
                                                    <h4><strong>Sámec</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">113. </strong>

ס Samej
 He odiado a los transgresores,
pero he amado Tu Toráh.[346] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">114. </strong>
Tú eres mi refugio y mi escudo.
pongo mi esperanza en Tus Palabras. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">115. </strong>
Déjenme estar, ustedes, obreros de maldad,
para que pueda buscar los mitzvot de mi Elohim. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">116. </strong>
Sostenme de acuerdo a Tu Palabra, y yo viviré;
no me desilusiones en mi esperanza. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">117. </strong>
Ayúdame; y yo seré salvo,
y meditaré en Tus ordenanzas continuamente. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">118. </strong>
Tú rechazas a todos los que ponen a nada Tus ordenanzas.
porque su pensamiento interno es falso. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">119. </strong>
Tú desecharás al perverso de la tierra como transgresores;
por esto amo Tu instrucción. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">120. </strong>
Mi cuerpo tiembla por temor de ti;
Tus juicios me dan miedo. </p>
                                                    <h4><strong>Ayin</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">121. </strong>

ע Ayin
 He hecho lo que es justo y recto;
no me abandones a mis opresores. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">122. </strong>
Garantiza el bienestar de tu siervo;
no permitas que el arrogante me acuse falsamente. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">123. </strong>
Mis ojos desfallecen por Tu Salvación
y por la palabra de Tu justicia. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">124. </strong>
Trata con tu siervo conforme a Tu misericordia
y enséñame Tus ordenanzas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">125. </strong>
Soy tu siervo; dame entendimiento,
para así poder conocer Tu instrucción.[347] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">126. </strong>
El tiempo para YAHWEH actuar ha llegado;
porque ellos han quebrantando Tu Toráh. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">127. </strong>
Por lo tanto yo amo Tus mitzvot,
más que el oro, o el topacio. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">128. </strong>
Por eso dirijo mis pasos por Tus mandamientos;
odio todo camino injusto. </p>
                                                    <h4><strong>Pe</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">129. </strong>

פ Pei
 Tu instrucción es una maravilla,
por esto mi alma la ha buscado. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">130. </strong>
La manifestación de Tu Palabra alumbrará;
dando instrucción a los descuidados. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">131. </strong>
Mi boca está completamente abierta mientras suspiro
con anhelo por Tus mitzvot. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">132. </strong>
Vuélvete a mí y muéstrame Tu misericordia,
conforme a Tus juicios para aquellos que aman Tu Nombre. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">133. </strong>
Ordena mis pasos por Tus Palabras;
no permitas que ninguna iniquidad domine en mí. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">134. </strong>
Redímeme de la opresión de los humanos,
y observaré Tus mandamientos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">135. </strong>
Haz que Tu Rostro resplandezca sobre tu siervo,
y enséñame Tus ordenanzas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">136. </strong>
Ríos de lágrimas fluyen de mis ojos
porque no observé Tu Toráh. </p>
                                                    <h4><strong>Tsade</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">137. </strong>

צ Tzadik
 Tú eres justo, O YAHWEH;
y Tus juicios son rectos </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">138. </strong>
Tú has ordenado Tus instrucciones
en justicia y gran fidelidad. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">139. </strong>
Mi celo me está destruyendo,
porque mis enemigos han olvidado Tus Palabras. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">140. </strong>
Tu Palabra está refinada a completa pureza,
y tu siervo la ama. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">141. </strong>
Puedo ser joven y despreciado,
pero no me olvido de Tus ordenanzas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">142. </strong>
Tu justicia es justicia eterna
y Tu Toráh es verdad. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">143. </strong>
Aflicción y angustia me han sobrecogido,
pero Tus mitzvot son mi meditación. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">144. </strong>
Tú instrucción es justa para siempre;
dame entendimiento, y yo viviré. </p>
                                                    <h4><strong>Cof</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">145. </strong>

ק Kuf
 De todo corazón clamo a ti;
respóndeme, O YAHWEH; yo guardaré Tus ordenanzas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">146. </strong>
Clamo a ti; sálvame
y observaré Tu instrucción. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">147. </strong>
Me levanto antes del alba y clamo por tu ayuda;
pongo mi esperanza en Tu Palabra. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">148. </strong>
Mis ojos están abiertos antes de las vigilias de la noche,
para que pueda meditar en Tus oráculos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">149. </strong>
En Tu misericordia, oye mi voz;
YAHWEH, conforme a Tu justicia, revíveme. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">150. </strong>
Los que persiguen la carnalidad se acercan;
se están distanciando de Tu Toráh. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">151. </strong>
Cerca estás Tú, YAHWEH;
y todos Tus mitzvot son verdad. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">152. </strong>
Hace ya mucho aprendí de Tu instrucción,
que estableciste para siempre. </p>
                                                    <h4><strong>Resh</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">153. </strong>

ר Reish
 Mira mi aflicción y rescátame,
porque no me olvido de Tu Toráh. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">154. </strong>
Intercede por mi causa, y redímeme;
conforme a Tu Palabra, revíveme. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">155. </strong>
La Salvación está muy lejos de los pecadores,
porque no buscan Tus ordenanzas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">156. </strong>
Grande es Tu misericordia, O YAHWEH;
conforme a Tus juicios, revíveme. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">157. </strong>
A pesar de que mis perseguidores y enemigos,
son muchos, no me he apartado de Tu instrucción. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">158. </strong>
Miro a los traidores con disgusto,
porque no guardan Tus oráculos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">159. </strong>
Mira como amo Tus mandamientos, YAHWEH;
conforme a Tu misericordia, revíveme. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">160. </strong>
Lo principal de Tu Palabra es que es verdad;
y todos Tus juicios permanecen para siempre.[348] </p>
                                                    <h4><strong>Sin</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">161. </strong>

ש Shin
 Los príncipes me persiguen, sin razón alguna;
pero mi corazón tiene temor de Tus Palabras. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">162. </strong>
Me regocijo en Tus oráculos;
como alguien que encuentra mucho botín. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">163. </strong>
Odio la injusticia, la detesto;
pero amo Tu Toráh. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">164. </strong>
Te alabo siete veces al día,
a causa de tus justos juicios. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">165. </strong>
Aquellos que aman Tu Toráh tienen una gran Shalom;
nada les hace tropezar.[349] </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">166. </strong>
Tengo esperanza en Tu Salvación, O YAHWEH;
y he amado Tus mitzvot. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">167. </strong>
Mi alma observa Tu instrucción,
¡Y yo la amo en gran manera! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">168. </strong>
Observo Tus mandamientos e instrucción, O YAHWEH,
porque todos mis caminos yacen al descubierto ante ti. </p>
                                                    <h4><strong>Tau</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">169. </strong>

ת Tav
 Mi llanto llegue ante ti, O YAHWEH;
instrúyeme de acuerdo a Tus oráculos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">170. </strong>
Mi oración llegue ante ti, O YAHWEH;
conforme a Tus oráculos, rescátame. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">171. </strong>
Mis labios rebocen de alabanza;
porque Tú me enseñas Tus ordenanzas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">172. </strong>
Mi lengua pronuncie Tus oráculos,
porque todos Tus mitzvot son justos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">173. </strong>
Tu mano esté lista para Salvarme,
porque escojo Tus mandamientos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">174. </strong>
Añoro Tu salvación, O YAHWEH,
y Tu Toráh es mi meditación. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">175. </strong>
Mi alma vivirá, y te alabaré;
y que Tus juicios me ayuden. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">176. </strong>
Me extravié como oveja perdida; busca a tu siervo;
porque no me he olvidado de Tus mitzvot.</p>
                                                        
