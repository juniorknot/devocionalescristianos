con anhelos por Tus juicios. 21. Tú reprendes a los soberbios, los malditos,
que se extravían de tus mitzvot. 22. Aparta de mí el escarnio y el menosprecio,
porque observo tu instrucción. 23. Aun cuando príncipes conspiran contra mí,
tu siervo medita en tus ordenanzas. 24. Y también Tus instrucciones son mi delicia;
y Tus ordenanzas son mis consejeros.[338] 25. ד Dalet
 Mi alma está postrada en el polvo;
vivifícame, según Tu Palabra.[339] 26. Te he contado de mis caminos, y me respondiste,
enséñame Tus ordenanzas. 27. Instrúyeme en el camino de Tus preceptos
y meditaré en Tus maravillas. 28. Me desvanezco de ansiedad y aflicción;
renueva mis fuerzas con Tu Palabra.[340] 29. Mantén el camino de la iniquidad lejos de mí,
y sé misericordioso a mí con Tu Toráh. 30. Escogí el camino de la verdad,
y no he olvidado tus juicios. 31. Me aferro a Tu instrucción;
¡YAHWEH, no permitas que sea puesto en vergüenza! 32. Correré por el camino de Tus mitzvot,
porque Tú has ampliado mi entendimiento. 33. ה Hei
 Enséñame, O YAHWEH, el camino de Tus ordenanzas;
y lo buscaré continuamente. 34. Dame entendimiento; entonces guardaré
Tu Toráh con todo mi corazón. 35. Guíame por la senda de Tus mitzvot,
porque tomo placer en ello. 36. Inclina mi corazón hacia Tus instrucciones
y no hacia ganancia egoísta.[341] 37. Aparta mis ojos de las vanidades;
con Tus caminos, dame vida. 38. Confirma Tu oráculo a tu siervo,
que él te tema a ti. 39. Quita el reproche que me espanta,
pues Tus juicios son buenos. 40. Mira cuanto anhelo por Tus mandamientos;
en Tu justicia, ¡dame vida! 41. ו Vav
 Que Tu misericordia venga a mí, O YAHWEH,
