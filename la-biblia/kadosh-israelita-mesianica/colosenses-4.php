                                                                                <p style="style=text-align: justify;"><strong id="numeros">1. </strong>
Amos, traten a sus esclavos con justicia y equidad. Recuerden que ustedes también tienen un amo en el cielo. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">2. </strong>
Sigan perseverando en la oración, velando en ella con acción de gracias. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">3. </strong>
Oren también por nosotros, que YAHWEH nos abra una puerta para proclamar el mensaje acerca del secreto del Mashíaj, pues por esto estoy en prisión, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">4. </strong>
y oren que yo pueda hablar como debo hacerlo, en tal forma que el mensaje sea con claridad. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">5. </strong>
Procedan sabiamente para con los de afuera, haciendo uso completo de toda oportunidad; </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">6. </strong>
que su conversación sea siempre con gracia e interesante, para que sepan como responder a cualquier individuo en particular. </p>
                                                    <h4><strong>Salutaciones finales</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">7. </strong>
Nuestro querido hermano Tíquico, quien es un fiel trabajador y consiervo y esclavo en el Adón les dará todas las noticias acerca de mí. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">8. </strong>
Lo he mandado a ustedes por esta misma razón, para que sepan como estamos y así los pueda alentar. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">9. </strong>
Les he mandado a Onésimo, el querido y fiel hermano, que es uno de ustedes, ellos les dirán todo lo que ha pasado aquí. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">10. </strong>
Aristarco, mi compañero de prisión, manda saludos, como también Marcos, el primo de Bar-Nabba, de quien han recibido instrucciones, si él va a ustedes, denle la bienvenida. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">11. </strong>
Yeshúa, el llamado Justo, también manda saludos. Estos tres están entre los circuncisos y entre mis compañeros de trabajo para el Reino de YAHWEH, y se han convertido en un consuelo para mí. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">12. </strong>
Epafras manda saludos, él es uno de ustedes, un siervo del Mashíaj Yahshúa, que siempre agoniza en sus oraciones por ustedes, orando que puedan mantenerse en pie, firmes, maduros y completamente confiados según se dedican como Kadosh totalmente al Adón. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">13. </strong>
Porque puedo testificar de él, que trabaja arduamente por ustedes y por los de Laodicea e Hierápolis. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">14. </strong>
Nuestro amado amigo Lucas el Doctor y Demas les mandan saludos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">15. </strong>
Den mis saludos a los hermanos en Laodicea, también a Ninfas y a la Asamblea que se reúne en su casa. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">16. </strong>
Después que esta carta haya sido leída a ustedes, que también se lea en la Asamblea de Laodicea; y ustedes lean la de Laodicea. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">17. </strong>
Díganle a Arquipo: "Mira, completa la tarea que te fue dada en el Adón." </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">18. </strong>
Este saludo, yo Shaúl, escribo con mi propio puño y letra.
¡Recuerden mis prisiones!
¡La misericordia esté con ustedes! Amein.</p>
                                                        
