Y pecaron contra tus mitzvot y juicios,
los cuales, si una persona los guarda,
tendrá vida por medio de ellos.
No obstante, ellos testarudamente volvieron sus espaldas,
endurecieron su cerviz y rehusaron oír. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">30. </strong>
Por muchos años les extendiste tu misericordia
y testificaste contra ellos por tu Ruaj por mano de los profetas;
sin embargo, no escucharon.
Así, Tú los entregaste a las manos de las naciones de la tierra. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">31. </strong>
Aún, Tú en muchas misericordias,
no los designaste para destrucción; °
ni los abandonaste,
porque Tú eres Poderoso, Compasivo y Misericordioso. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">32. </strong>
"Y ahora, nuestro Elohim,
grande, poderoso, temible Elohim,
quien guarda ambos Su Pacto y SuMisericordia; °
que todo este sufrimiento no parezca poco para ti
que ha venido a nosotros, a nuestros reyes, a nuestros príncipes,
a nuestros kohanim, a nuestros profetas, a nuestros padres
y a todo nuestro pueblo,
desde el tiempo de los reyes de Ashur
aun hasta este mismo día. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">33. </strong>
No hay duda de que Tú eres justo °
en todo lo que ha venido sobre nosotros;
porque nos has tratado fielmente. °
Somos nosotros los que hemos pecado grandemente. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">34. </strong>
Nuestros reyes, nuestros príncipes, nuestros kohanim y padres
no guardaron Tu Toráh,
ni prestaron atención a tus mitzvot
ni escucharon Tus testimonios que Tú les diste. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">35. </strong>
Y ellos no te sirvieron a Ti en Tu reino,
y en Tu gran bondad cual les diste,
en la gran, rica tierra que pusiste delante de ellos,
ellos no te sirvieron;
ni se volvieron de sus obras perversas. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">36. </strong>
"Así que, aquí estamos hoy, siervos. °
Sí, en La Tierra que Tú diste a nuestros padres,
para que comieran lo que produce
y disfrutaran su bien –
¡Aquí estamos en ella, siervos sobre ella! </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">37. </strong>
Su rica cosecha ahora va a los reyes
que has puesto sobre nosotros por nuestros pecados;
tienen poder sobre nuestros cuerpos,
pueden hacer lo que les plazca a nuestro ganado,
¡y estamos en gran aflicción! ° </p>
                                                    <h4><strong>Pacto del pueblo, de guardar la ley</strong></h4>                            <p style="style=text-align: justify;"><strong id="numeros">38. </strong>
A la vista de todo esto, estamos haciendo un Pacto que obliga, poniéndolo en escritura y siendo sellado por nuestros príncipes, nuestros Leviim y nuestros kohanim."</p>
                                                        
