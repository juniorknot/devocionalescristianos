 no sea que al acudir conmigo algunos macedonios y no hallarlos preparados, nosotros seamos avergonzados (por no decir que ustedes se avergonzarían) en cuanto al orgullo con el cual nos enorgullecíamos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">5. </strong>
 Por este motivo, tuve cuidado de pedir estas cosas a mis hermanos para que fueran a ustedes antes que yo, y prepararan la ofrenda que desde hace tiempo habíamos escuchado que estaría lista como ofrenda y no como codicia. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">6. </strong>
 Y digo esto: El que siembra escasamente, también cosechará escasamente, y el que siembra generosamente, también cosechará generosamente. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">7. </strong>
 Cada uno dé como se haya propuesto, no con tristeza ni por obligación, porque Dios ama al dador alegre, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">8. </strong>
 porque toda generosidad que abunda en ustedes llega de las manos de Dios, para que teniendo siempre lo suficiente en todo, abunden para toda buena obra. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">9. </strong>
 Como está escrito: "EL REPARTIÓ, DIO A LOS POBRES; SU JUSTICIA PERMANECE PARA SIEMPRE", </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">10. </strong>
 y el que suministra semilla al que siembra y pan para alimento, suministrará y multiplicará la semilla de ustedes, y hará aumentar el fruto de su justicia, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">11. </strong>
 para que en todo sean enriquecidos para toda liberalidad, la cual prodece mediante nosotros acción de gracias a Dios; </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">12. </strong>
 porque el suministro de esta ayuda no sólo suple plenamente para la necesidad de los santos, sino que también sobreabunda por medio de muchas acciones de gracias a Dios, </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">13. </strong>
 porque a causa de la prueba de esta ayuda glorificamos a Dios, pues ustedes se han sometido a la fe del Evangelio del Cristo, y debido a la liberalidad de ustedes han llegado a ser partícipes con ellos y con todos. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">14. </strong>
 Y ellos ofrecieron oración a favor de ustedes con mucho amor, debido a la abundancia de la gracia de Dios que está sobre ustedes. </p>
                                                                                <p style="style=text-align: justify;"><strong id="numeros">15. </strong>
 ¡Gracias a Dios por su don inefable! </p>
                                                        
